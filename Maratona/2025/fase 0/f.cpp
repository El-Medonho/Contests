const int mod = 1e9+7;
const ll inf = 1e18+5;

signed solve(){

    int n; cin >> n;

    ll b = 4005;

    vector<vector<ll>> dp(5, vector<ll>(2*b, 0LL));
    dp[0][b] = 1;

    for(int i = 0; i < n; i++){
        int x; cin >> x;

        for(int k = 3; k >= 0; k--){
            for(int j = dp[k].size(); j > -1; j--){
                if(x + j > dp[k].size()) continue;
                if(x + j < 0) break;

                dp[k+1][x+j] += dp[k][j];
            }
        }
    }

    int q; cin >> q;

    while(q--){
        int x; cin >> x;

        ll sum = dp[4][b+x];
        // if(x != 0) sum += dp[4][b-x];

        cout << sum << endl;
    }

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}