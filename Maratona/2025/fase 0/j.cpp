const int mod = 1e9+7;
const ll inf = 1e18+5;

int i = 0;
ll k;

struct comp {
    bool operator()(const pair<ll, ll>& a,
                    const pair<ll, ll>& b) const
    {
        ll va = a.first + (i-a.second) * k, vb = b.first + (i-b.second) * k;
        return va < vb;
    }
};

signed solve(){

    ll n; cin >> n >> k;

    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;

    // auto cmp = [](pair<ll,ll>&a, pair<ll,ll> &b) {
    //     ll va = a.first + (i-a.second) * k, vb = b.first + (i-b.second) * k;
    //     return va < vb;
    // };

    stack<pair<ll,ll>> st;

    vector<int> ans(n, -1);

    for(; i < n; i++){
        while(!st.empty()){
            auto [a, b] = st.top();
            ll val = a + (i-b) * k;
            if(val > arr[i]){
                ans[b] = i;
                st.pop();
            }
            else break;
        }

        st.emplace(arr[i], i);
    }

    for(; i < 2*n; i++){
        while(!st.empty()){
            auto [a, b] = st.top();
            ll val = a + (i-b) * k;
            if(val > arr[i-n]){
                ans[b] = i-n;
                st.pop();
            }
            else break;
        }
    }

    for(int j: ans) cout << j+1 << ' ';
    cout << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}