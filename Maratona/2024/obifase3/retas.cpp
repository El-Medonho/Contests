#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    ll x1, x2; cin >> x1 >> x2;

    vector<pair<ll,ll>> arr(n);

    set<ll> st;

    for(int i = 0; i < n; i++){
        ll a,b; cin >> a >> b;

        ll p = a*x1 + b;
        ll q = a*x2 + b;

        arr[i] = {q, -p};

        st.insert(q); st.insert(p);
    }

    sort(arr.begin(), arr.end());

    int cnt = 1;
    map<ll,ll> mp;

    for(ll cc: st){
        mp[cc] = cnt++;
    }

    vector<ll> bit(cnt, 0);

    function<void(int,int)> add;

    add = [&](int x, int val){
        while(x < cnt){
            bit[x] += val;
            x += x&-x;
        }
    };

    function<ll(int)> sum;

    sum = [&](int x){
        ll ans = 0;
        while(x){
            ans += bit[x];
            x -= x&-x;
        }
        return ans;
    };

    for(auto [q, p]: arr){
        p = -p;
        add(mp[p], 1);
    }

    ll ans = 0;

    for(auto [q, p]: arr){
        p = -p;
        add(mp[p], -1);
        ll cc = sum(mp[p]);
        ans += cc;
    }

    cout << ans << endl;
    

    return 0;
}