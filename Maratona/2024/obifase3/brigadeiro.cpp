#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const int inf = 1e9+5;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n, k, t; cin >> n >> k >> t;

    vector<int> arr(n);
    for(int &x: arr) cin >> x;

    vector<int> pos; pos.reserve(k);

    for(int i = 0; i < n; i++){
        char c; cin >> c;
        if(c == '1') pos.push_back(i);
    }

    int up = n*9+1;

    vector<vector<int>> dp(k+1, vector<int>(n*9+10, inf));

    for(int i = 0; i <= k; i++) dp[i][0] = 0;

    for(int i = 0; i < n; i++){ 
        for(int j = k; j > 0; j--){
            for(int a = arr[i]; a < up; a++){
                dp[j][a] = min(dp[j][a], dp[j-1][a-arr[i]] + abs(pos[j-1] - i));
            }
        }
    }

    for(int i = up-1; i >= 0; i--){
        if(dp[k][i] <= t){
            cout << i << endl;
            return 0;
        }
    }
    

    return 0;
}