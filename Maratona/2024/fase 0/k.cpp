const int mod = 998244353;
const int mxn = 3e6;

ll expo(ll b, ll e){
    ll res=1;
    while(e){
        if(e&1) res=res*b%mod;
        b=b*b%mod;
        e >>= 1;
    }
    return res;
}

int lp[mxn]; //lp[i] is the lowest prime factor of i

vector<int> crivo(int N){ // O(N)
    vector<int> primes;
    for(int i=2; i<=N; i++){
        if(!lp[i]){
            lp[i]=i;
            primes.push_back(i);
        }
        for(int p:primes){
            if(i>N/p) break;
            lp[i*p]=p;
            if(i%p==0) break;
        }
    }
    return primes;
}

signed solve(){

    ll t,k; cin >> t >> k;
    
    vector<ll> dp(k+5, 0);

    dp[1] = 1;

    vector<bool> mark(t+5, 0);

    ll cnt = 1;

    vector<int> p = crivo(t-1);

    for(int i: p){
        cnt++;
        for(int h = min(cnt, k); h > 0; h--){
            dp[h] = dp[h-1] * expo(i, mod-2) % mod + dp[h] * (i-1) % mod * expo(i, mod-2) % mod;
            dp[h] %= mod;
        }
    }

    cout << dp[k] * expo(t, mod-2) % mod << endl;


    return 0;
}

