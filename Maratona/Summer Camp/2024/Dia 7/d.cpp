signed solve(){

    
    int n,m,k; cin >> n >> m >> k;

    vector<string> arr(n);

    for(string &i: arr) cin >> i;

    queue<pair<int,int>> qw, qw2;

    vector<vector<int>> vis(n, vector<int>(m, 0)), dist2(n, vector<int>(m, 0));

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            if(arr[i][j] == '.') {qw.push({i,j}); vis[i][j] = 1;}
        }
    }

    vector<pair<int,int>> dir = { {0,1}, {1,0}, {-1,0}, {0,-1} };

    while(!qw.empty()){
        auto [x,y] = qw.front(); qw.pop();

        if(vis[x][y] >= k+2 && arr[x][y] == 'x'){
            qw2.push({x,y});
            dist2[x][y] = 1;
        }

        for(auto [xnn, ynn]: dir){
            int xn = x + xnn, yn = y + ynn;

            if(xn >= n || xn < 0 || yn < 0 || yn >= m || vis[xn][yn]) continue;

            vis[xn][yn] = vis[x][y] + 1;

            qw.push({xn,yn});
        }
    }

    while(!qw2.empty()){
        auto [x,y] = qw2.front(); qw2.pop();

        if(dist2[x][y] > k) continue;

        for(auto [xnn, ynn]: dir){
            int xn = x + xnn, yn = y + ynn;

            if(xn >= n || xn < 0 || yn < 0 || yn >= m || dist2[xn][yn]) continue;

            dist2[xn][yn] = dist2[x][y] + 1;

            qw2.push({xn,yn});
        }
    }

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            if((arr[i][j] == '.' && dist2[i][j] ) || (arr[i][j] == 'x' && !dist2[i][j])){
                cout << "NO\n";
                return 0;
            }
        }
    }

    cout << "YES\n";

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}