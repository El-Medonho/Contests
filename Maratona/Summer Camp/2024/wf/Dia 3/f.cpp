#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n,k; cin >> n >> k;

    string s; cin >> s;

    vector<pair<ll,ll>> pontos(26, {0,0});
    vector<ll> freq(26, 0), freqs;

    for(int i = 0; i < 26; i++) pontos[i].second = i;

    for(int i = 0; i < n; i++) freq[s[i]-'a']++;

    freqs = freq;

    for(int i = 0; i < 26; i++){
        int h = i;
        
        pontos[h].first += freqs[h]*(ll)(h+'a');
    }

    sort(pontos.begin(), pontos.end());

    ll ans = 0;

    for(int g = 0; g < 26; g++){
        freqs = freq;
        int kk = k;
        ll resp = 0;
        
        for(;kk;kk--){
            for(int h = 0; h < 26; h++){
                int cc = pontos[h].second;
                if(cc == g || freqs[cc] == 0) continue;

                freqs[cc]--;
                pontos[h].first = freqs[cc]*(ll)(h+'a');
                break;
            }
            sort(pontos.begin(), pontos.end());
        }

        freqs[g] += k-kk;

        for(int h = 0; h < 26; h++){
            resp += (freqs[h] * (freqs[h]-1) / 2) * (h+'a');
        }

        ans = max(ans, resp);
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}