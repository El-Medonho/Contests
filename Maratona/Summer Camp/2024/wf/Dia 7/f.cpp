signed solve(){

    int n,m; cin >> n >> m;

    vector<string> arr(n);

    for(string &i: arr) cin >> i;


    vector<vector<int>> vis(n, vector<int>(m, 0));

    vector<int> r(n, 0), c(m, 0);

    vector<char> ans(0);

    function<bool(int,int)> dfs;

    dfs = [&](int x, int y){

        if(x == n-1 && y == m-1) return true;
        if(vis[x][y]) return false;
        vis[x][y] = 1;

        if(x+1 < n && arr[x+1][y] != '#'){
            bool ok = false;
            if((arr[x+1][y] == 'O' && c[y]) || (arr[x+1][y] == '@' && !c[y])) { r[x+1] = 1; ok = true; }
            ans.push_back('D');
            
            if(dfs(x+1, y)) return true;
            
            ans.pop_back();
            if(ok) r[x+1] = 0;
        }

        if(y+1 < m && arr[x][y+1] != '#'){
            bool ok = false;
            if((arr[x][y+1] == 'O' && r[x]) || (arr[x][y+1] == '@' && !r[x])) { c[y+1] = 1; ok = true;}
            ans.push_back('R');
            
            if(dfs(x, y+1)) return true;
            
            ans.pop_back();
            if(ok) c[y+1] = 0;
        }

        return false;
    };

    if(arr[0][0] == '@') c[0] = 1;
    
    if(!dfs(0,0)){
        cout << "NO\n";
        return 0;
    }

    cout << "YES\n";

    for(int i: r){
        if(i) cout << 'Y';
        else cout << 'N';
    }

    cout << endl;

    for(int i: c){
        if(i) cout << 'Y';
        else cout << 'N';
    }

    cout << endl;

    for(char c: ans) cout << c;
    cout << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}