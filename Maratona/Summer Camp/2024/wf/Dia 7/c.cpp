#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

typedef double ld;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,k; cin >> n >> k;

    vector<ld> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i];
    }

    sort(arr.rbegin(), arr.rend());

    // for(int g = k; g <= n; g++){
    //     vector<double> dp(g, 0);
    //     dp[0] = 1;

    //     for(int i = 0; i < k; i++){
    //         for()
    //     }
    // }

    vector<ld> dp(2*n+4, 0), nxt(2*n+4, 0); nxt[n] = 1;
    ld sum = 0;
    ld ans = 0;

    for(int i = 0; i < n; i++){
        dp = nxt;
        fill(nxt.begin(), nxt.end(), 0);
        sum = 0;
        for(int j = 2*n; j > -1; j--){
            nxt[j+1] += dp[j]*arr[i];
            // if(j+1 >= k+n) sum += nxt[j+1];
            nxt[j-1] += dp[j]*(1-arr[i]);
        }

        for(int j = 2*n; j >= n+k; j--){
            sum += nxt[j];
        }
        // cout << dp[4] << '\n';

        ans = max(ans, sum);
    }

    cout << fixed << setprecision(15) << ans << endl;

    return 0;
}