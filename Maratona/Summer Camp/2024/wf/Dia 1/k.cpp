#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,k; cin >> n >> k;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<vector<int>> jump(n, vector<int>(23, 0));

    for(int g = 0; g < 22; g++){
        map<int,int> mp;    
        for(int i = 0; i < n; i++){

            mp[arr[i]]++;

            if(i >= (1<<g)){
                int j = i-(1<<g); 
                mp[arr[j]]--;
                if(mp[arr[j]] == 0) mp.erase(arr[j]);
            }

            jump[i][g] = mp.size();
        }
    }

    vector<vector<ll>> dp(n, vector<ll>(k+1, -1e18));
    dp[0][0] = 0;

    for(int i = 0; i < n; i++){
        for(int g = 0; g < 22; g++){
            dp[i][1] = max(dp[i][1], (ll)jump[i][21]);
            for(int a = 1; a <= k; a++){
                if(i < (1<<g)) break;
                dp[i][a] = max(dp[i][a], jump[i][g] + dp[i-(1<<g)][a-1]);
            }
        }
    }

    cout << dp[n-1][k] << endl;

    return 0;
}