#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    int c = 1<<24;

    vector<ll> dp(c, 0);

    ll ans = 0;
    for(int i = 0; i < n; i++){
        string s; cin >> s;
        // int x = 0;
        set<int> x; x.insert(0);
        for(char c: s) for(int i: x) x.insert(i | (1<<(c-'a')));
        for(int i: x) {
            if(i == 0) continue;
            int h = -1;
            for(int k = 23; k > -1; k--){
                if(i & (1<<k))h*=-1;
            }
            dp[i] += h;
        }
    }

    for(int j = 0; j < 24; j++){
        for(int i = 0; i < c; i++){
            if(i & (1<<j)) dp[i] += dp[i ^ (1<<j)];
        }
    }
    
    for(int i = 0; i < c; i++) ans ^= dp[i]*dp[i];
    cout << ans << endl;

    return 0;
}