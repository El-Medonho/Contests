signed solve(){

    int n; cin >> n;
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    // sort(arr.begin(), arr.end());

    int k = (1<<22);
    vector<int> dp(k, -1);

    vector<bool> ap(k+5, 0);
    for(int i: arr) ap[i] = 1;

    for(int i = 0; i < k; i++){
        dp[i] = ap[i] ? i : -1;
        for(int j = 0; j < 22; j++) if(i & (1<<j)) dp[i] = max(dp[i], dp[i ^ (1<<j)]);
    }

    // cout << dp[0][0] << endl;

    for(int i: arr){
        // cout << ((~i) & (k-1) )<< ' ';
        cout << dp[(~i) & (k-1)] << ' ';
    }
    cout << endl;
    

    return 0;
}

