#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const int mxn = 1e5+50;

int memo[mxn], low[mxn], tin[mxn], artic[mxn];
vector<pair<int,int>> bridges(0);
vector<int> vec[mxn];
int cont = 0, n;

void dfs(int x, int p) {
    memo[x] = 1;
    tin[x] = low[x] = ++cont;
    int children = 0;
    for (int i : vec[x]) {
        if (i == p) continue;
        if (memo[i]) {
            low[x] = min(low[x], tin[i]);
        } else {
            dfs(i, x);
            low[x] = min(low[x], low[i]);
            if (low[i] > tin[x]) {
                bridges.push_back({min(i, x), max(i, x)});
            }
            if (low[i] >= tin[x] && p) artic[x] = 1;
            children++;
        }
    }
    if (!p && children > 1) artic[x] = 1;
}

void build() {
    cont = 0;
    bridges.clear();
    for (int i = 1; i <= n; i++) {
        memo[i] = low[i] = tin[i] = artic[i] = 0;
    }
    for (int i = 1; i <= n; i++) {
        if(!memo[i]) dfs(i, 0);
    }
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int m; cin >> n >> m;

    vector<vector<int>> graph(n);

    //2*a in, 2*a+1 out

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b;
        vec[a].push_back(b);
        vec[b].push_back(a);
    }

    build();

    vector<int> ans(0);

    for(int i = 1; i <= n; i++){
        if(artic[i]) ans.push_back(i);
    }

    cout << ans.size() << endl;

    for(int i: ans) cout << i << ' ';
    cout << endl;

    return 0;
}