#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<vector<pair<int,int>>> graph(n);

    for(int i = 0; i < n-1; i++){
        int a,b; cin >> a >> b; a--; b--;

        graph[a].push_back({b,i});
        graph[b].push_back({a,i});
    }

    int sz = 0;

    for(int i = 0; i < n; i++) sz = max(sz, (int)graph[i].size());

    vector<vector<int>> ans(sz);

    vector<int> tempo(n, -1);

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        int tmp = 0;

        for(auto[j, ind]: graph[cc]){
            if(j == last) continue;

            if(tmp == tempo[cc]) tmp++;

            tempo[j] = tmp;

            ans[tmp].push_back(ind+1);

            dfs(j, cc);

            tmp++;
        }
    };

    dfs(0,0);

    cout << sz << endl;

    for(int i = 0; i < sz; i++){
        cout << ans[i].size() << ' ';
        for(int j: ans[i]) cout << j << ' ';
        cout << endl;
    }

    return 0;
}