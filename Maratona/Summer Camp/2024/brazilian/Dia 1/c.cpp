#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b;
        a--; b--;
        graph[a].push_back(b);
    }

    function<void(int)> dfs;

    vector<int> vis(n, 0), visg(n, 0);
    vector<int> lowlink(n, 0), depth(n, 0);
    int tempo = 0;
    stack<int> stk;

    dfs = [&](int cc){

        depth[cc] = lowlink[cc] = ++tempo;

        stk.push(cc);

        visg[cc] = 1;

        for(int j: graph[cc]){
            if(visg[j]){
                lowlink[cc] = min(lowlink[cc], depth[j]);
                continue;
            }

            if(vis[j]) continue;

            dfs(j);

            lowlink[cc] = min(lowlink[cc], lowlink[j]);
        }

        if(depth[cc] == lowlink[cc]){
            int u = -1;
            while(u != cc){
                u = stk.top(); stk.pop();
                visg[u] = 0;
                vis[u] = 1;
            }
        }
    };

    dfs(0);

    for(int i = 1; i < n; i++){
        if(lowlink[i] == 0){
            cout << "NO\n";
            cout << 1 << ' ' << i+1 << endl;
            return 0;
        }

        if(lowlink[i] == depth[i]){
            cout << "NO\n";
            cout << i+1 << ' ' << 1 << endl;
            return 0;
        }
    }

    cout << "YES\n";

    return 0;
}