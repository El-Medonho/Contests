#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,k; cin >> n >> k;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int x; cin >> x; x--;
        graph[x].push_back(i);
    }

    vector<int> sz(n, 0);

    function<void(int)> dfs;

    multiset<int> st;

    dfs = [&](int cc){

        int csz = -1;

        for(int j: graph[cc]){
            dfs(j);

            if(csz == -1) csz = sz[j];

            else if(csz < sz[j]){
                st.insert(csz);
                csz = sz[j];
            }

            else st.insert(sz[j]);
        }

        if(csz == -1) sz[cc] = 0;
        else sz[cc] = csz;

        sz[cc]++;

        return;
    };


    dfs(0);

    st.insert(sz[0]);

    int ans = 0;

    auto it = st.rbegin();
    while(k--){

        ans += *it;

        it++;
    }

    cout << ans << endl;

    return 0;
}