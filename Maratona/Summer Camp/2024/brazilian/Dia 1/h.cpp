#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    // freopen("kingdom.in", "r", stdin);
    // freopen("kingdom.out", "w", stdout);

    int n; cin >> n;

    vector<int> cnt(n, 0);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        cnt[a]++; cnt[b]++;
    }

    vector<int> arr(0);

    for(int i = 0; i < n; i++){
        if(cnt[i] == 1){
            arr.push_back(i+1);
        }
    }

    cout << arr.size()-1 << endl;

    for(int i = 1; i < arr.size(); i++) cout << arr[i-1] << ' ' << arr[i] << endl;  

    return 0;
}