#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,k; cin >> n >> k;

    k *= 2;

    vector<int> uni(n, 0), tot(n, 0);

    for(int i = 0; i < k; i++){
        int a; cin >> a; a--;
        uni[a] = 1;
    }

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    function<void(int,int)> dfs, dfs2;

    dfs = [&](int cc, int last){

        tot[cc] += uni[cc];

        for(int j: graph[cc]){
            if(j == last) continue;

            dfs(j, cc);
            tot[cc] += tot[j];
        }

        return;
    };

    dfs(0,0);

    ll ans = 0;

    dfs2 = [&](int cc, int last){
        int ctot = tot[cc];

        for(int j: graph[cc]){
            if(j == last) continue;

            ctot -= tot[j];

            ans += min(ctot, tot[j]);

            ctot += tot[j];
        }

        for(int j: graph[cc]){
            if(j == last) continue;

            tot[j] = ctot;

            dfs2(j, cc);
        }

        return;
    };

    dfs2(0,0);

    cout << ans << endl;


    return 0;
}