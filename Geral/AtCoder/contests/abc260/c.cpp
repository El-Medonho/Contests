signed solve(){

    int n; cin >> n;
    ll x,y; cin >> x >> y;

    vector<ll> r(n+3, 0), b(n+3, 0);
    r[n] = 1;

    for(int i = n; i > 1; i--){
        r[i-1] += r[i];
        b[i] += r[i]*x;
        r[i-1] += b[i];
        b[i-1] += b[i]*y;
    }

    cout << b[1] << endl;
    
    return 0;
}

