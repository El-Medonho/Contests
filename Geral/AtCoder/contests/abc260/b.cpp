signed solve(){

    int n,x,y,z; cin >> n >> x >> y >> z;

    vector<pair<int,int>> a(n), b(n), c(n);

    for(int i = 0; i < n; i++){
        int x; cin >> x;
        a[i] = {400-x, i};
        c[i] = {x, i};
    } 

    for(int i = 0; i < n; i++){
        int x; cin >> x;
        b[i] = {400-x, i};
        c[i] = {400-(c[i].first + x), i};
    }

    sort(a.begin(), a.end());
    sort(b.begin(), b.end());
    sort(c.begin(), c.end());

    set<int> st;

    int cnt = 0;
    for(int i = 0; i < n && cnt < x; i++){
        auto [x, ind] = a[i];
        if(st.find(ind) != st.end()) continue;
        cnt++;
        st.insert(ind);
    }

    cnt = 0;
    for(int i = 0; i < n && cnt < y; i++){
        auto [x, ind] = b[i];
        if(st.find(ind) != st.end()) continue;
        cnt++;
        st.insert(ind);
    }

    cnt = 0;
    for(int i = 0; i < n && cnt < z; i++){
        auto [x, ind] = c[i];
        if(st.find(ind) != st.end()) continue;
        cnt++;
        st.insert(ind);
    }

    for(int i: st) cout << i+1 << endl;

    return 0;
}

