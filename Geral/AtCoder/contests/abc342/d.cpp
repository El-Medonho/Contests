mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;

    vector<int> sq = {};

    for(int i = 2; i * i < 3e5; i++) sq.push_back(i*i);

    map<int,int> mp;
    ll ans = 0;

    int m = n;

    while(n--){
        int x; cin >> x;

        if(x == 0){
            ans += --m;
            continue;
        }

        for(int i: sq){
            while(x % i == 0) x /= i;
        }

        mp[x]++;
    }


    for(auto [val, qtt]: mp){
        ans += ((ll) qtt) * (qtt-1) / 2;
    }
    
    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}