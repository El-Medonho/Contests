int solve(){

    map<char,int> mp;
    string s; cin >> s;
    int fst = -1;
    for(int i = 0; i < 8; i++){
        mp[s[i]]++;
        if((mp['K'] && !mp['R']) || (!mp['K'] && mp['R'] == 2)){
            cout << "No\n";
            return 0;
        }
        if(s[i] == 'B'){
            if(fst == -1) fst = i;
            else if(fst%2 == i%2){
                cout << "No\n";
                return 0;
            }
        }
    }

    if(mp['K'] == 1 && mp['Q'] == 1 && mp['B'] == 2 && mp['R'] == 2 && mp['N'] == 2) cout << "Yes\n";
    else cout << "No\n";

    return 0;
}

