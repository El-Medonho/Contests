signed solve(){
    
    int n = 9;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    function<int(int, vector<int>)> dfs;

    dfs = [&](int cc, vector<int> tab){

        for(int i = 0; i < 3; i++){
            if(tab[i] == tab[i+3] && tab[i] == tab[i+6] && tab[i]) return tab[i];
        }
        for(int i = 0; i < 3; i++){
            if(tab[i*3] == tab[i*3+1] && tab[i*3] == tab[i*3+2] && tab[i*3]) return tab[i*3];
        }
        if(tab[0] == tab[4] && tab[4] == tab[8] && tab[0]) return tab[0];
        if(tab[2] == tab[4] && tab[4] == tab[6] && tab[2]) return tab[2];

        if(cc == 9){
            ll s[] = {0,0};

            for(int i = 0; i < 9; i++){
                s[tab[i]-1] += arr[i];
            }

            if(s[0] > s[1]) return 1;
            else return 2;
        }

        for(int i = 0; i < 9; i++){
            if(tab[i]) continue;
            tab[i] = cc % 2; tab[i]++;

            if(dfs(cc+1, tab) == cc%2+1) return cc%2+1;

            tab[i] = 0;
        }

        return (cc+1)%2+1;
    };

    if(dfs(0, vector<int>(9, 0)) == 1) cout << "Takahashi\n";
    else cout << "Aoki\n";


    return 0;
}

