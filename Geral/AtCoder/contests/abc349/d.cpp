mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1e9+7;

signed solve(){
    
    ll l,r; cin >> l >> r;

    vector<ll> div = {1};

    while(div.back() <= (1LL<<60)) div.push_back(div.back()*2);

    int n = div.size();

    vector<ll> ans(0);

    while(l < r){
        for(int i = n-1; i > -1; i--){
            if(r-l < div[i] || l % div[i] != 0) continue;

            ans.push_back(l);
            ans.push_back(l+div[i]);

            l += div[i];

            break;
        }
    }

    cout << ans.size()/2 << endl;

    for(int i = 0; i < ans.size(); i++){
        cout << ans[i] << (i % 2 ? '\n' : ' ');
    }


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}