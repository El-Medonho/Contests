const ll md = 998244353;

signed solve(){

    int n; cin >> n;

    vector<vector<int>> graph(n);
    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b;
        a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<vector<pair<ll,ll>>> dp(n, vector<pair<ll,ll>>(n+1, make_pair(0LL,0LL)));
    vector<int> maxi(n, 1);

    for(int i = 0; i < n; i++){
        dp[i][0].second = 1;
        dp[i][1].first = 1;
    }

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        for(int i: graph[cc]){
            if(i == last) continue;
            dfs(i, cc);
        }

        for(int v: graph[cc]){
            if(v == last) continue;
            vector<pair<ll, ll>> vis(maxi[cc] + maxi[v] + 2, {0,0});
            for(int i = maxi[cc]; i > -1; i--){
                

                for(int j = maxi[v]; j > -1; j--){
                    int k = i + j;
                    k--;

                    ll novo = 0;
                    novo = dp[cc][i].first * dp[v][j].first; novo %= md;
                    if(novo){
                        maxi[cc] = max(maxi[cc], k);
                        vis[k].first = (novo + vis[k].first) % md;
                    }

                    k++;

                    novo = dp[cc][i].second * dp[v][j].first; novo %= md;
                    if(novo){
                        maxi[cc] = max(maxi[cc], k);
                        vis[k].second = (novo + vis[k].second) % md;
                    }

                    novo = dp[cc][i].second * dp[v][j].second; novo %= md;
                    if(novo){
                        maxi[cc] = max(maxi[cc], k);
                        vis[k].second = (novo + vis[k].second) % md;
                    }

                    novo = dp[cc][i].first * dp[v][j].second; novo %= md;
                    if(novo){
                        maxi[cc] = max(maxi[cc], k);
                        vis[k].first = (novo + vis[k].first) % md;
                    }
                }

            }
            for(int i = 0; i < min(n, (int) vis.size()); i++) dp[cc][i] = vis[i];
        }
    };

    dfs(0, 0);

    for(int i = 1; i <= n; i++) cout << (dp[0][i].first + dp[0][i].second) % md << '\n';
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}