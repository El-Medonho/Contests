ll expo(ll b, ll e, ll m){
    ll res = 1;
    while(e){
        if(e&1) res = (res*b)%m;
        b = (b*b)%m;
        e>>=1;
    }
    return res;
}

signed solve(){

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<ll> dp(n, 1);

    vector<ll> ans(n,1);

    function<void(int,int)> dfs;
    function<void(int,int,ll)> dfs2;

    dfs = [&](int cc, int last){

        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j, cc);
            dp[cc] *= (dp[j]+1) % m;
            dp[cc] %= m;
        }

        return;
    };

    dfs(0,0);

    dfs2 = [&](int cc, int last, ll val){
        // ans[cc] = dp[cc];

        vector<ll> suml(graph[cc].size()+2,1);
        vector<ll> sumr = suml;

        suml[0] = val+1;
        ans[cc] = ans[cc] * (val+1) % m;
        
        for(int jj = 0; jj < graph[cc].size(); jj++){
            int j = graph[cc][jj];
            suml[jj+1] = suml[jj];
            if(j == last) continue;
            // dp[cc] *= expo(dp[j]+1, m-2, m);
            
            suml[jj+1] = suml[jj+1] * (dp[j]+1) % m;

            ans[cc] = ans[cc] * (dp[j]+1) % m;
        }

        for(int jj = graph[cc].size()-1; jj > -1; jj--){
            sumr[jj+1] = sumr[jj+2];
            int j = graph[cc][jj];

            if(j == last) continue;

            sumr[jj+1] = sumr[jj+1] * (dp[j]+1) % m;
        }

        for(int jj = 0; jj < graph[cc].size(); jj++){
            int j = graph[cc][jj];

            if(j == last) continue;

            ll val2 = suml[jj] * sumr[jj+2] % m;

            dfs2(j, cc, val2);
        }

        return;
    };

    dfs2(0,0,0);

    for(ll i: ans) cout << i << '\n';

    return 0;
}

