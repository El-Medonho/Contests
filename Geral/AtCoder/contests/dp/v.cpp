ll expo(ll b, ll e, ll m){
    ll res = 1;
    while(e){
        if(e&1) res = (res*b)%m;
        b = (b*b)%m;
        e>>=1;
    }
    return res;
}

signed solve(){

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<ll> dp(n, 1);

    vector<ll> ans(n);

    function<void(int,int)> dfs, dfs2;

    dfs = [&](int cc, int last){

        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j, cc);
            dp[cc] *= (dp[j]+1) % m;
            dp[cc] %= m;
        }

        return;
    };

    dfs(0,0);

    dfs2 = [&](int cc, int last){
        ans[cc] = dp[cc];
        
        for(int j: graph[cc]){
            if(j == last) continue;
            // dp[cc] *= expo(dp[j]+1, m-2, m);
            dp[cc] /= dp[j]+1;
            dp[cc] %= m;
            dp[j] *= (dp[cc]+1) % m;
            dp[j] %= m;

            dfs2(j, cc);

            dp[cc] *= dp[j]+1;
            dp[cc] %= m;
        }
    };

    dfs2(0,0);

    for(ll i: ans) cout << i << '\n';

    return 0;
}

