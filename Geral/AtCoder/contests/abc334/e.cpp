const int mod = 998244353;

ll expo(ll b, ll e){
    ll resp = 1;
    while(e){
        if(e&1) resp = (resp * b) % mod;
        b = (b*b) % mod;
        e >>=1;
    }
    return resp;
}

signed solve(){
    
    int n,m; cin >> n >> m;

    vector<string> arr(n);

    int k = n*m;

    for(string &i: arr) cin >> i;

    vector<int> pai(k), peso(k, 1);

    for(int i = 0; i < k; i++) pai[i] = i;

    function<int(int)> ff;

    ff = [&](int x){
        if(pai[x] == x) return x;
        return pai[x] = ff(pai[x]);
    };

    function<void(int,int)> uu;

    uu = [&](int x, int y){
        x = ff(x); y = ff(y);

        if(x == y) return;

        if(peso[x] > peso[y]) swap(x,y);

        pai[x] = y;
        peso[y] += peso[x];

        return;
    };

    vector<pair<int,int>> dir = {{1,0}, {0,1}, {-1,0}, {0,-1}};

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            if(arr[i][j] == '#'){
                for(auto [xn, yn]: dir){
                    int in = i + xn, jn = j + yn;

                    if(in < 0 || in >= n || jn < 0 || jn >= m || arr[in][jn] != '#') continue;

                    uu(i*m + j, in * m + jn);
                }
            }
        }
    }

    set<int> bs;

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            if(arr[i][j] == '#') bs.insert(ff(i*m + j));
        }
    }

    int base = bs.size();

    ll ans = 0;

    ll cnt = 0;

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            if(arr[i][j] == '.'){
                cnt++;
                set<int> st;
                for(auto [xn, yn]: dir){
                    int in = i + xn, jn = j + yn;

                    if(in < 0 || in >= n || jn < 0 || jn >= m || arr[in][jn] != '#') continue;

                    st.insert(ff(in * m + jn));
                }

                ans = (ans + base + 1 - st.size()) % mod; 
            }
        }
    }

    cnt = expo(cnt, mod-2);

    ans = ans * cnt % mod;

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}