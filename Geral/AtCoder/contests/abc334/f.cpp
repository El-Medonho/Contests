typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1e9+7;

signed solve(){
    
    int n,k; cin >> n >> k;

    pair<ll,ll> s;

    cin >> s.first >> s.second;

    vector<pair<ll,ll>> arr(n);

    function<ld(pair<ll,ll>, pair<ll,ll>)> dist;

    dist = [&](pair<ll,ll> a, pair<ll,ll> b){
        return sqrtl((a.first-b.first) * (a.first-b.first) + (a.second-b.second) * (a.second-b.second));
    };

    for(int i = 0; i < n; i++){
        cin >> arr[i].first >> arr[i].second;
    }

    ld tot = 0;

    vector<ld> dp(n+3, 0), save(n+3, 0);

    deque<int> qw;

    for(int i = 0; i < n; i++){
        dp[i] += dist(s, arr[i]);
        
        if(i != 0){
            tot += dist(arr[i], arr[i-1]);
        }

        save[i] = tot;

        while(!qw.empty()){
            int ind = qw.back();

            ld mn = dp[ind] + tot - save[ind];

            if(mn < dp[i]) break;

            qw.pop_back();
        }

        qw.push_back(i);

        while(!qw.empty() && i - qw.front() >= k) qw.pop_front();

        int ind = qw.front();

        ld mn = dp[ind] + tot - save[ind];

        dp[i+1] = mn + dist(arr[i], s);
    }

    while(!qw.empty() && n - qw.front() > k) qw.pop_front();

    cout << fixed << setprecision(10) << dp[n] << endl;


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}