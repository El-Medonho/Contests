signed solve(){
    
    int n; cin >> n;

    vector<vector<int>> graph(n);

    vector<int> pai(n, 0), repr(n), peso(n, 1), dp(n);

    for(int i = 0; i < n; i++) {repr[i] = i; dp[i] = i;}

    function<int(int)> ff;

    ff = [&](int x){
        if(repr[x] == x) return x;
        return repr[x] = ff(repr[x]);
    };

    function<void(int,int)> uu;

    uu = [&](int x, int y){
        x = ff(x); y = ff(y);

        if(x == y) return;

        int paizao = pai[y];

        if(peso[x] > peso[y]) swap(x, y);

        repr[x] = y;

        pai[y] = paizao;

        peso[y] += peso[x];

        dp[y] = min(dp[y], dp[x]);
    };

    pai[0] = 0;

    for(int i = 1; i < n; i++){
        int x; cin >> x;
        x--;
        graph[x].push_back(i);
        pai[i] = x;
    }

    function<void(int)> dfs;

    dfs = [&](int cc){
        for(int j: graph[cc]){
            dfs(j);
            dp[cc] = min(dp[cc], dp[j]);
        }
    };

    dfs(0);

    int q; cin >> q;

    while(q--){
        int op; cin >> op;

        if(op == 1){
            int u,v; cin >> u >> v; u--; v--;

            u = ff(u); v = ff(v);

            while(u != v){
                int h = ff(pai[u]);
                uu(u, h);

                u = ff(u); v = ff(v);
            }
        }

        else{
            int x; cin >> x; x--;

            cout << dp[ff(x)] + 1 << endl;
        }
    }

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}