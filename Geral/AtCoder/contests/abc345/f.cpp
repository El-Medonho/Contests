signed solve(){
    
    int n,m,k; cin >> n >> m >> k;

    vector<vector<pair<int,int>>> graph(n);

    vector<int> pai(n), peso(n, 1);

    for(int i = 0; i < n; i++) pai[i] = i;

    function<int(int)> ff;

    ff = [&](int x){
        if(pai[x] == x) return x;
        return pai[x] = ff(pai[x]);
    };

    function<void(int,int)> uu;

    uu = [&](int x, int y){

        x = ff(x);
        y = ff(y);

        if(x == y) return;

        if(peso[x] < peso[y]) swap(x,y);

        pai[y] = x;
        peso[x] += peso[y];

        return;
    };

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;

        int x = ff(a), y = ff(b);

        if(x == y) continue;

        graph[a].push_back({b, i});
        graph[b].push_back({a, i});

        uu(a,b);
    }

    if(k % 2){
        cout << "No\n";
        return 0;
    }

    function<void(int,int)> dfs;

    vector<int> ac(n, 0), vis(n, 0);

    vector<int> ans(0);

    dfs = [&](int cc, int last){

        vis[cc] = 1;

        for(auto [j, ind]: graph[cc]){
            if(j == last) continue;

            if(k == 0) return;

            dfs(j, cc);

            if(k == 0) return;

            if(!ac[j]){
                ans.push_back(ind);

                ac[j] = 1;
                if(!ac[cc]) k -= 2;

                ac[cc] = (ac[cc] + 1) % 2;
            }
        }

        return;
    };

    for(int i = 0; i < n; i++){
        if(k == 0) break;

        if(vis[i]) continue;

        dfs(i,i);
    }

    if(k){
        cout << "No\n";
        return 0;
    }

    cout << "Yes\n";

    cout << ans.size() << endl;

    for(int i: ans) cout << i+1 << ' ';
    cout << endl;

    return 0;
}

