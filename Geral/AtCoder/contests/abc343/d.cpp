signed solve(){

    set<ll> st;

    int n; cin >> n;
    int t; cin >> t;

    vector<pair<ll,ll>> arr(t);

    for(int i = 0; i < t; i++){
        cin >> arr[i].first >> arr[i].second; arr[i].first--;
    }

    vector<ll> vals(n, 0);

    map<ll,ll> mp;
    mp[0] = n;

    for(auto [a,b] : arr){
        mp[vals[a]]--;
        if(mp[vals[a]] == 0) mp.erase(vals[a]);
        vals[a] += b;
        mp[vals[a]]++;

        cout << mp.size() << endl;
    }
    

    return 0;
}

