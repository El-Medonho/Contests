signed solve(){

    ll x; cin >> x;

    string ans = "0";

    for(ll i = 0; i * i * i <= x; i++){
        string s = to_string(i*i*i);

        int n = s.size();
        bool ok = true;
        for(int j = 0; j < n; j++){
            if(s[j] != s[n-j-1]) ok = false;
        }

        if(ok) ans = s;
    }

    cout << ans << endl;
    

    return 0;
}

