signed solve(){

    int n,m; cin >> n >> m;

    vector<string> a(n),b(m);

    for(string &i: a) cin >> i;
    for(string &i: b) cin >> i;

    for(int i = 0; i < n-m+1; i++){
        for(int j = 0; j < n-m+1; j++){
            bool ok = true;

            for(int ii = 0; ii < m; ii++){
                for(int jj = 0; jj < m; jj++){
                    if(a[i+ii][j+jj] != b[ii][jj]){
                        ok = false;
                        break;
                    }
                }
            }

            if(ok){
                cout << "Yes\n";
                return 0;
            }

        }
    }
    
    cout << "No\n";

    return 0;
}

