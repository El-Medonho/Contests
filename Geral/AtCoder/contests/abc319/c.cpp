signed solve(){

    vector<vector<int>> grid(3, vector<int>(3));

    for(int i = 0; i < 3; i++){
        for(int j = 0; j < 3; j++) cin >> grid[i][j];
    }

    int cnt = 0, ans = 0;

    vector<int> perm = {0,1,2,3,4,5,6,7,8};

    do{
        cnt++;
        vector<bool> vis(9, 0);
        for(int i: perm){
            int x = i/3, y = i%3;
            vis[i] = 1;

            int num = -1;
            for(int j = 0; j < 3; j++){
                if(j*3 + y == i) continue;
                if(vis[j*3 + y]) {
                    if(num == -1) num = grid[j][y];
                    else num = 10000;
                }
            }

            if(num == grid[x][y]){
                ans++;
                break;
            }
            num = -1;

            for(int j = 0; j < 3; j++){
                if(x*3 + j == i) continue;
                if(vis[x*3 + j]) {
                    if(num == -1) num = grid[x][j];
                    else num = 10000;
                }
            }

            if(num == grid[x][y]){
                ans++;
                break;
            }
            num = -1;

            if(x == y){
                for(int j = 0; j < 3; j++){
                    if(x == j) continue;
                    if(vis[j*3 + j]) {
                        if(num == -1) num = grid[j][j];
                        else num = 10000;
                    }
                }
            }

            if(num == grid[x][y]){
                ans++;
                break;
            }
            num = -1;

            if(x == 2-y){
                for(int j = 0; j < 3; j++){
                    if(x == j) continue;
                    if(vis[j*3 + 2 - j]) {
                        if(num == -1) num = grid[j][2-j];
                        else num = 10000;
                    }
                }
            }

            if(num == grid[x][y]){
                ans++;
                break;
            }
            num = -1;
        }
    }
    while(next_permutation(perm.begin(), perm.end()));

    cout << fixed << setprecision(15) << 1 - double(ans) / cnt << '\n';
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}