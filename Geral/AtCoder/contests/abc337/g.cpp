void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}

signed solve(){

    int n; cin >> n;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> in(n, 0), out(n, 0);

    int t = 1;

    function<void(int,int)> dfse;

    vector<int> qtt(n, 0), par(n, 0);

    dfse = [&](int cc, int last){
        in[cc] = t++;

        par[cc] = last;

        for(int j: graph[cc]){
            if(j == last) continue;

            dfse(j, cc);
        }

        out[cc] = t++;
    };

    dfse(0,0);

    vector<ll> ans(t+5, 0), vis(t+5, 0);

    for(int i = 0; i < n; i++){
        ll suml = 0;
        for(int j: graph[i]){
            if(j == par[i]) continue;

            ll h = sum(out[j], vis) - sum(in[j]-1, vis);

            suml += h;

            upd(in[j], (i - h), ans);

            upd(out[j], -(i - h), ans);
        }

        upd(in[0], suml, ans);
        upd(in[i], -suml, ans);
        upd(out[i], suml, ans);

        upd(in[i], 1, vis);
    }

    for(int i = 0; i < n; i++){
        cout << sum(in[i], ans) + i << endl;
    }


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}