signed solve(){

    int n; cin >> n;

    int m = n/3;

    m <<= 1;

    if(n % 3) m++;

    cout << m << endl;
    int r = n - (n % 3);

    for(int i = 0; i < r; i += 3){
        cout << 2 << ' ' << i+1 << ' ' << i+2 << '\n';
        cout << 2 << ' ' << i+2 << ' ' << i+3 << '\n';
    }


    if(n % 3){
        cout << 1 << ' ' << r+1 << endl;
    }

    cout.flush();

    string s; cin >> s;

    int cnt = 0;

    for(char c: s){
        cnt += c == '1';
    }

    if(cnt == 0 || (n % 3 && s[m-1]-'0')){
        if(cnt == 1){
            cout << r+1 << endl;
        }
        else{
            cout << r+2 << endl;
        }

        return 0;
    }

    int cc = 0;

    for(int i = 0; i+1 < m; i+=2){

        if(s[i]-'0' || s[i+1]-'0'){
            if(s[i]-'0' && s[i+1]-'0'){
                cout << cc + 2 << endl;
            }
            else if(s[i]-'0'){
                cout << cc + 1 << endl;
            }
            else cout << cc + 3 << endl;

            return 0;
        }

        cc += 3;
    }
    

    return 0;
}

