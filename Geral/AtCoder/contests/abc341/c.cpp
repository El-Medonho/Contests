signed solve(){

    int n,m; cin >> n >> m;
    int k; cin >> k;
    string t; cin >> t;

    map<char, pair<int,int>> mp; 
    mp['U'] = {-1,0}; mp['D'] = {1,0}; mp['L'] = {0,-1}; mp['R'] = {0,1};

    vector<string> s(n);
    for(string &i: s) cin >> i;

    int ans = 0;

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            if(s[i][j] == '#') continue;
            bool ok = true;
            int x = i, y = j;

            for(char c: t){
                x += mp[c].first;
                y += mp[c].second;

                if(s[x][y] == '#'){
                    ok = false;
                    break;
                }
            }

            ans += ok;
        }
    }
    
    cout << ans << endl;

    return 0;
}

