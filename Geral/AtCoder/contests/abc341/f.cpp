signed solve(){

    int n,m; cin >> n >> m;

    vector<pair<int,int>> edges(m);

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        edges[i] = {a,b};
    }

    vector<ll> w(n);
    for(ll &i: w) cin >> i;

    vector<ll> p(n);
    for(ll &i: p) cin >> i;

    vector<ll> sz(n, 0);
    vector<int> aponta(n,0);

    vector<vector<int>> graph(n);

    for(int i = 0; i < m; i++){
        auto [a,b] = edges[i];

        if(w[a] > w[b]) {graph[a].push_back(b); aponta[b]++;}
        else if(w[b] > w[a]) {graph[b].push_back(a); aponta[a]++;}
    }

    ll ans = 0;
    function<void(int)> dfs;

    dfs = [&](int cc){
        sz[cc] = 1;

        vector<ll> dp(w[cc]+5, 0);
        dp[0] = 1;

        for(int j: graph[cc]){
            if(!sz[j]) dfs(j);

            for(int h = w[cc]-1; h >= w[j]; h--){
                if(dp[h-w[j]]) dp[h] = max(dp[h], dp[h-w[j]] + sz[j]);
            }
        }

        sz[cc] = *max_element(dp.begin(), dp.end());

        ans += sz[cc] * p[cc];

        return;
    };

    for(int i = 0; i < n; i++){
        if(!aponta[i]) dfs(i);
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}