template<class T> struct Point{
	typedef Point P;
	T x, y;
	explicit Point(T x=0, T y=0) : x(x), y(y) {}
	bool operator<(P p) const { return tie(x,y) < tie(p.x,p.y); }
	bool operator==(P p) const { return tie(x,y)==tie(p.x,p.y); }
	P operator+(P p) const { return P(x+p.x, y+p.y); }
	P operator-(P p) const { return P(x-p.x, y-p.y); }
	P operator*(T d) const { return P(x*d, y*d); }
	P operator/(T d) const { return P(x/d, y/d); }
	T dot(P p) const { return x*p.x + y*p.y; }
	T cross(P p) const { return x*p.y - y*p.x; }
	T cross(P a, P b) const { return (a-*this).cross(b-*this); }
	T dist2() const { return x*x + y*y; }
	double dist() const { return sqrt((double)dist2()); }
	// angle to x-axis in interval [-pi, pi]
	double angle() const { return atan2(y, x); }
	P unit() const { return *this/dist(); } // makes dist()=1
	P perp() const { return P(-y, x); } // rotates +90 degrees
	P normal() const { return perp().unit(); }
	// returns point rotated 'a' radians ccw around the origin
	P rotate(double a) const { return P(x*cos(a)-y*sin(a),x*sin(a)+y*cos(a)); }
};
using P = Point<double>;

vector<P> ch(vector<P> &v){
    sort(v.begin(), v.end());
    v.erase(unique(v.begin(), v.end()), v.end());
    if(v.size()<=1) return v;

    vector<P> up, lw, cv;
    
    // a condição do cross será < quando incluir colinear, senão <=
    
    int n = (int)v.size();
    for(int i=0; i<n; i++){
        while(lw.size()>1 && v[i].cross(lw[lw.size()-2], lw[lw.size()-1])>=0) lw.pop_back();
        lw.push_back(v[i]);
    }
    for(int i=n-1; i>=0; i--){
        while(up.size()>1 && v[i].cross(up[up.size()-2], up[up.size()-1])>=0) up.pop_back();
        up.push_back(v[i]);
    }
    lw.pop_back();
    up.pop_back();
    cv = up;
    for(P i:lw) cv.push_back(i);
    return cv;
    //carefull with duplicates
}

signed solve(){

    int n; cin >> n;

    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;

    vector<P> c(n+1); c[0] = P(0,0);
    vector<ll> pre(n+1, 0);

    ll sum = 0;

    for(int i = 0; i < n; i++){
        sum += arr[i];
        c[i+1] = P(i+1, sum);
        pre[i+1] = sum;
    } 

    vector<P> ans = ch(c);

    int j = 0;

    while(ans[j].x != 0) j++;

    for(int i = 0; i < n; i++){
        double cc = arr[i];

        if(ans[j].x <= i) {j++; j %= ans.size();}

        double h = pre[ans[j].x] - pre[i];

        cc = max(cc, h/(ans[j].x-i));

        cout << fixed << setprecision(10) << cc << endl;
    }
    

    return 0;
}

