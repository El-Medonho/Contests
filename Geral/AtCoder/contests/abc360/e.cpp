const int mod = 998244353;

ll expo(ll b, ll e){
    ll ans = 1;
    while(e){
        if(e&1) ans = ans*b%mod;
        b = b*b%mod;
        e>>=1;
    }
    return ans;
}

signed solve(){
    
    ll n,k; cin >> n >> k;

    if(n==1){
        cout << 1 << endl;
        return 0;
    }

    vector<ll> fat(k+5, 1), r1(k+5, 0), r2(k+5, 0);

    r1[0] = 1; r2[0] = 0;

    for(int i = 1; i < k+3; i++) fat[i] = fat[i-1] * i % mod;

    ll E = 0;

    ll tot = n*n % mod;

    ll p0 = 2*(n-1) % mod * expo(tot, mod-2) % mod;

    ll np0 = (1 - p0 + mod) % mod;

    ll p1 = expo(np0, k);
    ll quarto = expo(4, mod-2);

    E = p1;

    // ll p2 = expo(np0, k-1) * p0 % mod * k % mod;

    // E = (E + (n+2) * expo(2, mod-2) % mod * p2 % mod);

    // if(k == 1){
    //     cout << E << endl;
    //     return 0;
    // }

    ll media = (n+2) * expo(2, mod-2) % mod;

    for(int i = 1; i <= k; i++){
        ll sub = expo(n-1, mod-2);
        r1[i] = r2[i-1] * sub % mod;
        r2[i] = (r1[i-1] + r2[i-1] * (n-2) % mod * sub % mod) % mod;

        ll p3 = expo(np0, k-i) * expo(p0, i) % mod * fat[k] % mod * expo(fat[i] * fat[k-i] % mod, mod-2) % mod;
        
        E = (E + p3 * ((r1[i] + media * r2[i] % mod) % mod) % mod) % mod;
    }

    cout << E << endl;

    return 0;
}

