signed solve(){
    
    int n,m,k; cin >> m >> n >> k;

    multiset<int> h;
    vector<int> b(m);

    
    for(int &i: b) cin >> i;
    
    for(int i = 0; i < n; i++){
        int x; cin >> x; h.insert(x);
    }
    sort(b.rbegin(), b.rend());

    int cnt = 0;

    for(int i = 0; i < m; i++){
        auto it = h.lower_bound(b[i]);
        if(it == h.end()) continue;

        h.erase(it); cnt++;
    }

    if(cnt >= k) cout << "Yes\n";
    else cout << "No\n";


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}