#define endl "\n"
typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 998244353;
const ll inf = 1e18+5;

ll expo(ll b, ll e) {
    ll ans = 1;
    while(e) {
        if(e&1) ans = ans*b%mod;
        b = b*b%mod;
        e>>=1;
    }
    return ans;
}

signed solve(){
    
    int n,d; cin >> n >> d;

    vector<int> arr(n), rm(1e6+5, 0);

    for(int &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    ll ans = 1;
    int cnt = 1;

    vector<ll> fat(n+2, 1);
    for(int i = 2; i <= n; i++){
        fat[i] = fat[i-1] * expo(i, mod-2) % mod;
    }

    map<int,int> mp;
    for(int i = 0; i < n; i++){
        int x = arr[i];
        mp[x] = i;
    }

    for(int i = 0; i < n; i++){
        int x = arr[i];
        auto it = mp.upper_bound(x+d);
        if(it != mp.end()){
            int ind = (*it).first;
            rm[ind]++;
        }
    }

    for(int i = 0; i < n; i++) {
        cnt -= rm[arr[i]];
        rm[arr[i]] = 0;
        
        ans = ans * cnt % mod;
        cnt++;
    }

    cnt = 1;
    for(int i = 1; i < n; i++){
        if(arr[i] == arr[i-1]) cnt++;
        else {ans = ans * fat[cnt] % mod; cnt = 1;}
    }

    ans = ans * fat[cnt] % mod;

    cout << ans << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}