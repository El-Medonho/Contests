mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;

    map<int,int> mp;

    vector<int> arr(n);

    int best = 0;

    int mx = 0;

    for(int &i: arr){
        cin >> i;
        mx = max(mx, i);
    }

    for(int i = 3; i < 10000; i++){
        best = 0;
        mp.clear();
        
        for(int j: arr){
            int k = j%i;
            mp[k]++;
            if(mp[k] > best) best = mp[k];
        }

        if(best > (n-1)/2) {
            cout << i << endl;
            return 0;
        }
    }

    if(1005 <= mx){
        uniform_int_distribution<int> uid(1005, mx);

        for(int ii = 1; ii < 1000; ii++){
            int i = uid(rng);

            mp.clear();
            best = 0;
            
            for(int j: arr){
                int k = j%i;
                mp[k]++;
                if(mp[k] > best) best = mp[k];
            }

            if(best > (n-1)/2) {
                cout << i << endl;
                return 0;
            }
        }
    }

    cout << -1 << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}