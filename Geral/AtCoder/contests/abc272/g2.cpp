mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    uniform_int_distribution<int> uid(0,n-1);

    for(int g = 0; g < 50; g++){
        int a = uid(rng);
        int b = uid(rng);
        while(b == a) b = uid(rng);

        int diff = abs(arr[a] - arr[b]);

        map<int,int> mp;
        for(int i = 2; i * i <= diff; i++){
            while(diff % i == 0){
                mp[i]++;
                diff/=i;
            }
        }
        if(diff != 1) mp[diff]++;

        set<int> st; st.insert(1);
        for(auto[x, qtt] : mp){
            for(auto ii = st.rbegin(); ii != st.rend(); ii++){
                int i = *ii;
                int h = 1;
                for(int u = 0; u < qtt; u++){
                    h *= x;
                    st.insert(i*h);
                }
            }
        }

        for(int i: st){
            map<int,int> mp;
            int mx = 0;
            if(i < 3) continue;
            for(int j: arr){
                mp[j%i]++;
                mx = max(mx, mp[j%i]);
            }
            if(mx > n/2){
                cout << i << endl;
                return 0;
            }
        }
    }

    cout << -1 << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}