const int mod = 1e9+7;

vector<int> pai(0), peso(0);

int ff(int x){
    if(pai[x]==x) return x;
    return pai[x]=ff(pai[x]);
}

void uu(int x, int y){
    x=ff(x), y=ff(y);
    if(x==y) return;
    if(peso[y]>peso[x]) swap(x, y);
    pai[y]=x;
    peso[x]+=peso[y];
}

void init(int n){
    pai.resize(n); peso.resize(n);
    for(int i=0; i<n; i++){
        pai[i]=i;
        peso[i]=1;
    }
}

signed solve(){
    
    int n,m; cin >> n >> m;
    vector<vector<int>> qw(m);

    for(int i = 0; i < m; i++){
        int k,c; cin >> k >> c;
        qw[i].push_back(c); qw[i].push_back(k);

        while(k--){
            int x; cin >> x; x--;
            qw[i].push_back(x);
        }
    }

    sort(qw.begin(), qw.end());

    init(n);

    ll ans = 0;

    for(auto &v: qw){
        ll c = v[0], k = v[1];

        for(int i = 3; i - 2 < k; i++){
            if(ff(v[i]) == ff(v[i-1])) continue;
            ans += c;
            uu(v[i], v[i-1]);
        }
    }

    bool ok = true;

    int k = ff(0);

    for(int i = 0; i < n; i++){
        if(ff(i) != k) ok = false;
    }

    if(ok) cout << ans << endl;
    else cout << -1 << endl;


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}