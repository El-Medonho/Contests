const ll inf = 1e18+5;

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;
    vector<vector<pair<int,ll>>> graph(n);

    for(int i = 0; i < n-1; i++){
        int a,b,c; cin >> a >> b >> c; c--;
        graph[i].push_back({i+1, a});
        graph[i].push_back({c, b});
    }

    priority_queue<pair<ll,int>, vector<pair<ll,int>>, greater<pair<ll,int>>> pq;

    pq.push({0LL,0});

    vector<ll> dist(n, inf); dist[0] = 0;

    while(!pq.empty()){
        auto[d, cc] = pq.top(); pq.pop();

        if(cc == n-1) break;

        if(d > dist[cc]) continue;

        for(auto [j, w]: graph[cc]){
            if(d + w >= dist[j]) continue;
            dist[j] = d+w;
            pq.push({dist[j], j});
        }
    }
    
    cout << dist[n-1] << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}