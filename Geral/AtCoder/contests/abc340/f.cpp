template<typename T>
T egcd(T a, T b, T &x, T &y) {
	if (!a) { x = 0, y = 1; return b; }
	T g = egcd(b % a, a, y, x);
	x -= y * (b/a); return g;
}

signed solve(){

    ll x,y; cin >> x >> y;

    if(y == 0){
        if(2 % x != 0){
            cout << -1 << endl;
        }
        else{
            cout << "0 " << 2/x << endl;
        }
        return 0;
    }

    if(x == 0){
        if(2 % y != 0){
            cout << -1 << endl;
        }
        else{
            cout << -2/y << " 0" << endl;
        }
        return 0;
    }

    if( 2 % __gcd(x,y) != 0 ){
        cout << -1 << endl;
        return 0;
    }
    
    ll a,b;

    ll g = egcd(x,-y,a,b);

    a *= 2/g;
    b *= 2/g;

    if(a > 1e18 || b > 1e18){
        cout << -1 << endl;
    }

    cout << b << ' ' << a << endl;


    return 0;
}

