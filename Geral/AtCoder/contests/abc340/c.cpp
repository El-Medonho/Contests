map<ll,ll> memo;

ll dp(ll x){
    if(x == 1) return 0;
    ll h = x;

    if(memo.find(x/2) != memo.end()){
        h += memo[x/2];
    }
    else {
        h += dp(x/2);
    }

    if(memo.find((x+1)/2) != memo.end()){
        h += memo[(x+1)/2];
    }
    else {
        h += dp((x+1)/2);
    }

    memo[x] = h;

    return h;
}

