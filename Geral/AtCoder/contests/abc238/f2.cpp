const int mod = 998244353;

signed solve(){

    int n,k; cin >> n >> k;

    vector<int> p(n);
    vector<int> arr(n);

    for(int i = 0; i < n; i++){
        cin >> p[i]; p[i]--;
    }

    for(int i = 0; i < n; i++){
        int x; cin >> x; x--;
        arr[x] = p[i];
    }

    vector<vector<ll>> dp(n, vector<ll>(k+1, 0));
    vector<int> cnt(n, 0);
    vector<set<int>> pre(n);

    for(int i = 0; i < n; i++) dp[i][0] = 1;

    for(int i = 0; i < n; i++){
        for(int j = i+1; j < n; j++){
            int h = 0;
            if(arr[j] > arr[i]){
                cnt[j]++;
                pre[j].insert(i);
                continue;
            }
            else{
                h = cnt[i];
                for(int a: pre[i]){
                    if(pre[j].find(a) != pre[j].end())h--;
                }
            }
            for(int a = k-1; a-h > 0; a--){
                dp[j][a] = (dp[j][a] + dp[j][a-1-h]) % mod;
            }
        }
    }

    ll ans = 0;

    for(int i = 0; i < n; i++){
        if(cnt[i] < k) ans = (ans + dp[i][k-1-cnt[i]]) % mod;
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}