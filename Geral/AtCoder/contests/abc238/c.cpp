const int mod = 998244353;

ll expo(ll b, ll e){
    ll ans = 1;
    while(e){
        if(e&1) ans = (ans*b)%mod;
        b = (b*b)%mod;
        e>>=1;
    }
    return ans;
}

signed solve(){

    ll n; cin >> n;
    
    ll ans = 0;

    for(ll i = 1; i <= n; i*=10){
        ll last = min(n, i*10-1);
        
        ll cc = 1; last = last - i + 1;

        ll res = (last + cc) % mod * ((last-cc+1) % mod) % mod;
        res = res * expo(2, mod-2) % mod;

        ans += res; ans %= mod;
    }

    cout << ans << endl;

    return 0;
}

