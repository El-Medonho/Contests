signed solve(){
    
    set<int> st;

    int n,m,l; cin >> n;

    vector<int> a(n);

    for(int &i: a) cin >> i;

    cin >> m;
    vector<int> b(m);
    for(int &i: b) cin >> i;

    cin >> l;
    vector<int> c(l);
    for(int &i: c) cin >> i;

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            for(int k = 0; k < l; k++){
                st.insert(a[i]+b[j]+c[k]);
            }
        }
    }

    int q; cin >> q;

    while(q--){
        int x; cin >> x;

        if(st.find(x) != st.end()){
            cout << "Yes\n";
        }
        else cout << "No\n";
    }

    return 0;
}

