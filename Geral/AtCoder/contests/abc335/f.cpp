mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 998244353;

const int sqr = 500;

signed solve(){
    
    int n; cin >> n;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<ll> dp(n, 0); dp[0] = 1;

    vector<vector<ll>> dp2(sqr+5, vector<ll>(sqr+5, 0));

    ll ans = 0;

    for(int i = 0; i < n; i++){
        ll cs = dp[i];

        for(int a = 1; a <= sqr; a++){
            cs = (cs + dp2[a][i%a]) % mod;
        }

        ans = (ans + cs) % mod;

        int cc = arr[i];

        if(arr[i] <= sqr){
            int r = i % cc;
            dp2[cc][r] = (dp2[cc][r] + cs) % mod;
        }

        else{
            for(int j = i+cc; j < n; j+=cc){
                dp[j] = (dp[j] + cs) % mod;
            }
        }
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}