signed solve(){
    
    int n; cin >> n;

    vector<vector<int>> arr(n, vector<int>(n, 0));

    vector<pair<int,int>> dir = {{0,1}, {1,0}, {0,-1}, {-1,0}};

    pair<int,int> cc = {0,0};

    int cd = 0;

    for(int i = 1; i <= n*n-1; i++){
        arr[cc.first][cc.second] = i;

        bool ok = true;

        while(ok){
            ok = false;

            auto[xn, yn] = dir[cd];
            int x = cc.first+xn, y = cc.second+yn;

            if(x < 0 || x >= n || y < 0 || y >= n || arr[x][y]){
                ok = true;
                cd = (cd+1) % 4;
            }
            else{
                cc = {x,y};
            }
        }
    }

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            if(i == n/2 && j == n/2) cout << "T" << ' ';
            else cout << arr[i][j] << ' ';
        }
        cout << endl;
    }


    return 0;
}

