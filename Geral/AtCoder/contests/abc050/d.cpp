signed solve(){

    ll n; cin >> n;

    vector<ll> dp(1e4, 0);
    dp[0] = 1; dp[1] = 1;

    for(int i = 2; i < n; i++){

        set<ll> st;

        for(int j = i; j > -1; j--){
            st.insert(j ^ (i-j));
        }

        dp[i] = st.size();


        cout << i << ' ';

        for(int j = 8; j > -1; j--){
            if(i & (1<<j)) cout << 1;
            else cout << 0;
        }
        cout << ' ';

        cout << st.size() << endl;

        if((i & 1) && (dp[i] != dp[i>>1])) return 0;
    }
    

    return 0;
}

