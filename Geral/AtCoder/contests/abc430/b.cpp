signed solve(){
    
    set<string> st;
    int n,m; cin >> n >> m;
    
    vector<string> arr(n);
    for(string &i: arr) cin >> i;

    for(int i = 0; i +m<=n; i++){
        for(int j = 0; j +m<=n; j++){
            string cc = "";
            for(int a = 0; a < m; a++){
                for(int b = 0; b < m; b++){
                    cc += arr[i+a][j+b];
                }
            }
            st.insert(cc);
        }
    }

    cout << st.size() << endl;


    return 0;
}

