signed solve(){
    
    int n,a,b; cin >> n >> a >> b;
    vector<int> occ_a(n+1, -1), occ_b(n+1, -1);
    int cnt_a = 0, cnt_b = 0;

    string s; cin >> s;

    ll ans = 0;

    for(int i = 0; i < n; i++){
        if(s[i] == 'a'){
            cnt_a++;
            occ_a[cnt_a] = i;
        }
        else {
            cnt_b++;
            occ_b[cnt_b] = i;
        }

        if(cnt_a < a) continue;

        int ind1 = cnt_a-a+1;
        ind1 = occ_a[ind1]+1;


        if(cnt_b < b) {
            ans += ind1;
            continue;
        }
        int ind2 = cnt_b-b+1;
        ind2 = occ_b[ind2]+1;

        if(ind1-ind2 > 0) ans += ind1-ind2;
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}