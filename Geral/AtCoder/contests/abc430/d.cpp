const int mod = 1e9+7;
const ll inf = 1e18+5;

signed solve(){
    
    int n; cin >> n;

    set<ll> st;
    map<ll,ll> to;

    st.insert(0);
    to[0] = inf;
    ll ans = inf;

    for(int i = 0; i < n; i++){
        ll x; cin >> x;
        st.insert(x);

        auto it = st.find(x);

        ll t1 = inf, t2 = inf;

        it++;
        if(it != st.end()){
            ll nx = *it;
            if(abs(x-nx) < abs(to[nx]-nx)){
                ans -= abs(to[nx]-nx);
                to[nx] = x;
                ans += abs(x-nx);

            }
            t1 = nx;
        }

        it = st.find(x);
        if(it != st.begin()){
            it--;
            ll nx = *it;
            if(abs(x-nx) < abs(to[nx]-nx)){
                ans -= abs(to[nx]-nx);
                to[nx] = x;
                ans += abs(x-nx);

            }
            t2 = nx;
        }

        if(abs(x-t1) > abs(x-t2)) swap(t1, t2);
        ans += abs(x-t1);
        to[x] = t1;

        cout << ans << endl;
    }


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}