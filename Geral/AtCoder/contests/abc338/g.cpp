const int mod = 998244353;

signed solve(){
    
    string s; cin >> s;

    int n = s.size();

    ll sc = 0, num = 0;

    ll ans = 0;

    int ini = 0;

    for(int i = 0; i < n; i++){
        char c = s[i];

        if(c == '+'){
            ini = i+1;

        }
        if(c == '*'){
            ini = i+1;


        }

        ll d = c-'0';

        sc = sc * 10 + ini * d;

        sc %= mod;

        num = num * 10 + d;
        
        num %= mod;

        ans = (ans + sc) % mod;

        

    }


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}