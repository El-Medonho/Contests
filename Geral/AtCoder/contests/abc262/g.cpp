void upd(int x, int val, vector<int> &bit){
    for(; x < bit.size(); x += x&-x) bit[x] = max(bit[x], val);
}

int sum(int x, vector<int> &bit){
    int ans = 0;
    for(; x; x -= x&-x) ans = max(bit[x], ans);
    return ans;
}

signed solve(){

    int n; cin >> n;
    
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<vector<pair<int,int>>> seq(1, vector<pair<int,int>>(0));
    int l = -1;

    for(int i: arr){
        if(i < l) seq.push_back(vector<pair<int,int>>(1, make_pair(i, 1)));
        else if(i == l) seq.back().back().second++;
        else seq.back().push_back(make_pair(i, 1));
        l = i;
    }

    if(seq.size() == 1){
        cout << n << endl;
        return 0;
    }

    vector<int> bit(52, 0);

    for(vector<pair<int,int>> carr: seq){
        for(int k = 0; k < carr.size(); k++){
            auto[x, qtt] = carr[k];
            upd(x, sum(x, bit) + qtt, bit);
        }
    }

    int ans = 0;

    for(int i = 1; i <= 51; i++){
        vector<int> nbit(52, 0);

        upd(i, sum(i-1, bit), nbit);

        for(int h = seq.size()-1; h > -1; h--){
            vector<pair<int,int>> carr = seq[h];
            for(int k = carr.size()-1; k > -1; k--){
                auto[x, qtt] = carr[k];
                if(x < i) break;
                upd( x, sum(x, nbit) + qtt, nbit);
            }
        }

        ans = max(ans, sum(50, nbit));
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}