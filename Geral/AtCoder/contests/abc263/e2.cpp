const ll md = 998244353;

void upd(int x, ll val, vector<ll> &bit){
    for(; x < bit.size(); x+=x&-x) (bit[x] = (bit[x] + val) % md + md) % md;
}

ll sum(int x, vector<ll> &bit){
    ll ans = 0;
    for(; x; x-=x&-x) ans = ((ans + bit[x]) % md + md) % md;
    return ans;
}

ll expo(ll b, ll e){
    ll ans = 1;
    while(e){
        if(e&1) ans = (ans*b) % md;
        b = (b*b) % md;
        e>>=1;
    }
    return ans;
}

signed solve(){

    ll n; cin >> n;
    
    vector<ll> A(n-1, 0);
    for(ll &i: A) cin >> i;

    vector<ll> bit(n+5, 0);

    for(int i = 0; i < n-1; i++){
        ll h = A[i] + 1;


        ll div = expo(h-1, md-2);

        ll k = sum(i+1, bit);

        ll y = (expo(h, md-2)) % md;

        y = (y * div) % md;

        y = (y * h) % md;

        ll nn = (y+k+1) % md * expo(h-1, md-2) % md;

        upd(i+2, nn, bit);
        upd(i + h + 1, ((-nn) % md + md) % md, bit);
    }

    cout << sum(n, bit) << endl;
    

    return 0;
}

