pair<ll, vector<ll>> hungarian(const vector<vector<ll>> &a){
	if (a.empty()) return {0, {}};
	int n = a.size() + 1, m = a[0].size() + 1;
	vector<ll> u(n), v(m), p(m), ans(n - 1);
	for(int i = 1; i < n; ++i) {
		p[0] = i; ll j0 = 0; // add "dummy" worker 0
		vector<ll> dist(m, 1e18), pre(m, -1);
		vector<int> done(m + 1, 0);
        vector<int> todone(m + 1, 0);
        for(int i = 0, nn=n/2; i < m+1; i++, nn/=2){
            todone[i] = nn;
        }
        // todone[0] = 1;
		do {
			done[j0]++;;
			ll i0 = p[j0], j1, delta = 1e18;
			for(int j = 1; j < m; ++j) if (done[j] < todone[j]) {
				auto cur = a[i0-1][j-1] - u[i0] - v[j];
				if (cur < dist[j]) dist[j] = cur, pre[j] = j0;
				if (dist[j] < delta) delta = dist[j], j1 = j;
			}
			for(int j = 0; j < m; ++j)
				if (done[j] >= todone[j]) u[p[j]] += delta, v[j] -= delta;
				else dist[j] -= delta;
			j0 = j1;
		} while (p[j0]);
		while (j0) { // update alternating path
			ll j1 = pre[j0]; p[j0] = p[j1], j0 = j1;
		}
	}
	for(int j = 1; j < m; ++j) if (p[j]) ans[p[j]-1] = j-1;
	return {-v[0], ans}; // min cost
}

