signed solve(){

    ll score = 0;

    int n; cin >> n;
    vector<pair<ll,ll>> arr(n, {0,0});
    
    for(int i = 0; i < n; i++){
        string s; cin >> s;
        for(char c: s){
            if(c == 'X') arr[i].first++;
            else{
                int h = c-'0';
                arr[i].second += h;
                score += arr[i].first * h;
            }
        }
    }

    function<bool(pair<ll,ll>, pair<ll,ll>)> cmp;
    
    cmp = [&](pair<ll,ll> a, pair<ll,ll> b){
        if(a.second*b.first != a.first*b.second) return  a.second*b.first < a.first*b.second;
        return a.first < b.first;
    };

    sort(arr.begin(), arr.end(), cmp);

    ll tot = 0;

    for(auto[x, num]: arr){
        score += tot*num;
        tot += x;
    }

    cout << score << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}