signed solve(){

    int n; cin >> n;
    string s[3];
    cin >> s[0] >> s[1] >> s[2];
    int ans = 1e8;

    for(int k = 0; k < 10; k++){
        int cc = 0;
        for(int i = 0; i < n; i++){
            if(s[0][i] == '0'+k) cc += i;
        }
        for(int i = 0; i < n; i++){
            if(s[1][i] == '0'+k) cc += i;
        }
        for(int i = 0; i < n; i++){
            if(s[2][i] == '0'+k) cc += i;
        }
        ans = min(ans, cc);
    }

    cout << (ans == 1e8 ? -1 : ans) << endl;

    return 0;
}

