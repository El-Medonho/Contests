signed solve(){
    
    pair<int,int> l = {-5,-5}, l2 = {-5, 0};

    ll ans = 0;
    string s; cin >> s;

    for(int i = 0; i < s.size(); i++){
        int x = s[i]-'0';
        if(x == l.first){
            l.second++;
            if(x == l2.first+1 && l2.second >= l.second) ans++;
        }
        else{
            swap(l, l2);
            l = {x, 1};
            if(x == l2.first+1 && l2.second >= l.second) ans++;
        }
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}