// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;


signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;
    map<int,int> best;
    map<int, vector<int>> indices;

    for(int i = 0; i < n; i++){
        int x; cin >> x;
        indices[-x].push_back(i);
    }

    int ans = 0;

    for(auto[a, b]: indices) {
        if(best.size() == 0){
            for(int j: b) best[j] = 1;
            ans = 1;
            continue;
        }
        vector<int> bb(b.size(), 0);
        for(int i = 0; i < b.size(); i++){
            auto it = best.upper_bound(b[i]);
            if(it != best.end()){
                bb[i] = max(bb[i], (*it).second+1);
            }
            if(it != best.begin()){
                it--;
                bb[i] = max(bb[i], (*it).second+1);
            }
        }
        for(int i = 0; i < b.size(); i++) {
            best[b[i]] = bb[i];
            ans = max(ans, bb[i]);
        }
    }

    cout << ans << endl;


    return 0;
}