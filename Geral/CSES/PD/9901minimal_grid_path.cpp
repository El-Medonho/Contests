#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;


signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<string> arr(n);
    for(string &i: arr) cin >> i;

    string ans = "";
    pair<int,int> l = {-1, -1}, r = {arr[0][0]-'A', 0};
    vector<int> vis(n*n+1, 0);

    queue<pair<int,int>> qw; qw.emplace(0,0);
    while (!qw.empty())
    {
        auto[x, y] = qw.front(); qw.pop();
        if(x+y > l.second){
            swap(l,r);
            ans += 'A' + l.first;
            r = {26, x+y+1};
        }

        if(arr[x][y] - 'A' > l.first) continue;

        if(x < n-1){
            int cc = arr[x+1][y] - 'A';
            r.first = min(r.first, cc);
            if(!vis[(x+1)*n+y])
                qw.emplace(x+1, y);
            vis[(x+1)*n+y] = 1;
        }

        if(y < n-1){
            int cc = arr[x][y+1] - 'A';
            r.first = min(r.first, cc);
            if(!vis[x*n+y+1])
                qw.emplace(x, y+1);
            vis[x*n+y+1] = 1;
        }
    }
    
    cout << ans << endl;

    return 0;
}