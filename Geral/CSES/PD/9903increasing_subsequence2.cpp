#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const ll mod = 1e9+7;

void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val; bit[ind] %= mod;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind]; ans %= mod;
        ind-=ind&-ind;    
    }
    return ans;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    map<int,int> mp;
    int cnt = 1;

    vector<int> arr(n);
    set<int> st;
    for(int &i: arr) {cin >> i; st.insert(i);}

    for(int x: st) {
        mp[x] = cnt++;
    }

    int N = cnt;

    vector<ll> bit(N+5, 0LL);
    upd(1, 1, bit);

    ll ans = 0;

    for(int j: arr){
        int i = mp[j];
        ll s = sum(i, bit);
        ans += s; ans %= mod;
        upd(i+1, s, bit);
    }

    cout << ans << endl;

    return 0;
}