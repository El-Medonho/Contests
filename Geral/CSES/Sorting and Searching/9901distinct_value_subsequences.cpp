#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const ll mod = 1e9+7;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    ll ans = 1;
    int n; cin >> n;
    map<ll,ll> cnt;

    for(int i = 0; i < n; i++){
        int x; cin >> x; cnt[x]++;
    }

    for(auto[a, b]: cnt){
        ans = ans*(b+1)%mod;
    }

    cout << ans-1<< endl;

    return 0;
}