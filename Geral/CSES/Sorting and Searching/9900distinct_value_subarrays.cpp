#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    ll ans = 0;
    int n; cin >> n;
    map<int,int> mp;
    int l = 0;

    for(int i = 1; i <= n; i++){
        int x; cin >> x;
        l = max(l, mp[x]);
        mp[x] = i;
        ans += i-l;
    }

    cout << ans << endl;

    return 0;
}