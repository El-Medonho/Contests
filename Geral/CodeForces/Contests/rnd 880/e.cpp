typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int solve(){

    mt19937_64 rng(chrono::steady_clock::now().time_since_epoch().count());

    int k; cin >> k;
    int n = (1<<(k+1));

    vector<ll> arr(n);
    
    for(int i = 1; i <= n; i++) cin >> arr[i-1];

    vector<ll> sum(0); sum.push_back(0);

    for(ll i = 1; i <= n; i++) sum.push_back(sum[i-1]^arr[i-1]);

    map<ll, int> mp, mp2;

    for(int i = 0; i <= n; i++) {
        if(mp.find(sum[i]) == mp.end()) mp2[sum[i]] = i;
        mp[sum[i]] = i;
    }

    while(true){
        vector<int> aba(0);
        for(int i = 0; i < 2; i++){
            aba.push_back( uniform_int_distribution<ll> (1, n)(rng) );
        }
        sort(aba.begin(), aba.end());
        ll diff = sum[aba[1]] ^ sum[aba[0]-1];

        for(int i = 0; i < n; i++){
            ll h = sum[i]^diff;

            if(mp.find(h) != mp.end()){
                
                int r = mp[h];
                if(r <= i) continue;

                vector<int> naba(0);
                naba = aba;
                naba.push_back(r); naba.push_back(i+1);
                sort(naba.begin(), naba.end());

                if( (i+1) <= aba[1] && r >= aba[0]){
                    if(naba[0] == naba[1] || naba[3] == naba[2]) continue;
                    cout << naba[0] << ' ' << naba[1]-1 << ' ' << naba[2]+1 << ' ' << naba[3] << endl;
                    return 0;
                }else if(naba[1] != naba[2] && !((i+1) <= aba[1] && r >= aba[0])){
                    cout << naba[0] << ' ' << naba[1] << ' ' << naba[2] << ' ' << naba[3] << endl;
                    return 0;
                }
            }
        }

    }

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}