int solve(){

    int n, x; cin >> n >> x;
    vector<int> arr(n); for(int &i: arr) cin >> i;

    int sum = 0; int l = 0, r = 0;
    int curr = 0;
    int ans = -1;
    while(r < n){
        if(sum < x || (sum == x && arr[r] == 0)) {
            curr++;
            sum += arr[r];
            r++;
        }
        else{
            ans = max(ans, curr);
            curr--;
            sum-=arr[l]; l++;
        } 
    }
    if(sum == x) ans = max(ans, curr);

    ans = (ans == -1) ? -1:n-ans;

    cout << ans << endl;

    return 0;
}

