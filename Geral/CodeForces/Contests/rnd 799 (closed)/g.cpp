int solve(){

    ll n,k; cin >> n >> k;
    int ans = 0; int cc = 0;
    vector<ll> arr(n); for(ll &i : arr) cin >> i;
    for(int i = 1; i < n; i++){
        if(arr[i] > arr[i-1]>>1) cc++;
        else cc = 0;
        if(cc >= k) ans++;
    }

    cout << ans << endl;


    return 0;
}

