signed solve(){

    int n; cin >> n;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a; cin >> a; a--;
        graph[a].push_back(i);
    }

    ll ans = 0;

    string s; cin >> s;

    vector<int> estado(n, -1), ini(n, 0);

    for(int i = 0; i < n; i++){
        if(s[i] == 'P') ini[i] = 1;
        else if(s[i] == 'C') ini[i] = 2;
    }

    function<void(int)> dfs;
    
    dfs = [&](int cc){
        int c[] = {0,0,0};

        for(int j: graph[cc]){
            dfs(j);

            c[estado[j]]++;

            if(ini[cc] != 2) ans += estado[j] == (ini[cc]+1)%2;
        }

        estado[cc] = ini[cc];

        if(ini[cc] == 2){
            int a = 0, b = 1;
            if(c[a] < c[b]) swap(a,b);

            ans += c[b];

            if(c[a] != c[b]){
                estado[cc] = a;
            }
        }

        return;
    };

    dfs(0);

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}