mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    ll n,b,k; cin >> n >> b >> k;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    map<ll,ll> mp;
    for(ll i: arr) mp[i]++;

    int up = *max_element(arr.begin(), arr.end());

    ll ans = 0;

    for(int i = 1; i <= up; i++){
        ll cc = 0;
        for(auto [x, v]: mp){
            ll c = 0;
            ll h = x/i;

            c += h*(i-x%i) * (x-h);
            h++;
            c += h*(x%i) * (x-h);

            c/=2;

            cc += c * v * b;
        }
        ans = max(ans, cc - (i-1)*k);
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}