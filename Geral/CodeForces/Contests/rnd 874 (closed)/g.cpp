int solve(){

    int n; cin >> n;
    
    vector<vector<int>> graph(n), edge(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
        edge[a].push_back(i);
        edge[b].push_back(i);
    }

    queue<int> qw;
    vector<pair<int,int>> par(n, make_pair(-1,-1));

    {
        queue<int> qw2;
        qw2.push(0);
        vector<int> vis(n,0); vis[0] = 1;

        while(!qw2.empty()){
            int cc = qw2.front(); qw2.pop();

            bool h = true;
            for(int i = 0; i < graph[cc].size(); i++){
                int j = graph[cc][i];
                if(vis[j]) continue;
                h = false;
                vis[j] = 1;
                qw2.push(j);
                par[j] = make_pair(cc, edge[cc][i]);
            }
            if(h) qw.push(cc);
        }

    }

    vector<int> vis(n,0), req(n,0);
    vector<int> ans(0);

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();

        if(cc == 0) break;

        int j = par[cc].first;

        vis[j]++;
        if( ( j == 0 && vis[j] == graph[j].size() ) || (j != 0 && vis[j] == graph[j].size()-1 )) qw.push(j);

        int h = req[cc]+1;

        if(h < 3){
            if(req[j] == 1 && h == 1) req[j] = 2;
            else if(req[j]){
                cout << -1 << '\n';
                return 0;
            }
            else req[j] = h;
        }else{
            ans.push_back(par[cc].second);
        }
    }

    if(req[0] != 2){
        cout << -1 << '\n';
        return 0;
    }

    cout << ans.size() << '\n';
    
    for(int i: ans) cout << i << ' ';
    cout << '\n';

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}