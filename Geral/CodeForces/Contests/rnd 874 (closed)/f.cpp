ll expo(ll b, ll e){
    ll res = 1;
    while(e){
        if(e&1) res = (res*b)%MOD;
        b = (b*b)%MOD;
        e>>=1;
    }
    return res;
}

vector<ll> fact(1e6+5, 1);

int solve(){

    ll n,m; cin >> n >> m;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    ll ans = 0; ll curr = 1;

    if(n == 1){
        cout << 1 << endl;
        return 0;
    }

    sort(arr.begin(), arr.end());
    set<ll> st;
    map<ll,ll> vis;
    map<ll,ll> occur;
    for(ll i: arr) {st.insert(i); occur[i]++;}
    int j = 0; int dif = 0;

    for(ll i: st){
        int ind = find(arr.begin(),arr.end(),i) - arr.begin();
        while(j < n && arr[j]-i < m) {
            if(!vis[arr[j]]) {
                dif++;
                curr = (curr*occur[arr[j]])%MOD;
            }
            vis[arr[j]] = 1;
            j++;
        }
        j--;
        // ans += (curr*expo(2,dif-1))%MOD;
        if(dif >= m){
            ll h = (fact[dif-1] * expo(fact[m-1], MOD-2))%MOD;
            h = (h * expo(fact[dif-m], MOD-2))%MOD;
            ans = (ans + ((curr * h)%MOD) )%MOD;
        }
        curr = (curr * expo(occur[i], MOD-2))%MOD;
        dif--;
    }

    cout << ans%MOD << endl;

    return 0;
}

int main(){
    fastio;

    for(int i = 1; i < 1e6; i++) fact[i] = (fact[i-1]*i)%MOD;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}