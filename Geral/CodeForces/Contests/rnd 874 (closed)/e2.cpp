int solve(){

    int n; cin >> n;
    vector<int> corr(n), arr(n, 1);

    for(int i = 0; i < n; i++){
        int x; cin >> x; x--;
        corr[i] = x;
    }

    for(int i = 0; i < n; i++){
        int x = corr[i];
        if(corr[x] != i) arr[x]++;
    }


    vector<int> vis(n,0), ison(n,0);

    int dif1 = 0, dif2 = 0;

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        vis[cc] = 1;
        ison[cc] = 1;

        if(vis[corr[cc]]){
            if(ison[corr[cc]]){
                if(corr[cc] == last)dif1++;
                else dif2++;
            }
            ison[cc] = 0;
            return;
        }

        dfs(corr[cc], cc);
        
        ison[cc] = 0;
        return;
    };

    for(int i = 0; i < n; i++){
        if(arr[i] == 1 && !vis[i]){
            dfs(i,-1);
        }else if(arr[i] == 2 && !vis[i]) dfs(i,-1);
    }

    cout << min(1,dif1)+dif2 << ' ' << dif1+dif2 << '\n';

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}