signed solve(){

    int n; cin >> n;
    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    vector<int> ans(n, 1e9);

    int one = 1, r = n;

    queue<int> qw;
    vector<int> vis(n+1,0);

    for(int i: arr){
        if(i <= n && vis[i] == 0) {qw.push(i); vis[i] = 1;}
    }

    if(!qw.empty()){
        qw.push(qw.front());
        qw.pop();
    }

    fill(vis.begin(), vis.end(), 0);

    for(int i = 0; i < n; i++){
        int x = arr[i];
        if(x <= n && vis[x] == 0){
            ans[i] = qw.front();
            qw.pop();
            vis[x] = 1;
        }
    }

    for(int i = 0; i < n; i++){
        if(ans[i] <= n) continue;
        while(vis[one]){
            one++;
        }
        ans[i] = one++;
    }

    for(int i: ans) cout << i << ' ';
    cout << endl;
    

    return 0;
}

