const int mod = 1e9+7;
const ll inf = 1e18+5;

signed solve(){
    
    int n,m; cin >> n >> m;

    vector<ll> arr(n), v(m);

    for(ll &i: arr) cin >> i;
    for(ll &i: v) cin >> i;

    sort(arr.rbegin(), arr.rend());
    sort(v.begin(), v.end());

    int ind = 0, cn = v[0];
    ll ans = 0;

    for(int i = 0; i < n; i++){
        if(cn == 1){
            ind++;
            if(ind != m) cn = v[ind];
            else cn = 1e9;
            continue;
        }

        ans += arr[i];
        cn--;
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}