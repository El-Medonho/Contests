const int mod = 1e9+7;
const ll inf = 1e18+5;

ll sum(ll a, ll b) {return ((a+b) % mod + mod) % mod;}

signed solve(){
    int n; cin >> n;
    vector<vector<ll>> dp(n+1, vector<ll>(n+1, 0LL)); dp[0][0] = 1;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    for(int cc: arr){
        auto ndp = dp;

        for(int i = 0; i <= n; i++){
            for(int j = 0; j <= cc; j++){
                if(i <= cc) ndp[cc][j] = sum(dp[i][j], ndp[cc][j]);
                else ndp[i][cc] = sum(dp[i][j], ndp[i][cc]);
            }
        }

        dp = ndp;
    }

    ll ans = 0;

    for(int i = 0; i <= n; i++){
        for(int j = 0; j <= n; j++){
            ans = sum(ans, dp[i][j]);
        }
    }

    cout << ans << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}