const int mod = 1e9+7;
const ll inf = 1e18+5;

ll ss(ll a, ll b) {return ((a+b) % mod + mod) % mod;}

void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    ind++;
    while(ind<=bit.size()-1){
        bit[ind]=ss(val, bit[ind]);
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ind++;
    ll ans=0;
    while(ind){
        ans=ss(bit[ind], ans);    
        ind-=ind&-ind;    
    }
    return ans;
}

signed solve(){
    int n; cin >> n;
    vector<vector<ll>> dp(n+1, vector<ll>(n+1, 0LL)), bit(n+1, vector<ll>(n+5, 0LL)); dp[0][0] = 1;
    vector<vector<ll>> otbit = bit;

    upd(0, 1, bit[0]);
    upd(0, 1, otbit[0]);

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    for(int cc: arr){

        vector<ll> dpi(n+1, 0), dpcc(n+1, 0);
        for(int i = cc+1; i <= n; i++){
            ll csum = sum(cc, bit[i]);

            dp[i][cc] = ss(csum, dp[i][cc]);
            dpi[i] = csum;
        }

        for(int j = 0; j < cc; j++){
            ll csum = sum(cc, otbit[j]);

            dp[cc][j] = ss(csum, dp[cc][j]);
            dpcc[j] = csum;
        }

        for(int i = 0; i <= n; i++){
            if(i <= cc) upd(i, dpcc[i], bit[cc]);
            if(i <= cc) upd(cc, dpcc[i], otbit[i]);
            if(i > cc) upd(i, dpi[i], otbit[cc]);
            if(i > cc) upd(cc, dpi[i], bit[i]);
        }
    }

    ll ans = 0;

    for(int i = 0; i <= n; i++){
        for(int j = 0; j <= n; j++){
            ans = ss(ans, dp[i][j]);
        }
    }

    cout << ans << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}