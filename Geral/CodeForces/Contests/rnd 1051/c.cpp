signed solve(){
    
    int n; cin >> n;

    vector<vector<int>> from(n);
    vector<int> ch(n, 0);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        int x,y; cin >> x >> y;

        if(a > b) {swap(a, b); swap(x, y);}
        
        // a > b
        if(x > y){
            from[b].push_back(a);
            ch[a]++;
        }
        else{
            from[a].push_back(b);
            ch[b]++;
        }
    }

    queue<int> qw;
    vector<int> ass(n, 0);
    int cass = 1;

    for(int i = 0; i < n; i++){
        if(ch[i] == 0) qw.push(i);
    }

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();
        ass[cc] = cass++;

        for(int j: from[cc]){
            if(ass[j]) continue;

            ch[j]--;
            if(ch[j] == 0) qw.push(j);
        }
    }

    for(int i = 0; i < n; i++) cout << ass[i] << ' ';
    cout << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}