signed solve(){
    int n; cin >> n;
    vector<int> arr(n);

    for(int i = 0; i < n; i++){
        int x; cin >> x; x--;
        arr[x] = i;
    }

    int l = arr[n-1], r = arr[n-1];

    for(int i = n-2; i > -1; i--){
        int cc = arr[i];
        if(cc != l-1 && cc != r+1){
            cout << "NO\n";
            return 0;
        }
        l = min(l, cc); r = max(r, cc);
    }

    cout << "YES\n";


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}