#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

const int mod = 998244353;

signed main(){
    fastio;

    int w,h; cin >> w >> h;

    vector<ll> arr(1e5+50, 1);

    for(int i = 1; i < 1e5; i++) arr[i] = (arr[i-1] * 2) % mod;

    if(w > h) swap(w,h);

    ll ans = 0;

    if(w == 1) ans = 4LL * arr[h-1] % mod;

    else if(w == 2) ans = 4LL * arr[h] % mod;

    else ans = arr[h+w];

    cout << ans << '\n';

    return 0;
}