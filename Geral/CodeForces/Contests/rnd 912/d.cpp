mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n,q; cin >> n >> q;
    vector<ll> arr(n, 0);
    for(ll &i: arr) cin >> i;

    while(q--){
        ll kk; cin >> kk;
        llg k = kk;
        ll ans = 0;

        vector<ll> sarr = arr;

        for(ll i = 63; i > -1; i--){
            llg tot = 0;
            for(int j = 0; j < n; j++){
                ll cc = sarr[j];
                if(cc & (1LL<<i)) continue;
                cc &= (1LL<<i) - 1;
                tot += (1LL<<i) - cc;
            }

            if(k-tot < 0) continue;

            ans += (1LL<<i);
            k -= tot;

            for(int j = 0; j < n; j++){
                ll cc = sarr[j];
                if(cc & (1LL<<i)) continue;
                cc &= (1LL<<i) - 1;
                sarr[j] += (1LL<<i) - cc;
            }
        }

        cout << ans << endl;
    }
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}