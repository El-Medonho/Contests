signed solve(){

    int n,k; cin >> n >> k;
    vector<int> arr(n);
    for(int i = 0; i < n; i++) cin >> arr[i];

    vector<int> sum(0);

    for(int i = 0; i < n-1; i++){
        sum.push_back( abs(arr[i] - arr[i+1]) );
    }
    sort(sum.begin(), sum.end());

    ll ans = 0;

    for(int i = 0; i < n-1-(k-1); i++) ans += sum[i];

    cout << ans << endl;

    return 0;
}

