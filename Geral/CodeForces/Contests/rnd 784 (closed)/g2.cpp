#include "bits/stdc++.h"
using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

mt19937 rng((int)std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n,m; cin >> n >> m;

    vector<string> grid(n);

    for(int i = 0; i < n; i++){
        cin >> grid[i];
    }

    grid.push_back("");

    for(int i = 0; i < m; i++){
        grid[n] += 'o';
    }

// * .
// * .
// . .
// o o

    for(int j = 0; j < m; j++){
        int pedras = 0;

        for(int i = 0; i <= n; i++){
            if(grid[i][j] == '*') {
                pedras++;
                grid[i][j] = '.';
            }

            else if(grid[i][j] == 'o'){
                for(int k = i-1; pedras > 0 ; k--, pedras--){
                    grid[k][j] = '*';
                }
            }
        }
    }

    // cout << '\n';

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            cout << grid[i][j];
        }
        cout << '\n';
    }

    return 0;
}

signed main()
{
    fastio;

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}