#include "bits/stdc++.h"
using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

mt19937 rng((int)std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n,k; cin >> n >> k;

    vector<int> arr(n);
    for(int i = 0; i < n; i++) cin >> arr[i];

    vector<int> freq(31,0); 

    for(int i = 0; i <= 30; i++){
        for(int x: arr){
            if( (x & (1<<i)) > 0) freq[i]++;
        }
    }

    int ans = 0;

    // & and
    // | or
    // ^ xor
    // ~ not

    for(int i = 30; i >= 0; i--){
        if( k >= n - freq[i] ){
            k -= n - freq[i];
            ans |= (1<<i); 
        }
    }

    cout << ans << '\n';

    return 0;
}

signed main()
{
    fastio;

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}