#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int teste; cin >> teste;

    while(teste--){
        
        int n; cin >> n;

        vector<int> arr(n);

        for(int i = 0; i < n; i++) cin >> arr[i];  //pegando os inputs

        bool possivel = true;

        for(int i = 0; i < n; i+=2){
            if(arr[i] % 2 != arr[0] % 2){
                possivel = false;
                break;
            }
        }

        for(int i = 1; i < n; i+=2){
            if(arr[i] % 2 != arr[1] % 2){
                possivel = false;
                break;
            }        
        }

        if(possivel) cout << "YES\n";
        else cout << "NO\n";
    }

    return 0;
}