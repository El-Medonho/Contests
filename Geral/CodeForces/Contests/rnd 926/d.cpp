const int mod = 998244353;

signed solve(){

    int n; cin >> n;
    vector<vector<int>> graph(n);

    for(int i = 0; i < n-1; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<vector<ll>> dp(n, vector<ll>(3, 0));

    // for(int i = 0; i < n; i++){
    //     dp[i][0] = 1;
    // }

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        vector<vector<ll>> ans(3,vector<ll>(3, 0));
        ans[0][0] = 1;

        for(int j: graph[cc]){
            if(j == last) continue;
                vector<vector<ll>> ans2(3,vector<ll>(3, 0));

            dfs(j, cc);

            for(int i = 0; i < 3; i++){
                for(int k = 0; k + i < 3; k++){
                    int h = max(i,k), hh = i+k;

                    for(int q = 0; q < k+i; q++){
                        ans2[h][hh] += ans[i][q] * dp[j][k] % mod;
                        ans2[h][hh] %= mod; 
                    }

                    for(int q = k+i; q < 3; q++){
                        ans2[h][q] += ans[i][q] * dp[j][k] % mod;
                        ans2[h][q] %= mod; 
                    }
                }
            }

            ans = ans2;
        }

        for(int i = 0; i < 3; i++){
            for(int j = 0; j < 3; j++){
                dp[cc][i] += ans[i][j];
                dp[cc][i] %= mod;
            }
        }

        for(int i = 2; i > 0; i--){
            for(int j = 0; j+1 < 3; j++){
                dp[cc][i] = (ans[i-1][j] + dp[cc][i]) % mod;
            }
        }

        return;
    };

    ll resp = 0;

    dfs(0,0);

    for(int i = 0; i < 3; i++) resp += dp[0][i];

    resp %= mod;

    cout << resp << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}