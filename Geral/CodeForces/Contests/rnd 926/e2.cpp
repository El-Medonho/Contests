signed solve(){

    int n; cin >> n;
    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }
    
    int k; cin >> k;

    vector<pair<int,int>> arr(k);

    for(int i = 0; i < k; i++){
        int a,b; cin >> a >> b; a--; b--;
        arr[i] = {a,b};
    }

    vector<int> lvl(n, 0), sup(n, 0);

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        sup[cc] = last;

        for(int j: graph[cc]){
            if(j == last) continue;

            lvl[j] = lvl[cc]+1;
            dfs(j,cc);
        }

        return;
    };

    dfs(0,0);

    vector<int> nums(n, 0);

    for(int i = 0; i < k; i++){
        auto[a,b] = arr[i];

        if(lvl[a] < lvl[b]) swap(a,b);

        while(lvl[a] > lvl[b]){
            nums[a] += (1<<i);
            a = sup[a];
        }

        while(a != b){
            nums[a] += (1<<i);
            nums[b] += (1<<i);

            a = sup[a];
            b = sup[b];
        }

        nums[a] += (1<<i);
    }

    vector<int> dp((1<<k),1e8); dp[0] = 0;

    set<int> st, st2;

    for(int i = 0; i < n; i++){
        for(int j: graph[i]){
                st2.insert(nums[i] & nums[j]);
        }
    }

    for(int i: st2){
        dp[i] = 1;
        for(int j = 0; j < (1<<k); j++){
            int h = i|j;
            dp[h] = min(dp[h], dp[i]+dp[j]);
        }
    }

    cout << dp[(1<<k)-1] << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}