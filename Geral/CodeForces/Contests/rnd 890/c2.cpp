mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    ll n,k; cin >> n >> k;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    ll ans = *max_element(arr.begin(), arr.end());

    ll l = ans+1, r = 1e10;

    while(l <= r){
        ll mid = (l+r)/2;

        bool ok = false;

        for(int i = 0; i < n-1; i++){
            ll cc = mid, cnt = 0;
            for(int j = i; j < n-1; j++){
                if(cc <= arr[j]){
                    ok = true;
                    break;
                }
                cnt += cc - arr[j];
                if(cnt > k) break;

                cc--;
                if(cc <= arr[j+1]){
                    ok = true;
                    break;
                }
            }
            if(ok) break;
        }

        if(ok){
            l = mid+1;
            ans = max(ans, mid);
        }
        else r = mid-1;
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}