signed solve(){

    int n; cin >> n;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int x; cin >> x; x--;
        graph[x].push_back(i);
    }

    vector<int> sz(n, 1);
    vector<ll> dp(n, 0);

    function<void(int)> dfs;

    dfs = [&](int cc){
        int mx = 0;
        vector<bool> save(n, 0);
        save[0] = 1;

        for(int j: graph[cc]){
            dfs(j);

            for(int i = mx; i > -1; i--){
                if(save[i]) save[i+sz[j]] = 1;
            }
            mx += sz[j];

            dp[cc] += dp[j];

            sz[cc] += sz[j];
        }

        ll sum = 0;

        for(ll i = 1; i <= mx; i++){
            if(save[i]) sum = max(sum, i * (mx-i));
        }

        dp[cc] += sum;

        return;
    };

    dfs(0);

    cout << dp[0] << endl; 
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}