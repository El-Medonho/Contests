typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mxn = 1e6+3;

vector<int> graph[mxn];
int freq[mxn], vis[mxn], ch[mxn], sz[mxn], par[mxn];
ll ans = 0;
int n = 1000000;

signed solve(){

    cin >> n;

    for(int i = 1; i < n; i++){
        int a = 1; 
        cin >> a;
        a--;
        graph[a].push_back(i);
        sz[a]++; par[i] = a;
    }

    queue<int> qww;

    for(int i = 0; i < n; i++){
        if(sz[i] == 0) qww.push(i);
    }

    while(!qww.empty()){
        int cc = qww.front(); qww.pop();

        sz[par[cc]]--;
        if(sz[par[cc]] == 0) qww.push(par[cc]);

        ll tot = 0;

        int H = 0;

        for(int j: graph[cc]){
            ch[cc] += ch[j]+1;
            tot += ch[j]+1;
            H = max(H, ch[j]+1);
        }

        if(tot == 0) continue;

        if(H >= tot/2){
            ans += (tot-H) * H;
            continue;
        }

        for(int j: graph[cc]){
            freq[ch[j]+1]++;
        }


        for(int x = 0; x <= tot/2; x++){
            if(freq[x] > 2){
                freq[x]--;
                if(2*x <= tot/2)freq[2*x] += freq[x]/2;
                freq[x]%=2;
                freq[x]++;
            }
        }

        vis[0] = 1;

        for(int x = 0; x <= tot/2; x++){
            // int h = dp.size();

            while(freq[x]){
                for(int i = tot/2-x; i > -1; i--){
                    vis[x+i] |= vis[i];
                }
                freq[x]--;
            }
        }

        ll cans = 0;

        for(int i = tot/2; i > -1; i--){
            if(!vis[i]) continue;
            vis[i] = 0;

            cans = max(cans, (tot-i) * i);
        }

        ans += cans;
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}