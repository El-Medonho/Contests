#define endl "\n"
#define INF 0x3f3f3f3f
#define INFL 0x3f3f3f3f3f3f3f3f
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1004669333, mod2 = 2147483647;

signed solve(){
    
    int n; cin >> n;

    uniform_int_distribution<int> uid(70, mod-70);

    ll p1 = uid(rng), p2 = uid(rng);

    vector<int> arr(n);
    map<pair<ll,ll>, int> vis;

    for(int &i: arr) cin >> i;

    queue<vector<int>> qw;

    qw.push(arr);

    int ans = 0;

    while(!qw.empty()){
        vector<int> sarr = qw.front(); qw.pop();

        int N = sarr.size();

        pair<ll,ll> cc = {0,0};

        ll pp1 = 1, pp2 = 1;
        for(int i = 0; i < N; i++){
            pp1 = pp1 * p1 % mod;
            pp2 = pp2 * p2 % mod2;
            cc.first = (cc.first + (sarr[i]+1) * pp1 % mod) % mod;
            cc.second = (cc.second + (sarr[i]+1) * pp2 % mod2) % mod2;
        }

        int h = vis[cc];


        for(int i = 0; i < N-1; i++){

            if(sarr[i] == i+1){
                vector<int> nxt(0);

                cc = {0,0};
                pp1 = 1, pp2 = 1;

                for(int j = 0; j < i; j++) nxt.push_back(sarr[j]);
                for(int j = i+2; j < N; j++) nxt.push_back(sarr[j]);

                for(int i = 0; i < nxt.size(); i++){
                    pp1 = pp1 * p1 % mod;
                    pp2 = pp2 * p2 % mod2;
                    cc.first = (cc.first + (nxt[i]+1) * pp1 % mod) % mod;
                    cc.second = (cc.second + (nxt[i]+1) * pp2 % mod2) % mod2;
                }
                if(vis[cc]) continue;

                vis[cc] = h+1;

                qw.push(nxt);

                ans = max(ans, h+1);
            }
        }
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}