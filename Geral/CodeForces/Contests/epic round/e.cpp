mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1e9+7;

const ll mx = 1e16;

signed solve(){
    
    int n; cin >> n;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    vector<vector<int>> graph(n);
    vector<int> par(n, 0), ch(n, 0), hi(n, 1);

    for(int i = 1; i < n; i++){
        int a; cin >> a; a--;
        ch[a]++;
        par[i] = a;
        graph[a].push_back(i);
    }

    queue<int> qw;
    vector<vector<ll>> dp(n, vector<ll>(n+3, 0));

    for(int i = 0; i < n; i++){
        if(ch[i] == 0){
            dp[i][1] = 1e16;
            qw.push(i);
        }
    }

    ll ans = 0;


    while(!qw.empty()){
        int cc = qw.front(); qw.pop();

        ll sum = 0;

        for(int j: graph[cc]){
            sum += arr[j];
            hi[cc] = max(hi[cc], hi[j] + 1);

            for(int k = 1; k <= hi[j]; k++) dp[cc][k+1] = min(mx, dp[cc][k+1] + dp[j][k]);
        }

        if(arr[cc] <= sum){
            dp[cc][1] = min(mx, dp[cc][1] + (sum - arr[cc]));
        }

        else{
            sum = arr[cc] - sum;

            for(int i = 2; i <= hi[cc]; i++){
                if(dp[cc][i] >= sum){
                    dp[cc][i] -= sum;
                    ans += sum * (i-1);
                    sum = 0;
                    break;
                }

                ans += dp[cc][i] * (i-1);
                sum -= dp[cc][i];
                dp[cc][i] = 0;
            }
        }


        if(cc == 0) break;

        ch[par[cc]]--;

        if(ch[par[cc]] == 0) qw.push(par[cc]);
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}