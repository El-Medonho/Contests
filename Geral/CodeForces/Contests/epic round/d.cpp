signed solve(){
    
    int n; cin >> n;

    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    vector<int> freq(n+5, 0);

    for(int i = 0; i < n; i++){
        freq[arr[i]]++;
    }

    arr.clear();

    for(int i = 0; i < n+2; i++){
        if(freq[i] != 0) arr.push_back(freq[i]);
    }

    vector<vector<int>> dp(n, vector<int>(n+5, 0));

    int ans = 0;

    for(int i = 0; i < arr.size(); i++){
        int cap = (i+1)/2;
        if(i == 0) continue;

        int mx = 0;

        int qtt = arr[i];

        for(int j = 1; j <= n; j++){
            dp[i][j] = max(dp[i][j], dp[i-1][j]);
            dp[i][j] = max(dp[i][j-1], dp[i][j]);
            if(j < qtt) continue;
            if(qtt <= (i) - dp[i-1][j-qtt] - (j-qtt)) dp[i][j] = max(dp[i][j], dp[i-1][j-qtt] + 1);

            ans = max(ans, dp[i][j]);
        }
    }

    cout << arr.size() - ans << endl;


    return 0;
}

