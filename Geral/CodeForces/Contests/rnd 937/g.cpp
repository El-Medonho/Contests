#define endl "\n"
#define INF 0x3f3f3f3f
#define INFL 0x3f3f3f3f3f3f3f3f
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1004669333, mod2 = 2147483647;

signed solve(){
    
    int n; cin >> n;

    uniform_int_distribution<int> uid(70, mod-30);

    ll p = uid(rng), p2 = uid(rng);

    vector<pair<ll,ll>> arr(n);

    for(int i = 0; i < n; i++){
        string s; 
        
        cin >> s;

        ll h = 0, h2 = 0;

        ll pp = p, pp2 = p2;

        for(char c: s){
            int cc = c-'a'+1;

            h = (h + cc * pp % mod) % mod;
            pp = (pp * p) % mod;

            h2 = (h2 + cc * pp2 % mod2) % mod2;
            pp2 = (pp2 * p2) % mod2;
        }

        arr[i].first = h * mod + h2;

        cin >> s;

        h = 0, h2 = 0;

        pp = p, pp2 = p2;

        for(char c: s){
            int cc = c-'a'+1;

            h = (h + cc * pp % mod) % mod;
            pp = (pp * p) % mod;

            h2 = (h2 + cc * pp2 % mod2) % mod2;
            pp2 = (pp2 * p2) % mod2;
        }

        arr[i].second = h * mod + h2;
    }

    vector<vector<int>> dp((1<<n), vector<int>(n, 0));

    int ans = 1;

    for(int i = 0; i < n; i++) dp[1<<i][i] = 1;

    for(int i = 1; i < (1<<n); i++){
        int cnt = 0;

        for(int j = 0; j < n; j++){
            if((1<<j) & i) cnt++;
        }

        for(int a = 0; a < n; a++){
            if(!dp[i][a]) continue;
            
            for(int b = 0; b < n; b++){
                if(((1<<b) & i)) continue;

                if(arr[a].first != arr[b].first && arr[a].second != arr[b].second) continue;

                dp[i ^ (1<<b)][b] = 1;
                ans = max(ans, cnt+1);
            }
        }
    }

    cout << n-ans << endl;


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}