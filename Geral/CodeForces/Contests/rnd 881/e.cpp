void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}

int solve(){

    int n,m; cin >> n >> m;

    vector<pair<int,int>> seg(m);
    for(int i = 0; i < m; i++){
        cin >> seg[i].first >> seg[i].second;
    }

    int q; cin >> q;
    vector<int> qu(q);
    for(int i = 0; i < q; i++){
        cin >> qu[i];
    }

    int l = 0, r = q-1;
    int ans = -1;
    
    while(l <= r){
        int md = (l+r)>>1;
        
        vector<ll> arr(n+1,0), bit(n+1,0);
        for(int i = 0; i <= md; i++) {
            int x = qu[i];
            if(arr[x]) continue;
            arr[x] = 1;
            upd(x, 1, bit);
        }

        bool p = false;

        for(int i = 0; i < m; i++){
            int a = seg[i].first, b = seg[i].second;

            int h = sum(b, bit) - sum(a-1, bit);

            if( (b - a + 1)/2 < sum(b, bit) - sum(a-1, bit) ){
                p = true;
                break;
            }
        }

        if(p){
            ans = md+1;
            r = md-1;
        }else{
            l = md+1;
        }
    }

    cout << ans << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}