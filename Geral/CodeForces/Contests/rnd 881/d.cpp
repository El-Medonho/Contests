typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int solve(){

    int n; cin >> n;
    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    function<void(int,int)> dfs;
    vector<ll> depth(n,0);

    dfs = [&](int cc, int last){

        for(int j: graph[cc]){
            if(j == last) continue;

            dfs(j, cc);
            depth[cc] += depth[j];
        }

        if(depth[cc] == 0) depth[cc] = 1;

        return;
    };

    dfs(0,-1);

    int q; cin >> q;
    while(q--){
        int a,b; cin >> a >> b;
        a--; b--;
        cout << depth[a] * depth[b] << endl;
    }

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}