typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int solve(){

    int n; cin >> n;
    // vector<vector<int>> graph(n);
    vector<pair<int,int>> memo(n+1, make_pair(0,0)), best(n+1, make_pair(0,0));

    int nn = 1; memo[0] = {1,0}; best[0] = {1,0};
    for(int i = 0; i < n; i++){
        char c; cin >> c;
        
        if(c == '+'){
            int a,b; cin >> a >> b; a--;
            nn++;
            memo[nn-1] = memo[a];
            best[nn-1] = best[a];
            
            memo[nn-1].first = max(0, memo[nn-1].first+b);
            memo[nn-1].second = min(0, memo[nn-1].second+b);

            best[nn-1] = make_pair( max(best[nn-1].first, memo[nn-1].first), min(best[nn-1].second, memo[nn-1].second) );
        }
        else{
            int u,v,x; cin >> u >> v >> x;
            u--; v--;
            if(x <= best[v].first && x >= best[v].second){
                cout << "YES\n";
            }else{
                cout << "NO\n";
            }
        }
    }

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}