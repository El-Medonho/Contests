typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int solve(){

    int n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    bool h = arr[0] < 0;
    int ans = h;
    for(int i = 1; i < n; i++){
        if(h){
            if(arr[i] > 0) h = false;
        }else{
            if(arr[i] < 0){
                h = true;
                ans++;
            }
        }
    }

    ll sum = 0;

    for(ll i: arr) sum += abs(i);

    cout << sum << ' ' << ans << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}