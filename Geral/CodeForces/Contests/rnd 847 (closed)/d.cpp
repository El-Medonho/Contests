#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int solve(){

    int n; cin >> n;
    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    int ans = 0, curr = 0;

    int last = -1;

    sort(arr.begin(), arr.end());

    vector<int> occur(0);

    for(int i: arr){
        if(i != last) occur.push_back(1);
        else occur[occur.size()-1]++;
        last = i;
    }

    int ind = 0;

    last = -1;

    for(int i: occur){
        if(arr[ind] != last+1){
            curr = i;
            ans += i;
        }else {
            if(i < curr){
                curr = i;
            }else{
                ans += i-curr;
                curr = i;
            }
        }
        last = arr[ind];
        ind += i;

    }

    cout << ans << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}