#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int solve(){

    int n; cin >> n;

    vector<vector<int>> arr(n, vector<int> (n-1));

    for(int i = 0; i < n; i++){
        for(int &j: arr[i]) cin >> j;
    }

    int pn1 = 0, pn2 = 0, pn3 = 0;
    vector<int> ans(0);

    for(int i = 0; i < n; i++){
        if(arr[0][pn1] == arr[1][pn2] && arr[1][pn2] == arr[2][pn3]){
            ans.push_back(arr[0][pn1]);
            pn1++; pn2++; pn3++;
        }
        else if(arr[0][pn1] == arr[1][pn2]){
            ans.push_back(arr[0][pn1]);
            pn1++; pn2++;
        }
        else if(arr[0][pn1] == arr[2][pn3]){
            ans.push_back(arr[0][pn1]);
            pn1++; pn3++;
        }
        else if(arr[1][pn2] == arr[2][pn3]){
            ans.push_back(arr[1][pn2]);
            pn2++; pn3++;
        }
    }

    for(int i: ans) cout << i << " ";
    cout << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}