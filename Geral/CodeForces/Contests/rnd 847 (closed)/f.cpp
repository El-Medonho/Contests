#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int solve(){

    int n, c0; cin >> n >> c0; c0--;
    vector<vector<int>> graph(n);
    vector<int> arr(n);
    arr[0] = c0;

    for(int i = 1; i < n; i++) {cin >> arr[i]; arr[i]--;}

    for(int i = 1; i < n; i++){
        int u,v; cin >> u >> v; u--; v--;
        graph[u].push_back(v);
        graph[v].push_back(u);
    }

    vector<int> dist(n,-1);
    vector<int> vis(n,0);

    queue<pair<int,int>> qw;
    qw.push({c0,0});
    while(!qw.empty()){
        pair<int,int> curr = qw.front(); qw.pop();
        dist[curr.first] = curr.second;

        for(int j: graph[curr.first]){
            if(!vis[j]){
                vis[j] = 1;
                qw.push({j, curr.second+1});
            }
        }
    }
    fill(vis.begin(), vis.end(), 0);

    queue<int> qw2;

    int ans = n;
    for(int i = 1; i < n; i++){
        qw.push({arr[i],0}); vis[arr[i]] = 1;
        while(!qw.empty()){
            pair<int,int> curr = qw.front(); qw.pop();
            qw2.push(curr.first);
            ans = min(ans, curr.second + dist[curr.first]);
            if(dist[curr.first] <= curr.second) continue;
            dist[curr.first] = min(curr.second, dist[curr.first]);

            if(curr.second == ans-1) continue;

            for(int j: graph[curr.first]){
                if(!vis[j]){
                    vis[j] = 1;
                    qw.push({j, curr.second+1});
                }
            }
        }
        while(!qw2.empty()) {
            vis[qw2.front()] = 0;
            qw2.pop();
        }
        cout << ans << ' ';
    }

    cout << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){

        solve();
    }

    return 0;
}