#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int t; cin >> t;
    while(t--){

        int n,s,r; cin >> n >> s >> r;
        int side = s-r;
        r-= n-1;

        vector<int> ans(n-1,1);

        for(int i = 0; i < n-1 && r > 0; i++){
            ans[i] = min(side, r+1);
            r = max(0, r-side+1);
        }

        ans.push_back(side);

        for(int i: ans) cout << i << ' ';
        cout << '\n';

    }

    return 0;
}