mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;

    ld last = -1;

    ll sobra = 0;

    ll op = 0;
    for(int i = 0; i < n; i++){
        int x; cin >> x;

        ld u = log2l(x);

        if(u > last || last - u < ld(1e-15)) {last = u; continue;}
        ll h = last - u;
        h -= 5;
        h = max(h, 0LL);
        op += h;
        u += h;

        while(u < last && last - u > ld(1e-15) && u + 1 < 40){
            u += 1; op++;
        }
        if(u < last && last - u > ld(1e-15)){
            sobra++;
        }

        op += sobra;
        last = u;
    }
    
    cout << op << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}