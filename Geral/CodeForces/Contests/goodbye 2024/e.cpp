signed solve(){
    int n; cin >> n;

    vector<vector<int>> graph(n);

    ll ans = 0;

    vector<int> deg(n, 0);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
        deg[a]++; deg[b]++;
    }

    ll leaf = 0;

    ll nxt = 0;

    for(int i = 0; i < n; i++){
        if(deg[i] == 1) leaf++;
    }

    ans += leaf * (n-leaf);

    vector<ll> dp(n, 0);

    vector<bool> is(n, 0);

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){
        bool connect = false;
        for(int j: graph[cc]){
            if(graph[j].size() == 1) connect = true;
            if(j == last) continue;
            else dfs(j, cc);
            dp[cc] += dp[j];
        }

        if(!connect && graph[cc].size() != 1) dp[cc]++;
        else is[cc] = true;
    };

    dfs(0,0);

    function<void(int,int,ll)> dfs2;

    dfs2 = [&](int cc, int last, ll val){

        if(is[cc]){
            ll cnt = 0;
            for(int j: graph[cc]){
                if(graph[j].size() == 1) continue;
                cnt++;
            }

            if(last != cc && graph[last].size() != 1) cnt--;
            ans += val*cnt;
            if(last != cc && graph[last].size() != 1) cnt++;
            
            
            for(int j: graph[cc]){
                if(j == last) continue;
                if(graph[j].size() != 1) cnt--;
                ans += dp[j] * cnt;
                if(graph[j].size() != 1) cnt++;
            }
        }

        val += dp[cc];

        for(int j: graph[cc]){
            if(j == last) continue;
            val -= dp[j];
            dfs2(j, cc, val);
            val += dp[j];
        }
    };

    dfs2(0,0,0);

    cout << ans << endl;


    


    return 0;
}

