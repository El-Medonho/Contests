int solve(){

    ll n,k; cin >> n >> k;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    vector<int> cnt(31,0);
    for(int i = 0; i < 31; i++){
        for(ll j: arr) if(j & (1<<i)) cnt[i]++;
    }

    int ans = 0;
    for(int i = 30; i > -1; i--){
        if(n-cnt[i] <= k) {k -= n-cnt[i]; ans|=(1<<i);}
    }

    cout << ans << endl;

    return 0;
}

