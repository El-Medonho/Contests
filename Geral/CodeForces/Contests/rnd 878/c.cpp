int solve(){

    ll n,k,q; cin >> n >> k >> q;
    ll ans = 0;
    ll s = 0;
    vector<ll> days(0);
    days.push_back(0);
    for(ll i = 0; i < n; i++){
        ll x; cin >> x;
        s+=x;
        if(x > q) days.push_back(i+1);
    }
    days.push_back(n+1);
    for(int i = 1; i < days.size(); i++){
        if(days[i]-days[i-1]-1 < k) continue;
        ll c = days[i]-days[i-1]-1;
        c+=2-k;
        ans += c*(c-1)/2; 
    }

    cout << ans << endl;

    return 0;
}

