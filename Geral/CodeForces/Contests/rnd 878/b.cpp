int solve(){

    ll n,k; cin >> n >> k;
    ll ans = 0;
    for(ll p = min(k-1, 34LL); p > -1; p--){
        if( (1LL<<p) <= n ){
            ans += (1<<p);
            n-=(1LL<<p);
        }
    }

    cout << ans+1 << endl;

    return 0;
}

