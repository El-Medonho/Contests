signed solve(){
    
    int n; cin >> n;

    int ch = 0;

    string s; cin >> s;

    int l = 0;

    for(char c: s){
        int x = c-'0';

        if(x != l){
            l = x;
            ch++;
        }
    }

    if(ch == 2) ch--;
    else if(ch > 2) ch-=2;

    cout << n+ch << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}