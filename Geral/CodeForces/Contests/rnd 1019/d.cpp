signed solve(){
    
    int n; cin >> n;

    vector<int> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i];
    }

    int mx = *max_element(arr.begin(), arr.end());

    int l = 1, r = n;

    int last = -1;

    vector<int> ans(n, 0);

    for(int m = 1; m<=mx; m++){
        int qtt = 0;
        for(int i = 0; i < n; i++){
            if(arr[i] == m) {
                if(m%2 == 1) ans[i] = r--;
                else ans[i] = l++;

                last = i;
            }
        }

        vector<int> indices(0);

        bool kk = false;

        for(int i = last; i > -1; i--){
            if(arr[i] == m) indices.push_back(i);
            else if(arr[i] > m || arr[i] == -1) {kk = true; break;}
        }

        if(!kk) continue;

        for(int i = 0; i < indices.size()/2; i++) swap(ans[indices[i]], ans[indices[indices.size()-i-1]]);
    }

    for(int i = 0; i < n; i++){
        if(arr[i] == -1) ans[i] = l;
    }

    for(int &i: ans) cout << i << ' ';
    cout << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}