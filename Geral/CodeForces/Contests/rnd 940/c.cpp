const int mod = 1e9+7;

vector<ll> fat (4e5+5, 1);

ll expo(ll b, ll e){
    ll res=1;
    while(e){
        if(e&1) res=res*b%mod;
        b=b*b%mod;
        e >>= 1;
    }
    return res;
}

signed solve(){
    
    ll n,k; cin >> n >> k;

    while(k--){
        int a,b; cin >> a >> b;
        if(a == b) n--;
        else n-=2;
    }

    ll ans = 0;

    // for(int i = 0; i < n; i++){
    //     if(i+1 <= n) dp[i+1] = (dp[i+1] + dp[i] * (n-i) % mod ) % mod;
    //     if(i+2 <= n) dp[i+2] = (dp[i+2] + dp[i] * (n-i) % mod * (n-i-1) % mod ) % mod;
    // }

    // cout << dp[n] << endl;

    for(int i = 0; i <= n; i += 2){
        int j = n-i;

        ll cc = fat[n] * expo( fat[j] * fat[n-j] % mod, mod-2 ) % mod;

        ll nc = fat[i] * expo( fat[i/2] * fat[i/2] % mod, mod-2 ) % mod * fat[i/2] % mod;

        ans = (ans + nc * cc % mod) % mod;
    }

    cout << ans << endl;


    return 0;
}

