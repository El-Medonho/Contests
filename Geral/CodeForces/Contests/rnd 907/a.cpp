mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;
    vector<int> arr(n);

    for(int &i: arr) cin >> i;
    
    int h = 0;

    while(n >= (1LL << h)) h++;
    h--;

    bool ok = true;
    for(int i = 1LL << h; i < n-1; i++){
        if(arr[i] > arr[i+1] ){
            ok = false;
            break;
        }
    }

    for(int i = 0; i < h; i++){
        for(int j = 1LL << i; j < (1LL << (i+1)) - 1; j++){
            if(arr[j] > arr[j+1]){
                ok = false;
                continue;
            }
        }
    }

    if(ok) cout << "Yes\n";
    else cout << "No\n";
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}