void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}

signed solve(){

    int q; cin >> q;
    vector<vector<int>> graph(1);

    vector<vector<int>> querry(q, vector<int>(3, 0));
    int n = 1;

    for(int i = 0; i < q; i++){
        int op; cin >> op;
        querry[i][0] = op;

        if(op == 1){
            int a; cin >> a; a--;
            querry[i][1] = n;

            graph.push_back(vector<int>(0));
            if(n != 0){
                graph[a].push_back(n);
                graph[n].push_back(a);
            }
            n++;
        }
        else{
            ll a,b; cin >> a >> b; a--;
            querry[i][1] = a;
            querry[i][2] = b;
        }
    }

    vector<ll> bit(2*n+5, 0), diff(n, 0);


    int cnt = 1;
    vector<int> in(n, 0), out(n, 0);

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){
        in[cc] = cnt++;

        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j, cc);
        }

        out[cc] = cnt++;
    };

    dfs(0, 0);

    for(int i = 0; i < q; i++){
        int op = querry[i][0];

        if(op == 1){
            int ind = querry[i][1];

            diff[ind] = sum(in[ind], bit);
        }
        else{
            int ind = querry[i][1];
            ll val = querry[i][2];
            upd(in[ind], val, bit);
            upd(out[ind], -val, bit);
        }
    }

    for(int i = 0; i < n; i++){
        cout << sum(in[i], bit) - diff[i] << ' ';
    }
    cout << endl;



    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}