typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int solve(){

    int n; cin >> n;
    vector<ll> a(n), b(n);
    for(ll &i: a) cin >> i;
    for(ll &i: b) cin >> i;

    int sq = sqrt( (ld) (2*n) );

    vector<map<int,int>> cnt(sq+1); vector<int> vis(2*n+1,0);
    for(int i = 0; i < n; i++){
        if(a[i] <= sq) {
            cnt[a[i]][b[i]]++;
        }
    }
    
    ll ans = 0;

    for(int i = 0; i < n; i++){
        if(a[i] * a[i] <= 2*n && a[i] * a[i] == b[i]+b[i]){
            if(vis[a[i]]) continue;
            ans += ( (ll) cnt[a[i]][b[i]] * (ll) (cnt[a[i]][b[i]]-1) ) / 2LL;
            vis[a[i]] = 1;
        }
    }

    for(int k = 1; k <= sq; k++){
        for(int i = 0; i < n; i++){
            if(a[i] * a[i] <= 2*n){
                if(a[i] < k) continue;
                int k2 = a[i];
                if(k2 == k && k*k == b[i] + b[i]){  //a[i] e b[i] iguais
                    continue;
                }
                else if( (k2 == k && k*k-b[i] < b[i] && k*k-b[i] > 0) || (k2 > k && k*a[i]-b[i] > 0)){  // a[i] iguais e b[i] diferentes ou a[i] diferentes
                    if(cnt[k].find(k*a[i]-b[i]) != cnt[k].end()) ans += ( cnt[k][k*a[i]-b[i]] );
                }
            }
            else{       // a[i] > sqrt
                if(a[i]*k <= 2*n && k*a[i]-b[i] > 0){
                    if(cnt[k].find(k*a[i]-b[i]) != cnt[k].end()) ans += ( cnt[k][k*a[i]-b[i]] );
                }
            }
        }
    }

    cout << ans << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}