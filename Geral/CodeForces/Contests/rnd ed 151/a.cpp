template<class F>
ll ternSearch(ll a, ll b, F f) {
    assert(a <= b);
    while (b - a >= 5) {
        ll mid = (a + b) / 2LL;
        if (f(mid) < f(mid+1)) a = mid; // (A)
        else b = mid;
    }
    for(ll i = a+1; i <= b; ++i)
        if (f(a) < f(i)) a = i; // (B)
    return a;
}

int solve(){

    ll n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;


    function<ll(ll)> calc;

    calc = [&](ll k){

        ll sum = 0;

        for(ll i: arr){
            if(sum >= k) sum = max(k, sum+i);
            else sum += i;
        }

        return sum;
    };

    ll mx = 0, hh = 0;
    for(int i = 0; i < n; i++){
        if(arr[i] < 0) mx = max(mx, hh);
        hh+=arr[i];
    }

    ll mn = 1e18; hh = 0;
    for(int i = 0; i < n; i++){
        if(arr[i] < 0) mn = min(mn, hh);
        hh += arr[i];
    }

    if(mn == 1e18){
        cout << 0 << endl;
        return 0;
    }

    mn = max(mn, 0LL);

    ll ans = ternSearch(mn, mx, calc);

    cout << ans << endl;

    return 0;
}

