int solve(){

    ll n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    vector<ll> sum(n+1); sum[0] = 0;
    
    ll ss = 0;
    for(int i = 1; i <= n; i++){
        ss += arr[i-1];
        sum[i] = ss;
    }

    vector<ll> mn(n+1); 
    mn[n] = sum[n];
    for(int i = n-1; i > -1; i--){
        mn[i] = min(mn[i+1], sum[i]);
    }

    pair<ll,ll> ans = {-1e17, -1e17};

    for(int i = 0; i <= n; i++){
        ll diff = sum[i] - mn[i];
        if( diff > ans.second ){
            ans = {sum[i], diff};
        }
    }

    cout << ans.first << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}