mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;
    vector<ll> a(n), b(n);

    for(ll &i: a) cin >> i;
    for(ll &i: b) cin >> i;

    vector<bool> isUp(n, 0);

    vector<ll> diff(n);

    ll base = 0;

    pair<ll,ll> mn = {1e18, 1e18}, mx = {-1e18, -1e18};

    for(int i = 0; i < n; i++){
        diff[i] = abs(a[i] - b[i]);
        isUp[i] = b[i] > a[i];

        base += diff[i];

        ll k = min(a[i], b[i]), k2 = max(a[i], b[i]);

        mx = max(mx, {k, a[i]});
        mn = min(mn, {k2, a[i]});
    }

    ll ans = base;



    for(int j = 0; j < n; j++){
        // auto [k, ai] = mx;
        ll k = mx.first, ai = mx.second;

        ll novo = base + 2*(k - max(a[j], b[j]));
        ans = max(ans, novo);

        k = mn.first, ai = mn.second;
        novo = base + 2*(min(a[j], b[j]) - k);
        ans = max(ans, novo);
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}