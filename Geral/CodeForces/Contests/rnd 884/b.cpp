signed solve(){

    int n; cin >> n;

    vector<int> ans(n,0);

    if(n == 1){
        cout << 1 << endl;
        return 0;
    }
    if(n == 2){
        cout << 2 << ' ' << 1 << endl;
        return 0;
    }

    ans[n/2] = 1;
    ans[0] = 3;
    ans[n-1] = 2;

    int cnt = 4;
    for(int i = 0; i < n; i++){
        if(ans[i] == 0) ans[i] = cnt++;
    }

    for(int i: ans) cout << i << ' ';
    cout << endl;

    return 0;
}

