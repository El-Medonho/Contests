vector<ll> mapa(1e6+500, 0);

signed solve(){

    int n; cin >> n; int nn = n;

    set<int> div; div.insert(1);

    // ll h = 1;
    for(int i = 2; i * i <= n; i++){
        if(mapa[i] == 0 && n % i == 0){
            while(n % i == 0){
                for(auto j =  div.rbegin(); j != div.rend(); j++) div.insert( (*j) *i);
                n/=i;
            }
        }
    }
    if(n != 1){
        for(auto j =  div.rbegin(); j != div.rend(); j++) div.insert( (*j) * n);
        // n/=i;
    }

    n = nn;

    string ans = "a";

    for(int i = 1; i < n; i++){
        vector<int> hh(26,0);
        for(int j: div){
            if( i - j < 0) break;
            hh[ ans[i-j] - 'a' ]++;
        }
        for(int i = 0; i < 26; i++) {
            if(hh[i] == 0){
                ans += 'a'+i;
                break;
            }
            if(i == 25){
                cout << -1 << endl;
                return 0;
            }
        }
    }

    cout << ans << '\n';

    return 0;
}

