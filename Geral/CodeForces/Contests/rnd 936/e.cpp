const int mod = 1e9+7;

vector<ll> fat(1e6, 1);

ll expo(ll b, ll e){
    ll resp = 1;

    while(e){
        if(e&1) resp = (resp * b) % mod;
        b = (b * b) % mod;
        e>>=1;
    }
    return resp;
}

signed solve(){
    
    int n, e, d; cin >> n >> e >> d;

    int m = e;

    vector<int> arr(m);

    for(int &i: arr) cin >> i;

    int md = arr.back();

    ll ans = fat[n-1] * expo(fat[md-1] * fat[n-md] % mod, mod-2) % mod;     // n-1 escolhe md-1

    reverse(arr.begin(), arr.end());

    int last = md;

    for(int i = 1; i < m; i++){
        ans = ans * (fat[last-2] * expo(fat[arr[i]-1] * fat[last-2-(arr[i]-1)] % mod, mod-2) % mod) % mod;      // last - 2 escolhe arr[i] - 1

        ll r = last - arr[i] - 1;

        ans = ans * fat[r] % mod;       //caras da direita podem pegar qrr config

        last = arr[i];
    }

    bool ok = true;

    if(arr.back() != 1){
        ok = false;
    }

    m = d;

    arr.resize(0); arr.resize(m);

    for(int &i: arr) cin >> i;

    if(arr[0] != md || arr.back() != n || !ok){
        cout << 0 << endl;
        return 0;
    }

    last = md;

    for(int i = 0; i < m; i++){
        arr[i] = n+1 - arr[i];
    }

    last= arr[0];

    for(int i = 1; i < m; i++){
        ans = ans * (fat[last-2] * expo(fat[arr[i]-1] * fat[last-2-(arr[i]-1)] % mod, mod-2) % mod) % mod;

        ll r = last - arr[i] - 1;

        ans = ans * fat[r] % mod;       //caras da direita podem pegar qrr config

        last = arr[i];
    }

    cout << (ans) % mod << endl;

    
    return 0;
}

