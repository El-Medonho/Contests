mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1e9+7;

signed solve(){
    
    int n,x; cin >> n >> x;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<vector<int>> pos(32);

    set<pair<int,int>> st;

    bool ok = true;

    for(int i = 0; i < n; i++){
        st.insert({i,i});

        for(int j = 0; j < 31; j++){
            if(arr[i] & (1<<j)){
                pos[j].push_back(i);
            }
        }
    }

    int ans = -1;

    for(int j = 30; j >= 0; j--){
        int m = pos[j].size();

        if(m % 2) {
            if(x & (1<<j)) continue;
            else {
                ok = false;
                break;
            }
        }

        vector<pair<int,int>> del(0), nw(0);

        for(int k = 0; k < m; k+=2){
            int l = pos[j][k], r = pos[j][k+1];

            auto itl = st.upper_bound({l, 1e8}), itr = st.upper_bound({r, 1e8});

            itl--; itr--;

            if(itl != itr){
                l = (*itl).first; r = (*itr).second;

                while(itl != itr){
                    del.push_back((*itr));
                    st.erase(itr);
                    
                    itr = st.upper_bound({pos[j][k+1], 1e8});
                    itr--;
                }

                del.push_back((*(itl)));
                st.erase(itl);

                st.insert({l, r});
                nw.push_back({l, r});
            }
        }

        if(x & (1<<j)){
            ans = max(ans, (int)st.size());

            for(auto h: del) st.insert(h);
            for(auto h: nw) st.erase(h);
        }
    }

    if(ok){
        ans = max(ans, (int)st.size());
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}