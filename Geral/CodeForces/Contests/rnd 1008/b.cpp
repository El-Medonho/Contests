signed solve(){
    
    cout << 0 << endl;

    int a = 0, b = 0;

    int ans; cin >> ans;

    int q = 0;

    for(int i = 0; i < 30; i++){
        if((ans & (1<<i)) == 0) {
            q += (1<<i);
        }
    }

    cout << q << endl;

    int cc; cin >> cc;

    int l = 0, vai = 0;

    for(int i = 0; i < 31; i++){
        // if(!((1<<i) <= cc)) break;
    
        if(i == 0) {
            if(ans & 1) {
                l = 1;
                a += 1;
            }
            continue;
        }

        if(l == 1){
            if(((ans & (1<<i)) && vai == 0) || ((ans & (1<<i)) == 0 && vai == 1)) a += (1<<i);
            else l = 0;
        }

        else{
            if((cc & (1<<i))){
                a += (1<<(i-1));
                b += (1<<(i-1));
                vai = 1;
                if(ans & (1<<i) == 0){
                    a += (1<<i);
                    l = 1;
                }
            }
            else{
                vai = 0;
                if(ans & (1<<i)){
                    a += (1<<i);
                    l = 1;
                }
            }
        }
    }

    cout << '!' << endl;

    int m; cin >> m;

    cout << (m|a) + (m|b) << endl;




    return 0;
}

