signed solve(){

    int n; cin >> n;
    vector<int> arr(n), freq(105, 0), vis(n,0), ans(n, 0);
    vector<vector<int>> pos(105);
    for(int &i: arr){
        cin >> i;
        freq[i]++;
    }

    for(int i = 0; i < n; i++){
        pos[arr[i]].push_back(i);
    }

    bool yes = false;
    int cnt = 0;
    for(int i = 0; i < n; i++){
        if(freq[arr[i]] == 1 || vis[i]) continue;
        // vis[i] = 1;
        int cc = 0;

        for(int j: pos[arr[i]]){
            vis[j] = 1;
            ans[j] = cc;
            if(cnt) cc = 2;
            else cc = 1;
        }
        cnt++;
    }
    
    if(yes || cnt >= 2){
        for(int i: ans) cout << i+1 << ' ';
        cout << endl;
    }
    else cout << -1 << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}