#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int m; cin >> m;

    map<ll,ll> mp, amais, last;

    vector<ll> n(m), l(m), r(m), pre(m, 0);
    vector<vector<ll>> a(m), c(m);
    set<ll> uni;

    ll rsum = 0;

    for(int i = 0; i < m; i++){
        cin >> n[i] >> l[i] >> r[i];
        rsum = min( ll(1e17+50), rsum + r[i]);
        pre[i] = rsum;
        a[i].resize(n[i]);
        c[i].resize(n[i]);

        ll h = 0;

        for(int j = 0; j < n[i]; j++){
            cin >> a[i][j];    
            uni.insert(a[i][j]);
            
            ll cc = a[i][j];

            if(mp.find(cc) == mp.end()){

                mp[cc] += rsum - r[i];

            }else{

                mp[cc] += rsum - r[i] - pre[last[cc]];

            }

            last[cc] = i;
        }
        for(int j = 0; j < n[i]; j++){
            cin >> c[i][j];
            h += c[i][j];
        }

        for(int j = 0; j < n[i]; j++){
            ll temp = h - c[i][j];
            if(temp < l[i]) amais[a[i][j]] += l[i] - temp;

            temp = max(l[i], temp);
            temp = min(temp, r[i]);

            mp[a[i][j]] = min( ll(1e17+50), mp[a[i][j]] + temp);
        }
    }
    
    for(ll i: uni){
        mp[i] += rsum - pre[last[i]];
    }

    ll ans = 1e18;
    
    ll mn = 0, mx = 0;
    for(int i = 0; i < m; i++){
        mn += l[i];
        mx += r[i];
        if(mx > ll(1e17)+2) {
            cout << 0 << endl;
            return 0;
        }
    }

    if(mx - mn + 1 > uni.size()){
        cout << 0 << endl;
        return 0;
    }

    for(ll i = mn; i <= mx; i++){
        if(uni.find(i) == uni.end()){
            cout << 0 << endl;
            return 0;
        }

        ll cc = mp[i], gg = amais[i];

        if(cc < i) gg += i - cc;

        ans = min(ans, gg);
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}