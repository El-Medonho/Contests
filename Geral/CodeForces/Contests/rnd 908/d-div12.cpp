signed solve(){

    int n,m; cin >> n >> m;

    vector<int> a(n);
    for(int &i: a) cin >> i;

    vector<int> s(m), d(m);
    vector<vector<int>> ans(m);

    for(int &i: s) cin >> i;
    for(int &i: d) cin >> i;

    for(int i = 0; i < m; i++){
        ans[i].resize(s[i], -1);
    }

    vector<int> freq(n+5, 0);
    for(int i: a) freq[i]++;

    set<pair<int,int>> st;

    for(int i: a) st.insert( make_pair(freq[i], i) );

    for(int i = 0; i < m; i++){
        queue<int> qw;
        for(int k = 0; k < s[i] ; k++){
            if(qw.size() == d[i]){
                int cc = qw.front(); qw.pop();
                if(freq[cc]) st.insert( make_pair(freq[cc], cc) );
                
            }
            if(st.empty()){
                cout << -1 << endl;
                return 0;
            }

            int cc = ((*st.rbegin()).second);
            st.erase( make_pair(freq[cc], cc) );
            qw.push(cc);

            freq[cc]--;
            ans[i][k] = cc;
        }

        while(!qw.empty()){
            int cc = qw.front(); qw.pop();
            if(freq[cc]) st.insert( make_pair(freq[cc], cc) );
        }
        
    }   

    for(int i = 0; i < m; i++){
        for(int j: ans[i]) cout << j << ' ';
        cout << endl;
    }
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}