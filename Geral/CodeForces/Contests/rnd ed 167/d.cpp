const int mod = 1e9+7;

signed solve(){
    
    int n,m; cin >> n >> m;

    vector<ll> a(n),b(n),c(m);

    for(ll &i: a) cin >> i;
    for(ll &i: b) cin >> i;
    for(ll &i: c) cin >> i;

    vector<pair<ll,ll>> arr(n);

    for(int i = 0; i < n; i++) arr[i] = {a[i],b[i]};

    sort(arr.begin(), arr.end(), [&](pair<ll,ll> a, pair<ll,ll> b) {return (a.first - a.second < b.first - b.second);});

    int mx = 1e9;

    vector<pair<ll,ll>> sarr;

    for(int i = 0; i < n; i++){
        if(arr[i].first >= mx) continue;
        mx = arr[i].first;
        sarr.push_back(arr[i]);
    }

    vector<pair<int,int>> qtt(1e6+5, {-1,-1});

    n = sarr.size();

    int l = 1e6+2;

    for(int i = 0; i < n; i++){
        int k = sarr[i].first;

        for(; k < l; k++) qtt[k] = sarr[i];

        l = sarr[i].first;
    }

    ll ans = 0;

    function<ll(ll)> dfs;

    vector<ll> dp(1e6+10, -1);

    dfs = [&](ll cc){
        if(cc <= 1e6 && qtt[cc].first == -1) return 0LL;
        if(cc <= 1e6 && dp[cc] != -1) return dp[cc];

        pair<ll,ll> h = (cc > 1e6) ? qtt[1e6] : qtt[cc];

        ll diff = cc - h.first, d = h.first - h.second;

        ll x = (diff + d) / d;

        ll hc = cc;

        cc -= d*x;

        if(hc > 1e6) return 2*x+dfs(cc);

        dp[hc] = 0;

        dp[hc] = 2*x;

        return dp[hc] = dp[hc] + dfs(cc);
    };

    for(int i = 0; i < m; i++){
        ll cc = c[i];

        ans += dfs(cc);
    }

    cout << ans << endl;



    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}