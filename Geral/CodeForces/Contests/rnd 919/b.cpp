signed solve(){

    int n,x,k; cin >> n >> k >> x;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    sort(arr.rbegin(), arr.rend());
    for(int i = 0; i < k; i++) arr.push_back(0);

    ll sum = 0;

    for(int i = 0; i < n; i++){
        if(i < x) sum -= arr[i];
        else sum += arr[i];
    }

    ll ans = sum;

    for(int i = x, cnt = 0; cnt < k; i++, cnt++){
        sum -= 2*arr[i];
        sum += arr[i-x];

        ans = max(ans, sum);
    }
    
    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}