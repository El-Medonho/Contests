signed solve(){

    int n; cin >> n;

    vector<ll> a(n);
    for(ll &i: a) cin >> i;

    sort(a.begin(), a.end());

    ll ans = abs(a[0] - a[n-1]);
    ans += abs(a[n-1] - a[1]);

    ans += abs(a[1] - a[n-2]);

    ans += abs(a[n-2] - a[0]);

    cout << ans << endl;
    

    return 0;
}

