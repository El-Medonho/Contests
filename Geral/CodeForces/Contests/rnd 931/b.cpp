const ll lim = 1*3*6*10*15;

vector<ll> dp(lim+30, 1e18);
vector<ll> arr = {1,3,6,10,15};
int n = 5;

signed solve(){


    ll x; cin >> x;

    ll ans = 1e14;

    if(x < 60) {
        cout << dp[x] << endl;
        return 0;
    }
    
    for(int i = 15 * (x/15), cnt = 15; cnt <= lim && i > 0; i-=15, cnt += 15){
        ans = min(ans, dp[x - i] + (i/15));
    }


    // ans += dp[x];

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    dp[0] = 0;

    for(int i = 0; i < lim+1; i++){
        for(int j = n-1; j > -1; j--){
            if(i + arr[j] <= lim+20) dp[i+arr[j]] = min(dp[i+arr[j]], dp[i] + 1);
        }
    }

    // cout << dp[lim] << endl;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}