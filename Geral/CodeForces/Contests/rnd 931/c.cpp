signed solve(){

    int n,m; cin >> n >> m;

    cout << "? 1 1" << endl;

    cout.flush();

    int h; cin >> h;

    int ux = 1, uy = 1;

    if(h > m-1){
        uy = m, ux = h-m+2;
    }
    else uy = h+1;

    cout << "? " << ux << ' ' << uy << endl;
    
    cout.flush();

    int uh; cin >> uh;

    int dx = 1, dy = 1;

    if(h > n-1){
        dx = n, dy = h-n+2;
    }
    else dx = h+1;

    cout << "? " << dx << ' ' << dy << endl;
    
    cout.flush();

    int dh; cin >> dh;

    if(dh % 2 == 0){
        cout << "? " << dx - dh / 2 << ' ' << dy + dh / 2 << endl;

        cout.flush();

        int hh; cin >> hh;

        if(hh == 0){
            cout << "! " << dx - dh / 2 << ' ' << dy + dh / 2 << endl;
        }
        else{
            cout << "! " << ux + uh / 2 << ' ' << uy - uh / 2 << endl;
        }
        cout.flush();
    }
    else{
        cout << "! " << ux + uh / 2 << ' ' << uy - uh / 2 << endl;

        cout.flush();
    }


    return 0;
}

