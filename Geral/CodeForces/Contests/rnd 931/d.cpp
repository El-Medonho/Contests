mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    ll n,m; cin >> n >> m;

    vector<ll> ans(0);

    ans.push_back(n);

    queue<int> qw, geral;

    bool primeiro = false, p =false;

    int g = -1;

    for(int i = 63; i > -1; i--){

        if((1LL << i) & n){
            if(!p){
                p = true;

                primeiro = (1LL << i) & m;
                primeiro = !primeiro;

                g = i;
            }

            else if(!((1LL << i) & m)){
                qw.push(i);
                geral.push(i);
            }

            else{
                if(primeiro && qw.size() == geral.size()) {
                    qw.push(g);
                    geral.push(g);
                    primeiro = false;
                }
            }
        }
        else if((1LL << i) & m){
            if(qw.empty()){
                cout << -1 << endl;
                return 0;
            }

            geral.push(i);
        }
    }

    ll k = 0;

    while(!geral.empty()){
        int h = geral.front(); geral.pop();
        k ^= (1LL<<h);
    }

    if(k > 0) {
        n ^= k;
        ans.push_back(n);
    }


    if(primeiro){
        if(n == (1LL<<g)){
            cout << -1 << endl;
            return 0;
        }

        n ^= (1LL<<g);
        ans.push_back(n);
    }

    if(ans.size() - 1 > 63 || n != m){
        cout << -1 << endl;
        return 0;
    }

    cout << ans.size()-1 << endl;

    for(ll i: ans) cout << i << ' ';
    cout << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}