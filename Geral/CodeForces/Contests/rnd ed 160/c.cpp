signed solve(){

    int q; cin >> q;

    map<int,ll> mp;

    while(q--){
        int op; cin >> op;
        if(op == 1){
            int x; cin >> x;
            mp[x]++;
        }
        else{
            ll w; cin >> w;
            for(int i = 30; i > -1; i--){
                w -= min(mp[i], w / (1LL<<i)) * (1LL<<i);
            }
            if(w) cout << "NO\n";
            else cout << "YES\n";
        }
    }
    

    return 0;
}

