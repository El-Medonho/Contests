const int wpp = 998244353;

void upd(int ind, int x, vector<int> &bit){
    while(ind < bit.size()){
        bit[ind] = max(bit[ind], x);
        ind+=ind&-ind;
    }
}

int query(int ind, vector<int> &bit){
    int ans = -1;
    while(ind){
        ans = max(ans, bit[ind]);
        ind-=ind&-ind;
    }
    return ans;
}

signed solve(){

    int n; cin >> n;
    vector<int> arr(n), bit(n+5, -1);

    ordered_set st;

    for(int &i: arr) {cin >> i; st.insert(i);}

    for(int &i: arr) i = st.order_of_key(i)+1;

    vector<ll> save(n, 0), app(n+1, 0); 
    save[0] = 1; app[0] = 1; app[1] = 2;
    upd(arr[0], 0, bit);

    for(int i = 1; i < n; i++){
        int h = query(arr[i], bit);
        // save[i] = save[i-1]; 
        save[i] += app[i];
        if(h == -1) {;}
        else {save[i] -= app[h]; save[i] += save[h]; app[i] -= save[h];}
        save[i] %= wpp;
        app[i+1] = app[i]; app[i+1] += save[i]; app[i+1] %= wpp;
        // app[i] += save[i]; app[i] %= wpp;
        upd(arr[i], i, bit);
    }
    
    cout << (save[n-1]+wpp)%wpp << endl;

    return 0;
}

