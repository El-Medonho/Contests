mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1e9+7;

signed solve(){
    
    ll n,k; cin >> n >> k;

    vector<ll> a(n), b(n);

    for(ll &i: a) cin >> i;
    for(ll &i: b) cin >> i;

    if(n == 1){
        if(b[0]) a[0] += k;
        cout << a[0] << endl;
        return 0;
    }

    int pos = -1;

    for(int i = 0; i < n; i++){
        if(!b[i]) continue;

        if(pos == -1) pos = i;
        else if(a[pos] < a[i]) pos = i;
    }

    if(pos != -1) a[pos] += k;

    int mx = 0;

    for(int i = 0; i < n; i++){
        if(a[mx] < a[i]) mx = i;
    }

    vector<int> arr(0);

    for(int i = 0; i < n; i++){
        if(mx == i) continue;
        arr.push_back(a[i]);
    }

    sort(arr.begin(), arr.end());

    ll ans = a[mx] + arr[(arr.size()-1)/2];

    if(pos != -1) a[pos] -= k;

    mx = 0;

    for(int i = 0; i < n; i++){
        if(a[mx] < a[i]) mx = i;
    }
    
    vector<pair<int,int>> sarr(0);

    for(int i = 0; i < n; i++){
        if(mx == i) continue;
        sarr.push_back({a[i], i});
    }

    sort(sarr.begin(), sarr.end());

    ll l = 0, r = 1e9+5;

    ll p = 0;

    while(l <= r){
        ll md = (l+r)/2;

        ll kk = k;

        vector<int> na(0);

        for(int i = sarr.size()-1; i > -1; i--){
            if(sarr[i].first >= md || b[sarr[i].second] == 0){
                na.push_back(sarr[i].first);
                continue;
            }
            na.push_back(sarr[i].first);

            if(kk >= md - na.back()){
                kk -= md - na.back();
                na.back() = md;
            }
            else{
                na.back() += kk;
                kk = 0;
            }
        }

        sort(na.begin(), na.end());

        if(na[(na.size()-1)/2] >= md){
            p = md;
            l = md+1;
        }
        else r = md-1;
    }

    ans = max(ans, a[mx] + p);

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}