mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 998244353;

vector<ll> pot(3e5+5, 1);

ll expo(ll b, ll e){
    ll res=1;
    while(e){
        if(e&1) res=res*b%mod;
        b=b*b%mod;
        e >>= 1;
    }
    return res;
}

signed solve(){
    
    int n; cin >> n;

    vector<int> qtt(n+5, 0);

    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    for(int i = n-1; i > -1; i--){
        qtt[i] = qtt[i+1];
        if(arr[i] == 2) qtt[i]++;
    }

    vector<ll> sum(n+1, 0), num(n+1, 0);

    for(int i = 0; i < n; i++){
        if(i) {sum[i] = sum[i-1]; num[i] = num[i-1];}

        if(arr[i] == 1){
            sum[i] += pot[qtt[i]];
            sum[i] %= mod;
            num[i]++;
        }
    }

    ll ans = 0;

    for(int i = 0; i < n; i++){
        if(arr[i] == 3){
            ans += sum[i] * expo(pot[qtt[i]], mod-2) % mod;
            ans -= num[i];
            ans %= mod;
        }
    }

    ans %= mod;

    if(ans < 0) ans += mod;

    cout << ans%mod << endl;


    return 0;
}

signed main(){
    fastio;

    for(int i = 1; i < pot.size(); i++) pot[i] = pot[i-1]*2%mod;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}