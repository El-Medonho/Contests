int solve(){

    string s; cin >> s;
    int n = s.size();

    vector<pair<int,int>> sum(n+2,{0,0});

    for(int i = 1; i <= n; i++){
        sum[i] = sum[i-1];
        if(s[i-1] == '1') sum[i].first++;
        else sum[i].second++;
    } sum[n+1] = sum[n];

    int ans = 1e8;

    for(int i = 0; i <= n; i++){
        int l = i, r = n+1;
        while(l <= r){
            int md = (l+r)>>1;
            int a = sum[md].second - sum[i].second;
            int b = sum[n+1].first - ( sum[md].first - sum[i].first );

            ans = min(ans, max(a,b));

            if(a <= b) l = md+1;
            else r = md-1;
        }
    }

    cout << ans << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}