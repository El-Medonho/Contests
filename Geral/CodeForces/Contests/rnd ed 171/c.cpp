signed solve(){
    
    int n; cin >> n;

    set<int> st;
    set<int> st2;

    ll ans = 0;

    vector<int> mark(n+5, 0);

    for(int i = 0; i < n; i++){

        char c; cin >> c;

        if(c-'0' == 0) {st.insert(i+1); ans += i+1; continue;}

        st2.insert(i+1);
    }

    // reverse(pos.begin(), pos.end());

    int mx = 0;

    for(auto it = st2.rbegin(); it != st2.rend(); it++){
        if(mark[*it]) continue;
        bool did = false;
        if(st.size()){
            auto it2 = st.lower_bound(*it);
            if(it2 != st.begin()){
                it2--;
                st.erase(it2);
                did = true;
            }
        }

        if(did) continue;

        mx = (*st2.upper_bound(mx));
        
        ans += mx;
        mark[mx] = 1;
    }

    cout << ans << endl;


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}