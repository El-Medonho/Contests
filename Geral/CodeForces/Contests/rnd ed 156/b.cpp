signed solve(){

    pair<double, double> obj, l1, l2;

    {
        int xx,yy;
        cin >> xx >> yy;
        obj = {xx, yy};
    }
    
    {
        int xx,yy;
        cin >> xx >> yy;
        l1 = {xx, yy};
    }

    {
        int xx,yy;
        cin >> xx >> yy;
        l2 = {xx, yy};
    }

    double l = 0, r = 1e9;
    int cnt = 500;

    while(cnt--){
        double md = (l+r)/2;

        bool ok = false;

        int s1 = 0, s2 = 0;

        if( (l1.f * l1.f) + (l1.s * l1.s) < md*md ) s1 = 1;

        if( (l2.f * l2.f) + (l2.s * l2.s) < md*md ){
            if(s1) s1 = 3;
            else s1 = 2;
        }

        if( ((l1.f - obj.f) * (l1.f - obj.f)) + ((l1.s - obj.s) * (l1.s - obj.s)) < md*md ) s2 = 1;

        if( ((l2.f - obj.f) * (l2.f - obj.f)) + ((l2.s - obj.s) * (l2.s - obj.s)) < md*md ){
            if(s2) s2 = 3;
            else s2 = 2;
        }        

        if(s1 == s2 || s1 == 3 || s2 == 3) ok = true;

        else if( ((l2.f - l1.f) * (l2.f - l1.f)) + ((l2.s - l1.s) * (l2.s - l1.s)) < 4*md*md ) ok = true;

        if(!s1 || !s2) ok = false;

        if(ok){
            r = md;
        }
        else l = md;
    }

    cout << fixed << setprecision(15) << l << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}