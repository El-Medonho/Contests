const int mod = 1e9+7;

int N = 1e5+5;

vector<vector<int>> dp(N);
vector<int> sum(N, 1e8);

signed solve(){
    
    int k; cin >> k;

    if(k == 0){
        cout << 0 << '\n';
        return 0;
    }

    vector<pair<int,int>> ans(0);

    int cnt = 0;

    for(int sz: dp[k]){
        int cc = cnt++;

        for(int i = 0; i < sz; i++) {
            ans.emplace_back(cc, cnt++);
        }
    }

    cout << ans.size() << '\n';
    for(auto[a,b]: ans){
        cout << a << ' ' << b << '\n';
    }


    return 0;
}

signed main(){
    fastio;

    dp[0].push_back(0);
    sum[0] = 0;

    for(int i = 2; i * (i-1) / 2 <= N; i++){
        int cc = i*(i-1)/2;
        for(int j = 0; j < N; j++){
            if(j+cc >= N || dp[j].empty() || sum[j+cc] <= sum[j] + i || sum[j] + i > 500) continue;

            dp[j+cc] = dp[j];
            dp[j+cc].push_back(i);

            sum[j+cc] = sum[j]+i;
        }
    }

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}