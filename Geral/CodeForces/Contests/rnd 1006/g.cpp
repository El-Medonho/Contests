const int mod = 1e9+7;

vector<vector<int>> div2(3e5+5);

const int lim = 3e5+1;

signed solve(){
    
    ll n,k; cin >> n >> k;

    ll ans = 0;

    if(n == 1){
        cout << k-1 << '\n';
        return 0;
    }

    if(k > n){
        ans = n*((k-n)%mod)%mod;
    }

    int cn = 2;

    for(int i = 2; i*i <= n && i <= k; i++){
        cn = i+1;
        vector<int> cc(0);
        int j = n;
        while(j){
            cc.push_back(j%i);
            j/=i;
        }

        reverse(cc.begin(), cc.end());

        int h = 1, sum = 0;

        for(int j: cc){
            sum += h*j;
            h*=i;
        }

        ans += sum;
        if(ans >= mod) ans -= mod;
    }

    for(int i = 1; i * i < n; i++){
        int cc = n-i;

        for(int h: div2[cc]){
            if(h * h <= n || h > k) continue;

            ans += i*h;
            if(ans >= mod) ans -= mod;
        }
    }

    for(int i = cn; i <= n && i <= k;){
        int d = n/i;
        int l = min(n/d, k); l++;

        ans += d*(l-cn);
        if(ans >= mod) ans -= mod;

        i = l;
    }

    cout << ans << endl;






    return 0;
}

signed main(){
    fastio;

    for(int i = 2; i < lim; i++){
        for(int j = i; j < lim; j+=i){
            div2[j].push_back(i);
        }
    }

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}