mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    ll n,p,l,t; cin >> n >> p >> l >> t;

    ll h = (n-1)/7 + 1;

    ll x = (p + (l + 2LL*t - 1)) / (l + 2LL*t);

    if(2 * x <= h) {
        cout << n - x << endl;
        return 0;
    } 

    x = h/2;

    p -= (l + 2*t) * x;

    if(h&1) {p -= (l+t); x++;}

    if(p <= 0){
        cout << n-x << endl;
        return 0;
    }
    
    ll xx = (p + l - 1) / l;

    cout << n - (x+xx) << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}