signed solve(){
    
    int n,x; cin >> n >> x;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    int jj = -1;

    for(int i = 1; i < n; i++){
        if(arr[i] == x){
            jj = i;
        }
    }

    if(jj != -1){
        cout << 2 << endl;
        cout << 1 << ' ' << jj+1 << endl;

        swap(arr[0], arr[jj]);
    }

    else{
        cout << 1 << endl;
    }

    int l = 1, r = n+1;

    while(r-l > 1){
        int m = (r+l)/2;

        if(arr[m-1] <= x){
            l = m;
        }
        else r = m;
    }

    cout << 1 << ' ' << l << endl;

    return 0;
}

