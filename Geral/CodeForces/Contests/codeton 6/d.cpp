void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]=max(val, bit[ind]);
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans=max(bit[ind], ans);    
        ind-=ind&-ind;    
    }
    return ans;
}

signed solve(){

    int n; cin >> n;
    
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    ll k; cin >> k;

    map<ll,ll> mp;

    for(int i = 0; i < n; i++){
        mp[arr[i]] = i;
    }

    ll mx = -1;
    auto it = mp.begin();

    if(k / (*it).first == 0){
        for(int i = 0; i < n; i++) cout << 0 << ' ';
        cout << endl;
        return 0;
    }

    pair<ll,ll> best = {-1,-1};
    for(auto [x, ind] : mp){
        
        mx = max(mx, k/x);
        if(k / x < mx) break;
        best = max(best, make_pair(ind, x));
    }

    ll h = best.second;

    vector<ll> ans(n+50, 0);

    for(int i = 0; i <= best.first; i++) ans[i] = k/h;

    int ind = best.first + 1;

    int last = best.first;
    while(ind < n){
        ll l = 0, r = k/h;
        int jj = -1, ll = -1;
        while(l <= r){
            int md = (l+r)/2;
            int rr = -1;
            for(int i = ind; i < n; i++){
                if((k - h*md)/(arr[i]) == (k-h*md)/h){
                    rr = i;
                }
            }
            if(rr == -1){
                l = md+1;
            }else{
                r = md-1;
                jj = rr; 
                ll = md;
            }
        }

        if(jj == -1 || ll == 0) break;

        for(int i = last+1; i <= jj; i++) ans[i] = (k-h*ll)/h;
        last = jj;
        ind = jj+1;
        k -= h*ll;
        h = arr[jj];
    }

    for(int i = 0; i < n; i++) cout << ans[i] << ' ';

    cout << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}