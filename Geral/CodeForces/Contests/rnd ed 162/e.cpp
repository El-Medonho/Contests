signed solve(){

    int n; cin >> n;

    vector<int> cor(n);

    int m = 0;

    for(int &i: cor){
        cin >> i;
        i--;
    }
    
    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        swap(a,b);
        graph[a].push_back(b);
    }

    vector<ll> dp(n, 0), pcor(n, 0);

    // for(int i = 0; i < n; i++) pcor[cor[i]] = 1;

    vector<int> save(n, -1);

    function<void(int,int)> dfs;
    ll ans = 0;

    dfs = [&](int cc, int last){

        int hj = save[cor[cc]];
        save[cor[cc]] = cc;

        // ll k = 1;

        for(int j: graph[cc]){
            if(j == last) continue;

            dfs(j, cc);

            ans += (dp[cc]+1)*(dp[cc])/2;
            // ans %= mod;

            // k = k * dp[cc] % mod;;

            dp[cc] = 0;
        }

        // dp[cc] = k;

        if(hj == -1) 
            pcor[cor[cc]] = pcor[cor[cc]]+1;
        else {
            dp[hj] = dp[hj]+1;
        }
        save[cor[cc]] = hj;
    };

    dfs(0,0);

    for(int i = 0; i < n; i++){
        ans += (pcor[i] - 1) * (pcor[i]) / 2;
    }

    cout << ans << endl;

    return 0;
}

