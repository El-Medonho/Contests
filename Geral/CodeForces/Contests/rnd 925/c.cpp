signed solve(){

    int n; cin >> n;

    int l = 1, r = 1;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    for(int i = 1; i < n; i++){
        if(arr[i] == arr[i-1]) l++;
        else break;
    }

    for(int i = n-2; i > -1; i--){
        if(arr[i] == arr[i+1]) r++;
        else break;
    }

    if(l == n){
        cout << 0 << endl;
        return 0;
    }

    if(arr[0] == arr[n-1]) l += r;

    cout << n - max(l,r) << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}