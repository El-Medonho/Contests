signed solve(){

    int n,k; cin >> n >> k;


    vector<vector<int>> quests(k, vector<int>(n, 0));

    for(int i = 0; i < k; i++){
        for(int j = 0; j < n; j++){
            cin >> quests[i][j];
        }
    }

    if(k == 1){
        cout << "YES\n";
        return 0;
    }
    
    // bool ok = false;

    vector<int> ans(0), ans2(0);

    vector<int> arr = quests[0], arr2 = quests[1];

    int a = arr[0], a2 = arr2[0];

    arr.push_back(-1); arr2.push_back(-1);

    int cc = 1;

    for(int i = 1, i2 = 1; i < n || i2 < n; i++, i2++){
        cc = i;
        if(arr[i] != arr2[i2]) {
            if(arr[i] == a2 && arr2[i2] == a){
                ans.push_back(arr[i]); ans.push_back(arr2[i2]);
                ans2.push_back(arr2[i2]); ans2.push_back(arr[i]); 
            }
            else if(arr[i] == a2){
                ans.push_back(a2); ans2.push_back(a2);
                i2--;
            }
            else if(arr2[i2] == a){
                ans.push_back(a); ans2.push_back(a);
                i--;
            }
            else{
                cout << "NO\n";
                return 0;
            }
        }
        else if(arr[i] != -1) {ans.push_back(arr[i]); ans2.push_back(arr[i]);}
    }

    bool ok = true, ok2 = true;

    for(int g = 0; g < k; g++){
        int p1 = 0;

        for(int i = 1; i < n; i++, p1++){
            if(ans[p1] == quests[g][0]) p1++;

            if(ans[p1] != quests[g][i]){
                ok = false;
                break;
            }
        }

        p1 = 0;

        for(int i = 1; i < n; i++, p1++){
            if(ans2[p1] == quests[g][0]) p1++;

            if(ans2[p1] != quests[g][i]){
                ok2 = false;
                break;
            }
        }
    }

    if(ok || ok2){
        cout << "YES\n";
    }

    else cout << "NO\n";

    return 0;
}

