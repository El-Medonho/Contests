const int mod = 1e9+7;

signed solve(){
    
    int n,x; cin >> n >> x;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    set<int> div;

    div.insert(1);

    int ans = 1;

    for(int i: arr){
        for(auto j = --div.end(); j != div.begin(); j--){
            ll h = (ll)i*(*j);
            if(x % h == 0) div.insert(h);
        }

        if(x % i == 0) div.insert(i);

        if(div.find(x) != div.end()){
            div.clear(); div.insert(1); div.insert(i);
            ans++;
        }
    }

    cout << ans << endl;


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}