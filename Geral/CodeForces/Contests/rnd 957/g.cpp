const int mod = 1e9+7;

ll expo(ll b, ll e){
    ll res=1;
    while(e){
        if(e&1) res=res*b%mod;
        b=b*b%mod;
        e >>= 1;
    }
    return res;
}

signed solve(){
    
    int n; cin >> n;

    ll ans = 1;

    vector<ll> dp(n+1, 0), fat(n+1,1);

    for(int i = 1; i <= n; i++) fat[i] = fat[i-1] * i % mod;

    for(int i = 0; i <= n; i++) {
        dp[i] = fat[n] * expo(fat[i] * fat[n-i] % mod, mod-2);
    }

    dp[0] = 0;

    for(int i = 1; i <= n; i++){
        int h = n;
        {
            ll cc = (dp[0] * (h-1) % mod * expo(h, mod-2));

            dp[0] = ((dp[0] - cc) % mod + mod) % mod;  
            ans += i * cc % mod;
            ans %= mod;
        }

        for(int j = 1; j <= n; j++){
            ll cc = (dp[j] * (h-1) % mod * expo(h, mod-2));

            dp[j-1] = (dp[j-1] + cc) % mod;
            dp[j] = ((dp[j] - cc) % mod + mod) % mod;       
        }

    }

    for(int i = 0; i <= n; i++){
        ll k = i+n+1;

        ans += dp[i] * k % mod;
        ans %= mod;
    }

    cout << ans << endl;


    return 0;
}

