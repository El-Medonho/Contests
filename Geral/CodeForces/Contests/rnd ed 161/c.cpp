signed solve(){

    int n; cin >> n;
    vector<ll> r(n+2, 0), l(n+2, 0), arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i];
    }

    r[1] = 1; l[1] = 0;
    for(int i = 1; i < n-1; i++){
        r[i+1] = r[i];
        l[i+1] = l[i];
        if( abs(arr[i+1]-arr[i]) < abs(arr[i]-arr[i-1]) ){
            r[i+1]++;
            l[i+1] += abs(arr[i]-arr[i-1]);
        }
        else{
            l[i+1]++;
            r[i+1] += abs(arr[i+1]-arr[i]);
        }
    }
    r[n] = r[n-1]; l[n] = l[n-1];
    l[n]++;
    r[n+1] = r[n]; l[n+1] = l[n];

    int q; cin >> q;
    while(q--){
        int a,b; cin >> a >> b;

        if(b > a){
            cout << r[b-1]-r[a-1] << endl;
        }
        else{
            cout << l[a]-l[b] << endl;
        }
    }
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}