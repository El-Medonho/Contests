signed solve(){

    ll x; cin >> x;

    vector<int> ans(0);
    ll sum = 0;

    for(int i = 1; i < 200; i++){
        if(x < (1LL << i)) break;
        sum = (1LL << i);
        ans.push_back(i);
    }

    ll k = x-sum;

    for(int i = 63; i > -1; i--){
        if(k & (1LL<<i)) ans.push_back(i+1);
    }

    if(ans.size() > 200){
        cout << -1 << endl;
    }
    else{
        cout << ans.size() << endl;
        for(int i: ans) cout << i << ' ';
        cout << endl;
    }
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}