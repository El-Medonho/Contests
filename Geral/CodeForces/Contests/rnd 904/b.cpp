signed solve(){

    int n; cin >> n;
    string s; cin >> s;

    // reverse(s.begin(), s.end());

    ll sum = 0;
    ll op = 0;

    vector<int> ans(n, 0);

    for(int i = 0; i < n; i++){
        if(s[i] == '1') {
            sum++;
            op += n-1-i;
        }

        

        // op += sum;
        // if(sum > n-1-i) op = -1;
        // cout << op << ' ';
    }

    for(int i = n-1; i > -1; i--){
        if(s[i] == '1') sum--;
        op -= sum;
        ans[i] = (sum > n-i-1) ? -1 : op;

    }


    for(int i: ans) cout << i << ' ';

    cout << endl;
    

    return 0;
}

