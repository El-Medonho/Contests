signed solve(){

    ll n; cin >> n;

    vector<int> arr(n+5, 0), mark(n+5, 0);
    set<int> st;

    ll ans = 0;

    for(int i = 0; i < n; i++){
        int x; cin >> x;
        if(!arr[x]) st.insert(x);
        arr[x]++;
    }

    for(int i: st){
        int tot1 = 0;
        int cc = 0;
        if(mark[i]) continue;
        for(int j = i; j <= n; j += i){
            if(!mark[j]) tot1 += arr[j];
        }
        int tot2 = tot1;
        for(int j = i; j <= n; j += i){
            if(mark[j]){
                cc += tot2;
            }
            else{
                cc += tot1;
                tot1 -= arr[j];
                mark[j] = 1;
            }
        }

        ans += cc;
    }

    ans -= n;

    ans = (n*(n-1)/2) - ans; 

    cout << ans << endl;
    
    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}