signed solve(){

    int n; cin >> n;
    string s; cin >> s;

    reverse(s.begin(), s.end());

    vector<pair<ll,ll>> arr(0);

    vector<ll> ans(n, -1);

    ll p = 0, in = -1;;
    for(int i = 0; i < n; i++){
        if(p && s[i] == '0') {arr.push_back({in, p}); p = 0;}
        else if(s[i] == '1' && !p) {p++; in = i;}
        else if(s[i] == '1') p++;
    }
    if(p) arr.push_back({in, p});

    ll ss = 0, sum = 0;
    for(int i = 0; i < n; i++){
        if( (lower_bound(arr.begin(), arr.end(), make_pair(i + ss, -1LL))) != arr.end()){
            auto [ni, sz] = (*lower_bound(arr.begin(), arr.end(), make_pair(i + ss, 0LL)));
            if(ni == i + ss) ss += sz;
        }
        sum += ss;

        if(ss <= n-1-i) ans[i] = sum;
    }

    for(ll i: ans) cout << i << ' ';
    cout << endl;
    return 0;

    return 0;
}

