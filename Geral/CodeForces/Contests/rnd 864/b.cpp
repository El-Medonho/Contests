int solve(){

    ll n, k; cin >> n >> k; ll op = 0;
    vector<vector<int>> arr(n, vector<int>(n));

    for(int i = 0; i < n; i++){
        for(int &j: arr[i]) cin >> j;
    }

    for(int i = 0; i < n/2; i++){
        for(int j = 0; j < n; j++){
            if(arr[i][j] != arr[n-i-1][n-j-1]) op++;
        }
    }

    if(n & 1){
        for(int i = 0; i < n/2; i++){
            if(arr[n/2][i] != arr[n/2][n-i-1]) op++;
        }
    }

    op = k - op;
    if(op < 0 || (op & 1 && !(n & 1))){
        cout << "no" << endl;
    }
    else cout << "yes" << endl;

    return 0;
}

