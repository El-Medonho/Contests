ll expo(ll b, ll e){
    ll res = 1;
    while(e){
        if(e&1) res = mod(res*b,MOD)%MOD;
        res = res%MOD;
        b = mod(b*b,MOD)%MOD;
        b = b%MOD;
        e>>=1;
    }
    return res;
}

int solve(){

    // vector<ll> pow2(1e6+10,1);
    // for(int i = 1; i <= 1e6; i++) pow2[i] = (pow2[i-1]*2)%MOD;

    ll n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    ll ans = 0;

    for(int i = 1; i < n; i++){
        for(int j = 0; j < i; j++){
            ll df = arr[i]-arr[j];

            ll l = lower_bound(arr.begin(), arr.end(), arr[j]-df) - arr.begin(); l--;
            ll r = lower_bound(arr.begin(), arr.end(), arr[i]+df) - arr.begin();

            ans = mod(ans + expo(2,l+1l + n-r) ,MOD)%MOD;
            ans = (ans)%MOD;
        }
    }

    cout << ans << endl;

    return 0;
}

