#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define f first
#define s second

const int gr = 1e3+5;

typedef long long ll;

signed main(){
    fastio;

    int n; cin >> n;

    vector<pair<int,int>> arr(n);
    vector<vector<int>> mapa(2*1e3+100, vector<int>(2*1e3+100, 0));

    for(pair<int,int> &i: arr) {
        cin >> i.first >> i.second;
        mapa[i.f+gr][i.s+gr]++;
    }


    int ans = 0;

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            int x = arr[i].f-arr[j].f;
            int y = arr[i].s-arr[j].s;

            if(x == 0 && y == 0) continue;

            if(((x)&1) || ((y)&1)) continue;
            x/=2;
            y/=2;

            ans += mapa[arr[i].f-x+gr][arr[i].s-y+gr];
        }
    }

    ans /= 2;

    cout << ans << '\n';

    return 0;
}