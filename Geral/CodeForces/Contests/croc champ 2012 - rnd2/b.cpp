// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define f first
#define s second

const int gr = 1e3+5;

typedef long long ll;

signed main(){
    fastio;

    int n; cin >> n;

    vector<pair<int,int>> arr(n);

    for(pair<int,int> &i: arr) cin >> i.first >> i.second;

    vector<vector<int>> mapa(2*1e3+100, vector<int>(2*1e3+100, 0));

    int ans = 0;

    for(int i = 0; i < n; i++){
        set<pair<int,int>> st;

        for(int j = 0; j < n; j++){
            int x = arr[i].f - arr[j].f;
            int y = arr[i].s - arr[j].s;

            if(x < 0) {
                x = -x; 
                y = -y;
            }

            if(x == 0 && y == 0) continue;

            if(x == 0) y = 1;
            else if(y == 0) x = 1;
            else{
                int h = __gcd(abs(x), abs(y));
                x/=h;
                y/=h;
            }

            mapa[x+gr][y+gr]++;
            st.insert(make_pair(x,y));
        }

        for(pair<int,int> j: st){
            ll k = mapa[j.f+gr][j.s+gr];
            if(k >= 2) ans += (k*(k-1))/2LL;
            mapa[j.f+gr][j.s+gr] = 0;
        }

        st.clear();
    }

    ans /= 3;

    cout << ans << '\n';

    return 0;
}