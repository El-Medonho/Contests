int solve(){

    int n,m; cin >> n >> m;
    vector<vector<int>> arr(n, vector<int>(m));
    for(int i = 0; i < n; i++){
        for(int &j: arr[i]) cin >> j;
    }

    int save = 0;

    for(int i = 0; i < n; i++){
        vector<int> hh = arr[i];
        sort(hh.begin(), hh.end());
        if(hh != arr[i]) save = i;
    }

    vector<int> sarr = arr[save];
    sort(sarr.begin(), sarr.end());

    if(sarr == arr[save]){
        for(int i = 0; i < n; i++){
            sarr = arr[i];
            sort(sarr.begin(), sarr.end());
            if(sarr != arr[i]){
                cout << -1 << endl;
                return 0;
            }
        }
        cout << "1 1" << endl;
        return 0;
    }

    int v1 = -1, v2 = -1;
    for(int i = 0; i < m; i++){
        if(v1 == -1 && arr[save][i] != sarr[i]) v1 = i;
        else if(arr[save][i] != sarr[i] && v2 == -1) v2 = i;
        else if(arr[save][i] != sarr[i]){
            cout << -1 << endl;
            return 0;
        }
    }

        for(int i = 0; i < n; i++){
            swap(arr[i][v1], arr[i][v2]);
            sarr = arr[i];
            sort(sarr.begin(), sarr.end());
            if(sarr != arr[i]){
                cout << -1 << endl;
                return 0;
            }
        }

        cout << v1+1 << ' ' << v2+1 << endl;    

    return 0;
}

