int solve(){

    ll a,b,c; cin >> a >> b >> c;

    vector<ll> arr = {a,b,c};
    sort(arr.begin(), arr.end());

    vector<ll> ans = {-1,-1,-1};

    ans[2] = arr[2];
    ans[1] = arr[2]+arr[1];
    ans[0] = ans[1]+arr[0];

    for(int i = 0; i < 3; i++){
        if(arr[i] == a) cout << ans[i] << ' ';
    }

    for(int i = 0; i < 3; i++){
        if(arr[i] == b) cout << ans[i] << ' ';
    }

    for(int i = 0; i < 3; i++){
        if(arr[i] == c) cout << ans[i] << '\n';
    }

    return 0;
}

