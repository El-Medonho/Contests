typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int solve(){

    int n,k; cin >> n >> k; k = n-k;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    vector<vector<ll>> dp(n, vector<ll>(k+1,1e17)), dp2(n, vector<ll>(k+1,1e17));
    dp[0][0] = arr[0]; dp[0][1] = 0;

    for(int i = 1; i < n; i++){
        for(int j = 0; j <= k; j++){
            dp[i][j] = dp[i-1][j]+arr[i];
            if(j > 0) dp[i][j] = min(dp[i][j], dp[i-1][j-1]);
        }
    }

    dp2[n-1][0] = arr[n-1]; dp2[n-1][1] = 0;
    for(int i = n-2; i > -1; i--){
        for(int j = 0; j <= k; j++){
            dp2[i][j] = dp2[i+1][j]+arr[i];
            if(j > 0) dp2[i][j] = min(dp2[i][j], dp2[i+1][j-1]);
        }
    }

    ll ans = max(dp[n-1][k], dp2[0][k]);

    for(int i = 0; i < n-1; i++){
        for(int j = 0; j <= k; j++){
            ans = min(ans, max(dp[i][j], dp2[i+1][k-j]));
        }
    }

    cout << ans << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}