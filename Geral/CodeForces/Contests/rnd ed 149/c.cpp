int solve(){

    string s; cin >> s;

    int n = s.size();

    string ans = "";

    int in = 0;
    char l = '?';
    for(int i = 0; i < n; i++){
        char c = s[i];
        if(c != '?'){
            for(int j = in; j <= i; j++) ans+=c;
            in = i+1;
            l = c;
        }
    }

    if(ans == ""){
        for(int i = 0; i < n; i++) cout << 0;
        cout << endl;
        return 0;
    }

    if(in != n){
        for(int j = in; j <= n-1; j++) ans+=l;
    }

    cout << ans << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}