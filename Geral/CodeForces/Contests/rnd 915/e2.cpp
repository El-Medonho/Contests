const int wpp = 998244353;

ll expo(ll b, ll e){
    ll ans = 1; b %= wpp; e %= wpp;
    while(e){
        if(e&1) ans = (ans*b)%wpp;
        b = (b*b)%wpp;
        e>>=1;
    }
    return ans;
}

signed solve(){

    ll n; cin >> n;

    if(n == 1){
        cout << 1 << endl;
        return 0;
    }

    vector<ll> nums; nums.push_back(n);
    while(nums.back() != 1) nums.push_back(nums.back()/2);

    ll h = 0, last = 2*(n-1) + 1;
    ll ans = 0;

    ll fie = 0, fid = 0;
    for(ll cc : nums){
        
        ll sum = (((last - cc) % wpp) * ((last % wpp + cc % wpp + 1) % wpp) % wpp) * expo(2, wpp-2);
        sum %= wpp;
        
        // ll low = expo(2, h);

        ll qtt = 1;
        if(cc == n){
            qtt = 1;
        }
        else{
            ll plow = expo(2, h-1);
            qtt = (expo(2, plow) - 1 ) % wpp * ((expo(2, plow) - 1 ) % wpp) % wpp;
            // qtt = expo(2, low);
            // qtt -= 2*expo(2, expo(2, h-1)) % wpp; qtt = (qtt%wpp + wpp) % wpp;
            // qtt++; qtt%=wpp;
        }

        sum = (sum*qtt)%wpp;
        ans += sum; 
        ans %= wpp;

        last = cc - 1;

        if(cc == n){
            ans += n % wpp; ans %= wpp;
            fid = 1;
        }

        else{
            qtt = (expo(2, fid) - 1 ) % wpp * ((expo(2, fie) - 1 ) % wpp) % wpp;
            
            // qtt = expo(2, (fid+fie) % wpp);
            // qtt -= ( expo(2, fie) + expo(2, fid) ) % wpp; qtt = (qtt%wpp + wpp) % wpp;
            // qtt++; qtt%=wpp;

            sum = (cc % wpp) * qtt % wpp;
            ans += sum; ans %= wpp;
        }

        h++;
        if(cc&1) {
            fid = (fid+fie) % wpp;
            fie = expo(2, h);
        }

        else{
            fie = (fid+fie) % wpp;
            fid = expo(2, h-1);
        }
    }

    cout << ans << endl;
    

    return 0;
}

