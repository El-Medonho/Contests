signed solve(){

    int n; cin >> n;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<int> freq(n+1, 0);

    for(int i : arr) freq[i]++;

    int cap = 0;

    for(int i = 0; i < n; i++){
        if(!freq[i]) break;
        cap++;
    }

    if(cap == 0){
        cout << n << endl;
        for(int i = 1; i <= n; i++){
            cout << i << ' ' << i << endl;
        }
        return 0;
    }

    vector<int> f2(n, 0);
    vector<int> ans(0);

    int i = 0, cnt = 0;
    ans.push_back(1);
    
    for(; i < n; i++){
        int x = arr[i];
        freq[x]--;
        if(x < cap && !f2[x]){
            f2[x]++;
            cnt++;
        } 

        if(cnt == cap) break;
    }

    bool ok = true;

    for(int j = 0; j < cap; j++){
        if(!freq[j]) ok = false;
    }

    if(cnt != cap || !ok || i == n-1){
        cout << -1 << endl;
        return 0;
    }

    cout << 2 << endl;

    cout << "1 " << i+1 << endl << i+2 << ' ' << n << endl;

    return 0;
}

