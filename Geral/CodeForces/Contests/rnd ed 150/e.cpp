typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int solve(){

    int n; cin >> n;
    
    vector<ll> mp(n+1,0);
    set<int> st; st.insert(0); st.insert(n+1);
    vector<ll> arr(0); arr.push_back(n);

    priority_queue<pair<int,int>> pq;
    // vector<pair<int,int>> pq;

    for(int i = 1; i <= n; i++){
        int x; cin >> x;
        arr.push_back(x);

        // pq.push_back(make_pair(x, i));
        pq.push(make_pair(x, i));
    }
    arr.push_back(n);

    // sort(pq.begin(), pq.end());

    while(!pq.empty()){
        // int i = pq.back().second; pq.pop_back();
        int i = pq.top().second; pq.pop();

        auto l = st.upper_bound(i); 
        auto r = l; l--;

        mp[(*r)-(*l)-1] += min( arr[(*r)], arr[(*l)] ) - arr[i];
        st.insert(i);
    }

    ll ans = 0;
    ll m; cin >> m;
    for(ll i = n; i > -1; i--){

        if(m >= mp[i]*i) {
            ans += mp[i] * (i-1);
            m -= mp[i]*i;
        }else{
            ans += (m/i) * (i-1);
            m%=i;
            ans += max(m-1,0LL);
            m = 0;
        }

        if(m <= 1) break;
    }

    cout << ans << '\n';

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}