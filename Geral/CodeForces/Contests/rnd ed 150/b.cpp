int solve(){

    vector<int> arr(0);
    int q; cin >> q;

    bool did = false;
    while(q--){
        int x; cin >> x;
        
        if(arr.size() == 0){
            cout << 1;
            arr.push_back(x);
        }
        else if(x >= arr.back() && (did == false || x <= arr[0])){
            arr.push_back(x);
            cout << 1;
        }
        else if(x <= arr[0]){
            if(did){
                cout << 0;
            }
            else{
                did = true;
                arr.push_back(x);
                cout << 1;
            }
        }
        else cout << 0;
    }

    cout << '\n';

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}