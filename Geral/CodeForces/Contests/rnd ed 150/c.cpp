int solve(){

    string s; cin >> s;
    int n = s.size();
    vector<int> arr(0);
    for(char c: s){
        arr.push_back(c-'A');
    }

    vector<ll> pot = {1};
    for(int i = 1; i < 12; i++) pot.push_back(pot[i-1]*10);

    ll ans = -1e14;

    {
        int mx = 0;
        ll cc = 0;
        for(int i = n-1; i > -1; i--){
            mx = max(arr[i], mx);
            ll x = pot[arr[i]];

            if(x < pot[mx]) x = -x;

            cc += x;
        }

        ans = max(ans, cc);

    }

    for(int j = 0; j < 5; j++){
        for(int k = 0; k < 5; k++){

            ll cc = 0; bool did = false;
            ll mx = 0;

            for(int i = n-1; i > -1; i--){
                ll x = arr[i];
                if(x == j && !did) {x = k; did = true; }

                mx = max(mx, x);
                x = pot[x];
                if(x < pot[mx]) x = -x;

                cc += x;

            }

            ans = max(ans, cc);

        }
    }


    for(int k = 0; k < 4; k++){
        vector<int> sarr = arr;

        for(int i = 0; i < n; i++){
            if(sarr[i] == k) {
                sarr[i] = 4;
                break;
            }
        }

        int mx = 0;
        ll cc = 0;
        for(int i = n-1; i > -1; i--){
            mx = max(sarr[i], mx);
            ll x = pot[sarr[i]];

            if(x < pot[mx]) x = -x;

            cc += x;
        }

        ans = max(ans, cc);
    }
    

    cout << ans << '\n';

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}