int solve(){

    int n; cin >> n;

    vector<pair<ll,ll>> arr(n);

    for(int i = 0; i < n; i++){
        ll a,b; cin >> a >> b;
        arr[i] = {a,b};
    }

    sort(arr.begin(), arr.end());

    priority_queue<pair<ll,ll>, vector<pair<ll,ll>>, greater<pair<ll,ll>>> pq, pq2;
    for(int i = 0; i < n; i++){
        pq.push(make_pair(arr[i].first, i));
    }

    set<pair<ll,ll>> st;

    int cc = 0;

    while(!pq.empty() || !pq2.empty()){

        if(pq2.empty() || (!pq.empty() && pq.top().first > pq2.top().first)){
            int i = pq.top().second; pq2.push({arr[i].second, i}); pq.pop();
        }

        else{

            ll x = pq2.top().first; ll i = pq2.top().second; pq2.pop();

            auto it = st.upper_bound({arr[i].first, (ll)1e6}); 

            if(it != st.begin()){
                it--;
                st.erase(it);
                cc+=2;
            }
            else{
                st.insert({arr[i].first, i});
            }

        }

    }

    cout << n-cc << endl;

    return 0;
}

