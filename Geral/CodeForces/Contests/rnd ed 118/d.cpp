mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 998244353;

signed solve(){
    
    int n; cin >> n;

    vector<ll> dp(n+3, 0), dp2(n+3, 0); dp[0] = 1;

    for(int i = 0; i < n; i++){
        int x; cin >> x;

        if(x-1 >= 0) dp2[x-1] = (dp[x-1] + 2*dp2[x-1]) % mod;
        
        dp[x+1] = (dp[x+1] + dp[x+1]) % mod;
        dp2[x+1] = (dp2[x+1] + dp2[x+1]) % mod;
        
        dp[x+1] = (dp[x+1] + dp[x]) % mod;
    }

    ll sum = accumulate(dp.begin(), dp.end(), 0LL);
    sum = accumulate(dp2.begin(), dp2.end(), sum);

    sum--; sum += mod;

    sum %= mod;

    cout << sum << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}