mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1e9+7;

signed solve(){
    
    int n; cin >> n;

    ll h; cin >> h;

    ll l = 0 , r = 1e18;

    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;

    ll ans = 1e18;

    while(l <= r){
        ll md = (l+r)/2;

        ll cc = 0;

        ll last = -1e18-5;

        for(ll i: arr){
            if(last + md > i){
                cc -= last + md - i;
            }
            if(cc >= h) break;

            cc += md;
            last = i;
        }

        if(cc >= h){
            ans = md;
            r = md-1;
        }
        else l = md+1;
    }

    cout << ans << endl;


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}