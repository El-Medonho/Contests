typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int solve(){

    int n; cin >> n;

    vector<ll> arr(n), sum(n+1,0);

    for(ll &i: arr) cin >> i;
    sum[1] = arr[0];
    for(int i = 2; i <= n; i++) sum[i] = sum[i-1] + arr[i-1];

    vector<ll> dp(n+1,-1e9); dp[n] = 0;

    set<ll> st; map<ll,ll> mp; st.insert(sum[n]); mp[sum[n]] = n-1;

    // if(sum[n] > 0){
    //     cout << n << endl;
    //     return 0;
    // }

    for(ll i = n-1; i > -1; i--){
        ll x = sum[i];
        dp[i] = dp[i+1] + ( (arr[i] == 0) ? 0 : (arr[i] > 0 ? 1 : -1) );
        if(st.upper_bound(x) != st.end()){
            int k = (*st.upper_bound(x));
            dp[i] = max(dp[i], mp[k] - i + 1LL + dp[mp[k]+1]);
        }
        if(st.lower_bound(x) == st.end()){
            st.insert(x);
            mp[x] = i-1;
        }else{
            dp[i] = max(dp[i], dp[mp[x]+1]);
        }
    }

    cout << dp[0] << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}