int solve(){

    int n; cin >> n; 

    int ans = (2*(n/3)) + 1 - ( (n%3 == 0) ? 1:0 );

    int x = 1, y = 1;

    cout << ans << endl;

    int i = 0; int h = 1;
    for(;i < n; i+=3){
        if(x == y){
            cout << x << ' ' << y << endl;
        } else{
            cout << x << ' ' << y << endl;
            cout << y << ' ' << x << endl;
        }

        x+=h; y+= h+1; 
    }
    if(i == n){
        cout << x << ' ' << x << endl;
    }

    return 0;
}

