int solve(){

    int n; cin >> n;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    ll ans = 1e17;
    for(int i = 0; i < n; i++){
        ll op = 0; ll last = 0;
        for(int j = i-1; j > -1; j--){
            ll k = (last + arr[j])/ arr[j];
            op += k;
            last = arr[j] * k;
        } last = 0;
        for(int j = i+1; j < n; j++){
            ll k = (last + arr[j])/ arr[j];
            op += k;
            last = arr[j] * k;
        }
        ans = min(ans,op);
    }

    cout << ans << endl;

    return 0;
}

