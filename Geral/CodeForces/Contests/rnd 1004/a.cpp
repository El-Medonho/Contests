signed solve(){
    
    int n; cin >> n;

    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    vector<int> sarr = arr;

    sort(sarr.begin(), sarr.end());

    vector<int> freq(n+1, 0);

    for(int i: sarr){
        freq[i]++;
    }

    for(int i = 1; i <= n; i++){
        if(freq[i] == 0){
            cout << "? " << i << ' ' << (i)%n+1 << '\n';
            cout.flush();

            int res; cin >> res;

            if(res == 0){
                cout << "! A\n";
            }
            else cout << "! B\n";
            cout.flush();

            return 0;
        }
    }

    int n0 = -1, n1 = -1;

    for(int i = 0; i < n; i++){
        if(arr[i] == 1) n0 = i+1;
        else if(arr[i] == n) n1 = i+1;
    }

    cout << "? " << n0 << ' ' << n1 << '\n';

    cout.flush();

    int c1; cin >> c1;

    cout << "? " << n1 << ' ' << n0 << '\n';

    cout.flush();

    int c2; cin >> c2;

    if(c1 == c2 && c1 >= n-1){
        cout << "! B\n";
    }
    else cout << "! A\n";

    cout.flush();


    return 0;
}

