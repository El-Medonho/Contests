mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int inf = 1e9;

signed solve(){

    int n,m; cin >> n >> m;

    vector<vector<pair<int,int>>> graph(n);

    vector<map<int,int>> vis(n);

    for(int i = 0; i < m; i++){
        int a,b,c; cin >> a >> b >> c; a--; b--;

        vis[a][c] = inf; vis[b][c] = inf;

        graph[a].push_back({b,c});
        graph[b].push_back({a,c});
    } 

    int b,e; cin >> b >> e; b--; e--;

    priority_queue<pair<int,pair<int,int>>, vector<pair<int,pair<int,int>>>, greater<pair<int,pair<int,int>>>> qw;

    vector<int> dist(n, inf);
    dist[b] = 0;

    qw.push({0,{b, -1}});

    while(!qw.empty()){
        auto [d, h] = qw.top(); qw.pop();
        auto [cc, c] = h;

        if(d != vis[cc][c]) continue;

        for(auto[j, nc] : graph[cc]){
            int nd = d;
            if(nc != c) nd++;
            if(vis[j][nc] <= nd || dist[j] < nd) continue;

            qw.push({nd,{j,nc}});
            vis[j][nc] = nd;
            dist[j] = min(dist[j], nd);
        }
    }

    cout << dist[e] << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}