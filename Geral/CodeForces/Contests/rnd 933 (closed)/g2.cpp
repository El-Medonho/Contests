signed solve(){
    
    int n,m; cin >> n >> m;

    vector<vector<pair<int,int>>> graph(n);
    vector<vector<int>> cores(m);

    int cnt = 0;
    map<int,int> mp;

    for(int i = 0; i < m; i++){
        int a,b,c; cin >> a >> b >> c; a--; b--;

        if(mp.find(c) == mp.end()) mp[c] = cnt++;

        graph[a].push_back({b, mp[c]});
        graph[b].push_back({a, mp[c]});

        cores[mp[c]].push_back(a);
        cores[mp[c]].push_back(b);
    }

    queue<int> qw, qc;

    vector<int> vis(n, 0), visc(cnt, 0), dist(cnt, 0);

    int b,e; cin >> b >> e; b--; e--;

    if(b == e){
        cout << 0 << endl;
        return 0;
    }

    for(auto [j,c] : graph[b]){
        if(visc[c]) continue;

        visc[c] = 1;
        qc.push(c);
    }

    int h = 0;

    while(!qc.empty()){
        while(!qc.empty()){
            int c = qc.front(); qc.pop();
            for(int j: cores[c]){
                if(!vis[j]) {
                    qw.push(j);
                    vis[j] = 1;
                }
            }
        }

        while(!qw.empty()){
            int cc = qw.front(); qw.pop();

            for(auto[j,c] : graph[cc]){
                if(visc[c]) continue;

                qc.push(c);
                visc[c] = 1;
                dist[c] = h+1;
            }
        }

        h++;
    }

    int ans = 1e8;

    for(auto[j, c]: graph[e]){
        ans = min(ans, dist[c]);
    }

    cout << ans+1 << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}