mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;

    int a,b; cin >> a >> b;

    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    pair<ll,int> mn = {-1, -1};

    for(int i = 1; i < n; i++){
        ll cc = arr[i] - arr[i-1];

        if(cc > mn.first) mn = {cc, i};
    }

    vector<ll> m(a), f(b);

    for(ll &i: m) cin >> i;
    for(ll &i: f) cin >> i;

    sort(m.begin(), m.end());
    sort(f.begin(), f.end());

    set<ll> st;

    for(ll i: f) st.insert(i);

    f.clear();

    for(ll i: st) f.push_back(i);

    b = f.size();

    ll nw = mn.first;

    for(int i = 0; i < a; i++){

        int l = 0, r = b-1;

        while (r-l >= 5) {
            int mid = (r + l) / 2;

            ll fmid = (m[i] + f[mid]), fmid1 = (m[i] + f[mid+1]);

            fmid = max(arr[mn.second] - fmid, fmid - arr[mn.second-1]);
            fmid1 = max(arr[mn.second] - fmid1, fmid1 - arr[mn.second-1]);

            if (fmid > fmid+1) l = mid; // (A)
            else r = mid+1;
        }

        ll fl = m[i] + f[l];
        fl = max(arr[mn.second] - fl, fl - arr[mn.second-1]);

        for(int j = l+1; j <= r; ++j){
            ll fi = m[i] + f[j];
            fi = max(arr[mn.second] - fi, fi - arr[mn.second-1]);
            if (fl > fi) fl = fi; // (B)
        }

        nw = min(nw, fl);
    }

    ll ans = nw;

    for(int i = 1; i < n; i++){
        if(i == mn.second) continue;
        ans = max(ans, arr[i]-arr[i-1]);
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}