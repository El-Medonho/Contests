#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<ll,null_type, less<ll>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<ll,null_type, less_equal<ll>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((ll) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    ll n; cin >> n;

    ll a,b; cin >> a >> b;

    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    pair<ll,ll> mn = {-1, -1};

    for(ll i = 1; i < n; i++){
        ll cc = arr[i] - arr[i-1];

        if(cc > mn.first) mn = {cc, i};
    }

    ll tg = (arr[mn.second] + arr[mn.second-1]) / 2;

    vector<ll> m(a), f(b);

    for(ll &i: m) cin >> i;
    for(ll &i: f) cin >> i;

    sort(m.begin(), m.end());
    sort(f.begin(), f.end());

    ll nw = mn.first;

    for(ll i = 0; i < a; i++){
        ll x = tg - m[i];

        auto it = lower_bound(f.begin(), f.end(), x);

        if(it != f.end()){
            x = *it;

            ll cc = m[i] + x;

            nw = min(nw, max(cc - arr[mn.second-1], arr[mn.second] - cc));
        }
        if(it != f.begin()){
            it--;
            x = *it;

            ll cc = m[i] + x;

            nw = min(nw, max(cc - arr[mn.second-1], arr[mn.second] - cc));
        }
    }

    ll ans = nw;

    for(ll i = 1; i < n; i++){
        if(i == mn.second) continue;
        ans = max(ans, arr[i]-arr[i-1]);
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    ll t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}