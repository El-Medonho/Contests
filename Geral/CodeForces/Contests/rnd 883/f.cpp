signed solve(){

    int n; cin >> n;
    vector<int> arr(n);

    vector<int> mapa(10,0);

    for(int &i: arr){
        cin >> i;
        mapa[i]++;
    }

    int gg = 0;

    while(!gg){
        vector<int> nmapa = mapa;

        cout << "- 0\n";

        cout.flush();

        for(int &i: arr){
            int x; cin >> i; x = i;
            nmapa[x]--;
        }

        for(int i = 0; i < 10; i++){
            if(nmapa[i] == -1){
                gg = i;
                break;
            }
        }
    }

    vector<int> tira = {};
    for(int i = 0; i < n; i++){
        if(arr[i] != gg) tira.push_back(i);
    }

    cout << "- ";

    cout << tira.size() << ' ';

    for(int i: tira) cout << i+1 << ' ';
    cout << '\n';

    cout.flush();

    while(gg > 0){

        int kk = 0;

        for(int i = 1; i <= n-tira.size(); i++){
            int x; cin >> x;
            if(x != gg){
                kk = i;
                // cout << "! ";
                // cout << i << '\n';
                // cout.flush();
                // return 0;
            }
        }

        if(kk) {
            cout << "! ";
            cout << kk << '\n';
            cout.flush();
            return 0;
        }

        cout << "- 0\n";

        cout.flush();
    }


    return 0;
}

