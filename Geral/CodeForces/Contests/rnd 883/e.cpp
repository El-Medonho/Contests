signed solve(){

    ll n; cin >> n;

    for(ll i = 60; i >= 2; i--){

        ll l = 2, r = 1e9;

        while(l <= r){

            ll ans = 1;
            ll md = (l+r)>>1;

            ll h = md;

            bool pass = true;

            for(int j = 0; j < i; j++){
                if(!pass){
                    ans = -1;
                    break;
                }
                ans += h;
                if(h < 0 || h > n || ans > n || ans < 0){
                    ans = -1;
                    break;
                } 
                if(h > ll(1e18+5)/md) pass = false;
                h *= md;
            }

            if(ans == n){
                cout << "YES\n";
                return 0;
            }
            if(ans == -1 || ans > n){
                r = md-1;
            }else{
                l = md+1;
            }
        }
    }

    cout << "NO\n";

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}