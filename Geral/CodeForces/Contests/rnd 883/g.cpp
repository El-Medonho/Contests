int conv(string s){
    int n = s.size();

    int ans = 0;
    for(int i = 0; i < n; i++){
        ans ^= (s[n-i-1]-'0')<<i;
    }

    return ans;
}

signed solve(){

    int n,m; cin >> n >> m;
    string b; cin >> b;

    int base = conv(b);

    vector<int> med(m), get(m);
    vector<int> days(m);
    for(int i = 0; i < m; i++){
        cin >> days[i] >> b;
        med[i] = conv(b);
        cin >> b;
        get[i] = conv(b);
    }

    priority_queue<pair<int,int>, vector<pair<int,int>>, greater<pair<int,int>>> pq;
    vector<int> vis((1<<n), 0), best((1<<n), 1e9+5);
    pq.push(make_pair(0,base));
    best[base] = 0;

    while(!pq.empty()){
        int x = pq.top().first, mask = pq.top().second; pq.pop();

        if(vis[mask]) continue;
        if(mask == 0) break;

        vis[mask] = 1;

        for(int i = 0; i < m; i++){

            int novo = mask;

            for(int j = 0; j < n; j++){
                if(mask&(1<<j) && med[i]&(1<<j)) novo ^= (1<<j);
            }

            novo |= get[i];

            if(vis[novo] || best[novo] <= x + days[i]) continue;

            best[novo] = x + days[i];

            pq.push(make_pair(x + days[i], novo));
        }
    }

    if(best[0] > 1e9) cout << -1 << endl;
    else cout << best[0] << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}