#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

typedef long long ll;

int solve(){

    ll a; cin >> a;

    ll ans = a;

    ll p1 = a,p2 = a;

    ll a2,a3; cin >> a2 >> a3;

    ans += 2*min(a2,a3);

    ll ah = labs(a2-a3);

    ll a4; cin >> a4;

    if(a == 0){
        cout << 1 << '\n';
        return 0;
    }

    if(ah > a){
        ans += a+1;
    }
    else{
        ans += ah;
        a -= ah;

        ans += min(a4, a+1);
    }

    cout << ans << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;

    while(t--){
        solve();
    }

    return 0;
}