#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int t; cin >> t;
    while(t--){

        int n; cin >> n;
        vector<int> arr(n);
        for(int &i: arr) cin >> i;

        sort(arr.begin(), arr.end());

        int ans = 0, last = 0;
        for(int i = 0; i < n && arr[i] == 1; i++){
            last = i+1;
            if(i&1) ans--;
        }
        ans += n;

        cout << ans << '\n';
    }

    return 0;
}