void upd(int ind, pair<int,int> val, vector<pair<int,int>> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]=max(val, bit[ind]);
        ind+=ind&-ind;
    }
}
pair<int,int> sum(int ind, vector<pair<int,int>> &bit){         //O(logn)
    pair<int,int> ans={-1,-1};
    while(ind){
        ans=max(bit[ind], ans);    
        ind-=ind&-ind;    
    }
    return ans;
}

signed solve(){
    
    int n; cin >> n;

    vector<int> ans(n, 0);

    set<int> st;

    vector<pair<int,int>> arr(n);

    vector<pair<int,int>> bit(n+5, {-1,-1});

    vector<vector<int>> val_pos(n+5);

    vector<int> j(n+5, 0);

    for(int i = 0; i < n; i++){
        int x; cin >> x;
        arr[i] = {x, i};
        val_pos[x].push_back(i);
        st.insert(i);
    }

    sort(arr.rbegin(), arr.rend());

    for(int k = 0; k < n; k++){
        auto [h, i] = arr[k];
        while(j[h] < val_pos[h].size()){
            st.erase(val_pos[h][j[h]++]);
        } 

        upd(i+1, {h, i}, bit);


        ans[i] = h;

        int nxt = i;

        if(st.size() && st.upper_bound(i) != st.end()) nxt = (*st.rbegin());

        auto [mx_val, mx_pos] = sum(nxt+1, bit);

        if(mx_pos > -1){
            ans[i] = max(ans[i], ans[mx_pos]);
        }
    }

    for(int i: ans){
        cout << i << ' ';
    }
    cout << endl;


    return 0;
}

