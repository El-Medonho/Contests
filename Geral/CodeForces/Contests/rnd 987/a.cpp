signed solve(){
    
    int n; cin >> n;

    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    int c1 = 0, c2 = 0;
    int l = -1;

    for(int i = 0; i < n; i++){
        if(arr[i] != l){
            c1 = max(c1, c2);
            c2 = 0;
        }
        c2++;
        l = arr[i];
    }

    c1 = max(c1, c2);

    cout << n - c1 << endl;


    return 0;
}

