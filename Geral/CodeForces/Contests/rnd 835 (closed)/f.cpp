int solve(){

    ll n,c,d; cin >> n >> c >> d;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    sort(arr.rbegin(), arr.rend());

    for(int i = n; i < d; i++){
        arr.push_back(0);
    }

    ll sum = 0;
    vector<ll> pre(max(n,d),0);
    for(int i = 0; i < max(n,d); i++){
        sum += arr[i];
        pre[i] = sum;
        if(sum >= c && i < d){
            cout << "Infinity" << endl;
            return 0;
        }
    }

    if(arr[0] * d < c){
    cout << "Impossible" << '\n';
    return 0;
    }

    int ans = 0;

    for(int i = 0; i < max(n,d); i++){
        ll hh = pre[i] * (d/(i+1));
        if(d%(i+1) > 0) hh += pre[d%(i+1)-1];

        if(hh >= c){
            ans = i;
        }else{
            break;
        }
    }

    cout << ans << endl;

    return 0;
}





