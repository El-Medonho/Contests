int solve(){

    int n,in,out; cin >> n >> in >> out; in--; out--;
    vector<vector<pair<int,int>>> graph(n);

    for(int i = 0; i < n-1; i++){
        ll a,b,c; cin >> a >> b >> c; a--; b--;
        graph[a].push_back({b,c});
        graph[b].push_back({a,c});
    } 

    vector<int> vis(n,0);

    queue<pair<ll,ll>> qw;
    qw.push({in,0});

    set<ll> st;

    vis[in] = 1;
    st.insert(0);
    while(!qw.empty()){
        pair<ll,ll> curr = qw.front();
        qw.pop();

        for(pair<ll,ll> j: graph[curr.first]){
            if(!vis[j.first] && j.first != out){
                qw.push({j.first, j.second ^ curr.second});
                vis[j.first] = 1;
                st.insert(j.second ^ curr.second);
            }
            if(j.first == out && (j.second ^ curr.second) == 0){
                cout << "YES" << endl;
                return 0;                
            }
        }
    }

    fill(vis.begin(), vis.end(), 0);
    qw.push({out,0});
    vis[out] = 1;

    while(!qw.empty()){
        pair<ll,ll> curr = qw.front();
        qw.pop();

        for(pair<ll,ll> j: graph[curr.first]){
            if(!vis[j.first]){     //duvidoso
                qw.push({j.first, j.second ^ curr.second});
                vis[j.first] = 1;
                if(st.find(j.second ^ curr.second) != st.end()){
                    cout << "YES" << endl;
                    return 0;
                }
            }
        }
    }

    cout << "NO" << endl;

    return 0;
}

