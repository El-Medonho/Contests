// quantidade de inversões ~ n² ~ 10^10

int solve(){

    int n; cin >> n;
    vector<int> arr(n);
    for(int i = 0; i < n; i++) cin >> arr[i];

    long long ans = 0;
    int ones = 0;
    
    int ind1 = -1, ind0 = 1e9; 
    for(int i = 0; i < n; i++){
        if(arr[i] == 1) {
            ones++;
            ind1 = max(ind1, i);
        }
        else {
            ans += ones;
            ind0 = min(ind0, i);
        }
    }

    if(ind0 != 1e9){
        vector<int> arr2 = arr;
        arr2[ind0] = 1;

        long long ans2 = 0; ones = 0;

        for(int i = 0; i < n; i++){
            if(arr2[i] == 1) {
                ones++;
            }
            else {
                ans2 += ones;
            }
        }

        ans = max(ans, ans2);
    }


    if(ind1 != -1){
        vector<int> arr3 = arr;
        arr3[ind1] = 0;

        long long ans3 = 0; ones = 0;

        for(int i = 0; i < n; i++){
            if(arr3[i] == 1) {
                ones++;
            }
            else {
                ans3 += ones;
            }
        }

        ans = max(ans, ans3);
    }

    cout << ans << '\n';

    return 0;
}

