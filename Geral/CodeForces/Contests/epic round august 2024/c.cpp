signed solve(){
    
    int n; cin >> n;

    vector<pair<ll,ll>> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i].first >> arr[i].second;
    }

    pair<ll,ll> s, t;

    cin >> s.first >> s.second;

    cin >> t.first >> t.second;

    ll dist = (t.first - s.first) * (t.first - s.first) + (t.second - s.second) * (t.second - s.second);

    for(int i = 0; i < n; i++){
        pair<ll,ll> ss = arr[i];
        ll dist2 = (t.first - ss.first) * (t.first - ss.first) + (t.second - ss.second) * (t.second - ss.second);

        if(dist2 <= dist){
            cout << "NO\n";
            return 0;
        }
    }

    cout << "YES\n";


    return 0;
}

