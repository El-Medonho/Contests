int solve(){

    int n; cin >> n;
    vector<vector<int>> arr(n, vector<int> (n,0)), vis(n, vector<int> (n,0));

    for(int i = 0; i < n; i++){
        string s; cin >> s;
        for(int j = 0; j < n; j++) arr[i][j] = stoi( s.substr(j,1) );
    }

    int one = 0;
    for(int i = 0; i < n; i++){
        for (int j: arr[i]) if(j) one++;
    }

    int best = 0;

    for(int j = 0; j < n; j++){
        int a = 0, b = j, curr = 0;
        // if(!(arr[i][j])) continue;
        while(true){
            if(vis[a][b]) break;
            vis[a][b] = 1;
            if(arr[a][b]) curr++;
            a = (a+1)%n; b = (b+1)%n;
        }
        a = 0; b = j;
        for(int i = 0; i < curr; i++){
            vis[a][b] = curr+1;
            a = (a+1)%n; b = (b+1)%n;
        }
        best = max(best, curr);
    }

    cout << (n-best) + (one-best) << endl; 

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}