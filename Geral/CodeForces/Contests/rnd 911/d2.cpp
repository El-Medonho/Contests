mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    ll n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;
    sort(arr.begin(), arr.end());

    ll ans = 0;
    vector<ll> freq(1e5+50, 0), dp(1e5+50, 0), onward(1e5+50, 0);
    for(ll i: arr) freq[i]++;
    for(int i = 0; i < n; i++){
        onward[arr[i]] = n-1-i;
    }

    int m = 1e5+5;

    for(int cc = m; cc > 0; cc--){
        ll x = 0, f = 0;
        for(int i = cc; i < m; i+=cc){
            dp[cc] -= dp[i];
            if(freq[i]){
                x += f * (freq[i] * (freq[i] - 1 + 2*onward[i])/2);
                x += (freq[i] * (freq[i]-1) / 2) * onward[i];
                x += (freq[i] * (freq[i]-1) * (freq[i]-2))/6; 
                f += freq[i];
            }
        }
        dp[cc] = dp[cc] + x;
        if(dp[cc]){
            ans += dp[cc]*cc;
        }
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}