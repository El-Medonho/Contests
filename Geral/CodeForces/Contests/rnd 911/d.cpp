mt19937 rng((ll) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    ll n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;
    sort(arr.begin(), arr.end());

    ll ans = 0;

    if(n < sqrt(1e5+500)){
        for(ll i = 0; i < n; i++){
            for(ll j = i+1; j < n; j++){
                ans += gcd(arr[i], arr[j]) * (n - 1 - j);
            }
        }
    }
    else{   
        vector<ll> freq(1e5+50, 0), dp(1e5+50, 0), onward(1e5+50, 0);
        for(ll i: arr) freq[i]++;
        for(ll i = 0; i < n; i++){
            onward[arr[i]] = n-1-i;
        }

        ll m = 1e5+5;

        for(ll cc = m; cc > 0; cc--){
            ll x = 0, f = 0;
            for(ll i = cc; i < m; i+=cc){
                dp[cc] -= dp[i];
                if(freq[i]){
                    x += f * (freq[i] * (freq[i] - 1 + 2*onward[i])/2);
                    x += (freq[i] * (freq[i]-1) / 2) * onward[i];
                    f += freq[i];
                }
            }
            dp[cc] = dp[cc] + x;
            if(dp[cc]){
                ans += dp[cc]*cc;
            }
        }
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    ll t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}