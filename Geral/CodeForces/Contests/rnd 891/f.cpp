ll sq(ll x){
    ll s = sqrtl(x);
    for(auto sq: vector<ll>{s-1,s,s+1}){
        if(sq*sq == x) return sq;
    }
    return -1;
}

vector<ll> bask(ll a, ll b, ll c){
    ll delta = (b*b)-4*a*c;

    if(delta < 0 || a == 0){
        return {};
    }

    ll s = sq(delta);

    if(s == -1) return {};

    vector<ll> ans(0);

    ll cc = -b + s;
    if(cc % (2*a) == 0) ans.push_back(cc / (2*a));

    cc = -b - s;
    if(cc % (2*a) == 0) ans.push_back(cc / (2*a));

    return ans;
}

signed solve(){

    int n,q; cin >> n;

    map<ll,ll> mp;

    while(n--){
        int x; cin >> x; mp[x]++;
    }

    cin >> q;

    while(q--){
        ll x,y; cin >> x >> y;

        vector<ll> ans = bask(1, -x, y);

        if(ans.size() < 2){
            cout << 0 << ' ';
            continue;
        }

        ll cc = mp[ans[0]] * mp[ans[1]];

        ll z = mp[ans[0]];

        if(ans[0] == ans[1]) cc = z*(z-1LL)/2LL;

        cout << cc << ' ';
    }

    cout << '\n';
    

    return 0;
}

