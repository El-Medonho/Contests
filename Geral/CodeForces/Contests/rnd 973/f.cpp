signed solve(){
    
    int n; cin >> n;

    vector<vector<int>> graph(n), vis_edge(n);

    map<pair<int,int>, int> mp;

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    int u; cin >> u >> u; u--;

    vector<int> vis_a(n, 0), vis_b(n, 0);

    vis_a[0] = 1;
    vis_b[u] = 1;

    queue<int> qa, qb; qa.push(0); qb.push(u);

    int ha = 1, hb = 1;

    while(!qa.empty() || !qb.empty()){
        while(!qa.empty() && vis_a[qa.front()] <= ha){
            int cc = qa.front(); qa.pop();

            for(int j: graph[cc]){
                // pair<int,int> ce = {min(cc, j), max(cc, j)};
                if(vis_a[j] || vis_b[j]) continue;
                // mp[ce] = 1;
                vis_a[j] = vis_a[cc]+1;
                qa.push(j);
            }
        }

        ha++;

        while(!qb.empty() && vis_b[qb.front()] <= hb){
            int cc = qb.front(); qb.pop();

            for(int j: graph[cc]){
                // pair<int,int> ce = {min(cc, j), max(cc, j)};
                if(vis_a[j] || vis_b[j]) continue;
                // mp[ce] = 1;
                vis_b[j] = vis_b[cc]+1;
                qb.push(j);
            }
        }

        hb++;
    }

    int ansa = *max_element(vis_a.begin(), vis_a.end()), ansb = *max_element(vis_b.begin(), vis_b.end());

    if(ansa >= ansb){
        cout << "Alice\n";
    }
    else cout << "Bob\n";


    return 0;
}

