int solve(){

    int n; cin >> n;
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<int> prod(n,1);

    vector<int> ans = {-1,0,0};

    int l = -1, r = -1, ll = -1, rr = -1;
    for(int i = 0; i < n; i++){
        if(arr[i] == 0) {
            l = -1, r = -1, ll = -1, rr = -1;
            continue;
        }
        if(arr[i] > 0){
            if(l == -1) {prod[i] = (arr[i] == 2 ? 2:1); l = i; r = i;}
            else {
                prod[i] = prod[i-1];
                if(prod[i] < 0) prod[i] -= (arr[i] == 2 ? 1:0);
                else prod[i] += (arr[i] == 2 ? 1:0);
            }
        }else{
            if(l == -1) {prod[i] = arr[i]; l = i; ll = i;}
            else{
                prod[i] = -prod[i-1];
                if(prod[i] < 0) prod[i] -= (arr[i] == -2 ? 1:0);
                else prod[i] += (arr[i] == -2 ? 1:0);

                if(ll == -1) ll = i;
                else rr = i;
            }
        }
        r = i;

        if(prod[i] < 0){
            if(r-l == 0) continue;
            if(rr == -1){
                
                if( ll != r)  {
                    int p = abs(prod[r]) - abs(prod[ll]);
                    if( p > ans[0]) ans = {p, ll+1, r};
                }

                if(ll != l){
                    int p = abs( abs(prod[ll-1])-1);
                    if( p > ans[0]) ans = {p, l, ll-1};
                }
            }
            else{

                int p = abs(prod[r]) - abs(prod[ll]);
                if( p > ans[0]) ans = {p, ll+1, r};

                p = abs( abs(prod[rr-1])-1);
                if( p > ans[0]) ans = {p, l, rr-1};

            }
        }
        else{
            int p = abs(prod[r]-1);
            if( p > ans[0]) ans = {p, l, r};
        }

    }

    if(ans[0] < 1) cout << n << ' ' << 0 << endl;
    else cout << ans[1] << ' ' << n-1-ans[2] << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}