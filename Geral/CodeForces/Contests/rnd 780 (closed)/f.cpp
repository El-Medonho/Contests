void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}

int solve(){

    int n; cin >> n;
    string s; cin >> s;

    vector<vector<ll>> bit(3, vector<ll> (3*n+20,0));

    ll cnt = min(n+200, 2*n+4);

    upd(0+cnt, 1, bit[0]);

    ll ans = 0;
    int m = 0, p = 0;
    for(int i = 0; i < n; i++){
        if(s[i] == '-')m++;
        else p++;

        ll h = m-p;

        ans += sum(h + cnt, bit[mod(h,3)]);
        upd(h + cnt, 1, bit[mod(h,3)]);
    }

    cout << ans << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}