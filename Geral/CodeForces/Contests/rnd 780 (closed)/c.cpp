int solve(){

    string s; cin >> s;
    int n; n = s.size();
    int ans = 0;
    int i = 1;
    vector<int> dig(26,0);
    for(i = 0; i < n; i++){
        if(dig[s[i]-'a']) {
            fill(dig.begin(), dig.end(), 0);
            ans--;
        }else {ans++; dig[s[i]-'a']++;}
    }

    cout << ans << endl;

    return 0;
}

