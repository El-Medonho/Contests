typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n,k; cin >> n >> k;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    if(k >= 3){
        cout << 0 << endl;
        return 0;
    }

    set<ll> st, sto;

    for(ll i: arr) {sto.insert(i);}

    ll ans = *min_element(arr.begin(), arr.end());

    for(int i = 0; i < n; i++){
        for(int j = i+1; j < n; j++){
            ll x = arr[j]-arr[i];
            ans = min(ans, x);
            if(k == 2) st.insert(x);
        }
    }

    for(ll x: st){
        auto y = sto.lower_bound(x);
        if(y != sto.end()) ans = min(ans, abs( x - (*y) ) );
        if(y != sto.begin()) y--;
        ans = min(ans, abs( x - (*y) ) );
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}