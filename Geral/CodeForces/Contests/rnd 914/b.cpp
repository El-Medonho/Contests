signed solve(){

    int n; cin >> n;
    vector<pair<ll,ll>> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i].first; arr[i].second = i;
    }

    sort(arr.begin(), arr.end());

    ll sum = arr[0].first;
    ll last = 0;

    vector<ll> ans(n, n-1);

    for(int i = 1; i < n; i++){
        ll x = arr[i].first;
        if(sum < x){
            for(int j = last; j < i; j++){
                ans[arr[j].second] = i-1;
            }
            last = i;
        }
        sum += x;
    }

    for(ll i: ans) cout << i << ' ';
    cout << endl;
    

    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}