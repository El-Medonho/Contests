signed solve(){

    int n; cin >> n;
    vector<int> nxt(n);
    for(int &i: nxt) {cin >> i; i--;}
    vector<ll> w(n);
    for(ll &i: w) cin >> i;


    vector<int> vis(n,0), retro(n,0);
    for(int i: nxt){
        retro[i]++;
    }

    queue<int> qw;

    vector<int> ans(0);

    for(int i = 0; i < n; i++){
        if(retro[i] == 0) qw.push(i);
    }

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();
        vis[cc] = 1;

        int k = nxt[cc];
        retro[k]--;
        if(retro[k] == 0) qw.push(k);

        ans.push_back(cc);
    }

    int mn = 1e9+50, ind = -1;

    function<void(int,int)> dfs1, dfs2;

    dfs1 = [&](int cc, int ini){
        if(w[cc] < mn){
            mn = w[cc];
            ind = cc;
        }
        if(cc == ini) return;

        dfs1(nxt[cc], ini);
    };

    dfs2 = [&](int cc, int ini){
        ans.push_back(cc);
        vis[cc] = 1;
        if(cc == ini) return;

        dfs2(nxt[cc], ini);
    };

    for(int i = 0; i < n; i++){
        if(vis[i] == 0){
            dfs1(nxt[i], i);
            dfs2(nxt[ind], ind);
            ind = -1; mn = 1e9+50;
        }
    }

    for(int i: ans) cout << i+1 << " ";
    cout << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}