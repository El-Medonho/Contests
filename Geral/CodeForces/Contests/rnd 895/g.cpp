mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;
    vector<ll> arr(n), narr(0), nind(0);
    for(ll &i: arr) cin >> i;

    ll cc = 1;
    int ind = -1;
    nind.push_back(-1);
    for(int i = 0; i < n; i++){
        if(arr[i] == 1){
            if(cc > 1){
                narr.push_back(cc);
                nind.push_back(ind);
            }
            ind = -1;
            cc = 1;
            narr.push_back(cc);
            nind.push_back(i);
        }
        else{
            if(ind == -1) ind = i;
            if(1e18/cc < arr[i]){
                cc = 1e18;
            }
            else cc *= arr[i];
        }
    }

    int l = 0, r = n-1;

    ll h = 0;

    for(; l < r; l++){
        if(narr[l] != 1) break;
        h++;
    }
    for(; r > l; r--){
        if(narr[r] != 1) break;
        h++;
    }

    ll sum = 1;

    for(ll i: narr){
        if(1e18/sum < i) sum = 1e18+50;
        else sum *= i;
    }

    if(sum > 1e6){
        cout << nind[l]+1 << ' ' << nind[r]+1 << '\n';
    }

    h+= sum;

    while(l < r){

        ll nh = h-sum;
        int nl = l;
        nh += sum/narr[nl++];

        for(; nl < r; nl++){
            if(narr[nl] != 1) break;
            nh++;
        }
        if(nh >= h){
            sum/=narr[l];
            l = nl;
            h = nh;
        }
        else break;

    }

    while(l < r){

        ll nh = h-sum;
        int nl = r;
        nh += sum/narr[nl--];

        for(; nl > l; nl--){
            if(narr[nl] != 1) break;
            nh++;
        }
        if(nh >= h){
            sum/=narr[l];
            r = nl;
            h = nh;
        }
        else break;

    }


    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}