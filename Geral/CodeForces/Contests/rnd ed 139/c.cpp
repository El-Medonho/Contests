#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int solve(){

    int in = -1;

    int n; cin >> n;
    vector<vector<int>> arr(n, vector<int>(2,0));

    string str; cin >> str;
    for(int i = 0; i < n; i++){
        if(str[i] == 'B') arr[i][0] = 1;
    }


    cin >> str;
    for(int i = 0; i < n; i++){
        if(str[i] == 'B') arr[i][1] = 1;
    }

    for(int i = 0; i < n; i++){
        if(in != -1 && arr[i][in] == 0){
            cout << "NO" << '\n';
            return 0;
        }
        else if(arr[i][0] == arr[i][1]){
            if(in == -1) continue;
            else in = (in+1)%2;
        }else if(in == -1){
            if(arr[i][0]) in = 0;
            else in = 1;
        }
    }

    cout << "YES" << '\n';
    return 0;
}

int main(){
    fastio;

    int t;
    cin >> t;
    while(t--){
        solve();
    }

    return 0;
}