int main(){
    fastio;

    vector<int> primes(0);

    for(int i = 2; i <= 1e5; i++){
        bool isprime = true;
        for(int j: primes){
            if(j*j > i) break;
            if(i%j == 0){
                isprime = false;
                break;
            }
        }
        if(isprime) primes.push_back(i);
    }

    int t = 1;
    cin >> t;
    while(t--){
        int a,b; cin >> a >> b;
        if(a > b) swap(a,b);
        if(abs(a-b) == 1){
            cout << -1 << '\n';
            continue;
        }
        if(a == 1 && b == 1){
            cout << 1 << '\n';
            return 0;
        }
        int c = abs(a-b);

        int ans = 1e9;
        for(int j: primes){
            if(j*j > c) break;
            if(c%j == 0) {
                if(a%j == 0){
                    ans = 0;
                    break;
                }
                ans = min(ans, j-(a%j));
                // ans = min(ans, (c/j)-(a % (c/j)));
            }
            while(c%j == 0) c/=j;
        }
        if(c != 1) {
            ans = min(ans, c - (a%c));
            if(a % c == 0){
                ans = 0;
            }
        }
        
        cout << ans << '\n';
    }    

    return 0;
}