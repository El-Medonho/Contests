const ll inf = 1e18+5;

signed solve(){
    
    int n; cin >> n;

    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    ll s = 0;

    for(int i = 1; i < n; i++){
        if(arr[i] % arr[0] == 0){
            if(s == 0) s = arr[i];
            else s = __gcd(s, arr[i]);
        }
    }

    if(s == arr[0]){
        cout << "YES\n";
    }

    else cout << "NO\n";



    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}