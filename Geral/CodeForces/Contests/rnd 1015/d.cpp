signed solve(){
    
    int n,m,k; cin >> n >> m >> k;

    vector<int> ans(n, 0);

    int sz = k*m+k;

    int j = 0, mx = -1;

    int mans = 0;

    while(j*sz < n){
        int d = mx+1;
        for(int i = j*sz; i < min((j+1)*sz, n); i++){
            ans[i] = d+(i%k);
            mx = max(mx, ans[i]);
            if(i >= j*sz + k*m) mans = max(mans, ans[i]);
        }
        j++;
    }

    if(n / (m+1) >= k){
        int bc = n/(m+1);

        for(int i = 0; i < n; i++){
            ans[i] = i%bc;
        }
    }

    for(int i: ans) cout << i << ' ';
    cout << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}