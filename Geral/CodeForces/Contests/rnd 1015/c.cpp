signed solve(){
    
    int n; cin >> n;

    vector<int> a(n), b(n);

    for(int &i: a) cin >> i;
    for(int &i: b) cin >> i;

    int cnt = 0;
    int z = -1;
    map<pair<int,int>,int> mp;

    for(int i = 0; i < n; i++){
        if(a[i] == b[i]){
            if(z != -1){
                cout << -1 << endl;
                return 0;
            }
            z = i;
        }
    }

    vector<pair<int,int>> qw(0);

    if(z != -1){
        if(n%2 == 0){
            cout << -1 << endl;
            return 0;
        }

        if(z != n/2){
            swap(a[z], a[n/2]);
            swap(b[z], b[n/2]);
            qw.emplace_back(z, n/2);
        }

    }
    else if(z == -1 && n%2==1){
        cout << -1 << endl;
        return 0;
    }

    for(int i = 0; i < n; i++){
        pair<int,int> cc = make_pair(min(a[i],b[i]), max(a[i], b[i]));

        while(mp.find(cc) != mp.end() && n-1-mp[cc] != i){
            int nx = n-1-mp[cc];
            
            swap(a[i], a[nx]);
            swap(b[i], b[nx]);
            qw.emplace_back(i, nx);
            if(qw.size() > n) {
                cout << -1 << endl;
                return 0;
            }
            
            cc = make_pair(min(a[i],b[i]), max(a[i], b[i]));
        }

        if(mp.find(cc) == mp.end()){
            mp[cc] = i;
        }
    }

    for(int i = 0; i < n; i++){
        if(a[i] != b[n-1-i] || qw.size() > n){
            cout << -1 << endl;
            return 0;
        }
    }

    cout << qw.size() << endl;

    for(auto[a,b]: qw){
        cout << a+1 << ' ' << b+1 << endl;
    }


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}