mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    ll n,k,d; cin >> n >> k >> d;
    vector<int> arr(n);
    for(int &i: arr) cin >> i;
    ll ans = 0;

    vector<int> days(k);
    for(int &i: days) cin >> i;

    for(int i = 0; (i < 2*n+10) && i < d; i++){
        int cc = days[i%k];

        ll sum = 0;
        for(int j = 0; j < n; j++){
            if(arr[j] == j+1) sum++;
            if(j < cc) arr[j]++;
        }

        ans = max(ans, sum + (d - (i+1))/2);
    }

    cout << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}