vector<int> z_function(string s) {          
    int n = (int) s.length();
    vector<int> z(n);
    for (int i = 1, l = 0, r = 0; i < n; ++i) {
        if (i <= r)
            z[i] = min (r - i + 1, z[i - l]);
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
            ++z[i];
        if (i + z[i] - 1 > r)
            l = i, r = i + z[i] - 1;
    }
    return z;
}

signed solve(){

    int n,k; cin >> n >> k >> k;

    string s; cin >> s;

    if(k == 1){
        cout << n << endl;
        return 0;
    }
    
    vector<int> z = z_function(s);

    int l = 1, r = n;

    int ans = 0;

    while(l <= r){
        int md = (l+r)/2;

        int kk = k-1;

        for(int i = md; i < n; i++){
            if(z[i] >= md){
                kk--;
                i += md; i--;
            }
        }

        if(kk <= 0){
            ans = md;
            l = md+1;
        }
        else r = md-1;
    }

    cout << ans << endl;


    return 0;
}

