vector<int> z_function(string s) {          
    int n = (int) s.length();
    vector<int> z(n);
    for (int i = 1, l = 0, r = 0; i < n; ++i) {
        if (i <= r)
            z[i] = min (r - i + 1, z[i - l]);
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
            ++z[i];
        if (i + z[i] - 1 > r)
            l = i, r = i + z[i] - 1;
    }
    return z;
}

const int hf = 100;

signed solve(){

    int n,a,b; cin >> n >> a >> b;

    string s; cin >> s;

    if(a == 1){
        cout << n << ' ';
    }
    
    vector<int> z = z_function(s);

    function<int(int)> solve2;

    solve2 = [&](int k){

        int l = 1, r = n;

        int ans = 0;

        while(l <= r){
            int md = (l+r)/2;

            int kk = k-1;

            for(int i = md; i < n; i++){
                if(z[i] >= md){
                    kk--;
                    i += md; i--;
                }
            }

            if(kk <= 0){
                ans = md;
                l = md+1;
            }
            else r = md-1;
        }

        return ans;
    };

    int ini = -1;

    for(int i = max(a, 2); i <= min(hf, b); i++){
        ini = solve2(i);
        cout << ini << ' ';
    }

    if(ini == -1) ini = solve2(a);

    int l = max(a-1, hf);

    int i = ini;

    while(l < b && i){
        int cc = 1;

        for(int j = i; j < n; j++){
            if(z[j] >= i){
                j += i; j--;
                cc++;
            }
        }

        while(cc > l && l < b){
            l++;
            cout << i << ' ';
        }

        i--;
    }

    while(l < b){
        l++; cout << 0 << ' ';
    }

    cout << endl;


    return 0;
}

