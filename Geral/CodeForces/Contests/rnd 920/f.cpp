mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n,q; cin >> n >> q;
    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;

    int num = sqrtl(n);

    vector<vector<vector<pair<ll,ll>>>> pre(min(num+3, n+3));

    for(int i = 1; i <= num && i <= n; i++){
        for(int j = 0; j < i; j++){
            pre[i].push_back(vector<pair<ll,ll>>(0));
            for(int k = j; k < n; k += i){
                ll a = 0,b = 0;
                if(k-i >= 0){
                    a = pre[i][j][(k/i)-1].first;
                    b = pre[i][j][(k/i)-1].second;
                }

                a += arr[k] * (k/i + 1);
                b += arr[k];

                pre[i][j].push_back({a,b});
            }
        }
    }

    while(q--){
        int s,d,k; cin >> s >> d >> k; s--;
        if(d > num){
            ll ans = 0;
            for(int i = s, j = 1; j <= k; j++, i+=d){
                ans += arr[i]*j;
            }
            cout << ans << ' ';
        }
        else{
            int j = s%d;
            int f = s+d*(k-1);
            ll ans = pre[d][j][f/d].first;

            if(s/d){
                ans -= pre[d][j][(s/d)-1].first;
                ll diff = pre[d][j][f/d].second - pre[d][j][(s/d)-1].second;
                ans -= diff * (s/d);
            }
            cout << ans << ' ';
        }
    }

    cout << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}