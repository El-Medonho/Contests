struct Hashing
{
    ll n;
    ll p = 31;
    ll m = 1e9+7;
    string str;
    vector<ll> pow, hh;

    Hashing(string s){
        str = s;
        n = s.size();
        pow.resize(n+1,1); hh.resize(n+1,0);

        for(int i = 1; i <= n; i++) pow[i] = (pow[i-1] * p)%m;

        for(int i = 1; i <= n; i++) hh[i] = ((hh[i-1] * p) % m + (str[i-1] - 'a' + 1)) % m;
    }

    ll range(int l, int r){
        r++;
        return ((hh[r] - (hh[l] * pow[r-l]) % m) + m) % m;
    }
};

