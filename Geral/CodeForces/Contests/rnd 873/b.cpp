int gcd(int a, int b){
    return b == 0 ? a: gcd(b, a%b);
}

int solve(){

    int n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    ll ans = arr[0]-1; 

    for(int i = 1; i < n; i++){
        int j = arr[i]-1; 
        ll k = abs(j-i);
        if(k == 0) continue;
        ans = gcd(ans, k);
    }

    cout << abs(ans) << endl;

    return 0;
}

