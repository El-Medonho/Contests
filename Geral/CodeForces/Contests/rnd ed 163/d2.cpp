vector<int> kmp(string s){
    int n = (int)s.size();
    vector<int> pi(n);
    int j = pi[0];
    for(int i=1; i<n; i++){
        while(j>0 && (s[i]!=s[j] && s[i] != '?' && s[j] != '?')) j=pi[j-1];
        int posj = j;
        bool ok = false;
        if(s[i]==s[j] || s[i] == '?' || s[j] == '?') {
            j++;
        }
        pi[i]=j;
    }
    return pi;
}

signed solve(){

    string s; cin >> s;

    int n = s.size();

    int ans = 0;

    for(int i = 0; i < n; i++){
        vector<int> arr = kmp(s);

        for(int j = 1; j < arr.size(); j+=2){
            int sz = j+1;
            if(arr[j] >= sz/2) ans = max(ans, sz);
        }

        s = s.substr(1, s.size()-1);
    }

    cout << ans << endl;
    

    return 0;
}

