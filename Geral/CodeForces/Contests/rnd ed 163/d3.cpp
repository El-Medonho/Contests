signed solve(){
    
    string s; cin >> s;

    int n = s.size();

    int ans = 0;

    for(int g = 1; g <= n; g++){
        int cc = 0;
        for(int i = 0; i + g < n; i++){
            if(s[i] == '?' || s[i+g] == '?' || s[i] == s[i+g]) cc++;
            else cc = 0;

            if(cc >= g) ans = g*2;
        } 
    }

    cout << ans << endl;

    return 0;
}

