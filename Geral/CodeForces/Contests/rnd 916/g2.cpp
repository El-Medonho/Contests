const int mod = 998244353;

signed solve(){

    int n; cin >> n; n*=2;

    vector<int> mp(n+1, -1);

    vector<int> rep(0);

    vector<int> pai(n+5), peso(n+5, 2);

    for(int i = 0; i < n+2; i++) pai[i] = i;

    function<int(int)> ff;

    ff = [&](int x){
        if(pai[x] == x) return x;
        return pai[x] = ff(pai[x]);
    };

    function<void(int,int)> uu;

    uu = [&](int x, int y){
        x = ff(x); y = ff(y);

        if(x == y) return;

        if(peso[x] > peso[y]) swap(x, y);

        peso[y] += peso[x];
        pai[x] = y;
    };

    ll ans = 1;

    int cnt = 0;

    set<pair<int,int>> st;

    vector<int> ins(0);

    for(int i = 0; i < n; i++){
        int x; cin >> x;

        if(st.empty()) {
            cnt++;
            ins.push_back(x);
        }

        if(mp[x] != -1){
            auto it = st.upper_bound({mp[x], x});
            
            while(it != st.end()){
                auto [ind, y] = *it;

                uu(x, y);

                it++;
            }

            st.erase({mp[x], x});
        }
        else{
            mp[x] = i;
            st.insert({mp[x], x});
        }
    }

    for(int x: ins){
        x = ff(x);
        ans = ans * peso[x] % mod;
    }

    cout << cnt << ' ' << ans << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}