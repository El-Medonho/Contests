void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}

int solve(){

    int n; cin >> n;
    int q; cin >> q;

    vector<ll> bit1(n+1,0), bit2(n+1,0);
    vector<ll> arr1(n+1,0), arr2(n+1,0);

    while(q--){
        int t; cin >> t;
        if(t == 1){
            int x,y; cin >> x >> y;
            if(arr1[x] == 0){
                upd(x,1,bit1);
            }
            if(arr2[y] == 0){
                upd(y,1,bit2);
            }
            arr1[x]++;arr2[y]++;
        }
        if(t == 2){
            int x,y; cin >> x >> y;
            arr1[x]--; arr2[y]--;
            if(arr1[x] == 0){
                upd(x,-1,bit1);
            }
            if(arr2[y] == 0){
                upd(y,-1,bit2);
            }
        }
        if(t == 3){
            int x1,y1,x2,y2; cin >> x1 >> y1 >> x2 >> y2;
            int h1 = sum(x2,bit1) - sum(x1-1, bit1);
            int h2 = sum(y2,bit2) - sum(y1-1, bit2);
            if( (h1 == x2-x1+1) || (h2 == y2-y1+1) ){
                cout << "Yes\n";
            }
            else cout << "No\n";
        }
    }

    return 0;
}

