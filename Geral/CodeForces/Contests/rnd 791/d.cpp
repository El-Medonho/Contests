int solve(){

    ll n,m,k; cin >> n >> m >> k;

    vector<vector<int>> graph(n);
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<int> in(n,1);
    while(m--){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b); in[b] = 0;
    }

    vector<int> ison(n,0);         
    vector<ll> vis(n,0); 

    function<void(int,int,int)> dfs;

    dfs = [&](int curr, int last, int cap){

        ison[curr] = 1;

        for(int j: graph[curr]){
            if(arr[j] > cap) continue;
            if(ison[j]){
                vis[j] = 1e10;
                ison[curr] = 0;
                return;
            }
            if(vis[j]){
                vis[curr] = max(vis[curr], 1 + vis[j]);
                continue;
            }
            vis[j] = 1;
            dfs(j,curr,cap);
            vis[curr] = max(vis[curr], 1 + vis[j]);
        }
        ison[curr] = 0;
        return;
    };

    int l = 0, r = 1e9+5; int ans = 1e9+10;

    while(l <= r){
        int md = (l+r)>>1;

        bool pass = false;
        fill(vis.begin(), vis.end(), 0);
        for(int i = 0; i < n; i++){
            if(!vis[i] && arr[i] <= md) {vis[i] = 1; dfs(i,-1,md);}
            if(vis[i] > 1e9 || vis[i] >= k){
                pass = true;
                break;
            }
        }

        if(pass){
            r = md-1;
            ans = min(ans, md);
        }else{
            l = md+1;
        }
    }

    if(ans == 1e9+10) {
        cout << -1 << endl;
        return 0;
    }

    cout << ans << endl;

    return 0;
}

int main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}