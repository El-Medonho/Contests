signed solve(){

    int n; cin >> n;

    vector<ll> w(n);
    for(ll &i: w) cin >> i;

    vector<vector<int>> graph(n);
    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<ll> dp(n, -1e18), ans(n, -1e18);

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){
        for(int j: graph[cc]){
            if(j == last) continue;

            dfs(j, cc);
        }

        vector<ll> arr(4, -1e18); arr[0] = 0;

        for(int i = 0; i < graph[cc].size(); i++){
            int j = graph[cc][i];
            if(j == last) continue;
            for(int k = min(i, 3); k > -1; k--){
                int kk = min(k+1, 3);
                arr[kk] = max(arr[kk], arr[k] + dp[j]);
            }
        } 

        for(int i = 0; i < arr.size(); i++){
            if(i == 1 && cc != last) dp[cc] = max(arr[i], dp[cc]);
            else if(i == 2 && cc == last) 
                dp[cc] = max(arr[i], dp[cc]);
            else dp[cc] = max(arr[i] + w[cc], dp[cc]);
        }

        for(int i = 0; i < arr.size(); i++){
            if(i == 2) 
                ans[cc] = max(arr[i], ans[cc]);
            else ans[cc] = max(arr[i] + w[cc], ans[cc]);
        }
    };

    if(n == 2){
        cout << max( max(w[0], w[1]), max(0LL, w[1] + w[0]) ) << endl;
        return 0;
    }

    int one = -1;

    for(int i = 0; i < n; i++){
        if(graph[i].size() > 1){
            one = i;
            break;
        }
    }

    dfs(one, one);
    cout << max( *max_element(ans.begin(), ans.end()), 0LL) << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}