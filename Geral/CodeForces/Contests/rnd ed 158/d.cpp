signed solve(){

    int n; cin >> n;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    map<ll, pair<ll,ll>> mp;
    for(int i = 0; i < n; i++) mp[arr[i]] = {1e9, -1e9};

    for(ll i = 0; i < n; i++){
        mp[arr[i]].first = min(mp[arr[i]].first, i);
        mp[arr[i]].second = max(mp[arr[i]].second, i);
    }

    multiset<ll> ans;
    ll resp = 1e18; 

    for(auto [x , h]: mp){
        ans.insert(h.second + x);
    }

    for(int i = 0; i < n; i++){
        ll cc = arr[i];
        if(i == mp[cc].second) ans.erase(ans.find(cc + i));

        resp = min(resp, max(cc,  ans.empty() ? 0 : *ans.rbegin()));

        if(i == mp[cc].first) ans.insert(cc + (n - 1 - i));
    }

    cout << resp << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}