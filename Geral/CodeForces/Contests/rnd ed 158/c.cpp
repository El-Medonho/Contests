mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;
    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    ll in = arr[0], out = arr[n-1];
    ll cc = 0;
    ll ans = 0;

    bool did = false;

    for(int i = 32; i > -1; i--){
        ll h = (1LL << i) & out;
        if( ((1LL << i) & in) == ((1LL << i) & out) ) {
            in ^= (1LL << i) & in;
            out ^= (1LL << i) & out;
            continue;
        }
        if(did){
            ans = i+1;
            break;
        }
        ll l = 0, r = 0;
        in ^= (1LL << i) & in;
        out ^= (1LL << i) & out;

        l = (1LL << i) - in;
        r = (1LL << i) - out;

        if(l < r) {cc += l; out += l; in = 0; did = true; ans = 1;}
        else {
            ans = i+1;
            break;
        }
    }

    cout << ans << endl;
    if(ans > 0 && ans <= n){
        ans --; cout << cc << ' ';
        while(ans--) cout << 0 << ' ';
        cout << endl;
    }


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}