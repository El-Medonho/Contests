void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}

signed solve(){
    
    int n; cin >> n;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<ll> bit(n+1, 0);

    vector<int> sz(n, 0);

    function<void(int, int, int)> dfs;

    dfs = [&](int cc, int last, int depth){
        int mx = depth;
        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j,cc,depth+1);
            mx = max(mx, sz[j]);
        }

        upd(1, 1, bit);

        upd(depth, -1, bit);

        if(mx) upd(mx+1, 1, bit);

        sz[cc] = max(mx, depth);
    };

    dfs(0,0,1);

    ll ans = 1e9;

    for(int i = 1; i <= n; i++) ans = min(ans, sum(i, bit));

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}