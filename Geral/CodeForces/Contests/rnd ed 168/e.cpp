signed solve(){
    
    int n,q; cin >> n >> q;

    int h = 450;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<vector<int>> pre(h+5, vector<int>(n+1, n));

    for(int lvl = 1; lvl <= h+3; lvl++){
        vector<int> sum(n, 0);
        for(int i = 0; i < n; i++){
            if(i > 0) sum[i] = sum[i-1];
            if(lvl <= arr[i]) sum[i]++;
        }

        vector<int> first(n+1, n);

        for(int i = n-1; i > -1; i--) first[sum[i]] = i;

        for(int x = 1; x <= n; x++){
            int base;
            if(lvl == 1) base = 0;
            else if(pre[lvl-1][x] == n){
                pre[lvl][x] = n;
                continue;
            }
            else base = sum[pre[lvl-1][x]];

            base += x;

            if(base > n || first[base] == n) pre[lvl][x] = n;
            else pre[lvl][x] = first[base];
        }
    }

    vector<vector<int>> sum(h+5, vector<int>(n, 0));

    for(int i = 0; i < n; i++){
        for(int j = 1; j <= h+3; j++){
            if(i != 0) sum[j][i] = sum[j][i-1];
            if(arr[i] >= j) sum[j][i]++;
        }
    }


    while(q--){
        int i,x; cin >> i >> x;
        i--;

        if(arr[i]*x > i){
            cout << "YES\n";
            continue;
        }

        // cout << ".\n";

        if(arr[i] > h){
            if(pre[x][i] > arr[i]){
                cout << "NO\n";
            }
            else{
                cout << "YES\n";
            }
        }

        else{
            int j = -1, lvl = 1;

            while(j < i){
                j = pre[lvl][x];
                if(j >= i) break;
                lvl++;
            }

            if(lvl > arr[i]){
                cout << "NO\n";
            }
            else{
                cout << "YES\n";
            }
        }

    }


    return 0;
}

