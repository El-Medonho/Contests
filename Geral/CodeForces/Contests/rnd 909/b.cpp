mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;
    // sort(arr.begin(), arr.end());

    ll ans = 0;
    for(int k = 1; k < n; k++){
        if(n % k) continue;
        ll l = 1e18, r = 0;
        for(int i = 0; i < n; i+=k){
            ll h = 0;
            for(int j = i; j < i+k; j++){
                h += arr[j];
            }
            l = min(l, h);
            r = max(r, h);
        }
        ans = max(ans, r-l);
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}