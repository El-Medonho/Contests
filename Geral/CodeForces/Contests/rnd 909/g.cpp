signed solve(){

    int n,q; cin >> n >> q;

    vector<vector<int>> graph(n);
    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> arr(n);
    for(int &i: arr) {cin >> i; i--;}

    vector<int> pos(n);
    for(int i = 0; i < n; i++) pos[arr[i]] = i;
    
    vector<set<int>> st(n);
    vector<int> pai(n, 0), peso(n, 1);

    for(int i = 0; i < n; i++){
        st[i].insert(pos[i]+1);
        pai[i] = i;
    }

    vector<bool> ans(q, 0);

    vector<vector<pair<int,pair<int,int>>>> querry(n);

    for(int i = 0; i < q; i++){
        int l,r,j; cin >> l >> r >> j; j--;
        querry[j].push_back( {i, {l, r}} );
    }

    function<int(int)> ff;

    ff = [&](int x){
        if(pai[x] == x) return x;
        return pai[x] = ff(pai[x]);
    };

    function<void(int,int)> uu;

    uu = [&](int x, int y){

        x = ff(x); y = ff(y);
        if(x == y) return;
        if(peso[x] < peso[y]) swap(x, y);

        for(int i: st[y]) st[x].insert(i);
        peso[x] += peso[y];
        pai[y] = x;
    };

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j, cc);
            uu(cc, j);
        }

        ff(cc);

        for(int k = 0; k < querry[cc].size(); k++){
            int c = pai[cc];
            int ind = querry[cc][k].first, l = querry[cc][k].second.first, r = querry[cc][k].second.second;
            if(st[c].lower_bound(l) != st[c].end() && (*st[c].lower_bound(l)) <= r) ans[ind] = 1;
        }
    };

    dfs(0,0);

    for(int i = 0; i < q; i++){
        if(ans[i]) cout << "YES\n";
        else cout << "NO\n";
    }

    // cout << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}