signed solve(){

    int n; cin >> n;
    vector<ll> arr(n), na(0);
    for(ll &i: arr) cin >> i;

    ll ans = *max_element(arr.begin(), arr.end());
    if(ans <= 0) {
        cout << ans << endl;
        return 0;
    }
    
    int last = -1;
    ll g = 0;
    ans = 0; na.push_back(0); last = 0;
    for(int i = 0; i < n; i++){
        if( (arr[i] < 0 && na.empty()) || (arr[i] < 0 && na.back() < 0)) continue;
        if(abs(arr[i]) % 2 == last) g = max(arr[i], g);
        else{
            na.push_back(g);
            g = -1e8;
            g = max(g, arr[i]);
            last = abs(arr[i])%2;
        }
    }

    if(g > 0) na.push_back(g);

    int N = na.size();
    for(int i = 0; i < N; i++){
        if(na[i] >= 0) ans += na[i];
        else if(na[i] < 0 && (na[i-1] + na[i] + na[i+1] <= max(na[i-1], na[i+1])) && na[i-1] > 0 && na[i+1] > 0) {
            ans -= na[i-1];
            ans += max(na[i-1], na[i+1]);
        }
        else if(na[i-1] >= 0 && na[i+1] >= 0) ans += na[i];
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}