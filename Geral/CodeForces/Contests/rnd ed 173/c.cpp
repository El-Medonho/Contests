const int mod = 1e9+7;

signed solve(){
    
    int n; cin >> n;

    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    int pos = n;
    ll val = 0;

    set<ll> st; st.insert(0);

    for(int i = 0; i < n; i++){
        if(arr[i] < -1 || arr[i] > 1) {pos = i; val = arr[i];}
    }

    int lmx = 0, lmn = 0;

    for(int i = 0; i < pos; i++){
        lmx += arr[i]; lmn += arr[i];

        if(lmx < 0) lmx = 0;
        if(lmn > 0) lmn = 0;

        st.insert(lmx); st.insert(lmn);
    }

    int rmx = 0, rmn = 0;

    for(int i = n-1; i > pos; i--){
        rmx += arr[i]; rmn += arr[i];

        if(rmx < 0) rmx = 0;
        if(rmn > 0) rmn = 0;

        st.insert(rmx); st.insert(rmn);
    }

    int mx = lmx + rmx, mn = lmn + rmn;

    for(int i = 0; i <= mx; i++) st.insert(val + i);

    for(int i = 0; i >= mn; i--) st.insert(val + i);

    cout << st.size() << endl;
    for(ll i: st) cout << i << ' ';
    cout << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}