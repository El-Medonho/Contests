const int mod = 998244353;

signed solve(){
    
    int n; cin >> n;
    int q; cin >> q;

    vector<vector<int>> freq(n+1, vector<int>(51, 0));

    vector<int> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i];
        freq[i+1] = freq[i];
        freq[i+1][arr[i]]++;
    }

    while(q--){
        int l,r; cin >> l >> r;
        int ans = r-l+1;

        vector<ll> cfreq(51, 0);

        for(int i = 0; i < 51; i++){
            cfreq[i] = freq[r][i] - freq[l-1][i];
        }

        if(cfreq[0]){
            cout << ans-1 << ' ';
            cout << cfreq[0] << endl;
            continue;
        }

        ll sum = 0;

        bool ok = false;

        for(int i = 0; i < 51; i++){
            if(cfreq[i] >= 2) ok = true;
            sum = (sum + cfreq[i] * (cfreq[i]-1) / 2) % mod;
        }

        if(ok){
            cout << ans-2 << ' ';
            cout << sum << endl;
            continue;
        }

        vector<pair<ll,ll>> dp(64, {1000, 0LL});

        for(int i = 0; i < 51; i++){
            if(!cfreq[i]) continue;

            vector<pair<ll,ll>> ndp = dp;

            for(int j = 0; j < 64; j++){
                if(dp[j].first == 1000) continue;

                int cc = dp[j].first; cc++;

                int nxt = i ^ j;

                if(dp[nxt].first < cc) continue;

                if(dp[nxt].first > cc) ndp[nxt] = {cc, dp[j].second * cfreq[i] % mod};

                if(dp[nxt].first == cc) ndp[nxt].second = (ndp[nxt].second + dp[j].second * cfreq[i]) % mod;
            }

            ndp[i] = {1, cfreq[i]};

            dp = ndp;
        }

        if(dp[0].first != 1000){
            cout << ans - dp[0].first << ' ';
            cout << dp[0].second << '\n';
            continue;
        }

        cout << -1 << endl;
    }


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}