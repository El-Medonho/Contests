int solve(){

    int n,m; cin >> n >> m;
    vector<vector<ll>> grid(n, vector<ll> (m,0));
    int h = n*m;

    vector<ll> arr(n*m);
    for(ll &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    ll ans1 = 0, ans2 = 0;


    ans1 += ( (min(n,m) - 1) * (arr[h-1] - arr[1]) ) + ( (max(n,m) - 1) * (min(n,m)) * (arr[h-1] - arr[0]));

    ans2 += ( (min(n,m) - 1) * (arr[h-2] - arr[0]) ) + ( (max(n,m) - 1) * (min(n,m)) * (arr[h-1] - arr[0]));

    cout << max(ans1,ans2) << endl;

    return 0;
}

