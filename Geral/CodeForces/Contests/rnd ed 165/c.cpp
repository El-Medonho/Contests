const int mod = 1e9+7;

signed solve(){
    
    int n; cin >> n;
    int c; cin >> c;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    // vector<ll> sum(n+5, 0);
    vector<vector<ll>> dp(n+5, vector<ll>(c+1, 0));
    
    for(int i = 0; i < n; i++){
        ll sum = arr[i], mn = arr[i];

        for(int j = 0; j <= c; j++){
            if(j > 0) dp[i][j] = dp[i][j-1];
            if(i != 0) dp[i][j] = max(dp[i][j], dp[i-1][j]);
        }

        for(int j = 1; j <= c; j++){
            if(i - j < 0) break;
            sum += arr[i-j];
            for(int k = 0; k + j <= c; k++){
                mn = min(mn, arr[i-j]);

                ll h = sum - mn * (j+1);

                ll b = (i-j <= 0 ? 0 : dp[i-j-1][k]);

                dp[i][k+j] = max(dp[i][k+j], b + h);
            }
        }
    }

    ll ans = 0;

    ll sum = 0;

    for(int i = 0; i < n; i++){
        sum += arr[i];
        for(int j = 0; j <= c; j++){
            ans = max(ans, dp[i][j]);
        }
    }

    cout << sum-ans << endl;


    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}