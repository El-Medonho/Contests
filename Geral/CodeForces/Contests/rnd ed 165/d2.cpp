#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<ll,null_type, less_equal<ll>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 1e9+7;

signed solve(){
    
    int n, c; cin >> n >> c;

    vector<ll> a(n),b(n);

    for(ll &i: a) cin >> i;
    for(ll &i: b) cin >> i;

    set<pair<ll,ll>> st;

    vector<ll> arr(0);

    for(int i = 0; i < n; i++){
        st.insert({-b[i], i});
    }

    ll ans = 0;

    multiordered_set mt;
    multiset<ll> mt2;

    for(auto[x, i]: st) arr.push_back(i);

    ll h = 0;

    vector<ll> sum(n+1, 0);

    for(int i = n-1; i > -1; i--){
        sum[i] = sum[i+1] + max(b[arr[i]] - a[arr[i]], 0LL);
    }

    for(int j = 0; j < n; j++){
        int i = arr[j];
        if(j >= c){
            ans = max(ans, sum[j] - h);

            if(c && a[i] < (*mt.find_by_order(c-1))){
                h -= (*mt.find_by_order(c-1));
                h += a[i];
            }

        }
        else{
            h += a[i];
        }
        mt.insert(a[i]);
        mt2.insert(a[i]);
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}