void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}

int solve(){

    int n,q; cin >> n >> q;

    vll bit(n+1);
    for(int i = 0; i < n; i++){
        ll a; cin >> a;
        upd(i+1,a,bit);
    } 

    while(q--){
        ll l,r,k; cin >> l >> r >> k;

        ll total = sum(n,bit);

        ll sun = sum(r,bit) - sum(l-1, bit);

        total -= sun;
        total += (r-l+1)*k;

        if(total % 2 == 1) cout << "yes" << endl;
        else cout << "no" << endl;
    }

    return 0;
}

