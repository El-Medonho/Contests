int solve(){

    int n = 8; vector<string> g(8);
    for(string &s: g) cin >> s;

    vector<vector<int>> dir ={{1,1},{1,-1},{-1,1},{-1,-1}}; 

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            if(g[i][j] == '#'){
                int hh = 0;
                for(vector<int> &k: dir){
                    int x = i+k[0], y = j+k[1];
                    if(x > -1 && x < n && y > -1 && y < n){
                        if(g[x][y] == '#') hh++;
                    }else hh++;
                }
                if(hh == 4 && !((i == 7 && j == 7) || (i == 0 && j == 7) || (i == 7 && j == 0) || (i == 0 && j == 0))){
                    cout << i+1 << ' ' << j+1 << endl;
                    return 0;
                }
            }
        }
    }

    return 0;
}

