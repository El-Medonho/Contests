int solve(){

    string s; cin >> s;
    int n; cin >> n;
    vector<vector<int>> vis(24, vector<int>(60,0));
    int h1 = stoi(s.substr(0,2)), h2 = stoi(s.substr(3,2));

    int ans = 0;
    while(true){
        if(h1 == ( ((h2%10)*10) + (h2/10) )) 
        ans++;

        vis[h1][h2] = true;

        h2 += n;
        h1 += h2/60;
        h1%=24;
        h2%=60;

        if(vis[h1][h2]) break;
    }

    cout << ans << endl;

    return 0;
}

