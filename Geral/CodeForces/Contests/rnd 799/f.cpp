int solve(){

    int n; cin >> n;
    vector<int> qtt(10,0);
    for(int i = 0; i < n; i++){
        int x; cin >> x; x%=10;
        qtt[x]++;
    }

    for(int i = 0; i < 10; i++){
        if(qtt[i] == 0) continue;
        qtt[i]--;
        for(int j = 0; j < 10; j++){
            if(qtt[j] == 0) continue;
            qtt[j]--;
            for(int k = 0; k < 10; k++){
                if(qtt[k] == 0) continue;
                qtt[k]--;
                if( (i+j+k)%10 == 3 ){
                    cout << "Yes\n";
                    return 0;
                }
                qtt[k]++;
            }
            qtt[j]++;
        }
        qtt[i]++;
    }

    cout << "No\n";

    return 0;
}

