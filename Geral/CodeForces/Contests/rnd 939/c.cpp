signed solve(){
    
    ll n; cin >> n;

    if(n == 1){
        cout << "1 1\n1 1 1\n";
        return 0;
    }

    vector<vector<int>> ans(0);

    ll sum = 0;
    ll ss = (1+n)*(n)/2;

    for(int i = 1; i <= n; i++){
        vector<int> cc = {1, i};
        for(int j = 1; j <= n; j++) cc.push_back(j);

        ans.push_back(cc);
    }

    int i = 1;
    for(i = 1; i <= n; i++){
        if(i*n >= ss) {break;}
        sum += ss;

        vector<int> cc = {2, i};
        for(int j = 1; j <= n; j++) cc.push_back(j);

        ans.push_back(cc);

    }

    for(; i <= n; i++) sum += i*n;

    cout << sum << ' ' << ans.size() << endl;

    for(vector<int> x: ans){
        for(int i: x) cout << i << ' ';
        cout << endl;
    }


    return 0;
}

