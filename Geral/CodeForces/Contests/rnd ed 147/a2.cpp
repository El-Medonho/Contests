#include "bits/stdc++.h"

using namespace std;


int rec(string str, int lvl){

    int ans = 1;

    if(lvl == 0 && str[lvl] == '0'){
        return 0;
    }

    if(lvl < str.size()-1) ans = rec(str, lvl+1);

    if(lvl == 0 && str[lvl] == '?') ans *= 9;
    else if(str[lvl] == '?') ans *= 10;

    return ans;
}

int solve(){

    string st; cin >> st;
    
    long long ans = rec(st, 0);

    cout << ans << endl;

    return 0;
}

int main(){

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}