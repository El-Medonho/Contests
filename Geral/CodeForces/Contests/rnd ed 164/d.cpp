const int mod = 998244353;

signed solve(){
    
    int n; cin >> n;

    vector<ll> cnt(5005, 0); cnt[0] = 1;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    ll ans = 0;

    for(int i = 0; i < n; i++){
        ll x = arr[i];

        for(ll j = 0; j <= 5001; j++){
            if(x > j){
                ll cc = x;

                ans = (ans + cnt[j]*cc % mod) % mod;
            }
            else{
                ll cc = x+j;
                cc = cc/2 + cc%2;

                ans = (ans + cnt[j]*cc % mod) % mod;
            }
        }

        for(ll j = 5000; j > -1; j--){
            if(x + j > 5000) continue;

            cnt[x+j] = (cnt[x+j] + cnt[j]) % mod;
        }
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}