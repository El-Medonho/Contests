int solve(){

    int n; cin >> n;

    vector<ll> arr(n);
    map<ll,ll> occur;
    for(ll &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    for(ll i: arr) {
        occur[i]++;
    }

    int last = -1;

    ll ans = 0;

    for(ll i: arr){
        if(i == last) continue;
        if(occur[i] >= 3) ans += (occur[i] * (occur[i]-1) * (occur[i]-2));
        last = i;
    }

    last = -1;

    for(ll i: arr){
        if(i == last) continue;

        ll curr = i;
        for(ll k = 2; k <= sqrt(1e9)+10;){
            if(curr*k*k > arr[n-1]) break;
            if(occur[curr*k] && occur[curr*k*k]) ans += (occur[i] * occur[curr*k] * occur[curr*k*k]);

            auto x = upper_bound(arr.begin(), arr.end(), curr*k*k);
            if(x == arr.end()) break;
            if(k == ll(sqrt((*x)/curr))) k++;
            else k = sqrt((*x)/curr);
        }

        last = i;
    }


    for(int i: arr) {
        occur[i]--;
    }

    cout << ans << endl;

    return 0;
}

