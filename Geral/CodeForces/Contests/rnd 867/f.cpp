#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int solve(){

    int n; ll c,k; cin >> n >> k >> c;
    vector<vector<int>> graph(n);

    for(int i = 0; i < n-1; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<ll> depth(n,0), gdepth(n,0);

    function<ll(int,int)> dfs;

    dfs = [&](int curr, int last){
        ll ans = 0;
        for(int j: graph[curr]){
            if(j == last) continue;
            ans = max(ans, dfs(j, curr)+1);
        }
        depth[curr] = ans;

        return ans;
    };

    gdepth[0] = 0;

    function<ll(int,int)> dfs2;

    dfs2 = [&](int curr, int last){
        ll ans = 0;
        if(last != -1) gdepth[curr] = gdepth[last]+1;
        for(int j: graph[curr]){
            if(j == last) continue;
            ans = max(ans, dfs2(j, curr)+1);
        }
        depth[curr] = ans;

        return ans;
    };

    dfs(0,-1);

    ll pro = k*depth[0];

    if(k <= c){
        cout << pro << '\n';
        return 0;
    }

    dfs2(0,-1);

    bool h = true;

    function<void(int,int,ll)> dfs3;

    dfs3 = [&](int curr, int last, ll dist){
        int mx = dist, mx2 = 0;
        for(int j : graph[curr]){
            if(j == last) continue;
            int gg = depth[j]+1;
            if(gg >= mx) {
                mx2 = mx;
                mx = gg;
            }else if(gg > mx2){
                mx2 = gg;
            }
        }

        pro = max(pro, (mx * k) - (gdepth[curr] * c));

        for(int j: graph[curr]) {
            if(j == last) continue;
            if(depth[j]+1 == mx) dfs3(j,curr,mx2+1);
            else dfs3(j,curr,mx+1);
        }

        return;
    };

    dfs3(0,-1,0);

    cout << pro << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}