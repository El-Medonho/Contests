#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    vector<int> primes(0);

    for(int i = 2; i < 1e5; i++){
        bool pass = true;
        for(int j : primes){
            if(j*j > i) break;
            if(i%j == 0) {
                pass = false;
                break;
            }
        }
        if(pass) primes.push_back(i);
    }

    int t; cin >> t;
    while(t--){
        int cnt = 0;
        vector<int> occur(0);
        map<int,int> mp;

        int n; cin >> n;
        vector<int> arr(n);
        for(int &i: arr) cin >> i;

        for(int i: arr){
            for(int j: primes){
                if(j*j > i) break;
                if(i%j == 0){
                    if(mp.find(j) == mp.end()) {
                        mp[j] = cnt++;
                        occur.push_back(0);
                    }
                    while(i % j == 0){
                        occur[mp[j]]++;
                        i/=j;
                    }
                }
            }
            if(i != 1){
                if(mp.find(i) == mp.end()) {
                    mp[i] = cnt++;
                    occur.push_back(0);
                }
                occur[mp[i]]++;
                i = 1;
            }
        }

        // if((occur.size() == 1 && occur[0] == 1) || (occur.size() == 2 && occur[0] == 1 && occur[1] == 1)){
        //     cout << 0 << '\n';
        //     continue;
        // }

        int rabeira = 0;
        int ans = 0;
        for(int j: occur){
            ans += j/2;
            rabeira += j%2;
        }
        ans += rabeira/3;

        cout << ans << '\n';
    }

    return 0;
}