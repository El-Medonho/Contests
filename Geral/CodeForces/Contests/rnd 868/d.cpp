#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int solve(){

    int n,k; cin >> n >> k; 

    string s;

    vector<pair<int,int>> arr(k);
    for(int i = 0; i < k; i++) cin >> arr[i].first;
    for(int i = 0; i < k; i++) cin >> arr[i].second;

    sort(arr.begin(), arr.end());
    int i = 3;
    int sum = 3;
    s = "abc"; int gg = 0;
    int hh = 3;

    for(pair<int,int> curr : arr){
        if(curr.first == 3){
            if(curr.second != 3){
                cout << "NO" << '\n';
                return 0;
            }
            continue;
        }
        if(sum > curr.second || curr.second > curr.first - i + sum){
            cout << "NO" << '\n';
            return 0;
        }
        for(; i < curr.first; i++){
            if(sum < curr.second){
                sum++;
                s += 'a' + hh;
            }else{
                s += 'a' + gg;
                gg = (gg+1)%3;
            }
        }
        hh++;
    }

    cout << "YES\n";
    cout << s << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}