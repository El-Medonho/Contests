signed solve(){

    int n,k; cin >> n >> k;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a; cin >> a; a--;
        graph[a].push_back(i);
    }

    vector<int> d(n, 0), sub(n, 1);

    vector<int> jump(0);

    function<void(int)> dfs;

    dfs = [&](int cc){

        for(int j: graph[cc]){
            dfs(j);
            d[cc] = max(d[cc], d[j]);
            sub[cc] += sub[j];
        }

        d[cc]++; sub[cc]++;
    };

    dfs(0);

    ll ans = 2*(n-1);

    
    

    return 0;
}

