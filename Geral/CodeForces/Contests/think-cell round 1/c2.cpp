typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed solve(){

    int n; cin >> n;
    vector<int> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i]; arr[i] += i+1;
    }

    ordered_set st;

    for(int i = 1; i <= n; i++){
        int cc = arr[i-1];

        if(st.find(cc) == st.end()){
            st.insert(cc);
            continue;
        }

        int ii = i-1;

        int lb = *st.lower_bound(cc-ii);

        int ans = cc-ii;

        int r = st.order_of_key(cc), l = st.order_of_key(lb);
        int base = r;

        while(l <= r){
            int mid = (l+r)/2;
            int ni = base-mid;

            int x = *st.find_by_order(mid);

            if(cc - x == ni){
                if(st.find(x-1) == st.end() && x-1 >= cc-ii) ans = max(ans,x-1);
                r = mid-1;
            }
            else{
                l = mid+1;
            }
        }

        st.insert(ans);
    }

    for(auto it = st.rbegin(); it != st.rend(); it++) cout << *it << ' ';

    cout << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}