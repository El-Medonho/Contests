struct Seg{
    int n,m = 1;
    vector<ll> tree, lazy, todate;

    // int join(ll a, ll b){
    //     return max(a,b);
    // }
    
    Seg(int n) : n(n) {
        while(m < n) m <<= 1;
        tree.resize(2*m, 0);
        lazy.resize(2*m, 0);
        todate.resize(2*m, 1);
    }

    void unlazy(int node){
        if(todate[node]) return; 
        tree[node] = lazy[node];
        if(node < m){
            lazy[2*node] = lazy[node]; todate[2*node] = 0;
            lazy[2*node+1] = lazy[node]; todate[2*node+1] = 0;
        }
        todate[node] = 1;
    }

    void upd(int a, int b, ll val, int node, int l, int r){
        if(a > r || b < l) return;
        unlazy(node);
        if(l >= a && r <= b){
            lazy[node] = val; todate[node] = 0;
            unlazy(node);
            return;
        }
        int mid = (l+r)/2;
        upd(a,b,val,2*node,l,mid);
        upd(a,b,val,2*node+1,mid+1,r);
        // tree[node] = join(tree[2*node], tree[2*node+1]);
    }

    ll query(int pos, int node, int l, int r){
        if(pos > r || pos < l) 
            return 0;
        unlazy(node);
        if(l == r) 
            return tree[node];

        int mid = (l+r)/2;
        if(mid < pos) return query(pos, 2*node+1, mid+1, r);
        return query(pos, 2*node, l, mid);
    }

    void upd(int a, int b, ll val){
        upd(a,b,val,1,1,m);
    }

    ll query(int pos){
        return query(pos,1,1,m);
    }
};

signed solve(){

    int n,q; cin >> n >> q;

    Seg seg(n);

    while(q--){
        int op; cin >> op;

        if(op == 1){
            int l,r,v; cin >> l >> r >> v; l++;
            seg.upd(l,r,v);
        }
        else {
            int i; cin >> i; i++;
            cout << seg.query(i) << endl;
        }
    }
    

    return 0;
}

