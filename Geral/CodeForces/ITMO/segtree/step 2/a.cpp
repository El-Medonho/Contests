template <class T> struct Seg{
    int n, neutral=0;
    vector<ll> tree, lazy, pre, suf, resp;
    Seg(int n) : n(n), tree(4*(n+1)), lazy(4*(n+1)), pre(4*(n+1)), suf(4*(n+1)), resp(4*(n+1), -1) {}
    T join(T a, T b){
        return a+b;
    }
    void upd(int node, int l, int r, int pos, int val){
        ll cc = 0;
        if(l==r){
            tree[node]=val;
            suf[node] = max(0, val); cc = max(cc, suf[node]);
            pre[node] = max(0, val); cc = max(cc, pre[node]);
            resp[node] = cc;
            return;
        }
        int mid=(l+r)>>1;
        if(pos<=mid){
            upd(2*node, l, mid, pos, val);
        }else{
            upd(2*node+1, mid+1, r, pos, val);
        }
        tree[node]=join(tree[2*node], tree[2*node+1]);
        suf[node]=max(suf[2*node+1], tree[2*node+1] + suf[2*node]);
        pre[node]=max(pre[2*node], pre[2*node+1] + tree[2*node]);
        cc = max(cc, suf[node]); cc = max(cc, pre[node]); cc = max(cc, pre[2*node+1] + suf[2*node]);
        cc = max(cc, resp[2*node]); cc = max(cc, resp[2*node+1]);
        resp[node] = cc;
    }
    void upd(int pos, int val){
        upd(1, 1, n, pos, val);
    }
};

signed solve(){

    int n,q; cin >> n >> q;

    Seg<ll> seg(n);

    for(int i = 1; i <= n; i++){
        ll a; cin >> a;
        seg.upd(i, a);
    }

    cout << seg.resp[1] << endl;

    while(q--){
        int ind, val; cin >> ind >> val;
        seg.upd(ind+1, val);
        cout << seg.resp[1] << endl;
    }
    

    return 0;
}

