template <class T> struct Seg{
    int n; ll neutral=-1e18;
    vector<T> tree, lazy;
    Seg(int n) : n(n), tree(4*(n+1), neutral), lazy(4*(n+1)) {}
    T join(T a, T b){
        return max(a,b);
    }
    void upd(int node, int l, int r, int pos, int val){
        if(l==r){
            tree[node]=val;
            return;
        }
        int mid=(l+r)>>1;
        if(pos<=mid){
            upd(2*node, l, mid, pos, val);
        }else{
            upd(2*node+1, mid+1, r, pos, val);
        }
        tree[node]=join(tree[2*node], tree[2*node+1]);
    }
    T query(int node, int l, int r, ll x){
        if(l == r) return tree[node] >= x ? l : 0;
        int mid=(l+r)>>1;

        if(tree[2*node] >= x){
            return query(2*node, l, mid, x);
        }
        else if(tree[2*node+1] >= x) return query(2*node+1, mid+1, r, x);
        return 0;
    }
    void upd(int pos, int val){
        upd(1, 1, n, pos, val);
    }
    T query(ll x){
        return query(1, 1, n, x);
    }
};

signed solve(){

    int n,q; cin >> n >> q;

    Seg<ll> seg(n);

    for(int i = 1; i <= n; i++){
        ll a; cin >> a;
        seg.upd(i, a);
    }

    // cout << seg.resp[1] << endl;

    while(q--){
        int op; cin >> op;
        ll ind, val; cin >> ind;
        if(op == 1){
            cin >> val;
            seg.upd(ind+1, val);
        }
        else{
            cout << seg.query(ind)-1 << endl;
        }
    }
    

    return 0;
}

