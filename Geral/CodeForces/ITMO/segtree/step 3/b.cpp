void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}

signed solve(){

    int n; cin >> n;
    vector<ll> bit(n+5, 0);
    vector<int> ans(n), arr(n);

    for(int &i: arr) cin >> i;

    for(int i = n-1; i > -1; i--){
        int cc = arr[i];
        int l = 1, r = n;
        while(l <= r){
            int md = (l+r)/2;

            int h = n-md-sum(n+2-md, bit);

            if(h == cc) ans[i] = md;
            if(h >= cc) l = md+1;
            else r = md-1;
        }
        upd(n+2-ans[i], 1, bit);
    }

    for(int i: ans) cout << i << ' ';
    cout << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}