signed solve(){
    
    int n; cin >> n;
    set<int> st;
    vector<int> p(0); 
    vector<int> vis(n+1);

    vis[1] = 1; p.push_back(1);


    for(int i = (n/2 % 2 == 0) ? n/2-1 : n/2; i > 1; i-=2){
        p.push_back(i); p.push_back(i*2);
        vis[i] = 1; vis[2*i] = 1;
        if(i > 1){
            for(int j = 3; i*j <= n; j+=2){
                if(vis[i*j] == 0){
                    vis[i*j] = 1;
                    p.push_back(i*j);
                }
            }
        }
        // if(i*2 > n/2 && vis[2*i-1] == 0) {p.push_back(i*2-1); vis[2*i-1] = 1;}
        // if(i*2 > n/2 && 2*i+1 <= n && vis[2*i+1] == 0 ) {p.push_back(i*2+1); vis[2*i+1] = 1;}
    }

    queue<int> put;
    for(int j = 1; j <= n; j+=2){
        if(vis[j] == 0 && j % 2 == 1){
            vis[j] = 1;
            put.push(j);
        }
    }

    int h = 0;
    for(int i = 1; i <= n; i++){
        if(vis[i] == 0) {
            p.push_back(i);
            h++;
        }
        if(h == 2){
            h = 0;
            if(!put.empty()){
                p.push_back(put.front()); put.pop();
            }
        }
    }

    while(!put.empty()){
        p.push_back(put.front()); put.pop();
    }
    
    assert(p.size() == n);

    for(int i: p) cout << i << ' ';
    cout << endl;

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}