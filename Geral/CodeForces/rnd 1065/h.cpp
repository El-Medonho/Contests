const int mod = 1e9+7;
const ll inf = 1e18+5;

const int lim = 450;

void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]=max(val, bit[ind]);
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans=max(bit[ind], ans);    
        ind-=ind&-ind;    
    }
    return ans;
}

signed solve(){
    
    int n,m; cin >> n >> m;

    vector<ll> dp(m+5, 0LL);
    ll ans = 0;
    dp[0] = -1e9;

    for(int i = 2; i <= min(lim, n); i++){
        ll mx = 0;
        for(int j = m; j >= 1; j--) {
            dp[j] = dp[j-1];
        }
        for(int j = i; j <= m; j++){
            mx = max(mx, dp[j]);
            ll h = j;
            int cnt = 0;
            while(h % i == 0) {h /= i; cnt++;}
            dp[j] = max(mx+cnt, dp[j]);

            if(i == n) {
                ans = max(ans, dp[j]);
            }
        }
    }


    
    if(n > lim) {

        vector<ll> bit(m+5, 0ll);
        for(int j = lim; j+(n-lim) <= m; j++){
            upd(j+(n-lim), dp[j], bit);
            ans = max(ans, dp[j]);
        }

        // for(int i = 1; i+(n-lim) <= m; i++) upd(i, dp[i], bit);

        for(int i = lim+1; i <= n; i++){
            for(int j = m/i; j > 0; j--){
                if(i*j+(n-i) > m) continue;
                ll mx = sum(i*j+(n-i), bit);
                ans = max(ans, mx+1);
                upd(i*j+(n-i), mx+1, bit);
            }
        }
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}