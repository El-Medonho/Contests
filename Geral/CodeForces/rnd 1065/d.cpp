signed solve(){
    
    int n; cin >> n;
    queue<int> qw;

    vector<pair<int,int>> ans(0);

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    int mx = -1;

    for(int i = n-1; i > -1; i--){
        while(!qw.empty() && qw.front() > arr[i]){
            int x = qw.front(); qw.pop();
            ans.emplace_back(arr[i], x);
        }
        int cc = arr[i];
        if(cc > mx){
            if(mx != -1) qw.push(mx);
            mx = cc;
        } else {
            ans.emplace_back(arr[i], mx);
        }
    }

    assert(ans.size() <= n-1);

    if(ans.size() < n-1){
        cout << "No\n";
        return 0;
    }
    else cout << "Yes\n";

    for(auto[a, b]: ans) cout << a << ' ' << b << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}