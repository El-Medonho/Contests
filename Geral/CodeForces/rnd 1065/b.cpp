signed solve(){
    
    int n; cin >> n;

    vector<int> arr(n, 0);

    for(int &i: arr){
        cin >> i;
    }

    if(arr[0] == -1 && arr[n-1] != -1) arr[0] = arr[n-1];
    else if(arr[0] != -1 && arr[n-1] == -1) arr[n-1] = arr[0];

    for(int i = 0; i < n; i++){
        if(arr[i] == -1){
            arr[i] = 0;
        }
    }

    cout << abs(arr[0]-arr[n-1]) << endl;
    for(int i: arr) cout << i << ' ';
    cout << endl;
    


    return 0;
}

