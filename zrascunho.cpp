#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n; cin >> n;
    vector<int> dp(n+1,-1e7); dp[0] = 1;

    int a,b,c; cin >> a >> b >> c;


    for(int i = 0; i <= n; i++){
        if(dp[i] == -1e7) continue;
        if(i+a <= n) dp[i+a] = max(dp[i+a], dp[i]+1);
        if(i+b <= n) dp[i+b] = max(dp[i+b], dp[i]+1);
        if(i+c <= n) dp[i+c] = max(dp[i+c], dp[i]+1);
    }

    cout << dp[n]-1 << '\n';

    return 0;
}