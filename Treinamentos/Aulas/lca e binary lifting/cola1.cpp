#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,q; cin >> n >> q;

    vector<vector<int>> jump(33, vector<int>(n));

    for(int i = 0; i < n; i++){
        cin >> jump[0][i]; jump[0][i]--;
    }

    for(int i = 1; i < 33; i++){
        for(int j = 0; j < n; j++){
            jump[i][j] = jump[i-1][jump[i-1][j]];
        }
    }

    while(q--){
        int x,k; cin >> x >> k; x--;
        for(int i = 32; i > -1; i--){
            if( (1LL<<i) & k ){
                x = jump[i][x];
            }
        }

        cout << x+1 << endl;
    }

    return 0;
}