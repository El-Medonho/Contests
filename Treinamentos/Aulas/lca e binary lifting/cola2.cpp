#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,q; cin >> n >> q;

    vector<vector<int>> bl(22, vector<int>(n));

    for(int i = 1; i < n; i++){
        int x; cin >> x; x--;
        bl[0][i] = x;
    }
    bl[0][0] = -2;

    for(int i = 1; i < 22; i++){
        for(int j = 0; j < n; j++){
            if(bl[i-1][j] == -2) bl[i][j] = -2;
            else bl[i][j] = bl[i-1][bl[i-1][j]];
        }
    }

    while(q--){
        int x,k; cin >> x >> k; x--;
        for(int i = 0; i < 22; i++){
            if(k & (1LL<<i)){
                x = bl[i][x];
                if(x == -2) break;
            }
        }
        cout << x+1 << endl;
    }

    return 0;
}