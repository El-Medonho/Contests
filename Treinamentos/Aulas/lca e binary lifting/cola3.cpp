#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,q; cin >> n >> q;

    vector<vector<int>> bl(22, vector<int>(n));
    vector<vector<int>> graph(n);
    vector<int> lvl(n, 0);

    function<void(int)> dfs;

    dfs = [&](int cc){
        for(int j: graph[cc]){
            lvl[j] = lvl[cc]+1;
            dfs(j);
        }
    };


    for(int i = 1; i < n; i++){
        int x; cin >> x; x--;
        bl[0][i] = x;
        graph[x].push_back(i);
    }
    
    dfs(0);

    bl[0][0] = 0;
    for(int i = 1; i < 22; i++){
        for(int j = 0; j < n; j++){
            bl[i][j] = bl[i-1][bl[i-1][j]];
        }
    }

    function<int(int,int)> lca;

    lca = [&](int a, int b){
        if(lvl[a] < lvl[b]) swap(a,b);

        int k = lvl[a]-lvl[b];
        for(int i = 0; i < 22; i++){
            if(k & (1<<i)){
                a = bl[i][a];
            }
        }

        if(a==b) return a;

        for(int i = 21; i > -1; i--){
            if(bl[i][a] != bl[i][b]){
                a = bl[i][a];
                b = bl[i][b];
            }
        }

        return bl[0][a];
    };

    while(q--){
        int a,b; cin >> a >> b;
        a--; b--;

        cout << lca(a,b)+1 << endl;
    }

    return 0;
}