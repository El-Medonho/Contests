#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);
    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b;
        a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> visited(n,0);

    function<void(int)> dfs;

    dfs = [&](int curr){

        for(int x: graph[curr]){
            if(visited[x]) continue;

            visited[x] = (visited[curr] == 1) ? 2 : 1;
            dfs(x); 
        }
    };

    for(int i = 0; i < n; i++){
        if(!visited[i]) {

            if(graph[i].empty()){
                cout << "NIE" << '\n';
                return 0;
            }

            dfs(i);
        }
    }

    cout << "TAK" << "\n";

    for(int i = 0; i < n; i++){
        if(visited[i] == 1) cout << 'K' << '\n';
        else cout << 'S' << '\n';
    }

    return 0;
}