#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;

    vector<vector<vector<vector<ll>>>> dp(n-2, vector<vector<vector<ll>>> (4, vector<vector<ll>> (4, vector<ll> (4, 0))));
    for(int i = 0; i < 4; i++){
        for(int j = 0; j < 4; j++){
            for(int k = 0; k < 4; k++){
                dp[0][i][j][k] = 1;
            }
        }
    }

    dp[0][0][1][2] = 0;
    dp[0][1][0][2] = 0;
    dp[0][0][2][1] = 0;

    for(int i = 1; i < n-2; i++){

        for(int j = 0; j < 4; j++){
            for(int k = 0; k < 4; k++){
                for(int l = 0; l < 4; l++){
                    for(int m = 0; m < 4; m++){

                        bool b1 = k == 0 && l == 1 && m == 2;
                        bool b2 = j == 0 && l == 1 && m == 2; 
                        bool b3 = j == 0 && k == 1 && m == 2;
                        bool b4 = k == 1 && l == 0 && m == 2;
                        bool b5 = k == 0 && l == 2 && m == 1;

                        if( b1 || b2 || b3 || b4 || b5 ) continue;

                        dp[i][k][l][m] += (dp[i-1][j][k][l]);
                        dp[i][k][l][m] %= ll(1e9+7);

                    }
                }
            }
        }
    }

    ll sum = 0;
    for(int i = 0; i < 4; i++){
        for(int j = 0; j < 4; j++){
            for(int k = 0; k < 4; k++){
                sum += dp[n-3][i][j][k];
                sum %= ll(1e9+7);
            }
        }
    }

    cout << sum << '\n';

    // j k l m

    // x 0 1 -> not 2
    // 0 1 x -> not 2
    // 0 x 1 -> not 2
    // x 0 2 -> not 1

    return 0;
}