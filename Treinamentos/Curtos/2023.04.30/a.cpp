#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;

    vector<vector<vector<vector<ll>>>> dp(n-2, vector<vector<vector<ll>>> (4, vector<vector<ll>> (4, vector<ll> (4, 0))));
    for(int i = 0; i < 4; i++){
        for(int j = 0; j < 4; j++){
            for(int k = 0; k < 4; k++){
                dp[0][i][j][k] = 1;
            }
        }
    }

    dp[0][0][1][2] = 0;
    dp[0][1][0][2] = 0;
    dp[0][0][2][1] = 0;

    for(int i = 1; i < n-3; i++){

        for(int j = 0; j < 4; j++){
            for(int k = 0; k < 4; k++){
                if(k == 2) continue;
                dp[i][0][1][k] += dp[i-1][j][0][1];
            }
        }

        for(int j = 0; j < 4; j++){
            for(int k = 0; k < 4; k++){
                if(k == 2) continue;
                dp[i][1][j][k] += dp[i-1][0][1][j];
            }
        }

        for(int j = 0; j < 4; j++){
            for(int k = 0; k < 4; k++){
                if(k == 2) continue;
                dp[i][0][1][k] += dp[i-1][j][0][1];
            }
        }

        for(int j = 0; j < 4; j++){
            for(int k = 0; k < 4; k++){
                if(k == 2) continue;
                dp[i][0][1][k] += dp[i-1][j][0][1];
            }
        }

    }

    // x 0 1 -> not 2
    // 0 1 x -> not 2
    // 0 x 1 -> not 2
    // x 0 2 -> not 1

    return 0;
}