#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;
    int k; cin >> k;
    ll a,b; cin >> a >> b;

    vector<vector<int>> graph(n), blockwho(n);
    vector<set<int>> arr(n);
    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);

        arr[a].insert(b);
        arr[b].insert(a);
    }

    k--;

    queue<int> qw;
    vector<int> vis(n, 0), visp(n, 0), block(n, -2);
    qw.push(k); vis[k] = 1; visp[k] = 1;

    while(!qw.empty()){
        int cc; cc = qw.front(); qw.pop();

        for(int j: graph[cc]){
            if(vis[j]) continue;
            vis[j] = vis[cc]+1;
            qw.push(j);
        }
    }

    qw.push(k);

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();

        for(int j: graph[cc]){
            queue<int> q2;
            for(int k: arr[j]){
                if(arr[cc].find(k) != arr[cc].end()) continue;
                q2.push(k);
                if(visp[k]) continue;
                visp[k] = visp[cc]+1;
                qw.push(k);
            }
            while(!q2.empty()){
                arr[j].erase(q2.front()); q2.pop();
            }
        }
    }

    for(int i = 0; i < n; i++){
        ll h = vis[i]; h--;
        ll ans = 2e18;
        ans = h*a;
        ans = min(ans, (h/2)*b + (h%2)*a);
        if(visp[i] == 0) visp[i] = 1e8;
        visp[i]--;
        ans = min(ans, (visp[i])*b);
        cout << ans << endl;
    }

    return 0;
}