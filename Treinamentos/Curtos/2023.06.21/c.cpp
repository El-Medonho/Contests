// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int w,n; cin >> w >> n;
    vector<pair<int,int>> arr(n);       //tempo, peso
    for(int i = 0; i < n; i++) cin >> arr[i].first >> arr[i].second;

    int m = (1<<(n));

    vector<int> dp(m,1e8);

    for(int i = 0; i < m; i++){
        int tot = 0, sum = 0;
        for(int j = 0; j < n; j++){
            if(i & (1<<j)){
                sum = max(sum, arr[j].first);
                tot += arr[j].second;
            }
        }
        if(tot > w) continue;
        else dp[i] = sum;
    }

    for(int i = 1; i < m; i++){
        if(dp[i] == 1e8){
            int h = 1e8;
            for(int j = i; j > 0; j = (j-1) & i){
                if(j == i) continue;
                h = min(h, dp[j]+dp[j^i]);
            }
            dp[i] = h;
        }
    }

    cout << dp[m-1] << '\n';

    return 0;
}