#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int w; cin >> w;
    int n; cin >> n;
    vector<int> arr(n);
    for(int &i: arr) cin >> i;
    sort(arr.begin(), arr.end());
    int l = 0, r = n-1;
    int ans = 0;
    while(l <= r){
        if(l == r){
            ans++;
            l++;
            continue;
        }
        if(arr[l]+arr[r] <= w){
            l++;
        }
        ans++;
        r--;
    }

    cout << ans << '\n';

    return 0;
}