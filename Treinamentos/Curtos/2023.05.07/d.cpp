#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    string s; cin >> s;
    int n = s.size();
    ll ans = 0;

    vector<ll> mask(1024, 0);
    int curr = 0;

    mask[0]++;
    for(int i = 0; i < n; i++){
        int cc = s[i]-'0';
        curr ^= (1<<cc);
        mask[curr]++;
    }

    for(int i = 0; i < 1024; i++){
        if(mask[i] > 1){
            ans += mask[i]*(mask[i]-1l)/2l;
        }
    }

    cout << ans << '\n';

    return 0;
}