#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<int> arr(n);
    vector<int> caras(m);

    for(int &i: arr) cin >> i;
    for(int &i: caras) cin >> i;

    vector<int> sum(n+1);
    sum[0] = 0;
    for(int i = 0; i < n; i++){
        sum[i+1] = sum[i]+arr[i];
    }
    
    for(int pp : caras){

        queue<int> qw;
        vector<int> next(n, -1);

        int p = min(pp, sum[n]);

        int cc = 0;
        bool possible = true;

        for(int i = 0; i < n; i++){
            qw.push(sum[i] + p);
            while(!qw.empty() && qw.front() < sum[i]){
                if(cc == i-1){
                    possible = false;
                    break;
                }
                next[cc] = i-1; cc++;
                qw.pop();
            }

            if(!possible) break;
        }

        if(!possible){
            cout << "NIE\n";
            continue;
        }

        for(int i = 0; i < n; i++){
            while(!qw.empty() && qw.front() < sum[i]+sum[n]){
                int gg = (i-1+n)%n;
                if(cc == gg && cc == n-1){
                    possible = false;
                    break;
                }
                next[cc] = gg; cc++;
                qw.pop();
            }

            if(!possible) break;
        }

        if(next[n-1] == -1) next[n-1] = n-1;

        if(!possible){
            cout << "NIE\n";
            continue;
        }

        int h = 0;

        for(int i = 0; i <= n; i++) h = next[h];

        int f = h;
        int ans = 1; h = next[h];

        while(h != f){
            h = next[h];
            ans++;
            if(next[f] < f && (h > f || h <= next[f])) break;
            if(next[f] > f && (h > f && h <= next[f])) break;
        }


        cout << ans << '\n';
    }

    return 0;
}