#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);
    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    function<int(int,int,int)> dfs;

    int diametro = 0;

    dfs = [&](int cc, int last, int val){

        int best = val;
        for(int j: graph[cc]){
            if(j == last) continue;

            int h = dfs(j, cc, val+1);
            diametro = max(diametro, best + h - 2*val + 1);
            best = max(best, h);
        }

        return best;
    };

    dfs(0,0,0);

    int ans = min(diametro, m+1);
    m -= diametro-1;

    if(m<=0){
        cout << ans << '\n';
        return 0;
    }

    int h = n-diametro;

    ans += min(m/2, h);

    cout << ans << endl;


    return 0;
}