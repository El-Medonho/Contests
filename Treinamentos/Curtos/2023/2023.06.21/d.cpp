#include "bits/stdc++.h"

using namespace std;

#define MOD 998244353
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

ll expo(ll b, ll e){
    ll ans = 1;
    while(e){
        if(e & 1) ans = (ans*b)%MOD;
        b = (b*b)%MOD;
        e>>=1;
    }
    return ans;
}

signed main(){
    fastio;

    ll n, a, b, p, q;  cin >> n >> a >> b >> p >> q;

    vector<vector<ll>> dp1(n+1, vector<ll> (n+1, 0)), dp2 = dp1; dp1[0][0] = 1; dp2[0][0] = 1;

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            for(int k = 1; k <= p; k++){
                if(j + k > n) continue;
                dp1[i+1][j+k] += dp1[i][j];
                if( dp1[i+1][j+k] > MOD ) dp1[i+1][j+k] -= MOD;
            }
        }
    }

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            for(int k = 1; k <= q; k++){
                if(j + k > n) continue;
                dp2[i+1][j+k] += dp2[i][j];
                if( dp2[i+1][j+k] > MOD ) dp2[i+1][j+k] -= MOD;
            }
        }
    }

    a = n-a; b = n-b;

    

    return 0;
}