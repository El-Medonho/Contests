#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n,q; cin >> n >> q;

    vector<pair<int,int>> arr(q);
    vector<bool> complete(n,0);

    for(int i = 0; i < q; i++){
        int a,b; cin >> a >> b;
        arr[i] = {a,b};
    }

    sort(arr.begin(), arr.end());

    complete[0] = 1;
    for(int i = 0; i < q; i++){
        pair<int,int> curr = arr[i];

        if(complete[curr.first-1] || complete[curr.second-1]){
            complete[curr.second-1] = 1;
            if(curr.second == n){
                cout << "Yes" << '\n';
                return 0;
            }
        }
    }

    cout << "No" << '\n';

    return 0;
}