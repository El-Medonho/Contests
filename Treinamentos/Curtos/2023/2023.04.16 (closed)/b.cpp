#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
#define endl "\n"

int solve(){
    ll n,m; cin >> n >> m;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    int numz = 0;
    for(ll i: arr) if(i == 0)numz++;

    // if(n == m){
    //     cout << "Richman" << endl;
    //     return 0;
    // }

    ll l = 0,r = 1e18;

    ll ans = -1;
    while(l <= r){
        ll md = (l+r)>>1;

        ll curr = md, sum = 0;
        for(int i = 0; i < n; i++){
            if(curr >= arr[i]){
                curr -= arr[i];
                sum++;
            }
        }

        if(sum <= m){
            l = md+1;
            if(sum == m){
                ans = max(ans, md);
            }
        }else{
            r = md-1;
        }
    }

    ll ans2 = 0; int ab = numz; int cd = 0;
    for(int i = 0; ab < m; i++) {
        ans2 += arr[i];
        if(arr[i] != 0) ab++;
        cd = i;
    }
    ll dif = 1e10;
    for(int i = cd+1; i < n; i++){
        if(arr[i] == 0) continue;
        dif = min(dif, arr[i]);
    }
    ans2 += dif-1;

    if(r == 1e18){
        cout << "Richman" << endl;
        return 0;
    }

    if(numz > m){
        cout << "Impossible" << endl;
    }else{
        // cout << max(ans,ans2) << endl;
        cout << ans2 << endl;
    }

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}