#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
#define endl "\n"

int solve(){
    ll n,m; cin >> n >> m;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    int numz = 0;
    for(ll i: arr) if(i == 0)numz++;

    if(n == m){
        cout << "Richman" << endl;
        return 0;
    }

    if(numz > m){
        cout << "Impossible" << endl;
        return 0;
    }


    ll ans2 = 0; int ab = 0; int cd = -1;
    for(int i = 0; ab < (m-numz); i++) {
        ans2 += arr[i];
        if(arr[i] != 0) ab++;
        cd = i;
    }
    ll dif = 1e10; 
    for(int i = cd+1; i < n; i++){
        if(arr[i] == 0) continue;
        dif = min(dif, arr[i]);
    }
    if(dif != 1e10) ans2 += dif-1;
    // ans2 += dif-1;


    cout << ans2 << endl;

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}