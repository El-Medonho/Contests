#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    // ifstream cin("diamond.in");
    // ofstream cout("diamond.out");

    ll n,k; cin >> n >> k;

    vector<ll> arr(n);
    for(ll &i : arr) cin >> i;

    sort(arr.begin(), arr.end());

    ll last = -1;
    pair<ll,ll> ans = {0,-1};
    for(ll i = 0; i < n; i++){
        if(arr[i] == last) continue;
        last = arr[i];
        ll curr = 0;
        for(ll j = i; j< n && abs(arr[j]-arr[i]) <= k; j++){
            curr++;
        }
        if(curr > ans.first) ans = {curr, arr[i]};
    }
    last = -1;
    ll hh = 0;
    for(ll i = 0; i < n; i++){
        if(arr[i] == last || abs(arr[i] - ans.second) <= k) continue;
        last = arr[i];
        ll curr = 0;
        for(ll j = i; j< n && abs(arr[j]-arr[i]) <= k; j++){
            if(abs(arr[j] - ans.second) <= k) break; 
            curr++;
        }
        if(curr > hh) hh = curr;
    }

    cout << hh + ans.first << "\n";

    return 0;
}