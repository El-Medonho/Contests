// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define f first
#define s second
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    vector<pair<ll,ll>> arr(n);

    for(pair<ll,ll> &i: arr) cin >> i.first >> i.second;

    vector<int> vis(n+1,0), ch(n+1,0);
    int ab = -1;
    
    ll ans = 0, b = 1e12;
    function<void(int)> dfs;

    dfs = [&](int cc){

        vis[cc] = 1;
        b = min(b, arr[cc].s);
        ans += arr[cc].s;

        if(!vis[arr[cc].f-1] && arr[cc].f-1 != ab) dfs(arr[cc].f-1);

        return;
    };

    function<void(int)> check;

    check = [&](int cc){

        if(ch[cc] || vis[cc]){
            ab = cc;
            ch[cc] = 0;
            return;
        }
        ch[cc] = 1;

        check(arr[cc].f-1);
        ch[cc] = 0;

        return;
    };

    for(int i = 0; i < n; i++){
        if(!vis[i]){
            check(i);
            if(ab == i) ab = -1;
            dfs(i);
            if(ab == -1) ans -= b;
            ab = -1;
            b = 1e12;
        }
    }

    cout << ans << '\n';

    return 0;
}