#include "bits/stdc++.h"
#include <ext/pb_ds/tree_policy.hpp>
#include <ext/pb_ds/assoc_container.hpp>

using namespace std;
using namespace __gnu_pbds;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef tree<ll,null_type, less_equal<ll>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

int main(){
    fastio;

    int n; cin >> n;
    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;
    vector<ll> sarr = arr;
    sort(sarr.begin(), sarr.end());

    vector<ll> sum(n+1,0);
    sum[0] = 0;
    for(int i = 1; i <= n; i++){
        sum[i] = sarr[i-1];
        sum[i] += sum[i-1];
    }

    ll ans = 0;
    for(ll i = 0; i < n; i++){
        ans += (i+1)*sarr[i];
    }

    int q; cin >> q;
    while(q--){
        ll i,x; cin >> i >> x; i--;
        ll k = ans;

        if(x == arr[i]){
            cout << ans << '\n';
            continue;
        }

        int ind = upper_bound(sarr.begin(), sarr.end(), arr[i]) - sarr.begin();
        k -= arr[i] * ind;
        if(upper_bound(sarr.begin(), sarr.end(), arr[i]) != sarr.end()) k -= sum[n] - sum[ind]; 

        int nind = upper_bound(sarr.begin(), sarr.end(), x) - sarr.begin() + 1;
        k += x * nind;
        if(upper_bound(sarr.begin(), sarr.end(), x) != sarr.end()){
            k += sum[n] - sum[nind-1];
            if(nind <= ind) k -= arr[i];  
        }
        if(nind > ind) k -= x;

        cout << k << '\n';
    }

    return 0;
}