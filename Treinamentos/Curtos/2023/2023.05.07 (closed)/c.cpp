#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef long double ld;

bool compare(ld a, ld b){
    if(a+1e-9 > b) return true;
    return false;
}

int main(){
    fastio;

    int n; cin >> n;
    vector<pair<ld,ld>> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i].first;
    }

    for(int i = 0; i < n; i++){
        cin >> arr[i].second;
    }

    ld ans = 1e9+10;

    ld l = 0, r = 1e9+50;
    int cnt = 100;
    while(cnt--){
        ld md = (l+r)/2;

        ld cc = 0;
        int h = 0;
        for(int i = 0; i < n; i++){
            ld gg = abs(md-arr[i].first)/arr[i].second;
            if( compare(gg,cc) ){
                if(arr[i].first > md) h = 1;
                else h = 0;
                cc = gg;
            }
        }
        ans = min(ans,cc);
        if(h){
            l = md;
        }else{
            r = md;
        }
    }

    cout << fixed << setprecision(7) << ans << '\n';

    return 0;
}