#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n; cin >> n;

    vector<int> par(n,-1), in(n,1), qtt(n,0); in[0] = 0;

    for(int i = 1; i < n; i++){
        cin >> par[i]; par[i]--;
        qtt[par[i]]++;

        in[par[i]] = 0;
    }

    vector<int> div(0);

    for(int i = 1; i * i <= n; i++){
        if(n % i == 0){
            div.push_back(i);
            if(i*i != n) div.push_back(n/i);
        }
    }

    vector<int> ans(0);

    for(int k: div){
        queue<int> qw;

        for(int i = 0; i < n; i++) if(in[i]) qw.push(i);

        vector<int> vis(n,0), req(n,0);

        bool g = true;
        while(!qw.empty()){

            int cc = qw.front(); qw.pop();

            if(cc == 0) break;

            int j = par[cc];
            vis[j]++;

            if(vis[j] == qtt[j]) qw.push(j);

            int h = req[cc]+1;

            if(h < k){
                if(req[j]+h >= k){
                    g = false;
                    break;
                }
                req[j] += h;
            }
        }

        if(req[0] == k-1 && g) ans.push_back(n/k);
    }

    sort(ans.begin(), ans.end());

    for(int i = 0; i < ans.size()-1; i++) cout << ans[i] << ' ';
    cout << ans.back() << '\n';

    return 0;
}