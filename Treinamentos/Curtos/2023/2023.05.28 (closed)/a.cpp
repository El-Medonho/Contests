#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    vector<vector<ll>> arr(n, vector<ll> (n));

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++) cin >> arr[i][j];
    }

    ll sum = 0;
    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++) sum += arr[i][j];
    }

    ll ans = 0;

    for(int i = 0; i < n; i++){
        ans = max(ans, sum - arr[n-1-i][i]);
    }

    cout << ans << '\n';

    return 0;
}