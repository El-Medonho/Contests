#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    ll n,k; cin >> n >> k;
    vector<ll> h(n);
    for(ll &i: h) cin >> i;
    multiset<ll> c;
    for(int i = 0; i < k; i++){
        ll x; cin >> x;
        c.insert(x);
    }

    sort(h.begin(), h.end());

    int ans = 0;
    for(int i = n-1; i > -1; i--){
        ll b = h[i]*5;
        if(c.lower_bound(b) != c.end()){
            ans++;
            c.erase(c.lower_bound(b));
        }else{
            b = h[i]*2;
            if(c.lower_bound(b) == c.end()){
                cout << "NIE\n";
                return 0;
            }
            ans++;
            c.erase(c.lower_bound(b));
            b = h[i]*3;
            if(c.lower_bound(b) == c.end()){
                cout << "NIE\n";
                return 0;
            }
            ans++;
            c.erase(c.lower_bound(b));
        }
    }

    cout << ans << endl;

    return 0;
}