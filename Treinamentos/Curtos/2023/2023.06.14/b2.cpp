#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n; cin >> n;

    vector<int> arr(n,-1);
    vector<int> deg(n,0);


    for(int i = 0; i < n; i++){
        int x; cin >> x; x--;
        arr[i] = x;
        deg[x]++;
    }    

    queue<int> qw;


    vector<bool> vis(n,0), match(n,0);

    function<void(int)> dfs;

    int ans = 0;

    dfs = [&](int cc){
        vis[cc] = 1;

        int nxt = arr[cc];

        deg[nxt]--;

        if(!match[cc]){
            if(!match[nxt]){
                ans++;
                match[cc] = 1; match[nxt] = 1;
                
                dfs(nxt);
                
            }
            else if(!vis[nxt] && !deg[nxt]) dfs(nxt); 
        }
        else if(!vis[nxt] && !deg[nxt]) dfs(nxt);

        return;
    };

    for(int i = 0; i < n; i++){
        if(deg[i] == 0 && vis[i] == 0) dfs(i);
    }

    for(int i = 0; i < n; i++){
        if(!vis[i]) dfs(i);
    }

    cout << ans << '\n';

    return 0;
}