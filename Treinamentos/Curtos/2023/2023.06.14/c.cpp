// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    ll n,m; cin >> n >> m;

    vector<ll> a(n), b(n);

    for(ll &i: a) cin >> i;
    for(ll &i: b) cin >> i;

    ll r = 2*1e18, l = 0;

    ll mx = n*m;

    ll ans = 0;

    while(l <= r){
        ll md = (l+r) >> 1;

        ll sum = 0;
        for(int i = 0; i < n; i++){
            if(b[i] >= a[i]){
                sum += (b[i] + md - 1) / b[i];
            }
            else{
                ll h = 0;
                ll cc = min(m, (a[i] + md - 1) / a[i]);
                h += cc * a[i];
                sum += cc;
                h = md-h;
                if(h > 0) sum += (b[i] + h - 1) / b[i];
            }
            if(sum > mx) break;
        }

        if(sum > mx) {
            r = md-1;
        }else{
            ans = max(ans, md);
            l = md+1;
        }
    }

    cout << ans << '\n';

    return 0;
}