#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n,m; cin >> n >> m;
    
    vector<ll> arr(n);
    for (ll &i: arr) cin >> i;

    vector<ll> score(m + 1);
    for(int i = 1; i <= m; i++) cin >> score[i];

    map<ll,ll> mp;
    int i = 0, j = 0;
    ll sum = 0, ans = 0;

    while(j < n){

        sum += score[arr[j]];
        mp[arr[j]]++;
        
        while(mp[arr[j]] > 1){
            mp[arr[i]]--;
            sum -= score[arr[i]];
            i++;
        }

        ans = max(ans, sum);

        j++;
    }

    cout << ans << '\n';

    return 0;
}