// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<int> arr(n), s(m);

    ll l = 0, r = 0, ans = 0;

    for(int &i: arr) {cin >> i; i--;}
    for(int &i: s) {cin >> i; r += i;}

    ll tot = r;

    while(l <= r){
        ll md = (l+r)/2;

        vector<int> freq(m, 0);

        ll res = tot - md;

        ll cc = 0, lc = 0, b = 0;

        int i = 0, j = 0;

        for(; i < n; i++){
            if(freq[arr[i]] == 1) {
                cc -= s[arr[i]];
                lc += s[arr[i]];
            }

            else if(!freq[arr[i]]) cc += s[arr[i]];
            
            freq[arr[i]]++;

            while(lc > res){
                freq[arr[j]]--;

                if(freq[arr[j]] == 1) {
                    lc -= s[arr[j]];
                    cc += s[arr[j]];
                }
                else if(!freq[arr[j]]) cc -= s[arr[j]];

                j++;
            }

            b = max(b, cc);
        }

        ans = max(ans, b);

        if(b >= md){
            l = md+1;
        }
        else r = md-1;
    }

    cout << ans << endl;

    return 0;
}