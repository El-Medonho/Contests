#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n,k; cin >> n >> k; k--;
    int nn = n;

    vector<ll> diff(0);

    ll l; cin >> l; n--;
    while(n--){
        ll x; cin >> x;
        diff.push_back(x-(l+1));
        l = x;
    }

    sort(diff.begin(), diff.end());
    ll ans = 0; int j = diff.size()-1;
    while(k-- && j > -1){
        ans -= diff[j]; j--;
    }

    ans += nn;
    for(ll i: diff) ans += i;

    cout << ans << '\n';


    return 0;
}