#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n; cin >> n;

    vector<vector<int>> revgraph(n);
    vector<int> graph(n);

    for(int i = 0; i < n; i++){
        int a,b; cin >> a; a--; b = i;

        graph[b] = a;
        revgraph[a].push_back(b);
    }

    int ans = 0, ch = -1;

    vector<int> vis(n,0);

    function<void(int)> dfs;

    dfs = [&](int cc){
        vis[cc] = 1;

        if(vis[graph[cc]]) ch = graph[cc];
        else dfs(graph[cc]);

        vis[cc] = 0;

        return;
    };



    for(int i = 0; i < n; i++){
        if(vis[i]) continue;

        dfs(i);

        queue<int> qw; qw.push(i);
        vis[i] = 1;

        int h1 = 1, h2 = 0;

        while(!qw.empty()){
            int cc = qw.front(); qw.pop();
            
            for(int j: revgraph[cc]){
                if(vis[j]) continue;
                
                if(revgraph[j].size() == 0) vis[j] = -1;
                else {
                    vis[j] = vis[cc]+1;
                    if(vis[j]%2 == 1) h1++;
                    else h2++;
                }
                qw.push(j);
            }
            if(vis[graph[cc]]) continue;
            int j = graph[cc];
            
            vis[j] = vis[cc]+1;
            if(vis[j]%2 == 1) h1++;
            else h2++;
            qw.push(j);

        }

        ans += max(h1,h2);
    }

    cout << ans << '\n';

    return 0;
}