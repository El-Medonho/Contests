#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define endl "\n"

int main(){
    fastio;

    ifstream cin("diamond.in");
    ofstream cout("diamond.out");

    int n, k; cin >> n >> k;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    if(n < 3){
        cout << n << endl;
        return 0;
    }

    map<int,int> mp, invmp;
    set<int> st;

    for(int i: arr){
        st.insert(i);
    }

    vector<int> cnt(n,0);
    int occ = 0;
    for(auto x: st){
        mp[occ] = (x);
        mp[(x)] = occ++;
    }

    for(int i: arr){
        auto x = st.find(i);
        cnt[mp[*x]]++;

        while(x != st.end()){
            x++;
            if((*x) - k > i) break;
            cnt[mp[*x]]++;
        }

        x = st.find(i);

        while(x != st.begin()){
            x--;
            if((*x) + k < i) break;
            cnt[mp[*x]]++;
        }
    }

    int fst = 0, ind = -1;
    for(int i = 0; i < cnt.size(); i++){
        if(cnt[i] > fst){
            fst = cnt[i];
            ind = i;
        }
    }
    int ans = fst; ind = invmp[ind]; fst = 0;
    for(int i: arr){
        if(abs(i-ind) > k) continue;
        fst = max(fst, cnt[mp[ind]]);
    }
    ans += fst;
    cout << ans << endl;

    return 0;
}