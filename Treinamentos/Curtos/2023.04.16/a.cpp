#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n,a,b; cin >> n >> a >> b;

    vector<ll> arr(n);
    ll sum = 0;
    for(ll &i: arr) {
        ll kk; cin >> kk;
        sum += kk;
        i = sum;
    }

    multiset<ll> st;
    a--; b--;
    for(int i = a; i <= b; i++){
        st.insert(arr[i]);
    }

    ll ans = -1e17;
    for(int i = 0; i < n && a < n; i++){
        ll dif = (i == 0) ? 0 : arr[i-1];
        ans = max(ans, (*st.rbegin() - dif));
        st.erase(st.find(arr[a]));
        a++; b++;
        if(b < n) st.insert(arr[b]);
    }

    cout << ans << "\n";

    return 0;
}