// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

void upd(ll ind, ll val, map<ll,ll> &bit, vector<ll> &h){   //O(logn)
    while(ind<=1e7+2){
        h.push_back(bit[ind]);
        bit[ind]=max(bit[ind],val);
        ind+=ind&-ind;
    }
}
void updd(ll ind, ll val, map<ll,ll> &bit, vector<ll> &h){   //O(logn)
    int i = 0;
    while(ind<=1e7+2){
        bit[ind]=h[i]; i++; 
        ind+=ind&-ind;
    }
}
ll sum(ll ind, map<ll,ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans=max(ans,bit[ind]);    
        ind-=ind&-ind;    
    }
    return ans;
}

int main(){
    fastio;

    int n; cin >> n;
    vector<ll> arr(n);
    vector<ll> st;
    for(int i = 0; i < n; i++){
        int x; cin >> x;
        arr[i] = x; st.push_back(x);
    }
    sort(st.begin(),st.end());
    for(int i = 0; i < n; i++){
        arr[i] = lower_bound(st.begin(), st.end(), arr[i]) - st.begin() + 1;
    }
    map<ll,ll> mp;

    vector<vector<int>> graph(n);
    for(int i = 0; i < n-1; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    function<void(int,int)> dfs;

    vector<ll> ans(n,0);

    dfs = [&](int curr, int last){

        vector<ll> h(0);

        ans[curr] = sum(arr[curr]-1, mp) + 1;
        upd(arr[curr], ans[curr], mp, h);
        ans[curr] = max(ans[curr], sum(1e6, mp));

        for(int j: graph[curr]){
            if(j == last) continue;
            dfs(j,curr);
        }
        updd(arr[curr], ans[curr], mp, h);

        return;
    };

    dfs(0,-1);

    for(int i: ans) cout << i << '\n';

    return 0;
}