#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    ll n,w; cin >> n >> w;
    vector<vector<ll>> arr(n, vector<ll>(3,0));
    for(int i = 0; i < n; i++){
        cin >> arr[i][0] >> arr[i][1] >> arr[i][2];
    }
    sort(arr.begin(), arr.end());

    priority_queue<pair<ll,ll>, vector<pair<ll,ll>>, greater<pair<ll,ll>>> qw;
    int ind = 0;
    ll wtr = 0;
    for(int i = 0; i < 1e5*2+10; i++){
        while(ind < n && arr[ind][0] <= i){
            wtr += arr[ind][2];
            qw.push({arr[ind][1], arr[ind][2]});
            ind++;
        }
        while(!qw.empty() && qw.top().first <= i){
            wtr -= qw.top().second;
            qw.pop();
        }
        if(wtr > w){
            cout << "No\n";
            return 0;
        }
    }

    cout << "Yes\n";

    return 0;
}