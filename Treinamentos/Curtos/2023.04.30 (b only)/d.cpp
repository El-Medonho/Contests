#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    ll n,m; cin >> n >> m;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    sort(arr.begin(), arr.end());
    int r = n-1, l = 0;

    vector<int> done(n,0);
    ll ans = 0;
    
    ans = accumulate(arr.begin(), arr.end(), 0ll);

    while(l < r){
        if(done[r]) break;
        if(arr[l]+arr[r] <= m){
            done[l] = 1;
            l++;
            r--;
        }
        else{
            ans++;
            r--;
        }
    }

    cout << ans+1 << '\n';

    return 0;
}