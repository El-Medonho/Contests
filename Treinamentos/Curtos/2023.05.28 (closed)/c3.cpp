#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n; cin >> n;
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }    

    set<int> st;

    vector<int> ans(n,0); 

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        int rem = -1;

        if( st.lower_bound(arr[cc]) != st.end() ){
            rem = (*st.lower_bound(arr[cc]));
            st.erase(rem);
        }
        st.insert(arr[cc]);
        ans[cc] = st.size();

        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j, cc);
        }

        st.erase(arr[cc]);
        if(rem != -1){
            st.insert(rem);
        }
        
        return;
    };

    dfs(0,-1);

    for(int i: ans) cout << i << '\n';

    return 0;
}