#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef long double ld;

int solve(){

    int n,k; cin >> n >> k;

    if(n >= 500){
        cout << 0 << '\n';
        return 0;
    }

    vector<vector<ll>> dp(n+5, vector<ll> (6*n+8, 0LL));

    dp[0][0] = 1;

    for(int i = 0; i < n ; i++){
        for(int j = 0; j <= 6*n + 3; j++){
            for(int p = 1; p <= 6; p++){
                dp[i+1][j+p] += dp[i][j];
            }
        }
    }

    ll div = accumulate(dp[n].begin(), dp[n].end(), 0);

    ld h = (ld) (dp[n][k]) / div;
    // ll hh = 100* (dp[n][k]) / div;

    cout << floor(h*100) << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}