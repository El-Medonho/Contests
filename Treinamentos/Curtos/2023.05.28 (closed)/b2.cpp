#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;
typedef long double ld;

int solve(){

    int n,k; cin >> n >> k;

    if(n >= 800 || k > 6*n || k < n){
        cout << 0 << '\n';
        return 0;
    }

    vector<vector<ld>> dp(n+5, vector<ld> (6*n+30, 0));

    dp[0][0] = 1;

    for(int i = 0; i < n ; i++){
        for(int j = 0; j <= 6*n+1; j++){
            for(int p = 1; p <= 6; p++){
                dp[i+1][j+p] += dp[i][j]/6;
            }
        }
    }

    int h = floor(dp[n][k]*100);

    cout << h << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}