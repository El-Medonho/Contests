#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)


long long mod(long long b, long long m){
    return (b % m + m)%m;
}

int solve(){

    int n,x,p; cin >> n >> x >> p;

    if(p <= n){
        int soma = x;
        for(int i = 1; i <= p; i++){
            soma += i;
            soma%=n;
            if(soma == 0){
                cout << "Yes" << '\n';
                return 0;
            }
        }
        cout << "No" << '\n';
        return 0;
    }


    vector<int> arr(n,0);
    int sum = x;
    for(int i = 1; i <= n; i++){
        sum = mod(sum+i, n);
        arr[sum] = 1;
        if(sum == 0){
            cout << "Yes" << '\n';
            return 0;
        }
        if(i == p){
            cout << "No" << '\n';
            return 0;
        }
    }

    if(p <= n){
        cout << "No" << '\n';
        return 0;
    }

    int dif = mod(sum-x,n);
    int atual = x;
    int zero = 0;
    for(int i = 0; i < min((p/n)-1, n+10); i++){
        atual = mod(atual + dif, n);
        zero = mod(zero-dif, n);

        if(arr[zero]){
            cout << "Yes" << '\n';
            return 0;
        }
    }

    for(int i = 1; i <= (p%n)+n; i++){
        atual = (atual+i)%n;
        if(atual == 0){
            cout << "Yes" << '\n';
            return 0;
        }
    }

    cout << "No" << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}