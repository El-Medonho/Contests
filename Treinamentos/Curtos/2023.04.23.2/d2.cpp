#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n,q; cin >> n >> q;

    vector<vector<int>> graph(n+1, vector<int>(0));

    for(int i = 0; i < q; i++) {
        int l,r; cin >> l >> r;
        l--;
        graph[r].push_back(l);
        graph[l].push_back(r);
    }

    vector<int> vis(n+1,0);

    function<bool(int)> dfs;

    dfs = [&](int curr){
        if(vis[curr]) return false;
        if(curr == n) return true;
        vis[curr] = 1;

        for(int i: graph[curr]){
            if(!vis[i] && dfs(i)) return true; 
        }

        return false;
    };

    if(dfs(0)) {
        cout << "Yes" << '\n';
    }else{
        cout << "No" << '\n';
    }

    return 0;
}