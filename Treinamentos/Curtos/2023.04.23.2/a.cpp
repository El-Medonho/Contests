#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; ll w; cin >> n >> w;

    vector<ll>wei(n);
    vector<ll>val(n);

    set<ll> pesos;

    for(int i = 0; i < n; i++){
        cin >> wei[i] >> val[i];
    }

    pesos.insert(w);
    for(int j = 0; j < n; j++){
        for(ll i:pesos){
            if(i-wei[j] >= 0) pesos.insert(i-wei[j]);
        }
    }

    vector<ll> dp(pesos.size(), 0);

    map<ll,ll> mp;
    int cnt = 0;
    for(ll i: pesos){
        mp[i] = cnt++;
    }

    pesos.clear(); pesos.insert(w);
    for(int i = 0; i < n; i++){
        for(ll j:pesos){
            if(j-wei[i] >= 0) {
                pesos.insert(j-wei[i]);
                dp[mp[j-wei[i]]] = max(dp[mp[j-wei[i]]], dp[mp[j]] + val[i]);
            }
        }
    }

    ll ans = 0;
    for(int i = 0; i < cnt; i++){
        ans = max(ans, dp[i]);
    }

    cout << ans << '\n';

    return 0;
}