#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int solve(){

    int n; cin >> n;

    vector<int> kk(10,0);

    while(n--){
        string str; cin >> str;
        int h = str.back() - '0';
        kk[h]++;
    }

    for(int i = 0; i < 10; i++){
        if(kk[i]){
            kk[i]--;
            for(int j = i; j < 10; j++){
                if(kk[j]){
                    kk[j]--;
                    for(int k = j; k < 10; k++){
                        if(kk[k] && (i+j+k) % 10 == 3){
                            cout << "yes" << '\n';
                            return 0;
                        }
                    }
                    kk[j]++;
                }
            }
            kk[i]--;
        }
    }

    cout << "no" << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--) solve();

    return 0;
}