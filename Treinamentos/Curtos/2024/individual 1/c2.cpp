const ll inf = 1e18;

signed solve(){

    int n,m; cin >> n >> m;

    vector<pair<pair<int,int>,pair<int,int>>> edges(m);
    vector<vector<pair<int,int>>> graph(n);

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b;
        int w,c; cin >> w >> c;
        a--; b--;
        edges[i] = {{w,c},{a,b}};
    }
    
    ll ans = 0;

    sort(edges.begin(), edges.end());

    for(int i = 0; i < m; i++){
        auto [h,hh] = edges[i];
        auto[w,c] = h;
        auto[a,b] = hh;

        priority_queue<pair<ll,ll>, vector<pair<ll,ll>>, greater<pair<ll,ll>>> pq;

        vector<bool> vis(n, 0);
        vector<ll> dist(n, inf);

        dist[a] = 0;

        pq.push({0,a});

        while(!pq.empty()){
            auto[d,cc] = pq.top(); pq.pop();

            if(vis[cc]) continue;

            for(auto[j, d2]: graph[cc]){
                if(d + d2 >= dist[j] || vis[j]) continue;

                dist[j] = d+d2;
                pq.push({d+d2, j});
            }
        }

        if(w < dist[b]) {
            ans += c;
            graph[a].push_back({b,w});
            graph[b].push_back({a,w});
        }
    }

    cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}