struct edge{
    int j, l, c;
    edge(int _j = -1, int _l = 0, int _c = 0) : j(_j), l(_l), c(_c) {}
};

signed solve(){

    int n,m; cin >> n >> m;

    vector<vector<edge>> graph(n);

    ll ans = 0;
    ll sum = 0;

    for(int i = 0; i < m; i++){
        int a,b,l,c; cin >> a >> b >> l >> c; a--; b--;
        graph[a].push_back(edge(b,l,c));
        graph[b].push_back(edge(a,l,c));
        ans += c;
    }

    for(int i = 0; i < n; i++){
        vector<ll> dist(n, 1e18), cost(n, 1e18);
        vector<bool> vis(n, 0);
        dist[i] = 0;
        cost[i] = 0;
        vis[i] = 1;

        priority_queue<pair<pair<ll,ll>,ll>, vector<pair<pair<ll,ll>,ll>>, greater<pair<pair<ll,ll>,ll>>> pq;
        
        for(auto[j,w,c]: graph[i]){
            vis[j] = 1;
            cost[j] = c+1;
            dist[j] = w+1;
            pq.push({{w+1,c+1},j});
        }

        while(!pq.empty()){
            // cout << 'a' << endl;
            auto[h, cc] = pq.top(); pq.pop();

            auto[w2,c2] = h;

            if(vis[cc]) continue;
            vis[cc] = 1;

            for(auto[j,w,c]: graph[i]){
                dist[j] = min(w+w2, dist[j]);
                if(w2 + w >= dist[j] || vis[j]) continue;

                // if(w2 + w == dist[j]){
                //     if(c+c2 >= cost[j]) continue;
                // }

                cost[j] = c+c2;

                pq.push({{dist[j], cost[j]}, j});
            }
        } 

        for(auto[j,w,c]: graph[i]){
            if(dist[j] < w+1) sum += c;
        }
    }

    cout << ans-sum/2 << endl;
    

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}