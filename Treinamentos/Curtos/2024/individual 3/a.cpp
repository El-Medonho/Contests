#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m,k; cin >> n >> m >> k;

    uniform_int_distribution<int> uid(0, n-1);

    vector<string> arr(n);

    for(string &i: arr) cin >> i;

    int cnt = 70;
    int cnt2 = cnt;

    vector<int> ac(n, 0);

    while(cnt--){
        int cc = uid(rng);

        int c2 = 0;

        for(int i = 0; i < n; i++){
            if(i == cc) continue;

            int ch = 0;
            for(int j = 0; j < m; j++){
                if(arr[i][j] != arr[cc][j]) ch++;
            }

            if(ch == k) {ac[i]++; c2++;}
        }

        if(c2 == n-1){
            cout << cc+1 << endl;
            return 0;
        }
    }

    cnt = cnt2;

    vector<int> tof(0);

    for(int i = 0; i < n; i++){
        if(ac[i] == cnt) {tof.push_back(i); ac[i] = 1;}
    }

    for(int cc : tof){

        if(!ac[cc]) continue;
        
        int c2 = 0;

        for(int i = 0; i < n; i++){
            if(i == cc) continue;

            int ch = 0;
            for(int j = 0; j < m; j++){
                if(arr[i][j] != arr[cc][j]) ch++;
            }

            if(ch != k) ac[i] = 0;

            else c2++;
        }

        if(c2 == n-1){
            cout << cc+1 << endl;
            return 0;
        }
    }
    

    return 0;
}