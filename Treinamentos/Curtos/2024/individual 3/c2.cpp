#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    vector<vector<int>> dp(n, vector<int>(n, 1e8));

    for(int i = 0; i < n; i++) {
        dp[i][i] = 1;

        if(i < n-1) dp[i][i+1] = (arr[i] == arr[i+1] ? 1 : 2);
    }

    for(int sz = 3; sz <= n; sz++){
        for(int i = 0; i <= n-sz; i++){
            int sum = 0;
            int last = i;

            dp[i][i+sz-1] = 1 + dp[i+1][i+sz-1];

            for(int j = i+1; j < i+sz; j++){
                if(arr[j] != arr[i]) continue;

                if(j-last >= 2) sum = dp[last+1][j-1];
                else sum = 0;

                // last = j;

                int cc = sum + 1;

                cc += dp[j][i+sz-1] - 1;                

                dp[i][i+sz-1] = min(dp[i][i+sz-1], cc);
            }
        }
    }

    cout << dp[0][n-1] << endl;

    return 0;
}