struct edge{
    int j, t, h;
    edge(int _j, int _t, int _h) : j(_j), t(_t), h(_h) {}
};

const ll inf = 1e14;

signed solve(){

    int k,n,m; cin >> k >> n >> m;

    vector<vector<edge>> graph(n);

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        int t,h; cin >> t >> h;

        graph[a].push_back(edge(b,t,h));
        graph[b].push_back(edge(a,t,h));
    }

    int ini, fim; cin >> ini >> fim;
    ini--; fim--;

    priority_queue<pair<pair<ll,ll>,int>, vector<pair<pair<ll,ll>,int>>, greater<pair<pair<ll,ll>,int>>> pq;

    pq.push({{0,0},ini});

    vector<vector<ll>> dist(n, vector<ll>(205, inf));

    dist[ini][0] = 0;

    while(!pq.empty()){
        auto[g, cc] = pq.top(); pq.pop();

        auto[h1, d] = g;

        if(dist[cc][h1] < d) continue;

        for(auto[j,t,h2] : graph[cc]){
            int h3 = h1 + h2;
            if(h3 >= k || dist[j][h3] <= t+d) continue;

            dist[j][h3] = t+d;

            pq.push({{h3, t+d}, j});
        }
    }

    ll ans = *min_element(dist[fim].begin(), dist[fim].end());
    
    if(ans == inf) cout << -1 << endl;
    else cout << ans << endl;

    return 0;
}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}