signed solve(){

    int n; cin >> n;

    ll d; cin >> d;

    multiset<pair<pair<ll,ll>,ll>> st[2];

    vector<ll> ans(2*n, -1);

    for(int i = 0; i < n; i++){
        ll a,b; cin >> a >> b;

        st[0].insert({{b,a}, i});
    }

    for(int i = 0; i < n; i++){
        ll a,b; cin >> a >> b;

        st[1].insert({{a,b}, n+i});
    }

    queue<vector<ll>> qw;

    for(int i = 0; i < 2; i++){
        vector<pair<pair<ll,ll>,ll>> arr(0);
        for(auto [h, ind]: st[i]){
            auto [a,b] = h;


            if(a == 0){
                arr.push_back({h,ind});
                qw.push({a,b,ind,i});
                ans[ind] = 1;
            }
            else break;
        }

        for(auto h: arr) st[i].erase(st[i].find(h)); 
    }

    while(!qw.empty()){
        auto hh = qw.front();
        qw.pop();

        // auto [a,b,ind,i] = hh;

        ll a = hh[0], b = hh[1], ind = hh[2], i = hh[3];

        ll nxt = (i + 1) % 2;

        if(st[nxt].empty()) continue;

        auto it = st[nxt].upper_bound({{b,1e11},0});

        if(it == st[nxt].begin()) continue;

        it--;

        vector<pair<pair<ll,ll>,ll>> arr(0);

        bool ok = true;

        for(; ok; it--){
            if(it == st[nxt].begin()) ok = false;

            auto[h, j] = *it;

            if(ans[j] != -1) continue;

            if(h.first > b) continue;

            if(h.first + d < b) break;

            ans[j] = ans[ind] + 1;

            arr.push_back(*it);

            qw.push({h.first, h.second, j, nxt});
        }

        for(auto x: arr){
            st[nxt].erase(st[nxt].find(x));
        }
    }

    for(int i = 0; i < n; i++){
        cout << ans[i] << endl;
    }

    // for(int i = 0; i < 2*n; i++){
    //     cout << ans[i] << endl;
    // }

    return 0;
}

