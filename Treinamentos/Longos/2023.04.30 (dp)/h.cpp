#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;


int main(){
    fastio;

    int n,m; cin >> n >> m;
    vector<string> grid(n);
    for(string &s: grid) cin >> s;

    vector<vector<ll>> occur(n, vector<ll> (m,0)); occur[0][0] = 1;
    vector<vector<int>> vis(n, vector<int>(m,0));

    queue<pair<int,int>> qw; qw.push({0,0});

    ll mo = 1e9+7;

    vector<vector<int>> arr(0);
    arr.push_back({1,0});
    arr.push_back({0,1});


    while(!qw.empty()){
        pair<int,int> curr = qw.front(); qw.pop();
        vis[curr.first][curr.second] = 1;

        for(int i = 0; i < 2; i++){
            pair<int,int> next = {curr.first + arr[i][0], curr.second + arr[i][1]};

            if(next.first >= n || next.first < 0 || next.second >= m || next.second < 0 || grid[next.first][next.second] == '#' || vis[next.first][next.second]) continue;

            if(!occur[next.first][next.second]) qw.push(next);
            occur[next.first][next.second] = (occur[curr.first][curr.second] + occur[next.first][next.second]) % mo;
        }
    }

    cout << occur[n-1][m-1] << '\n';

    return 0;
}