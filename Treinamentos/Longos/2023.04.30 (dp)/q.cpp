// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]=max(bit[ind],val);
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans=max(ans,bit[ind]);    
        ind-=ind&-ind;    
    }
    return ans;
}

int main(){
    fastio;

    int n; cin >> n;
    vector<ll> bit(n+1,0);
    
    vector<int> h(n);
    for(int &i: h) cin >> i;

    vector<ll> a(n);
    for(ll &i: a) cin >> i;

    for(int i = 0; i < n; i++){
        ll p = sum(h[i], bit);
        upd(h[i], p+a[i], bit);
    }

    cout << sum(n,bit) << '\n';

    return 0;
}