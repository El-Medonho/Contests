#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);
    vector<int> arr(n,0);

    while(m--){
        int x,y; cin >> x >> y; x--; y--;
        graph[x].push_back(y);
        arr[y]++;
    }

    queue<pair<int,int>> qw;
    for(int i = 0; i < n; i++){
        if(arr[i] == 0) qw.push({i,0});
    }

    vector<int> occur(n,0);
    int ans = 0;
    while(!qw.empty()){
        pair<int,int> curr = qw.front(); qw.pop();
        ans = curr.second;

        for(int j: graph[curr.first]){
            occur[j]++;
            if(occur[j] == arr[j]) qw.push({j,curr.second+1});
        }
    }

    cout << ans << '\n';

    return 0;
}