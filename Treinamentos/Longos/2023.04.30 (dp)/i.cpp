#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long double ld;
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;

    vector<ld> arr(n);
    for(int i = 0; i < n; i++){
        cin >> arr[i];
    }

    vector<vector<ld>> dp(n+1, vector<ld>(n+1,0));
    dp[0][0] = 1;

    // dp[1][0] = arr[0];
    // dp[0][1] = 1-arr[0];

    for(int i = 1; i <= n; i++){
        for(int j = 0; j <= i; j++){
            if(j != 0) dp[i-j][j] += dp[i-j][j-1] * (1-arr[i-1]);
            if(j != i) dp[i-j][j] += dp[i-j-1][j] * (arr[i-1]);
        }
        // while(dp[i-j][j] > 1e14) dp[i-j][j]/=10;
    }

    ld ans = 0;
    for(int i = n/2+1; i <= n; i++){
        ans += dp[i][n-i];
    }

    cout << setprecision(9) << fixed << ans << '\n';

    return 0;
}