// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define MOD 1000000007
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n,k; cin >> n >> k;
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<vector<ll>> dp(n, vector<ll>(k+1,0)), oldp(n, vector<ll>(k+1,0)); oldp[0][k] = 1;

    for(int i = 0; i < n; i++){
        ll sum = 0;
        for(int j = k; j > -1; j--){
            sum += oldp[i][j];
            if(j+arr[i] < k) sum -= oldp[i][j+arr[i]+1];
            dp[i][j] = sum;
            
            if(i < n-1) oldp[i+1][j] += (dp[i][j]%MOD);
        }
    }

    cout << (dp[n-1][0]%MOD) << '\n';

    return 0;
}