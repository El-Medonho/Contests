#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    vector<vector<ll>> dp(n, vector<ll> (n,1e15));
    vector<ll> sum(n,0);
    ll s = 0;
    for(int i = 0; i < n; i++) {
        ll x; cin >> x;
        s+=x;
        sum[i] = s;
        dp[i][i] = 0;
    }
    for(int d = 1; d < n; d++){
        for(int i = 0; i < n; i++){
            int j = i+d;
            if(j >= n) break;
            for(int k = i; k < j; k++){
                dp[i][j] = min(dp[i][j], dp[i][k]+dp[k+1][j]+sum[j]-((i == 0) ? 0 : sum[i-1]));
            }
        }
    }

    // for(int i = 0; i < n; i++) dp[0][n-1] -= dp[i][i];
    cout << dp[0][n-1] << '\n';

    return 0;
}