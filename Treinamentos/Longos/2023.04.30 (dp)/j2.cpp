// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long double ld;

int main(){
    fastio;

    vector<int> qtt(3,0);
    int nn; cin >> nn;

    vector<vector<vector<ld>>> dp(nn+1, vector<vector<ld>>(nn+1, vector<ld> (nn+1,0)));

    for(int i = 0; i < nn; i++){
        int x; cin >> x;
        if(x == 3) qtt[0]++;
        if(x == 2) qtt[1]++;
        if(x == 1) qtt[2]++;
    }

    ld n = nn;

    dp[qtt[0]][qtt[1]][qtt[2]] = 0;

    for(int i = qtt[0]; i > -1; i--){
        ld ii = i;
        int cnti = qtt[0]-i;
        for(int j = qtt[1] + cnti; j > -1; j--){
            ld jj = j;
            int cntj = qtt[1]+cnti - j;
            for(int k = qtt[2] + cntj; k > -1; k--){
                if(i == 0 && j == 0 && k == 0) continue;
                ld kk = k;
                ld p = (ii+jj+kk);
                dp[i][j][k] += n/p; 
                if(i != 0) dp[i-1][j+1][k] += dp[i][j][k] * (ii/p);
                if(j != 0) dp[i][j-1][k+1] += dp[i][j][k] * (jj/p);
                if(k != 0) dp[i][j][k-1] += dp[i][j][k] * (kk/p);
            }
        }
    }

    cout << dp[0][0][0] << '\n';

    return 0;
}