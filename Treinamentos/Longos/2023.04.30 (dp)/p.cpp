// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define MOD 1000000007
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<pair<ll,ll>> dp(n, {1,1}); 

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        for(int j: graph[cc]){

            if(j == last) continue;
            dfs(j,cc);
            
            dp[cc].first *= dp[j].second;
            dp[cc].first %= MOD;
            dp[cc].second *= (dp[j].first + dp[j].second)%MOD;
            dp[cc].second %= MOD;
            
        }
    };

    dfs(0,-1);

    cout << (dp[0].first + dp[0].second)%MOD << '\n';

    return 0;
}