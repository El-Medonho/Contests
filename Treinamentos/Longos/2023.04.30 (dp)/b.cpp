#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n,k; cin >> n >> k;
    vector<long long> dp(n,1e15);
    dp[0] = 0;

    vector<long long> arr(n);
    for(long long &i: arr) cin >> i;

    for(int i = 0; i < n; i++){
        for(int j = 1; j <= k && i+j < n; j++){
            dp[i+j] = min(dp[i+j], dp[i] + labs(arr[i]-arr[i+j]));
        }
    }

    cout << dp[n-1] << '\n';

    return 0;
}