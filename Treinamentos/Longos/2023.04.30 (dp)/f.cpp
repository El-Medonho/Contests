#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    string s,t; cin >> s >> t;
    if(s.size() < t.size()) swap(s,t);

    // dp ve o menor indice em que j caras aparecem ate o indice i de t
    //dp[i][j] = min(dp[i-1][j], )
    vector<vector<int>> dp(t.size(), vector<int> (t.size() + 1, s.size()));

    for(int i = 0; i < t.size(); i++){
        dp[i][0] = -1;

        char c = t[i];

        if(i == 0){
            for(int k = 0; k < s.size(); k++){
                if(c == s[k]) {dp[i][1] = k; break;}
            }
            continue;
        }

        for(int j = 0; j < t.size() + 1; j++){
            dp[i][j] = dp[i-1][j];
        }
        int h = 1;

        for(int k = 0; k < s.size(); k++){
            if(c == s[k]){
                while(h <= t.size() && dp[i-1][h-1] < k){
                    dp[i][h] = min(dp[i][h], k);
                    h++;
                }
                if(dp[i-1][h-1] == s.size()) break;
            }
        }
    }

    int ans = 0;
    for(int i = 1; i <= t.size(); i++){
        if(dp[t.size()-1][i] != s.size()) ans = i;
    }

    if(ans == 0){
        cout << '\n';
        return 0;
    }

    int p = t.size()-1;
    int last = dp[p][ans];
    string str = ""; str += s[last];
    for(int i = p; i > -1; i--){
        if(dp[i][ans] == last) continue;
        else{
            ans--;
            if(ans == 0) break;
            last = dp[i][ans];
            str += s[last];
        }
    }

    reverse(str.begin(), str.end());

    cout << str << '\n';

    return 0;
}