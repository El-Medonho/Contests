// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define MOD 1000000007
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    vector<vector<int>> grid(n, vector<int>(n));
    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            cin >> grid[i][j];
        }
    }

    vector<ll> dp((1<<n), 0); dp[0] = 1;

    for(int i = 0; i < (1<<n); i++){
        int cnt = 0;
        for(int j = 0; j < n; j++){
            if(i & (1<<j)) cnt++;
        }
        for(int j = 0; j < n; j++){
            if( !(i & (1<<j)) && grid[cnt][j]) dp[i ^ (1<<j)] = ( dp[i ^ (1<<j)] + dp[i] )%MOD;
        }
    }

    cout << dp[(1<<n)-1] << '\n';

    return 0;
}