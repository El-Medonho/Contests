// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    ll n,w; cin >> n >> w;
    vector<vector<long long>> dp(n, vector<long long> (1e5+1,1e10));

    vector<int> ww(1e5+1,0); ww[0] = 1; 
    for(int i = 0; i < n; i++){
        dp[i][0] = 0;
        long long a,b; cin >> a >> b;
        if(i == 0){
            dp[0][b] = a;
            ww[b] = 1;
            continue;
        }
        for(int j = ww.size()-1; j > -1; j--){
            if(ww[j]){
                dp[i][j] = min(dp[i][j], dp[i-1][j]);
            }
            if(ww[j] && dp[i-1][j] + a <= w){
                dp[i][j+b] = min(dp[i-1][j+b], dp[i-1][j] + a);
                ww[j+b] = 1;
            }
        }
    }

    int ans = 0;
    for(int i = 0; i < 1e5+1; i++){
        if(ww[i]) ans = i;
    }

    cout << ans << '\n';

    return 0;
}