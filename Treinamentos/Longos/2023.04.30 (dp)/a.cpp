#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n; cin >> n;
    vector<long long> dp(n,1e15);
    dp[0] = 0;

    vector<long long> arr(n);
    for(long long &i: arr) cin >> i;

    for(int i = 0; i < n-2; i++){
        dp[i+1] = min(dp[i+1], dp[i] + labs(arr[i]-arr[i+1]));
        dp[i+2] = min(dp[i+2], dp[i] + labs(arr[i]-arr[i+2]));
    }
    dp[n-1] = min(dp[n-1], dp[n-2] + labs(arr[n-1]-arr[n-2]));

    cout << dp[n-1] << '\n';

    return 0;
}