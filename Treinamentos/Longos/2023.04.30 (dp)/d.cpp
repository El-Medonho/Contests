#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n,w; cin >> n >> w;
    vector<vector<long long>> dp(n, vector<long long> (w+1,0l));

    vector<pair<int,int>> arr(n);
    vector<int> ww(w+1,0); ww[0] = 1;
    for(int i = 0; i < n; i++){
        long long a,b; cin >> a >> b;
        // cin >> arr[i].first >> arr[i].second;
        if(i == 0){
            dp[0][a] = b;
            ww[a] = 1;
            continue;
        }
        for(int j = ww.size()-1; j > -1; j--){
            if(ww[j]){
                dp[i][j] = max(dp[i][j], dp[i-1][j]);
            }
            if(ww[j] && j+a<=w){
                dp[i][j+a] = max(dp[i-1][j+a], dp[i-1][j] + b);
                ww[j+a] = 1;
            }
        }
    }

    cout << (*max_element(dp[n-1].begin(), dp[n-1].end())) << '\n';

    return 0;
}