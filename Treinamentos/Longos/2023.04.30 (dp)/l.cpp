#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    vector<vector<vector<ll>>> dp(n, vector<vector<ll>> (n, {0LL,0LL}));
    
    for(int i = 0; i < n; i++) dp[i][i][0] = arr[i];


    for(int d = 1; d < n; d++){
        for(int i = 0; i < n; i++){
            int j = i+d;
            if(j >= n) break;
            if(d%2 == 0){
                if(dp[i+1][j][0] + arr[i] - dp[i+1][j][1] >= dp[i][j-1][0] + arr[j] - dp[i][j-1][1]) dp[i][j] = {dp[i+1][j][0] + arr[i], dp[i+1][j][1]};
                else dp[i][j] = {dp[i][j-1][0] + arr[j], dp[i][j-1][1]};
            }else{
                if(dp[i+1][j][1] + arr[i] - dp[i+1][j][0] >= dp[i][j-1][1] + arr[j] - dp[i][j-1][0]) dp[i][j] = {dp[i+1][j][0], dp[i+1][j][1] + arr[i]};
                else dp[i][j] = {dp[i][j-1][0], dp[i][j-1][1] + arr[j]};
            }
        }
    }

    int k = n%2;
    cout << -dp[0][n-1][k] + dp[0][n-1][(k+1)%2] << '\n';

    return 0;
}