#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    int n; cin >> n;
    string s; cin >> s;

    ll ans = 0, j = 0;
    vector<int> kmp(n,0);

    for(int i = 1; i < n; i++){
        while(j > 0 && s[j] != s[i]) j = kmp[j-1];
        if(s[i] == s[j]) j++;
        kmp[i] = j;

        int k = (i-j+1);

        ans += ((j+k-1)/k)*k;
        cout << ((j+k-1)/k)*k << ' ';
    }

    cout << '\n';

    cout << ans << '\n';

    return 0;
}