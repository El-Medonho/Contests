#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    string s; cin >> s;
    int k; cin >> k;
    int n = s.size();

    int ans = 0;

    for(int gg = 0; gg < n;){

        ans++;

        vector<int> kmp(1,0);
        int j = 0;
        int l = gg;


        for(int i = gg+1; i < n; i++){
            while(j > 0 && s[j+gg] != s[i]) j = kmp[j-1];
            if(s[i] == s[j+gg]) j++;
            if(i-j >= gg+k) break;
            l = i;
            kmp.push_back(j);
        }

        gg = l+1;
    }

    cout << ans << '\n';

    return 0;
}