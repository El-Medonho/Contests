#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    // vector<ll> ans(n,0);
    vector<vector<int>> graph(n);
    for(int i = 0; i < n-1; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<ll> nos(n,0), pnos(n,0), dist(n,0), pdist(n,0);

    function<void(int,int)> dfs;

    dfs = [&](int curr, int last){
        nos[curr] = 1;

        for(int j: graph[curr]){
            if(j == last) continue;
            dfs(j, curr);
            nos[curr] += nos[j];
            dist[curr] += dist[j] + nos[j];
        }
        return;
    };

    dfs(0,-1);

    // pnos[0] = nos[0]; pdist[0] = dist[0];

    function<void(int,int)> dfs2;

    dfs2 = [&](int curr, int last){
        pnos[curr] = nos[curr]; pdist[curr] = dist[curr];

        if(last != -1){
            pnos[curr] += pnos[last] - nos[curr];
            pdist[curr] += pdist[last] - dist[curr] - nos[curr] + pnos[last] - nos[curr];
        }

        for(int j: graph[curr]){
            if(j == last) continue;
            dfs2(j, curr);
        }
        return;
    };

    dfs2(0,-1);

    for(int i = 0; i < n; i++) cout << pdist[i] << ' ';
    cout << '\n';

    return 0;
}