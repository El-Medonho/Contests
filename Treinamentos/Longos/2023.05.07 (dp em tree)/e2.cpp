#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n; cin >> n;
    
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<int> cor = {0,0};
    for(int i: arr){
        if(i == 0) continue;
        cor[i-1]++;
    }

    vector<vector<int>> graph(n);
    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<vector<int>> sub(n,{0,0});

    function<void(int,int)> dfs;

    dfs = [&](int curr, int last){

        if(arr[curr] != 0) sub[curr][arr[curr]-1]++;

        for(int j: graph[curr]){
            if(j == last) continue;
            dfs(j,curr);
            sub[curr][0] += sub[j][0]; sub[curr][1] += sub[j][1];
        }

        return;
    };

    int ans = 0;

    dfs(0,-1);

    function<void(int,int)> dfs2;

    dfs2 = [&](int curr, int last){

        vector<int> temp = {0,0};
        if(last != -1){
            temp[0] += cor[0] - sub[curr][0];
            temp[1] += cor[1] - sub[curr][1];
        }

        bool pass = true;
        if(temp[0] != 0 && temp[1] != 0){
            pass = false;
        }
        for(int j: graph[curr]){
            if(j == last) continue;
            if(sub[j][0] != 0 && sub[j][1] != 0){
                pass = false; 
                continue;
            }
        }

        if(pass){
            ans++;
        }

        for(int j: graph[curr]){
            if(j == last) continue;
            dfs2(j,curr);
        }

        return;
    };

    dfs2(0,-1);

    cout << max(0,ans-1) << '\n';

    return 0;
}