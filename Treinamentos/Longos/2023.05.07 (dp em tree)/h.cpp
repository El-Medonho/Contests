#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define MOD 1000000007

typedef long long ll;

ll expo(ll b, ll e){
    ll res = 1;
    while(e){
        if(e&1) res = (res*b)%MOD;
        b = (b*b)%MOD;
        e>>=1;
    }
    return res;
}

int main(){
    fastio;

    ll n; cin >> n;
    vector<ll> ans(n,0);

    ll fat = 1;
    for(int i = 1; i <= n; i++) fat = (fat*i)%MOD;

    vector<ll> inv(n+1,1);
    inv[0] = expo(1 ,MOD-2);
    for(int i = 1; i <= n; i++) inv[i] = expo(i, MOD-2);

    vector<ll> nos(n,0);

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    function<void(int,int)> dfs;
    dfs = [&](int curr, int last){

        nos[curr]++;

        for(int j: graph[curr]){
            if(j == last) continue;
            dfs(j,curr);
            nos[curr] += nos[j];
        }

        return;
    };

    dfs(0,-1);

    ll hh = fat;
    for(ll i: nos) hh = (hh*inv[i])%MOD;

    function<void(int,int)> dfs2;
    dfs2 = [&](int curr, int last){

        ll g = hh;
        ll temp = 0;
        if(last != -1){
            g = (g * nos[curr]) % MOD;
            g = (g * inv[n - nos[curr]]) % MOD;
        }
        hh = g;

        ans[curr] = hh;

        for(int j: graph[curr]){
            hh = g;
            if(j == last) continue;
            dfs2(j,curr);
            nos[curr] += nos[j];
        }

        hh = g;

        return;
    };

    dfs2(0,-1);

    for(ll i: ans) cout << i << '\n';

    return 0;
}