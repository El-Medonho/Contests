#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n; cin >> n;
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<vector<int>> graph(n);
    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    // function<void(int,int)>

    queue<int> qw; qw.push(0);
    int atual = 0;

    vector<int> cnt(3,0);

    for(int i = 0; i < n; i++){
        cnt[arr[i]]++;
    }

    vector<int> vis(n,0); vis[0] = 1;
    int h1 = -1, h2 = -1;
    while(!qw.empty()){
        int cc = qw.front(); qw.pop();
        for(int j: graph[cc]){
            if(vis[j]) continue;
            if(arr[j] && atual && arr[j] != atual) {
                if(h1 == -1){
                    h1 = j;
                    continue;
                }
                else{
                    cout << 0 << '\n';
                    return 0;
                }
                continue;
            }
            if(arr[j] && atual == 0){
                atual = arr[j];
            }else{
                cnt[atual]--;
            }
            qw.push(j);
            vis[j] = 1;
        }
    }

    qw.push(h1);
    fill(vis.begin(), vis.end(), 0);

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();
        for(int j: graph[cc]){
            if(vis[j]) continue;
            if(arr[j] == atual){
                if(h2 == -1){
                    h2 = j;
                    continue;
                }
                else{
                    cout << 0 << '\n';
                    return 0;
                }
            }
            if(arr[j] && arr[j] != atual) cnt[atual%2+1]--;
            vis[j] = 1;
            qw.push(j);
        }
    }

    function<int(int,int)> dfs;
    
    dfs = [&](int curr, int last){
        int ans = 0;    
        if(curr == h2) return 1;
        for(int j: graph[curr]){
            if(j == last) continue;
            ans = dfs(j,curr);
            if(ans) return ans+1;
        }
        return 0;
    };
    
    cout << dfs(h1,-1)-1 << '\n';

    return 0;
}