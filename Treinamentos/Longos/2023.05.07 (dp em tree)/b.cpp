#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    vector<vector<int>> graph(n);
    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<ll> depth(n,0), pdepth(n,0);

    function<void(int,int)> dfs;

    dfs = [&](int curr, int last){

        for(int j: graph[curr]){
            if(j == last) continue;
            dfs(j,curr);
            depth[curr] = max(depth[curr], depth[j]+1);
        }

    };

    dfs(0,-1);

    function<void(int,int)> dfs2;

    dfs2 = [&](int curr, int last){

        ll temp = 0;
        bool pass = true;
        for(int j: graph[curr]){
            if(j == last) continue;
            if(pass && depth[j] == depth[curr]-1){
                pass = false;
                continue;
            }
            temp = max(temp, depth[j]+1);
        }

        for(int j: graph[curr]){
            if(j == last) continue;
            if(depth[j]+1 == depth[curr]){
                depth[j] = max(depth[j], temp+1);
            }else{
                depth[j] = max(depth[j], depth[curr]+1);
            }
            dfs2(j,curr);
        }
        return;
    };

    dfs2(0,-1);

    for(int i = 0; i < n; i++) cout << depth[i] << ' ';
    cout << '\n';

    return 0;
}