#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n; cin >> n;

    vector<vector<int>> graph(n), cor(n);
    for(int i = 1; i < n; i++){
        int a,b,c; cin >> a >> b >> c; a--; b--;
        graph[a].push_back(b);
        cor[a].push_back(c);
        graph[b].push_back(a);
        cor[b].push_back(c);
    }

    int qtt = 0;
    vector<int> ans(0);
    vector<map<int,int>> occ(n);

    function<void(int,int)> dfs;
    
    dfs = [&](int curr, int last){


        int h = -1;
        for(int i = 0; i < graph[curr].size(); i++){
            if(graph[curr][i] == last) h = i;
            occ[curr][cor[curr][i]]++;
        }

        if(last != -1 && occ[curr][cor[curr][h]] > 1) qtt++;

        for(int j: graph[curr]){
            if(j == last) continue;
            dfs(j,curr);
        }
        return;
    };

    dfs(0,-1);

    function<void(int,int)> dfs2;

    dfs2 = [&](int curr, int last){


        int h = -1;
        for(int i = 0; i < graph[curr].size(); i++){
            if(graph[curr][i] == last) h = i;
        }

        if(last != -1 && occ[curr][cor[curr][h]] > 1) qtt--;

        if(!qtt) ans.push_back(curr);

        for(int i = 0; i < graph[curr].size(); i++){
            int j = graph[curr][i];
            if(j == last) continue;
            if(occ[curr][cor[curr][i]] == 1) dfs2(j,curr);
        }
        if(last != -1 && occ[curr][cor[curr][h]] > 1) qtt++;
        return;
    };

    dfs2(0,-1);
    sort(ans.begin(), ans.end());

    cout << ans.size() << '\n';
    for(int i: ans) cout << i+1 << '\n';

    return 0;
}