#include "bits/stdc++.h"

using namespace std;

#define MOD 1000000007
#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

typedef long long ll;

void modsum(ll &a, ll b){
    a = ((a+b)%MOD + MOD)% MOD;
    return;
}

void modsum2(ll &a, ll b){
    a = ((-a+b)%MOD + MOD)% MOD;
    return;
}

int main(){
    fastio;

    int n = 1e6+5;
    vector<ll> dp(n+1, 0);

    vector<ll> pot(n+50, 1);
    for(int i = 1; i < n+30; i++) pot[i] = (pot[i-1]<<1)%MOD;

    for(int i = 1; i <= n; i++){
        
        modsum(dp[i], pot[i]);
        for(int j = i*2; j <= n; j+=i){
            modsum(dp[j], -dp[i]);
        }

    }

    int t; cin >> t;

    while(t--){
        ll h; cin >> h;

        if(h == 0) cout << 0 << '\n';

        else{
            ll g = h;
            h = dp[h];
            modsum2(h, pot[g]);
            cout << h << '\n';
        }
    }

    return 0;
}