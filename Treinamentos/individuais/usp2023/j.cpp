#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,m; cin >> m >> n;

    vector<vector<pair<int,int>>> graph(n);

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b;
        a--; b--;
        graph[a].push_back({b,i});
        graph[b].push_back({a,i});
    }

    vector<int> arr(n);

    for(int i = 0; i < n; i++) cin >> arr[i];

    vector<int> vis(n, 0);

    for(int i = 0; i < n; i++){
        if(arr[i] && !vis[i]){
            int cnt = 0;

            queue<int> qw;
            vis[i] = 1; qw.push(i); cnt = 1;

            while(!qw.empty()){
                int cc = qw.front(); qw.pop();

                for(auto [j,a]: graph[cc]){
                    if(vis[j]) continue;
                    qw.push(j);
                    if(arr[j]) cnt++;
                    vis[j] = 1;
                }
            }

            if(cnt & 1){
                cout << -1 << '\n';
                return 0;
            }
        }
    }

    vector<int> ans(0);
    fill(vis.begin(), vis.end(), 0);

    // vector<vector<int>> edge(n);

    function<bool(int,int)> dfs;

    dfs = [&](int cc, int num){

        bool hit = false;
        vis[cc] = 1;

        if(arr[cc]) hit = true;

        for(auto [j,a]: graph[cc]){
            if(vis[j]) continue;

            hit = hit ^ dfs(j,a);
        }

        if(hit) ans.push_back(num+1);

        return hit;
    };

    // dfs(0, -1);

    for(int i = 0; i < n; i++){
        if(arr[i] && !vis[i]) dfs(i, -1);
    }

    cout << ans.size() << '\n';

    for(int i = 0; i < ans.size(); i++) cout << ans[i] << " \n"[i == ans.size()-1];

    return 0;
}