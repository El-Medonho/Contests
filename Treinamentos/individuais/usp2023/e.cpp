#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;


signed main(){
    fastio;

    int n; cin >> n;
    vector<ll> a(n),b(n),c(n), w(n);

    for(int i = 0; i < n; i++) cin >> a[i] >> b[i] >> c[i] >> w[i];

    function<ll(vector<ll>&, ll)> f;

    f = [&](vector<ll> &x, ll cc){
        ll res = 0;
        for(int i = 0; i < n; i++){
            res += (x[i]-cc) * (x[i]-cc) * w[i];
        }
        return res;
    };

    function<ll(ll, ll, vector<ll>&)> ternary;

    ternary = [&](ll l, ll r, vector<ll> &x){
        while(r-l>=5){
            int mid=(l+r)>>1;
            if(f(x, mid)>f(x, mid+1)){
                l=mid;
            }else{
                r=mid+1;
            }
        }
        for(int i=l+1; i<=r; i++){
            if(f(x, l)>f(x, i)) l=i;
        }
        return l;
    };

    ll xx = ternary(1, 1e6, a);
    ll y = ternary(1, 1e6, b);
    ll z = ternary(1, 1e6, c);

    cout << xx << ' ' << y << ' ' << z << '\n';

    return 0;
}