#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,m; cin >> n >> m;
    vector<vector<int>> graph(n);
    vector<int> visited(n,0); vector<int> lowlink(n,0); vector<int> depth(n,0);

    while(m--){
        int a,b; cin >> a >> b; a--; b--;

        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    int tempo = 0;

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        lowlink[cc] = depth[cc] = ++tempo;
        visited[cc] = 1;

        for(int j: graph[cc]){
            if(j == last) continue;

            if(visited[j]) lowlink[cc] = min(lowlink[cc], depth[j]);
            
            else {
                dfs(j, cc);
                lowlink[cc] = min(lowlink[cc], lowlink[j]);
            }
        }

    };

    dfs(0, -1);

    vector<vector<int>> scc(n+1);

    for(int i = 0; i < n; i++){
        scc[lowlink[i]].push_back(i);
    }

    int cnt = 0;

    for(int i = 0; i < n; i++){
        if(scc[i].size() == 0) continue;
        if(scc[i].size() < 3){
            cout << -1 << '\n';
            return 0;
        }
        else cnt++;
    }

    cout << cnt << '\n';

    for(int i = 0; i < n; i++){
        if(scc[i].size() == 0) continue;
        cout << scc[i].size() << ' ';
        for(int i: scc[i]) cout << i+1 << ' ';
        cout << '\n';
    }

    return 0;
}