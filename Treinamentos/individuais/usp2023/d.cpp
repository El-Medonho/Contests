#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,q; cin >> n >> q;

    vector<queue<int>> arr(q);
    vector<int> entry(0), ind(n, -1);
    int l = 0;

    vector<int> ans(0);

    for(int i = 0; i < 2*n; i++){
        int c; cin >> c;

        if(c == 1){
            int x,k; cin >> x >> k; x--; k--;

            entry.push_back(x);
            ind[x] = entry.size()-1;
            arr[k].push(x);
        }

        else{
            int k; cin >> k; k--;
            int x = arr[k].front(); arr[k].pop();

            for(; l < ind[x]; l++){
                if(ind[entry[l]] != -1) ans.push_back(entry[l]+1);
            }
            ind[x] = -1;
        }
    }

    cout << ans.size() << '\n';

    sort(ans.begin(), ans.end());

    for(int i = 0; i < ans.size(); i++) cout << ans[i] << " \n"[i == ans.size()-1];

    return 0;
}