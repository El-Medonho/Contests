#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    int n; ll f; cin >> n >> f;

    bool h = false;

    while(n--){
        ll a,b; cin >> a >> b;

        if(h) continue;

        if(a < b) swap(a,b);

        if(f < b){
            cout << "N\n";
            h = true;
            continue;
        }

        if(f < a) f += b;
        else f +=a ;
    }

    if(!h) cout << "S\n";

    return 0;
}