#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    int n; cin >> n;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    vector<ll> pos(0);

    for(int i = 0; i < n; i++){
        if(arr[i] & 1) pos.push_back(i);
    }

    ll a,b; cin >> a >> b; a = min(a,b);

    if(pos.size() & 1){
        cout << -1 << '\n';
        return 0;
    }

    int N = n;

    n = pos.size();

    if(n == 0){
        cout << 0 << '\n';
        return 0;
    }

    ll ans = 0;

    for(int i = 0; i < n; i+=2){
        ans += abs(pos[i] - pos[i+1]);
    }

    ll ans2 = abs(pos[0] + N - pos[n-1]);

    for(int i = 1; i < n-1; i+=2){
        ans2 += abs(pos[i] - pos[i+1]);
    }

    cout << min(ans*a, ans2*a) << '\n';


    return 0;
}