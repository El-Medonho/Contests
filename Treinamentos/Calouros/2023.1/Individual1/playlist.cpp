#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n; cin >> n;
    
    vector<int> arr(n);
    for(int i = 0; i < n; i++){
        cin >> arr[i];
    }

    map<int,int> mapa;

    for(int i: arr) mapa[i] = -1;

    int l = -1;
    int ans = 0;

    for(int i = 0; i < n; i++){
        int k = arr[i];
        if(mapa[k] > l){
            ans = max(ans, i-1-l);
            l = mapa[k];
        }
        mapa[k] = i;
    }
    ans = max(ans, n-1-l);

    cout << ans << '\n';

    return 0;
}