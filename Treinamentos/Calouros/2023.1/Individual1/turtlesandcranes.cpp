#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int x,y; cin >> x >> y;

    if(y%2==1) {
        cout << "No\n";
    }
    else{

        bool ans = false;
        for(int i = 0; i <= x; i++){
            int j = x-i;
            if(2*i + 4*j == y) ans = true;
        }

        if(ans) cout << "Yes\n";
        else cout << "No\n";
    }

    return 0;
}