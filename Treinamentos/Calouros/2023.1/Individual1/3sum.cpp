#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int solve(){

    int n; cin >> n;

    vector<int> mapa(10, 0);

    for(int i = 0; i < n; i++){
        int x; cin >> x;
        x = x % 10;
        mapa[x]++;
    }

    for(int i = 0; i <= 9; i++){
        if(mapa[i] <= 0) continue;
        mapa[i]--;

        for(int j = 0; j <= 9; j++){
            if(mapa[j] <= 0) continue;
            mapa[j]--;

            for(int k = 0; k <= 9; k++){
                if(mapa[k] <= 0) continue;

                int sum = (i+j+k)%10;
                if(sum == 3){
                    cout << "YES\n";
                    return 0;
                }
            }

            mapa[j]++;
        }

        mapa[i]++;
    }

    cout << "NO\n";

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}