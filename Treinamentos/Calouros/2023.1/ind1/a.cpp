#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n; cin >> n;
    vector<int> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i];
    }

    set<int> st;
    int ans = 0;
    int l = 0, r = 0;

    while(r < n){

        if(st.find(arr[r]) != st.end()){     //O(logn)
            st.erase(arr[l]);
            l++;
        }
        else{
            st.insert(arr[r]);
            ans = max(ans, (int) st.size());
            r++;
        }
    }

    cout << ans << '\n';

    return 0;
}