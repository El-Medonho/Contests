#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<vector<int>> edges(m, vector<int>(3));
    vector<ll> dist(n, 0);
    vector<int> pre(n, -1);
    // dist[0] = 0;
    for(int i = 0; i < m; i++){
        cin >> edges[i][0] >> edges[i][1] >> edges[i][2]; edges[i][0]--; edges[i][1]--;
    }

    int N = n-1;
    while(N--){
        for(int i = 0; i < m; i++){
            ll a = edges[i][0], b = edges[i][1], custo = edges[i][2];        
            // if(dist[a] == 1e18) continue;
            
            if(dist[a] + custo < dist[b]){
                dist[b] = dist[a] + custo;
                pre[b] = a;
            }
        }
    }

    int ciclo = -1;

    for(int i = 0; i < m; i++){
        int a = edges[i][0], b = edges[i][1], custo = edges[i][2];        
        // if(dist[a] == 1e18) continue;
        
        if(dist[a] + custo < dist[b]){
            dist[b] = dist[a] + custo;
            pre[b] = a;
            ciclo = a;
        }
    }

    if(ciclo == -1){
        cout << "NO\n";
        return 0;
    }
    
    cout << "YES\n";


    N = n;

    while(N--) ciclo = pre[ciclo];
    
    vector<int> resp(0);

    int inicial = ciclo; resp.push_back(ciclo);

    do{
        ciclo = pre[ciclo];
        resp.push_back(ciclo);
    } 
    while(ciclo != inicial);

    reverse(resp.begin(), resp.end());
    
    for(int i: resp) cout << i+1 << ' ';
    cout << '\n';
    
    return 0;
}