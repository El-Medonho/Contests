#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;


signed main(){
    fastio;

    int n,m; cin >> n >> m;
    vector<vector<pair<ll,int>>> graph(n);

    while(m--){
        int a,b,c; cin >> a >> b >> c; a--; b--;
        // a e b sao os vertices da aresta, c é o peso
        
        graph[a].push_back( {c,b} );

    }

    vector<ll> dist(n, 1e18);

    priority_queue<pair<ll,int>, vector<pair<ll,int>>, greater<pair<ll,int>>> pq;
    pq.push({0,0});
    dist[0] = 0;

    // achar o menor caminho entre o 0 e o n-1

    // vector<int> pre(n, -1);

    while(!pq.empty()){

        int atual = pq.top().second; ll custo_total = pq.top().first;
        pq.pop();
        
        if(custo_total > dist[atual]) continue;

        for(pair<ll,int> next: graph[atual]){
        
            ll prox = next.second, peso = next.first;
            if(custo_total + peso < dist[prox]){
                ll prox_custo = custo_total + peso;
                pq.push({prox_custo, prox});
                dist[prox] = prox_custo;
            }
        }
    }

    int cc = n-1;
    vector<int> resp(0); resp.push_back(n-1);

    while(pre[cc] != -1){
        resp.push_back(pre[cc]);
        cc = pre[cc];
    }

    reverse(pre.rbegin(), pre.rend());

    for(int i = 0; i < n-1; i++){
        cout << dist[i] << ' ';
    }

    cout << dist.back() << '\n';


    return 0;
}