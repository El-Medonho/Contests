#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed solve(){
    
    int n; cin >> n;

    map<int,int> freq;

    for(int i = 0; i < n; i++){
        int x; cin >> x;
        freq[x]++;
    }

    vector<int> lugares(n+1,0);

    for(pair<int,int> cc: freq){
        int num = cc.first, x = cc.second;

        for(int sum = num; sum <= n; sum += num){
            lugares[sum] += x;
        } 
    }

    pair<int,int> best = {-1,-1};

    for(int i = 1; i <= n; i++){
        if(lugares[i] > best.first){
            best = {lugares[i], i};
        }
    }

    cout << best.first << '\n';

    return 0;
}

signed main(){
    fastio;

    int t; cin >> t;

    while(t--){
        solve();
    }
    
    return 0;
}