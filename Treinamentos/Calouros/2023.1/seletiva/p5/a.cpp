#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed solve(){
    
    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);

    while(m--){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
    }

    vector<int> qnt_antecessores(n,0);
    vector<int> vis(n,0);

    queue<int> qw; qw.push(0); vis[0] = 1;

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();

        for(int j: graph[cc]){
            qnt_antecessores[j]++;
            if(vis[j]) continue;
            vis[j] = 1;
            qw.push(j);
        }
    }

    if(vis[n-1] == 0){
        cout << "IMPOSSIBLE\n";
        return 0;
    }

    fill(vis.begin(), vis.end(), 0);

    qw.push(0); vis[0] = 1;

    vector<int> antecessor(n,-1);

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();
        if(cc == n-1) break;

        for(int j: graph[cc]){
            if(vis[j]) continue;
            qnt_antecessores[j]--;
            if(qnt_antecessores[j]) continue;

            antecessor[j] = cc;
            vis[j] = 1;
            qw.push(j);
        }
    }

    vector<int> resp(0);

    int cc = n-1;
    while(cc != -1){
        resp.push_back(cc);
        cc = antecessor[cc];
    }

    reverse(resp.begin(), resp.end());
    cout << resp.size() << '\n';
    for(int i: resp) cout << i+1 << ' ';
    cout << '\n';

    return 0;

}

signed main(){
    fastio;

    int t = 1;
    // cin >> t;

    while(t--){
        solve();
    }
    
    return 0;
}