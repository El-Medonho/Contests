#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<string> grid(n);
    for(string &s: grid) cin >> s;

    map<char,char> mp;
    mp['s'] = 'n';
    mp['n'] = 'u';
    mp['u'] = 'k';
    mp['k'] = 'e';
    mp['e'] = 's';

    vector<vector<int>> dir = { {-1,0}, {1,0}, {0,-1}, {0,1} }, vis(n, vector<int> (m,0));


    queue<pair<int,int>> qw; qw.push(make_pair(0,0));
    vis[0][0] = 1;

    while(!qw.empty()){
        int x = qw.front().first, y = qw.front().second; qw.pop();

        for(vector<int> dirr: dir){
            int xx = x+dirr[0], yy = y+dirr[1];

            if( xx > -1 && xx < n && yy > -1 && yy < m && grid[xx][yy] == mp[grid[x][y]] && !vis[xx][yy]){
                vis[xx][yy] = 1;
                qw.push(make_pair(xx,yy));
            }
        }
    }

    if(vis[n-1][m-1]) cout << "Yes\n";
    else cout << "No\n";

    return 0;
}