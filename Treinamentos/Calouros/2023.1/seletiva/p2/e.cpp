#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n; double d; cin >> n >> d;

    vector<pair<double,double>> arr(n);

    for(int i = 0; i < n; i++) cin >> arr[i].first >> arr[i].second;

    vector<bool> is(n,0); is[0] = 1;

    queue<int> qw; qw.push(0);

    while(!qw.empty()){
        int q = qw.front(); qw.pop();
        for(int i = 0; i < n; i++){
            int x = arr[i].first - arr[q].first, y = arr[i].second - arr[q].second;

            if( x*x + y*y <= d*d ){
                if(!is[i]){
                    is[i] = true;
                    qw.push(i);
                }
            }
        }
    }

    // double x == double y

    for(int i = 0; i < n; i++){
        if(is[i]) cout << "Yes\n";
        else cout << "No\n";
    }


    return 0;
}