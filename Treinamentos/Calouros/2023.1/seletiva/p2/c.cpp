#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n; cin >> n;
    vector<string> arr(n);

    for(int i = 0; i < n; i++) cin >> arr[i];

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            if(j == i) continue;
            string s = arr[i]+arr[j];

            bool is = true;
            for(int k = 0; k < s.size()/2; k++){       //abaaba  s.size() = 7  
                if(s[k] != s[s.size()-1-k]){
                    is = false;
                    break;
                }
            }

            if(is){
                cout << "Yes\n";
                return 0;
            }
        }
    }

    cout << "No\n";

    return 0;
}