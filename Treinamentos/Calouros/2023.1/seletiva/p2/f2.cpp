#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,m; cin >> n >> m;
    
    vector<string> grid(n);
    for(int i = 0; i < n; i++) cin >> grid[i];

    vector<vector<int>> vis(n, vector<int>(m,0));

    if(grid[0][0] != 's'){
        cout << "No\n";
        return 0;
    }

    queue<pair<int,int>> qw;
    vis[0][0] = 1;
    qw.push(make_pair(0,0));

    vector<pair<int,int>> dir = {make_pair(1,0), make_pair(-1,0), make_pair(0,1), make_pair(0,-1)};

    //s n u
    //a b c [0,1]
    //d d d

    map<char,char> mp;

    mp['s'] = 'n';
    mp['n'] = 'u';
    mp['u'] = 'k';
    mp['k'] = 'e';
    mp['e'] = 's';

    while(!qw.empty()){
        int x = qw.front().first, y = qw.front().second;
        qw.pop();

        for(pair<int,int> cc: dir){
            int xc = cc.first + x, yc = cc.second + y;
            if(xc >= n || xc < 0 || yc >= m || yc < 0) continue;
            
            if(vis[xc][yc]) continue;
            
            char c = grid[x][y], nxt = grid[xc][yc];
            if(nxt != mp[c]) continue;

            qw.push(make_pair(xc,yc));
            vis[xc][yc] = 1;
        }
    }

    if(vis[n-1][m-1]) cout << "Yes\n";
    else cout << "No\n";

    return 0;
}