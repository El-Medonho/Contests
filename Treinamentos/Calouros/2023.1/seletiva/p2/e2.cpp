#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,d; cin >> n >> d;  //d = 2000 no maximo

    vector<pair<int,int>> arr(n);
    for(int i = 0; i < n; i++){
        cin >> arr[i].first >> arr[i].second;
    }

    queue<int> qw;

    vector<int> vis(n,0); //[1,0,0,0,0,...]
    
    vis[0] = 1; qw.push(0);

    while(!qw.empty()){
        int x0 = arr[qw.front()].first, y0 = arr[qw.front()].second;
        qw.pop();

        for(int i = 0; i < n; i++){
            int x1 = arr[i].first, y1 = arr[i].second;

            if(vis[i]) continue;

            if( (x1-x0)*(x1-x0) + (y1-y0)*(y1-y0) <= d*d ){
                vis[i] = 1;
                qw.push(i);
            }
        }
    }

    for(int i = 0; i < n; i++){
        if(vis[i]) cout << "Yes\n";
        else cout << "No\n";
    }

    return 0;
}