#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<string> grid(n);

    for(string &s: grid) cin >> s;

    pair<int,int> h = {1e8, -1},w = {1e8, -1};

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            char c = grid[i][j];

            if(c == '#'){
                h.first = min(h.first, i); h.second = max(h.second, i);
                w.first = min(w.first, j); w.second = max(w.second, j);
            }
        }
    }
    
    for(int i = h.first; i <= h.second; i++){
        for(int j = w.first; j <= w.second; j++){
            char c = grid[i][j];

            if(c == '.'){
                cout << i+1 << ' ' << j+1 << '\n';
                return 0;
            }
        }
    }


    return 0;
}