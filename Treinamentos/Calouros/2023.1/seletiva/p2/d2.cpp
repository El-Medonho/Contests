#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int n,m; cin >> n >> m;

    // vector<vector<char>> grid(n, vector<char>(m));
    vector<string> grid(n);

    for(int i = 0; i < n; i++) cin >> grid[i];

    pair<int,int> a = {1e9,-10}, b = {1e9,-10};
    // vector<int> a(2), b(2);

    // . . . .
    // . # # .
    // . . # .
    // . . . .

    // # #
    // . #

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            char c = grid[i][j];

            if(c == '#'){
                a.first = min(a.first, j); a.second = max(a.second, j);
                b.first = min(b.first, i); b.second = max(b.second, i);
            }
        }
    }

    for(int i = b.first; i <= b.second; i++){
        for(int j = a.first; j <= a.second; j++){
            if(grid[i][j] == '.'){
                cout << i+1 << ' ' << j+1 << '\n';
                return 0;
            }
        }
    }

    return 0;
}