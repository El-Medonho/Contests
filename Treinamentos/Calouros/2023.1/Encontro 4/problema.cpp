#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int solve(){

    int n; cin >> n;
    vector<ll> arr(n);
    for(int i = 0; i < n; i++){
        cin >> arr[i];
    }
    sort(arr.begin(), arr.end());

    ll l = 0, r = 1e9+50;

    ll ans = 1e10;

    while(l <= r){
        ll mid = (l+r)/2;

        ll curr = -1e10, b = 0;

        for(int i = 0; i < n; i++){
            if( abs(arr[i] - curr) > mid ){
                curr = arr[i] + mid;
                b++;
            }
        }

        if(b <= 3){
            r = mid-1;
            ans = min(ans, mid);
        }
        else{
            l = mid+1;
        }
    }

    cout << ans << '\n';

    return 0;
}

int main(){
    fastio;

    int t; cin >> t;
    while(t--){
        solve();
    }

    return 0;
}