#include<bits/stdc++.h>
using namespace std;

int main(){
    cin.tie(NULL);
    ios_base::sync_with_stdio(false);

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);

    while(m--){
        int a,b; cin >> a >> b;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> vis(n,0);

    function<void(int)> dfs;

    dfs = [&](int cc){

        vis[cc] = 1;

        for(int j: graph[cc]){
            if(vis[j]) continue;
            dfs(j);
        }

    };

    dfs(0);

    return 0;
}

