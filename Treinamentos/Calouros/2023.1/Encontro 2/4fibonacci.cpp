#include "bits/stdc++.h"

using namespace std;

pair<int,int> fibonacci(int x){

    if(x == 1) return {1,1};
    if(x == 0) return {0,1};

    pair<int,int> p1 = fibonacci(x-1), p2 = fibonacci(x-2);

    return {p1.first + p2.first, p1.second + p2.second + 1};
}


int main(){

    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n;

    for(cin >> n; n > 0; n--){

        int x; cin >> x;

        pair<int,int> answer = fibonacci(x);

        cout << "fib(" << x << ") = " << answer.second - 1 << " calls = " << answer.first << "\n";
    }

    return 0;
}