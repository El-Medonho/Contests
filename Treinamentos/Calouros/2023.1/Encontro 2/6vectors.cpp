#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){

    vector<int> divisores;

    cout << "Insira um numero:\n";
    int n; cin >> n;

    for(int i = 1; i <= n; i++){
        if(n % i == 0) divisores.push_back(i);
    }

    cout << "O numero possui " << divisores.size() << " divisores, sendo eles:\n";

    for(int i : divisores) {
        cout << i << ' ';
    }
    cout << '\n';

    return 0;
}