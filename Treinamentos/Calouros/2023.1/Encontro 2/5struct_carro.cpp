#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

struct carro{

    int ano_fabricao;
    string pais_de_origem;
    double velocidade_maxima;

    carro(int ano, string pais, double vel){
        ano_fabricao = ano;
        pais_de_origem = pais;
        velocidade_maxima = vel;
    }

    int preco(int ano_da_compra){
        if(ano_da_compra > 2020) return 1e4 + 1e3*(ano_da_compra - 2020);
        else return 1e4 + 1e3*(2020 - ano_da_compra);
    }
};

int main(){

    carro uno(1984, "italia", 1000.0);
    int ano; cin >> ano;
    cout << uno.preco(ano) << "\n";

    return 0;
}