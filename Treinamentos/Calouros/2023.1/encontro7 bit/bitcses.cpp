// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}


signed main(){
    fastio;

    int n,q; cin >> n >> q;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    vector<ll> bit(n+1,0);

    for(int i = 0; i < n; i++){
        upd(i+1, arr[i], bit);
    }

    // 1 2 3 4 5 6 7

    while(q--){
        int op; cin >> op;

        if(op == 1){
            ll k,x; cin >> k >> x; k--;

            ll diff = x - arr[k];

            arr[k] = x;

            upd(k+1, diff, bit);
        }
        else{
            int a,b; cin >> a >> b;

            cout << sum(b, bit) - sum(a-1, bit) << '\n';
        }
    }

    return 0;
}