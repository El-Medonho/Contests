#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

ll fast_exp(ll b, ll e, ll m){
    ll res = 1;
    while(e){
        if(e&1) res = (res*b)%m;
        b = (b*b)%m;
        e>>=1;
    }
    return res;
}

signed main(){
    fastio;

    int n; cin >> n;

    while(n--){
        ll b,e; cin >> b >> e;
        cout << fast_exp(b,e,1e9+7) << '\n';
    }

    return 0;
}