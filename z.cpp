#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    ll ans = 0;

    ll l; cin >> l; 
    if(l > 0) ans = l + 2;

    for(int i = 1; i < n; i++){
        ll x; cin >> x;
        if(ans == 0){
            if(x == 0) continue;
            ans = x + 2;
            l = x; 
            continue;
        }
        if(x > l){
            ans++; ans += x-l;
        }
        l = x;
    }

    cout << ans << endl;

    return 0;
}