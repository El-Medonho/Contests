#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef double ld;

ld n, p; 

template<class F>
int ternSearch(int a, int b, F f) {
    assert(a <= b);
    while (b - a >= 5) {
        int mid = (a + b) / 2;
        if (f(mid) < f(mid+1)) a = mid; // (A)
        else b = mid+1;
    }
    for(int i = a+1; i <= b; ++i)
        if (f(a) < f(i)) a = i; // (B)
    return a;
}

int main(){
    fastio;

    cin >> n >> p;

    function<ld(ld)> calc;

    calc = [&](ld md){

        ld nn = n, pp = p;

        ld ans = pp;

        ans *= md/(nn+md);
        md--; pp--;

        while (pp--)
        {
            ans *= nn/(nn+md);
            nn--;
        }
        
        return ans;
    };

    cout << fixed << setprecision(12) << calc( ternSearch(1, n*10, calc) ) << '\n';

    return 0;
}