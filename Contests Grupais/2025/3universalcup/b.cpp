#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const ll inf = 1e17;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<vector<vector<ll>>> fw(3, vector<vector<ll>>(n, vector<ll>(n, inf)));

    int m; cin >> m;

    for(int i = 0; i < m; i++){
        int a,b,c,d; 
        cin >> a >> b >> c >> d; 
        a--; b--;

        for(int j = 0; j <= d; j++){
            fw[j][a][b] = min(fw[j][a][b], (ll)c);
        }
    }

    for(int i = 0; i < n; i++) {
        fw[0][i][i] = 0;
        fw[1][i][i] = 0;
        fw[2][i][i] = 0;
    }

    for(int k = 0; k < n; k++){
        for(int i = 0; i < n; i++){
            for(int j = 0; j < n; j++){
                fw[0][i][j] = min(fw[0][i][j], fw[0][i][k]+fw[0][k][j]);
                fw[1][i][j] = min(fw[1][i][j], fw[1][i][k]+fw[1][k][j]);
                fw[2][i][j] = min(fw[2][i][j], fw[2][i][k]+fw[2][k][j]);
            }
        }
    }

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            fw[2][i][j] = min(fw[2][i][j], 2*fw[1][i][j] + fw[0][j][i]);
        }
    }

    for(int k = 0; k < n; k++){
        for(int i = 0; i < n; i++){
            for(int j = 0; j < n; j++){
                fw[2][i][j] = min(fw[2][i][j], fw[2][i][k]+fw[2][k][j]);
            }
        }
    }

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            cout << (fw[2][i][j] == inf ? -1 : fw[2][i][j]) << ' ';
        }
        cout << '\n';
    }

    return 0;
}