#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const int mod = 1e9+7;

ll expo(ll b, ll e){
    ll res = 1;
    while(e){
        if(e&1) res = res*b%mod;
        b = b*b%mod;
        e>>=1;
    }
    return res;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;
    int k; cin >> k;

    vector<ll> fat(n+5, 1);

    for(int i = 2; i <= n; i++) fat[i] = fat[i-1]*i%mod;

    vector<int> arr(n);

    map<int,int> mp, uso;

    for(int &i: arr) {
        cin >> i; 

        mp[i]++;
    }

    int in = -1;

    sort(arr.begin(), arr.end());

    ll s1 = 0,s2 = 0;

    for(int i = 0; i < k; i++){
        uso[arr[i]]++;
        in = arr[i];
        s1+=arr[i];
    }

    int p = uso[in];
    int out = -1;

    uso.clear();

    for(int i = n-1, j = 0; j < k; j++, i--){
        uso[arr[i]]++;
        out = arr[i];
        s2+=arr[i];
    }

    int q = uso[out];

    cout << abs(s1-s2) << ' ';

    if(in != out){
        ll ans = fat[mp[in]] * expo(fat[p] * fat[mp[in]-p] % mod, mod-2)%mod;
        ll a2 = fat[mp[out]] * expo(fat[q] * fat[mp[out]-q] % mod, mod-2)%mod;

        ans = ans*a2%mod;
        cout << ans << '\n';
    }
    else{
        ll ans = fat[mp[in]] * expo(fat[p] * fat[mp[in]-p] % mod, mod-2)%mod;
        int res = mp[in]-p;
        ll a2 = fat[res] * expo(fat[q] * fat[res-q] % mod, mod-2)%mod;

        ans = ans*a2%mod;

        if(mp[in] == n) ans = ans*expo(2,mod-2)%mod;

        cout << ans << '\n';

    }

    return 0;
}