#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; ll m; cin >> n >> m;
    vector<ll> arr(n);

    for(ll &i: arr) cin >> i;

    priority_queue<pair<ll,ll>> pq;

    for(int i = 0; i < n; i++){
        pq.push({arr[i],i});
    }

    vector<int> vis(n,0);

    while(!pq.empty()){
        pair<ll,ll> curr = pq.top(); pq.pop();
        if(vis[curr.second]) continue;
        vis[curr.second] = 1;

        if(curr.second != 0 && arr[curr.second-1] < arr[curr.second]){
            if(arr[curr.second] - m > arr[curr.second-1]){
                arr[curr.second-1] = arr[curr.second] - m;
                pq.push({arr[curr.second-1], curr.second-1});
            }
        }

        if(curr.second != n-1 && arr[curr.second+1] < arr[curr.second]){
            if(arr[curr.second] - m > arr[curr.second+1]){
                arr[curr.second+1] = arr[curr.second] - m;
                pq.push({arr[curr.second+1], curr.second+1});
            }
        }
    }

    for(ll i: arr) cout << i << ' ';
    cout << '\n';

    return 0;
}