#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    int n; cin >> n;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int x; cin >> x; x--;
        graph[i].push_back(x);
        graph[x].push_back(i);
    }

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    multiset<ll> st;
    vector<int> val(n, -1), ans(n, 0);

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){

        auto it = st.lower_bound(arr[cc]);
        if(it != st.end()){
            // cout << cc << '\n';
            val[cc] = (*it);
            st.erase(it);
        }

        st.insert(arr[cc]);

        ans[cc] = st.size();

        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j, cc);
        }

        st.erase(st.find(arr[cc]));
        if(val[cc] != -1){
            st.insert(val[cc]);
        }

        return;

    } ;

    dfs(0, -1);

    for(int i = 1; i < n; i++) cout << ans[i] << " \n"[i == n-1];

    return 0;
}