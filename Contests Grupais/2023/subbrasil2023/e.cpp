// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,k; cin >> n >> k;

    vector<int> nxt(1e6+50, 0);

    for(int i = 1; i < 1e6+40; i++){
        int h = i;
        int sub = 0;
        while(h){
            sub += h%10;
            h/=10;
        }

        nxt[i] = i-sub;
    }

    vector<int> freq(1e6+50, 0);

    priority_queue<int> pq;

    for(int i = 0; i < n; i++){
        int x; cin >> x;
        if(freq[x] == 0) pq.push(x);
        freq[x]++;
    }

    int cnt = 0, last = -1;

    while( cnt < k){
        int cc = pq.top(); pq.pop();

        last = cc;
        cnt += freq[cc];
        if(cc == 0) break;


        if(!freq[nxt[cc]]) pq.push(nxt[cc]);
        freq[nxt[cc]] += freq[cc];

        freq[cc] = 0;
    }

    cout << last - nxt[last] << '\n';

    return 0;
}