#include "bits/stdc++.h"

using namespace std;

typedef long long ll;
typedef long double ld;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    ll e, s, f; cin >> e >> s >> f;

    // ll h = max(s, f);

    if(s > f) swap(s, f);

    ld ans = 1e18;

    for(ll i = 0; f*i < e;){
        i++;
        ll ee = e - f*i;
        ll ops = (ee+s-1)/s;
        if(f*i > e ) 
            ee = e - f*(i-1);
            ops = ee/s;
        // ll sobra = ee%s;

        ll h = ops/i;

        ld temp = pow( 2+(ops/i), i - ops % i ) * pow( 2+(ops/i+1), ops % i) - 1;

        ans = min(ans, 200 / temp );

        temp++;
        ans = min(ans, 225 / temp);
    }

    cout << fixed << setprecision(6) << ans << '\n';    

    return 0;
}