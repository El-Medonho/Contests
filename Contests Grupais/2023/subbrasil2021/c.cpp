#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    int b,l; cin >> b >> l;

    vector<int> arr(l);
    for(int &i: arr) cin >> i;

    reverse(arr.rbegin(), arr.rend());

    int md = 0;

    for(int i = 0; i < l; i++){
        int x = -arr[i];
        if(i&1) x=-x;

        md = (md+x)%(b+1);
        md = (md+b+1)%(b+1);
    }

    if(md == 0){
        cout << "0 0\n";
        return 0;
    }

    for(int i = l-1; i > -1; i--){
        if(i&1){
            if(md <= arr[i]){
                cout << l-i << ' ' << arr[i]-md << '\n';
                return 0;
            }
        }
        else{
            int h = (b+1)-md;
            if(h <= arr[i]){
                cout << l-i << ' ' << arr[i]-h << '\n';
                return 0;
            }
        }
    }

    cout << "-1 -1\n";

    return 0;
}