#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    ll n; cin >> n;

    vector<ll> fib = {1,1};

    for(int i = 2; i < 1000; i++){
        ll g = fib[i-1] + fib[i-2];
        if(g > n) break;
        fib.push_back(g);
    }

    string ans = "";

    if(n == 1){
        cout << "B\n";
        return 0;
    }

    int sz = fib.size();

    function<bool(ll, ll)> dfs;

    vector<string> hh(0);

    dfs = [&](ll cc, ll ind){

        if(cc == n) return true;
        if(cc > n) return false;

        for(int i = ind; i > 1; i--){
            if(n/fib[i] < cc || cc*fib[i] > n) continue;
            else if( dfs(cc*fib[i], i) ){
                string s = "";
                s += string(i-1, 'A');
                s += 'B';
                hh.push_back(s);
                return true;
            }
        }

        return false;

    };


    if(!dfs(1, sz-1)) {
        cout << "IMPOSSIBLE\n";
    }
    else {
        for(int i = hh.size()-1; i > -1; i--) ans += hh[i];
        cout << ans << '\n';
    }

    return 0;
}