#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m,q; cin >> n >> m >> q;

    vector<vector<int>> graph(n), ngraph(n);

    while(m--){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> comp(n, -1), mark(n, 0), vis(n, 0);
    
    vector<int> depth(n, -1), lowlink(n, -1);
    int time = 1;

    function<void(int, int)> dfs, dfs2;

    dfs = [&](int cc, int last){
        depth[cc] = lowlink[cc] = time++;
        vis[cc] = 1;

        for(int j: graph[cc]){
            if(j == last) continue;
            if(!vis[j]) {
                dfs(j, cc);
                lowlink[cc] = min(lowlink[cc], lowlink[j]);
            }else{
                if(depth[j] < depth[cc]) mark[j] = 1;
                lowlink[cc] = min(lowlink[cc], depth[j]);
            }


        }

        if(lowlink[cc] != depth[cc]) mark[cc] = 1;
    };

    for(int i = 0; i < n; i++) if(!vis[i]) dfs(i,i);

    fill(vis.begin(), vis.end(), 0);
    int qual = 1;

    dfs2 = [&](int cc, int last){

        comp[cc] = qual;
        vis[cc] = 1;

        for(int j: graph[cc]){
            if(vis[j] || mark[j]) continue;
            dfs2(j, cc);
        }
    };

    for(int i = 0; i < n; i++) if(!vis[i] && !mark[i]) {dfs2(i,i); qual++;}

    while(q--){
        int a,b; cin >> a >> b; a--; b--;
        if(mark[a] || mark[b] || comp[a] != comp[b]){
            cout << "NO\n";
        }else{
            cout << "YES\n";
        }
    }

    return 0;
}