#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

signed main(){
    fastio;

    vector<vector<int>> grid(101, vector<int>(101,-1));

    int n,q; cin >> q; n = 101;

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            set<int> st;

            int a = i-1, b = j-1;
            while(a >= 0 && b >= 0){
                st.insert(grid[a--][b--]);
            }
            a = i-1; b = j;
            while(a >= 0 && b >= 0){
                st.insert(grid[a--][b]);
            }
            a = i; b = j-1;
            while(a >= 0 && b >= 0){
                st.insert(grid[a][b--]);
            }
            int cnt = 0;
            for(int x: st){
                if(x != cnt){
                    // if(x == -1) return 0;
                    grid[i][j] = cnt;
                    break;
                }
                cnt++;
            }
            if(grid[i][j] == -1) grid[i][j] = cnt;
        }
    }

    int ans = 0;

    while(q--){
        int i,j; cin >> i >> j;
        ans ^= grid[i][j];
        if(i == j){
            cout << "Y\n";
            return 0;
        }
        // cout << grid[i][j] << '\n';
    }

    if(ans) cout << "Y\n";
    else cout << "N\n";

    return 0;
}