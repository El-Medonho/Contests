#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

template <class T> int sgn(T x) { return (x > 0) - (x < 0); }
template<class T>
struct Point {
	typedef Point P;
	T x, y;
	explicit Point(T x=0, T y=0) : x(x), y(y) {}
	bool operator<(P p) const { return tie(x,y) < tie(p.x,p.y); }
	bool operator==(P p) const { return tie(x,y)==tie(p.x,p.y); }
	P operator+(P p) const { return P(x+p.x, y+p.y); }
	P operator-(P p) const { return P(x-p.x, y-p.y); }
	P operator*(T d) const { return P(x*d, y*d); }
	P operator/(T d) const { return P(x/d, y/d); }
	T dot(P p) const { return x*p.x + y*p.y; }
	T cross(P p) const { return x*p.y - y*p.x; }
	T cross(P a, P b) const { return (a-*this).cross(b-*this); }
	T dist2() const { return x*x + y*y; }
	double dist() const { return sqrt((double)dist2()); }
	// angle to x-axis in interval [-pi, pi]
	double angle() const { return atan2(y, x); }
	P unit() const { return *this/dist(); } // makes dist()=1
	P perp() const { return P(-y, x); } // rotates +90 degrees
	P normal() const { return perp().unit(); }
	// returns point rotated 'a' radians ccw around the origin
	P rotate(double a) const {
    return P(x*cos(a)-y*sin(a),x*sin(a)+y*cos(a)); }
};
using P = Point<double>;

template<class T>
T polygonArea(vector<Point<T>> &v) {
	T a = v.back().cross(v[0]);
	for(int i = 0; i < v.size()-1; ++i) 
		a += v[i].cross(v[i+1]);
	return abs(a)/2.0;
}

signed main(){
    fastio;

    int n; cin >> n;

    vector<vector<int>> graph(n);

    map<pair<Point<int>, Point<int>>, int> mp;

    vector<double> peso(n);

    for(int i = 0; i < n; i++){
        vector<P> sides;
        vector<Point<int>> h(3);
        for(int j = 0; j < 3; j++){
            int x,y; cin >> x >> y;
            Point<int> z = Point<int>(x,y);
            h[j] = (z);
            // sort(h.begin(), h.end());
            sides.push_back(P(x,y));
        }

        for(int j = 0; j < 3; j++){
            int k = (j+1)%3;

            pair<Point<int>, Point<int>> z = {h[j], h[k]};
            if(h[j] < h[k]) swap(z.first, z.second);

            if(mp.find(z) != mp.end()){
                graph[i].push_back(mp[z]);
                graph[mp[z]].push_back(i);
            }else{
                mp[z] = i;
            }
        }

        peso[i] = abs(polygonArea(sides));
    }

    vector<pair<double,double>> arr(n, {1e18,1e18});

    function<void(int, int)> dfs;

    dfs = [&](int cc, int last){

        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j, cc);
        }

        arr[cc].first = 0;

        for(int j: graph[cc]){
            if(j == last) continue;
            arr[cc].first += min(arr[j].first, arr[j].second);
        }
        arr[cc].first += peso[cc];

        if(graph[cc].size() == 3){
            arr[cc].second = 0;
            for(int j: graph[cc]){
                if(j == last) continue;
                arr[cc].second += arr[j].first;
            }
        }

        return;
    };

    dfs(0, -1);

    cout << fixed << setprecision(1) << min(arr[0].first, arr[0].second) << '\n';

    return 0;
}