#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,k; cin >> n >> k;
    vector<int> dist(1<<k, -1);
    queue<int> qw;
    for(int i = 0; i < n; i++){
        string s; cin >> s;
        int num = 0;
        for(int j = 0; j < k; j++){
            if(s[j]-'0') num += 1<<j;
        }
        dist[num] = 0;
        qw.push(num);
    }

    int ans = 0;
    while(!qw.empty()){
        int x = qw.front(); qw.pop();
        ans = x;
        for(int i = 0; i < k; i++){
            int h = x ^ (1<<i);
            if(dist[h] == -1){
                qw.push(h);
                dist[h] = dist[x]+1;
            }
        }
    }

    for(int i = 0; i < k; i++){
        if(ans & (1<<i)) cout << 1;
        else cout << 0;
    }
    cout << '\n';

    return 0;
}