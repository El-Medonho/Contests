#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    int t = 5;
    string s;

    // while(t--){ cin >> s;
    while(cin >> s){
        
        int n = s.size();
        ll ans = 0;
        

        for(int gg = 0; gg < n; gg++){

            vector<int> kmp(n-gg, 0);

            int j = 0;

            for(int i = gg+1; i < n; i++){
                while(j > 0 && s[j+gg] != s[i]) j = kmp[j-1];
                if(s[j+gg] == s[i]) ++j;
                kmp[i-gg] = j;
            }

            for(int i: kmp) ans += i;
        }

        cout << ans << '\n';

    }



    return 0;
}