#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    int m; cin >> m;
    vector<ll> arr2(m), rarr2;
    for(ll &i: arr2) cin >> i;

    rarr2 = arr2;
    reverse(rarr2.begin(), rarr2.end());

    // if(m == 1){
    //     if( accumulate(arr.begin(), arr.end(), 0) == arr2[0]) cout << 'S' << '\n';
    //     else cout << 'N' << '\n';
    //     return 0;
    // }

    
    auto cmp = [](const vector<ll>& a, const vector<ll>& b) -> bool
    {
     if (a.size() < b.size())
          return true;
     if (a.size() > b.size())
          return false;
     return a < b;
    };

    set<vector<ll>, decltype(cmp)> st(cmp); 
    // set<vector<ll>> st; 
    st.insert(arr);
    auto in = st.rbegin();

    while(in != st.rend()){
        if((*in) == arr2 || (*in) == rarr2){
            cout << 'S' << '\n';
            return 0;
        }
        
        vector<ll> h = (*in);

        for(int gg = 0; gg < 2; gg++){
            for(int i = 1; i < h.size(); i++){
                if( max(i, (int) h.size() - i) < m ) continue;

                vector<ll> hn(0);
                for(int j = 0; j < max(i, (int) h.size() - i); j++){
                    ll s = 0;
                    if(j+i < h.size()) s += h[i+j]; 
                    int l = j+1;
                    if(i-l >= 0) s += h[i-l];
                    hn.push_back(s);
                }
                st.insert(hn);
            }
            reverse(h.begin(), h.end());
        }

        in++;
    }

    cout << 'N' << '\n';

    return 0;
}