#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

pair<double, vector<int>> hungarian(const vector<vector<double>> &a) {
	if (a.empty()) return {0, {}};
	int n = a.size() + 1, m = a[0].size() + 1;
	vector<double> u(n), v(m);
    vector<int> p(m), ans(n - 1);
	for(int i = 1; i < n; ++i) {
		p[0] = i;
		int j0 = 0; // add "dummy" worker 0
		vector<double> dist(m, 1e12);
        vector<int> pre(m, -1);
		vector<bool> done(m + 1);
		do { 
			done[j0] = true;
			int i0 = p[j0], j1;
            double delta = 1e12;
			for(int j = 1; j < m; ++j) if (!done[j]) {
				double cur = a[i0-1][j-1] - u[i0] - v[j];
				if (cur < dist[j] && abs(cur-dist[j]) > 1e-9) dist[j] = cur, pre[j] = j0;
				if (dist[j] < delta && abs(delta-dist[j]) > 1e-9) delta = dist[j], j1 = j;
			}
			for(int j = 0; j < m; ++j) {
				if (done[j]) u[p[j]] += delta, v[j] -= delta;
				else dist[j] -= delta;
			}
			j0 = j1;
		} while (p[j0]);
		while (j0) { // update alternating path
			int j1 = pre[j0];
			p[j0] = p[j1], j0 = j1;
		}
	}
	for(int j = 1; j < m; ++j) if (p[j]) ans[p[j]-1] = j-1;
	return {-v[0], ans}; // min cost
}

signed main(){
    fastio;

    int n; cin >> n;

    vector<vector<double>> arr(n, vector<double>(n));

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++) {cin >> arr[j][i]; arr[j][i] = -log2(arr[j][i]);}  //talvez seja [j][i]
    }

    auto [a,v] = hungarian(arr);

    for(int i = 0; i < n; i++) cout << v[i]+1 << " \n"[i == n-1];

    return 0;
}