#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
#define endl '\n'

int main(){
    fastio;

    int n; cin >> n;
    stack<int> h1,h3;
    vector<int> arr(n);
    for(int i = 0; i < n; i++){
        int cr; cin >> cr;
        arr[n-1-i] = cr;
    }

    vector<pair<int,int>> ans;

    int op = 0;
    for(int i : arr){
        ans.push_back({1,2});
        op++;
        
        while(!h3.empty()){
            if(h3.top() > i) break;
            ans.push_back({3,1}); op++;
            h1.push(h3.top());
            h3.pop();
        }
        ans.push_back({2,3}); op++;
        h3.push(i);
        while(!h1.empty()){
            h3.push(h1.top());
            h1.pop();
            ans.push_back({1,3}); op++;
        }
    }

    cout << op << endl;
    for(pair<int,int> x: ans) cout << x.first << ' ' << x.second << endl;

    return 0;
}