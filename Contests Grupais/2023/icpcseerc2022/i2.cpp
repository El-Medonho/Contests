#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;
    ll n; cin >> n;
    
    vector<ll> arr(n);
    
    for(int i = 0; i < n; i++) cin >> arr[i];

    ll sum = 0;
    for(int i = 0; i < n; i++){
        sum += arr[i];
        if(i) sum -= (abs(arr[i]-arr[i-1]));
    }

    cout << sum << '\n';

    return 0;
}