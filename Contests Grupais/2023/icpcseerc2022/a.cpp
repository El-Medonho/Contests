#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    string s,t; cin >> s >> t;

    vector<vector<int>> ss(26);

    for(int i = 0; i < s.size(); i++){
        int k = s[i]-'a';
        ss[k].push_back(i);
    }

    int last = -1, ans = 1;
    for(char c: t){
        int k = c-'a';
        if(ss[k].back() <= last) {last = -1; ans++;}
        last = (*upper_bound(ss[k].begin(),ss[k].end(), last));
    }

    cout << ans << '\n';

    return 0;
}