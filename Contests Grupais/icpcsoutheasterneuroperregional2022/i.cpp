#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

int main(){
    fastio;

    int n; cin >> n;

    vector<long long> arr(n);
    ll menor = 1e10;
    for(ll &i: arr){
        cin >> i;
        menor = min(menor,i);
    }

    ll answer = 0;

    if(n & 1){
        answer = menor;
        bool pass = false;
        for(int i = 0; i < n;){
            if(pass){
                answer += max(arr[i], arr[i+1]);
                i+=2;

            }else{
                if(arr[i] == menor){
                    pass = true;
                    i++;
                    continue;
                }
                if(arr[i+1] == menor){
                    pass = true;
                    answer += arr[i] + arr[i+2];
                    i+=3;
                    continue;
                }
                answer += max(arr[i], arr[i+1]);
                i+=2;
            }
        }
    }
    else{
        for(int i = 0; i < n; i+=2){
            answer += max(arr[i],arr[i+1]);
        }
    }

    cout << answer << '\n';

    return 0;
}