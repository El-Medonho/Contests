#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

#include "bits/stdc++.h"
#include <ext/pb_ds/tree_policy.hpp>
#include <ext/pb_ds/assoc_container.hpp>

using namespace __gnu_pbds;
using namespace std;

typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<ll,null_type, less_equal<ll>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

signed main(){
    fastio;

    int n; cin >> n;

    vector<ll> a(n), b(n);

    for(ll &i: a) cin >> i;
    for(ll &i: b) cin >> i;

    int k,v; cin >> k >> v;

    int l = -1, r = n;

    ll sum = 0;
    multiordered_set mt;

    for(int i = 0; i < k; i++){
        mt.insert(b[i]);
        sum += a[i];
        l++;
    }

    ll ans = 0;

    int g = mt.size();

    int gg = (*mt.find_by_order(1));

    auto it = mt.find_by_order(mt.size() - (v));

    while(it != mt.end()) {
        sum += (*it);
        it++;
    }

    ans = sum;

    while(l > -1){
        if( (*mt.find_by_order(mt.size() - (v))) <= b[l]){
            sum -= b[l];
            mt.erase(--mt.lower_bound(b[l]));
            sum += (*mt.find_by_order(mt.size() - (v)));
        }else mt.erase(--mt.lower_bound(b[l]));

        sum -= a[l];

        l--;

        r--;

        sum += a[r];

        ll h = (*mt.find_by_order(mt.size() - (v)));

        if(b[r] > h){
            sum -= h;
            mt.insert(b[r]);
            sum += b[r];
        }
        else mt.insert(b[r]);

        ans = max(ans, sum);
    }

    cout << ans << '\n';
    

    return 0;
}