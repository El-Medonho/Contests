#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)



signed main(){
    fastio;

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);
    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
    }

    function<void(int)> tarjan;

    vector<int> comp(n,-1), visiting(n,0), depth(n,-1), lowlink(n,-1);

    int tempo = 0, cont = 0;

    stack<int> stk;

    tarjan = [&](int curr){
        depth[curr] = lowlink[curr] = ++tempo;
        stk.push(curr);
        visiting[curr] = 1;

        for(int i: graph[curr]){
            if(lowlink[i] == -1){
                
                tarjan(i);
                lowlink[curr] = min(lowlink[curr], lowlink[i]);

            }else if(visiting[i] == 1){

                lowlink[curr] = min(lowlink[curr], depth[i]);

            }
        }

        if(depth[curr] == lowlink[curr]){
            int u = -1;
            while(u != curr){
                u = stk.top(); stk.pop();
                visiting[u] = 0;
                comp[u] = cont;
            }
            cont++;
        }

        return;
    };

    for(int i = 0; i < n; i++){
        if(depth[i] == -1) tarjan(i);
    }

    if(cont == 1){
        cout << 0 << '\n';
        return 0;
    }

    vector<int> entrada(cont,0), saida(cont,0);

    for(int i = 0; i < n; i++){
        for(int j: graph[i]){
            if(comp[i] == comp[j]) continue;
            saida[comp[i]]++;
            entrada[comp[j]]++;
        }
    }


    int ent = 0, si = 0;
    for(int i = 0; i < cont; i++){
        if(saida[i] == 0) si++;
        if(entrada[i] == 0) ent++;
    }

    cout << max(si,ent) << '\n';

    return 0;
}