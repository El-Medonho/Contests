int main(){
    fastio;

    ll n; cin >> n;
    ll p,h; cin >> p >> h; 
    p--; h--;
    ll um = 1;
    ll sz = (1ll<<n)-1;

    vll arr(n, 1);

    for(ll i = n-1; i > -1; i--){
        if(h & (ll)(um<<i)) h = ~h;
        else arr[i] = 0;
    }

    for(ll x: arr){
        ll in = (p) > (sz>>um);
        if(x == 1){
            if(in) cout << "R";
            else cout << "L";
            p = abs(sz-p);
        }
        else{
            if(in) cout << "L";
            else cout << "R";
        }
        sz>>=um;
        p = p % (sz+um);
    }

    cout << endl;

    return 0;
}