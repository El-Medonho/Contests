#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    map<string, int> mp;

    int k,n; cin >> k >> n;

    while(n--){
        string s; cin >> s;

        int cnt = 1;

        while(s == "Re:") {
            cnt++;
            cin >> s;
        }

        mp[s] = max(mp[s], cnt);
    }   

    int ans = 0;

    for(auto[i, x]: mp) ans += x;

    if(ans <= k) cout << "YES\n";
    else cout << "NO\n";

    return 0;
}