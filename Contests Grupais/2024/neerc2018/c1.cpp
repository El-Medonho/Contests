#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const int mod = 998244353;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<vector<ll>> dp(n+1, vector<ll>(n+1, 0));

    for(int i = 1; i <= n; i+=2) dp[i][i/2+1] = 1;

    for(int k = 1; k <= n; k++){
        for(int a = 1; a < (k/2+1); a++){
            for(int i = 1; i < a; i++){
                dp[k][a] += dp[k-2*a+1][i];
                dp[k][a] %= mod;
            }
        }
    }

    for(int i = 1; i <= n; i++){
        ll ans = 0;

        for(int g = 1; g <= i; g++) ans += dp[i][g];

        cout << i << ": " << ans << endl;
    }

    return 0;
}