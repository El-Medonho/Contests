#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;

    uniform_int_distribution<int> uid(1, 1000);

    int a;

    for(int i = 0; i < m; i++){
        cin >> a >> a;
    }

    while(m--){
        int x; x = uid(rng);

        x %= 3;

        if(x == 0) cout << "R\n";
        if(x == 1) cout << "G\n";
        if(x == 2) cout << "B\n";
    }

    return 0;
}