#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<array<ll,3>> arr(n);

    vector<ll> dl(n, 0), c(n, 0);

    for(int i = 0; i < n; i++){
        cin >> arr[i][1] >> arr[i][0];
        arr[i][2] = i;

        dl[i] = arr[i][0];
        c[i] = arr[i][1];
    }

    sort(arr.begin(), arr.end());

    vector<ll> tempo(n, 0);

    vector<int> ans(n);

    ll t = 0;

    for(int i = 0; i < n; i++){
        t += arr[i][1];
        tempo[arr[i][2]] = t;

        ans[i] = arr[i][2];

        if(t > arr[i][0]){
            cout << '*' << endl;
            return 0;
        }
    }

    bool ok = true;

    while(ok){
        ok = false;

        for(int i = 1; i < n; i++){
            if(ans[i] > ans[i-1]) continue;

            t = tempo[ans[i-1]] + c[ans[i]];

            if(t > dl[ans[i-1]]) continue;

            ok = true;

            t = tempo[ans[i-1]] - c[ans[i-1]];

            t += c[ans[i]];

            tempo[ans[i]] = t;

            t += c[ans[i-1]];

            tempo[ans[i-1]] = t;

            swap(ans[i], ans[i-1]);
        }
    }

    for(int i: ans) cout << i+1 << ' ';
    cout << endl;


    return 0;
}