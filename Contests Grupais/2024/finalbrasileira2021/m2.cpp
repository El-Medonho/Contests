#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<array<ll,3>> arr(n);

    vector<ll> dl(n, 0), c(n, 0);

    for(int i = 0; i < n; i++){
        cin >> arr[i][1] >> arr[i][0];
        arr[i][2] = i;

        dl[i] = arr[i][0];
        c[i] = arr[i][1];
    }

    sort(arr.begin(), arr.end());

    vector<ll> tempo(n, 0);

    vector<int> ans(n);

    ll t = 0;

    for(int i = 0; i < n; i++){
        t += arr[i][1];
        tempo[arr[i][2]] = t;

        ans[i] = arr[i][2];

        if(t > arr[i][0]){
            cout << '*' << endl;
            return 0;
        }
    }

    bool ok = true;

    for(int i = 0; i < n; i++){
        int p = i;
        ll mn = 1e15;
        for(int j = i; j < n; j++){
            if(ans[j] < ans[p] && c[ans[j]] <= mn) p = j;

            mn = min(mn, dl[ans[j]] - tempo[ans[j]]);
        }

        t = tempo[ans[i]] - c[ans[i]];

        int h = ans[p];

        for(int j = p; j > i; j--) ans[j] = ans[j-1];

        ans[i] = h;

        for(int j = i; j <= p; j++){
            tempo[ans[j]] = t + c[ans[j]];
            t += c[ans[j]];
        }
    }

    for(int i: ans) cout << i+1 << ' ';
    cout << endl;


    return 0;
}