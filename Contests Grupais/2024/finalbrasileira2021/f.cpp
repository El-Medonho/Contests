#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> vis(n,0);

    queue<int> qw;
    qw.push(n-2); vis[n-2] = 2;

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();

        for(int j: graph[cc]){
            if(vis[j] || j == n-1) continue;
            vis[j] = 2; qw.push(j);
        }
    }

    vis[n-1] = 1;
    qw.push(n-1);

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();

        for(int j: graph[cc]){
            if(vis[j]) continue;
            vis[j] = 1; qw.push(j);
        }
    }

    for(int i: vis) cout << (char)('A'+(i-1));
    cout << endl;

    return 0;
}