#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;
typedef double ld;

int cmp(double a, double b=0, double eps=1e-9){
    return a+eps>b?b+eps>a?0:1:-1;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    vector<ld> arr(4);
    for(ld &i: arr) cin >> i;

    ld x; cin >> x;

    ld l = 0, r = 1e3;
    
    int cnt = 200;

    while (cnt--) {
        ld md = (l+r)/2;
        vector<ld> temp = arr; temp.push_back(md);
        sort(temp.begin(), temp.end());

        ld t = temp[1]+temp[2]+temp[3];
        t/=3;

        if(cmp(t, x) != 1) l = md;
        else r = md;
    }

    if(cmp(l, 30) == 1){
        cout << "infinite\n";
        return 0;
    }

    if(cmp(l) != 1){
        cout << "impossible\n";
        return 0;
    }

    cout << fixed << setprecision(2) << l << endl;
    

    return 0;
}