// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<vector<int>> save(2e5+5);

    vector<int> pesos(n, 1), pai(n);

    for(int i = 0; i < n; i++) pai[i] = i;

    function<int(int)> ff;

    ff = [&](int x){
        if(pai[x] == x) return x;
        return pai[x] = ff(pai[x]);
    };

    function<bool(int,int)> uu;

    uu = [&](int x, int y){
        x = ff(x); y = ff(y);
        if(x == y) return false;

        if(pesos[x] > pesos[y]) swap(x,y);

        pesos[y] += pesos[x];
        pai[x] = y;

        return true;
    };

    map<int,int> mp;
    int N = 1;

    vector<tuple<int,int,int>> ans(0);

    for(int i = 0; i < n; i++){
        int c; cin >> c;

        while(c--){
            int a; cin >> a;
            if(!mp[a]) mp[a] = N++;

            int ind = mp[a];

            if(save[ind].size()){
                int b = save[ind].back();

                if(uu(i, b)){
                    ans.push_back({i+1, b+1, a});
                }
            }

            save[ind].push_back(i);
        }
    }

    if(ans.size() != n-1){
        cout << "impossible\n";
        return 0;
    }

    for(auto[p,q,r]: ans){
        cout << p << ' ' << q << ' ' << r << endl;
    }

    return 0;
}