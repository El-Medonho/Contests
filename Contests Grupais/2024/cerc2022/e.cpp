#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;
typedef long double ld;

int cmp_double(ld a, ld b = 0, ld eps = 1e-6) {
    return a + eps > b ? b + eps > a ? 0 : 1 : -1;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n, m = 1e4; cin >> n;

    int h = 0;


    vector<ld> arr(n);

    for(int i = 0; i < n; i++) cin >> arr[i];

    vector<int> ans(n, -1);

    for(int i = 0; i < n; i++){
        if(arr[i] > arr[h]){
            h = i;
        }
    }

    for(int i = 1; i <= 10000; i++){

        ans[h] = i;

        bool ok = true;
        ll gcd = i;

        ll sum = gcd * gcd;
        ld ep = i/arr[h];

        for(int j = 0; j < n; j++){

            if(j == h) continue;

            ld cc = arr[j] * ep;

            ll l = -1;

            for(int k = cc-2, cnt = 0; cnt < 5; cnt++, k++){
                if(cmp_double(cc, k) == 0){
                    l = k;
                }
            }

            if(l == -1){
                ok = false;
                break;
            }

            gcd = __gcd(gcd, l);
            ans[j] = l;

            sum += l * l;
        }

        if(ok && gcd == 1 && cmp_double(sqrtl(sum), ep) == 0){
            for(int i: ans) cout << i << endl;
            return 0;
        }
    }

    // for(int i: ans) cout << i << endl;

    cout << arr[1e6] << endl;

    return 0;
}