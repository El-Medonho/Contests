#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    vector<int> vis(1e7+5, 0);

    int b = arr[0];

    for(int i: arr) b = __gcd(b, i);

    for(int x: arr){
        x /= b;

        for(int j = 2; j * j <= x; j++){
            if(x % j) continue;
            if(vis[j]){
                cout << "NO\n";
                return 0;
            }
            vis[j] = 1;
            while(x % j == 0) x/=j;
        }
        if(x > 1){
            if(vis[x]){
                cout << "NO\n";
                return 0;
            }
            vis[x] = 1;
        } 
    }


    cout << "YES\n";

    return 0;
}