// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const int mod = 998244353;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;

    //first mid, second corner

    vector<vector<pair<int,int>>> add(n, vector<pair<int,int>>(n, {0,0}));
    vector<int> tot(n, 0);

    for(int i = 0; i < m; i++){
        int a,b,c; cin >> a >> b >> c; a--; b--; c--;

        add[a][b].first++; add[a][c].second++;
        add[b][a].second--; add[b][c].second--;
        add[c][b].first++; add[c][a].second++;

        tot[b]++;
    }

    int N = 1<<n;

    vector<ll> dp(N, 0);

    vector<int> vis(N, 0);
    vector<vector<pair<int,int>>> memo(N, vector<pair<int,int>>(n, {0,0}));

    dp[0] = 1;

    for(int mask = 0; mask < N; mask++){
        if(!dp[mask]) continue;
        for(int i = 0; i < n; i++){
            if(mask & (1<<i)) continue;

            int nm = mask^(1<<i);
            
            if(memo[mask][i].second == 0 && memo[mask][i].first == tot[i]){
                if(vis[nm] == 0){
                    memo[nm] = memo[mask];
                    for(int j = 0; j < n; j++) {
                        memo[nm][j].first += add[i][j].first;
                        memo[nm][j].second += add[i][j].second;
                    }
                }

                vis[nm] = 1;

                dp[nm] = (dp[nm] + dp[mask]) % mod;
            }
        }
    }

    cout << dp[N-1] << endl;

    return 0;
}