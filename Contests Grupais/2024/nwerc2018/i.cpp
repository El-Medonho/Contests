#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    double ans = 1;

    for(int i = 1; i <= n; i++) {
        if(arr[i-1] > i) {
            cout << -1 << endl;
            return 0;
        }

        ans = min(ans, double(arr[i-1])/i);
    }

    cout << fixed << setprecision(10) << ans << endl;

    return 0;
}