#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,c,b; cin >> n >> c >> b;

    vector<int> is(n, 0), arr(n, 0);

    while(b--){
        int x; cin >> x; x--;
        is[x] = 1;
    }

    int i = 0;

    if(c%2==0)i++;

    c += c%2;

    bool ok = true;

    for(; i < n && c > 0; i++){
        if(ok){
            if(is[i]) continue;

            arr[i] = 1;
            c -= 2;
        }

        ok = ok != true;
    }

    for(int i: arr) cout << i;
    cout << endl;

    return 0;
}