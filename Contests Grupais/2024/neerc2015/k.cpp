#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    set<pair<int,int>> edges;

    int n,m; cin >> n >> m;

    vector<int> pai(n), peso(n, 1);
    for(int i = 0; i < n; i++){
        pai[i] = i;
    }

    function<int(int)> ff;

    ff = [&](int x){
        if(pai[x]==x) return x;
        return pai[x]=ff(pai[x]);
    };

    function<bool(int,int)> uu;

    uu = [&](int x, int y){
        x=ff(x), y=ff(y);
        if(x==y) return false;
        if(peso[y]>peso[x]) swap(x, y);
        pai[y]=x;
        peso[x]+=peso[y];
        return true;
    };

    vector<vector<int>> graph(n);

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;

        edges.insert({b,a});
    }

    for(auto [a,b]: edges){
        if(!uu(a,b)) continue;

        graph[a].push_back(b);
    }


    vector<int> vis(n, 0);
    vector<int> lw(n, 0);
    vector<int> par(n, 0), ans(0);

    for(int i = 0; i < n; i++){
        for(int j: graph[i]) par[j]++;
    }

    set<int> st;
    st.insert(0);

    function<void(int)> dfs;

    dfs = [&](int cc){

        vis[cc] = 1;
        st.insert(cc);

        for(int j: graph[cc]){
            par[j]--;

            int h = (*--st.lower_bound(j));

            lw[j] = max(lw[j], h);

            
        }

    };

    for(int i = 0; i < n; i++){
        if(graph[i].size() == 0){
            lw[i] = 0;

            dfs(i);
        }
    }


    return 0;
}