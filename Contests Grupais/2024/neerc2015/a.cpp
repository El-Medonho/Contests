#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    ll x; cin >> x;

    x--;

    vector<ll> ans(0);

    for(ll i = 1; i * i <= x; i++){
        if(x % i == 0){
            if(i * i != x) ans.push_back(x/i);
            ans.push_back(i);
        }
    }

    sort(ans.begin(), ans.end());

    for(ll i: ans) cout << i << ' ';

    cout << endl;

    return 0;
}