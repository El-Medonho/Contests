#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed solve(){

    int n; cin >> n;

    vector<ll> dp(n, 0LL);

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    if(n <= 2){
        cout << *max_element(arr.begin(), arr.end()) << endl;
        return 0;
    }

    dp[0] = arr[0]; dp[1] = arr[1];
    dp[2] = max(arr[2] + arr[0], arr[2] + min(dp[0], dp[1]));

    for(int i = 3; i < n; i++){
        // dp[i] = dp[i-1];

        dp[i] = max(dp[i], dp[i-3] + min(arr[i-1], arr[i-2]) + arr[i]);
        dp[i] = max(dp[i], dp[i-2] + arr[i]);
    }

    cout << *max_element(dp.begin(), dp.end()) << endl;

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1; 
    cin >> t;

    while(t--){
        solve();
    }

    return 0;
}