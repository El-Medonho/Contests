#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed solve(){

    int n; cin >> n;

    vector<int> arr(n);

    for(int &i: arr) cin >> i;

    sort(arr.begin(), arr.end());

    vector<ll> sum(n, 0);

    sum[0] = arr[0];

    for(int i = 1; i < n; i++) sum[i] = sum[i-1] + arr[i];

    for(int i = n-1; i > 1; i--){
        if(sum[i-1] > arr[i]){
            cout << sum[i] << endl;
            return 0;
        }
    }

    cout << 0 << endl;

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1; 
    cin >> t;

    while(t--){
        solve();
    }

    return 0;
}