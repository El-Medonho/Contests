#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed solve(){

    int k; cin >> k; k--;

    int n; cin >> n;

    int c = 0;

    int mx = 210;

    vector<pair<int,char>> arr(n);

    for(int i = 0; i < n; i++){
        cin >> arr[i].first >> arr[i].second;
    }

    for(auto[x, cc]: arr){
        c += x;

        if(c >= mx) break;

        if(cc == 'C') k = (k+1)%8;
    }

    cout << k+1 << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1; 
    // cin >> t;

    while(t--){
        solve();
    }

    return 0;
}