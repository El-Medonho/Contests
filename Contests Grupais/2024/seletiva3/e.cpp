#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int c; cin >> c;

    string s,t; cin >> s >> t;
    int n = s.size();
    int e = n-c;

    int ans = 0;

    for(int i = 0; i < n; i++){
        if(s[i] != t[i]){
            if(e){
                e--; ans++;
            }
            else c--;
        }
        else{
            if(c){
                c--; ans++;
            }
            else e--;
        }
    }

    cout << ans << endl;

    return 0;
}