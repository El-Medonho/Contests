#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;
    int a,b; cin >> a >> b; a--; b--;
    n<<=1;

    vector<int> dist(n, -1); dist[a] = 0;

    queue<int> qw;
    qw.push(a);

    n/=2;

    while(!qw.empty()){
        int x = qw.front(); qw.pop();

        int j = (x%n)*2 + x/n;
        if(dist[j] == -1){
            dist[j] = dist[x]+1;
            qw.push(j);
        }

        j = (x/2)*2+(x%2+1)%2;
        if(dist[j] == -1){
            dist[j] = dist[x]+1;
            qw.push(j);
        }
    }

    cout << dist[b] << endl;



    return 0;
}