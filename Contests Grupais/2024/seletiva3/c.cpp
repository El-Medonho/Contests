#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<int> freq(n+1 ,0);

    for(int i = 0; i < 2*n-1; i++){
        int x; cin >> x; freq[x]++;
    }

    for(int i = 1; i <= n; i++){
        if(freq[i] == 1){
            cout << i << endl;
            return 0;
        }
    }

    return 0;
}