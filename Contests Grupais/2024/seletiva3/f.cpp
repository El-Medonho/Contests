#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<vector<int>> graph(n);

    for(int i = 1; i < n; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> dp(n, 0);

    int ans = 0;

    function<void(int,int)> dfs;

    dfs = [&](int cc, int last){
        int b = graph[cc].size();

        for(int j: graph[cc]){
            if(j == last) continue;
            dfs(j, cc);

            ans = max(ans, dp[j] + b-1);
        }

        b -= 2;

        int mx = 0, mx2 = 0;

        for(int j: graph[cc]){
            if(j == last) continue;
            if(dp[j] > mx) {mx2 = mx; mx = dp[j];}
            else if(dp[j] > mx2) mx2 = dp[j];
        }

        ans = max(ans, mx + mx2 + b);

        dp[cc] = max(mx + b, b+1);
    };

    dfs(0,0);

    cout << ans << endl;

    return 0;
}