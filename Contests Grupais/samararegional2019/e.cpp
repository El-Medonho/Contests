#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    ll n,m; cin >> n >> m;

    vector<pair<pair<ll,ll>, ll>> arr(n);
    for(ll i = 0; i < n; i++){
        cin >> arr[i].first.first >> arr[i].first.second;
        arr[i].second = i;
    }

    sort(arr.begin(), arr.end());

    ll cc = 0; pair<ll,ll> mx = {-1,-1};

    set<ll> ans;

    for(ll i = 0; i < n; i++){
        ll a = arr[i].first.first, b = arr[i].first.second, k = arr[i].second;

        if(a > cc+1){
            if(mx.first <= cc | mx.first < a-1){
                cout << "NO\n";
                return 0;
            }
            cc = mx.first;
            ans.insert(mx.second);
        }

        if(b > mx.first) mx = {b,k};
    }

    ans.insert(mx.second);

    if(mx.first < m){
        cout << "NO\n";
        return 0;        
    }

    cout << "YES\n";
    cout << ans.size() << '\n';
    for(ll i : ans) cout << i+1 << ' ';
    cout << '\n';
    // cout << ans.back()+1 << '\n';

    return 0;
}