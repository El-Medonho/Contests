#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed main(){
    fastio;

    ll n,p; cin >> p >> n;
    vector<int> vis(p+50, 0);
    int in = 0;
    ll ans = 1;
    vis[0] = 1;

    for(int i = 0; i < min(n,p*2+2); i++){
        in = in+i+1;
        in %= p;
        ans += (vis[in] == 0);
        vis[in] = 1;
    }

    cout << ans << '\n';

    return 0;
}