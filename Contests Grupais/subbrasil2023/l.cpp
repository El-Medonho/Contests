#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    string s; int k; cin >> s >> k;

    int n = s.size();
    string ans(n, '0');

    for(int i = 0; i < k; i++){
        string h = "";
        for(int cc = i; cc < n; cc+=k){
            h += s[cc];
        }
        sort(h.begin(), h.end());

        for(int cc = i, cnt = 0; cc < n; cc+=k, cnt++){
            ans[cc] = h[cnt];
        }
    }

    cout << ans << '\n';

    return 0;
}