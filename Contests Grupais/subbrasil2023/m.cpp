#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<vector<int>> graph(n, vector<int>(n, 0)), mark(n, vector<int>(n,0));

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++) cin >> graph[i][j];
    }

    int ans = 0;

    for(int k = 0; k < n; k++){
        for(int i = 0; i < n; i++){
            if(i == k) continue;
            for(int j = 0; j < n; j++){
                if(i == j || j == k) continue;
                if(graph[i][k] + graph[k][j] < graph[i][j]){
                    cout << -1 << '\n';
                    return 0;
                }
                if(graph[i][k] + graph[k][j] == graph[i][j] && mark[i][j] == 0){
                    ans++;
                    mark[i][j]++;
                }
            }
        }
    }

    cout << ans/2 << '\n';

    return 0;
}