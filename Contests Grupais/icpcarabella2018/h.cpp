void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]+=val;
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans+=bit[ind];    
        ind-=ind&-ind;    
    }
    return ans;
}


signed solve(){

    int n,m,k; cin >> n >> m >> k;
    string a,b; cin >> a >> b;

    set<pair<int,int>> st;

    vector<vector<ll>> bit(26, vector<ll>(m+1, 0));
    vector<vector<bool>> p(26, vector<bool>(26,0));

    for(int i = 0; i + k - 1 < n; i++){
        p[a[i]-'a'][a[i+k-1]-'a'] = 1;
    }

    ll ans = 0;

    for(int i = 0; i < m; i++){
        int cc = b[i]-'a';
        upd(i+1, 1, bit[cc]);
        for(int j = 0; j < 26; j++){
            if(p[j][cc]) ans += sum(i+1, bit[j]);
        }
    }

    cout << ans << endl;
    

    return 0;
}

