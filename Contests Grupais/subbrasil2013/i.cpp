// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n,c,t1,t2; cin >> n >> c >> t1 >> t2;
    vector<int> arr(n); 
    for(int i = 0; i < n; i++) cin >> arr[i];
    sort(arr.begin(), arr.end());

    vector<int> dp(n,0), vis(n,0);
    int ans = 1e7;

    for(int i = 0; i < n; i++){
        fill(dp.begin(), dp.end(), 1e7); dp[i] = 0;
        fill(vis.begin(), vis.end(), 0);
        priority_queue<int, vector<int>, greater<int>> pq;
        pq.push(i);
        while(!pq.empty()){
            int cc = pq.top(); pq.pop();

            if(vis[cc]) continue;
            vis[cc] = 1;

            int h = t1+arr[cc];
            if(t1 + arr[cc] >= arr[n-1]) h = h - c;

            int nx = upper_bound(arr.begin(), arr.end(), h) - arr.begin();
            if( (cc >= i && nx <= cc && nx >= i) || (cc < i && nx >= i) || (t1 == c) ) 
            ans = min(ans, dp[cc] + t1);
            else{
                dp[nx] = min(dp[nx], dp[cc] + t1);
                pq.push(nx);
            }

            h = t2+arr[cc];
            if(t2 + arr[cc] >= arr[n-1]) h = h - c;

            nx = upper_bound(arr.begin(), arr.end(), h) - arr.begin();
            if( (cc >= i && nx <= cc && nx >= i) || (cc < i && nx >= i) || (t2 == c)) 
            ans = min(ans, dp[cc] + t2);
            else{
                dp[nx] = min(dp[nx], dp[cc] + t2);
                pq.push(nx);
            }
        }
    }

    cout << ans << '\n';

    return 0;
}