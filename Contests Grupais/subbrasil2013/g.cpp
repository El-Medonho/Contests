#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)

int main(){
    fastio;

    int n,m; cin >> n >> m;
    vector<vector<int>> grid(n, vector<int> (m));
    for(int i = 0; i < n; i++){
        for(int &j: grid[i]) {cin >> j; j--;}
    }

    for(int i = 0; i < m; i++){
        bool h = true;
        int g = (grid[0][i])%m;
        for(int j = 0; j < n; j++) if((grid[j][i])%m != g) h = false;
        
        if(!h) {cout << "*\n";
        return 0;}
    }

    for(int i = 0; i < n; i++){
        bool h = true;
        int g = (grid[i][0])/m;
        for(int j = 0; j < m; j++) if((grid[i][j])/m != g) h = false;
        
        if(!h) {cout << "*\n";
        return 0;}
    }

    vector<int> v(n);
    for(int i = 0; i < n; i++) v[i] = grid[i][0]/m;
    vector<int> c(m);
    for(int i = 0; i < m; i++) c[i] = (grid[0][i])%m;

    int ans = 0;

    map<int,int> mp;
    for(int i = 0; i < n; i++) mp[v[i]] = i;
    for(int i = 0; i < n; i++){
        if(v[i] != i) {
            ans++;
            int h = v[i];
            swap(v[i], v[mp[i]]);
            swap(mp[i], mp[h]);
        }
    }

    mp.clear();

    for(int i = 0; i < m; i++) mp[c[i]] = i;
    for(int i = 0; i < m; i++){
        if(c[i] != i) {
            ans++;
            int h = c[i];
            swap(c[i], c[mp[i]]);
            swap(mp[i], mp[h]);
        }
    }

    cout << ans << '\n';

    return 0;
}