#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

void upd(int ind, ll val, vector<ll> &bit){   //O(logn)
    while(ind<=bit.size()-1){
        bit[ind]= max(val, bit[ind]);
        ind+=ind&-ind;
    }
}
ll sum(int ind, vector<ll> &bit){         //O(logn)
    ll ans=0;
    while(ind){
        ans = max(ans, bit[ind]);    
        ind-=ind&-ind;    
    }
    return ans;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,r; cin >> n >> r;
    int w; cin >> w; cin >> w;

    vector<pair<ll,ll>> arr(n);
    set<ll> st;
    map<ll,ll> mp;

    for(int i = 0; i < n; i++){
        cin >> arr[i].first >> arr[i].second;
        arr[i].first *= r;
        arr[i] = { arr[i].first + arr[i].second, -arr[i].first + arr[i].second};
        st.insert(arr[i].first); st.insert(arr[i].second);
    }

    int cnt = 1;
    for(ll i : st) mp[i] = cnt++; 

    for(int i = 0; i < n; i++){
        arr[i] = { mp[arr[i].first], mp[arr[i].second] };
    }

    ll ans = 0;

    sort(arr.begin(), arr.end());
    vector<ll> bit(cnt+2, 0);

    for(int i = 0; i < n; i++){
        ll y = arr[i].second;

        ll cc = sum(y, bit); cc++;

        ans = max(ans, cc);

        upd(y, cc, bit);
    }

    cout << ans << endl;
    

    return 0;
}