#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed solve(){

    int n; cin >> n;
    vector<pair<ll,ll>> arr(n);
    for(int i = 0; i < n; i++){
        cin >> arr[i].first;
        arr[i].second = i;
    }

    sort(arr.begin(), arr.end());

    ll sum = 0, last = 0;
    for(int i = 0; i < n; i++){
        if(sum < arr[i].first) last = i;
        sum += arr[i].first;
    }


    vector<int> ans;
    for(int i = last; i < n; i++) ans.push_back(arr[i].second);

    sort(ans.begin(), ans.end());

    cout << ans.size() << '\n';

    for(int i: ans) cout << i+1 << ' ';
    cout << '\n';

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--) solve();

    return 0;
}