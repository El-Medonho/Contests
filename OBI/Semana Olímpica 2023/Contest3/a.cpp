#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;
    vector<int> arr(n);
    for(int &i: arr) cin >> i;

    int q; cin >> q;

    sort(arr.begin(), arr.end());

    while(q--){
        int x; cin >> x;

        int l = 0, r = n-1;

        int ans = 0;

        while(l <= r){
            int mid = (l+r)/2;
            if(arr[mid] <= x){
                ans = mid+1;
                l = mid+1;
            }
            else r = mid-1;
        }

        cout << ans << '\n';
    }

    return 0;
}