#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n; ll x; cin >> x;
    vector<pair<ll,ll>> arr(n);
    for(int i = 0; i < n; i++){
        cin >> arr[i].first; arr[i].second = i;
    }

    sort(arr.begin(), arr.end());

    for(int k = 0; k < n; k++){
        ll x2 = x-arr[k].first;
        for(int i = k+1, j = n-1; i < j;){
            if(arr[i].first + arr[j].first == x2){
                cout << arr[k].second + 1 << ' ' << arr[i].second + 1 << ' ' << arr[j].second + 1 << '\n';
                return 0;
            }

            if(arr[i].first+arr[j].first < x2) i++;
            else j--;
        }
    }


    cout << "IMPOSSIBLE\n";

    return 0;
}