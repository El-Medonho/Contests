#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    ll n,t; cin >> n >> t;

    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    ll l = 0, r = 1e18+50;
    ll ans = 0;
    while(l <= r){
        ll mid = (l+r)/2;

        ll sum = 0;
        for(ll i: arr) {
            sum += mid/i;
            if(sum >= t) break;
        }

        if(sum < t) l = mid+1;
        else{
            ans = mid;
            r = mid-1;
        }
    }

    cout << ans << '\n';

    return 0;
}