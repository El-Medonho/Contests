#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    ll n,t; cin >> n >> t;

    vector<ll> arr(n);
    for(int i = 0; i < n; i++) cin >> arr[i];

    ll l = 0, r = 1e18+50;

    ll ans = 1e18+50;

    while(l <= r){
        ll mid = (l+r)/2;

        ll sum = 0;
        for(int i = 0; i < n; i++){
            sum += mid/arr[i];
            if(sum >= t) break;
        }

        if(sum >= t){
            ans = mid;
            r = mid-1;
        }
        else{
            l = mid+1;
        }
    }

    cout << ans << '\n';

    return 0;
}