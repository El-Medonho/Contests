#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n; ll k; cin >> k;
    vector<pair<ll,ll>> arr(n);
    for(int i = 0; i < n; i++){
        cin >> arr[i].first;
        arr[i].second = i;
    }

    sort(arr.begin(), arr.end());

    for(int i = 0; i < n; i++){
        for(int j = n-1; j > i; j--){
            ll tg = k - arr[i].first - arr[j].first;
            for(int a = i+1, b = j-1; a < b;){
                ll sum = arr[a].first + arr[b].first;
                if(sum < tg) a++;
                if(sum > tg) b--;
                if(sum == tg) {
                    cout << arr[i].second+1 << ' ' << arr[a].second+1 << ' ' << arr[b].second+1 << ' ' << arr[j].second+1 << '\n';
                    return 0;
                }
            }
        }
    }

    cout << "IMPOSSIBLE" << '\n';

    return 0;
}