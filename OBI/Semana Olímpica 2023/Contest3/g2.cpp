#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    multiset<int> torres;

    while(n--){
        int x; cin >> x;
        if(torres.upper_bound(x) != torres.end()) 
            torres.erase(torres.upper_bound(x));

        torres.insert(x);
    }

    cout << torres.size() << '\n';
}