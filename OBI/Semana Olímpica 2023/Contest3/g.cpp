#include "bits/stdc++.h"

using namespace std;

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    multiset<int> st;

    while(n--){
        int h; cin >> h;
        if(st.upper_bound(h) != st.end()) st.erase(st.upper_bound(h));
        st.insert(h);
    }

    cout << st.size() << '\n';

    return 0;
}